* @param IN_CWCAG_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CWCAG_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_AGENCIA_CONTACTO_INSERT(
IN IN_CWCAG_CODIGO SII.CGG_RES_AGENCIA_CONTACTO.CWCAG_CODIGO%TYPE,
IN IN_CWAGE_CODIGO SII.CGG_RES_AGENCIA_CONTACTO.CWAGE_CODIGO%TYPE,
IN IN_CRTCO_CODIGO SII.CGG_RES_AGENCIA_CONTACTO.CRTCO_CODIGO%TYPE,
IN IN_CWCAG_DESCRIPCION SII.CGG_RES_AGENCIA_CONTACTO.CWCAG_DESCRIPCION%TYPE,
IN IN_CWCAG_CONTACTO SII.CGG_RES_AGENCIA_CONTACTO.CWCAG_CONTACTO%TYPE,
IN IN_CWCAG_ESTADO SII.CGG_RES_AGENCIA_CONTACTO.CWCAG_ESTADO%TYPE,
IN IN_CWCAG_USUARIO_INSERT SII.CGG_RES_AGENCIA_CONTACTO.CWCAG_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CWCAG_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_AGENCIA_CONTACTO','CWCAG_CODIGO','CWCAG') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CWCAG_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_AGENCIA_CONTACTO(
		CWCAG_CODIGO, 
		CWAGE_CODIGO, 
		CRTCO_CODIGO, 
		CWCAG_DESCRIPCION, 
		CWCAG_CONTACTO, 
		CWCAG_ESTADO, 
		CWCAG_FECHA_INSERT, 
		CWCAG_USUARIO_INSERT, 
		CWCAG_FECHA_UPDATE, 
		CWCAG_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CWAGE_CODIGO, 
		IN_CRTCO_CODIGO, 
		IN_CWCAG_DESCRIPCION, 
		IN_CWCAG_CONTACTO, 
		IN_CWCAG_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CWCAG_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CWCAG_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

