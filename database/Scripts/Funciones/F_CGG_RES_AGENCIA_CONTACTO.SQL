/**
* FUNCION SII.F_CGG_RES_AGENCIA_CONTACTO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_AGENCIA_CONTACTO
* @param IN_CWCAG_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CWAGE_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CRTCO_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TIPO DE CONTACTO
* @param IN_CWCAG_DESCRIPCION VALOR ASIGNADO AL CONTACTO
