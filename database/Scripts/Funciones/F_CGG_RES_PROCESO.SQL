/**
* FUNCION SII.F_CGG_RES_PROCESO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_PROCESO
* @param IN_CRPRO_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE PROCESO
* @param IN_CRPRO_NOMBRE INFORMACION DE IDENTIFICACION DEL PROCESO
* @param IN_CRPRO_ESTADO ESTADO DEL REGISTRO
* @param IN_CRPRO_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRPRO_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_PROCESO_INSERT(
IN IN_CRPRO_CODIGO SII.CGG_RES_PROCESO.CRPRO_CODIGO%TYPE, 
IN IN_CRPRO_NOMBRE SII.CGG_RES_PROCESO.CRPRO_NOMBRE%TYPE, 
IN IN_CRPRO_ESTADO SII.CGG_RES_PROCESO.CRPRO_ESTADO%TYPE, 
IN IN_CRPRO_USUARIO_INSERT SII.CGG_RES_PROCESO.CRPRO_USUARIO_INSERT%TYPE, 
IN IN_CRPRO_USUARIO_UPDATE SII.CGG_RES_PROCESO.CRPRO_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRPRO_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_PROCESO','CRPRO_CODIGO','CRPRO') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRPRO_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_PROCESO(
		CRPRO_CODIGO, 
		CRPRO_NOMBRE, 
		CRPRO_ESTADO, 
		CRPRO_FECHA_INSERT, 
		CRPRO_USUARIO_INSERT, 
		CRPRO_FECHA_UPDATE, 
		CRPRO_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRPRO_NOMBRE, 
		IN_CRPRO_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRPRO_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRPRO_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

