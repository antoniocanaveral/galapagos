/**
* FUNCION SII.F_CGG_RES_GARANTIA_SOLICITUD_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_GARANTIA_SOLICITUD
* @param IN_CRGTS_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CRGRT_CODIGO CODIGO IDENTIFICATIVO DE REGISTRO
* @param IN_CRTST_CODIGO IDENTIFICATIVO UNICO DE REGISTRO TIPO SOLICITUD
* @param IN_CRGTS_ESTADO ESTADO DEL REGISTRO
* @param IN_CRGTS_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRGTS_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_GARANTIA_SOLICITUD_INSERT(
IN IN_CRGTS_CODIGO SII.CGG_RES_GARANTIA_SOLICITUD.CRGTS_CODIGO%TYPE, 
IN IN_CRGRT_CODIGO SII.CGG_RES_GARANTIA_SOLICITUD.CRGRT_CODIGO%TYPE, 
IN IN_CRTST_CODIGO SII.CGG_RES_GARANTIA_SOLICITUD.CRTST_CODIGO%TYPE, 
IN IN_CRGTS_ESTADO SII.CGG_RES_GARANTIA_SOLICITUD.CRGTS_ESTADO%TYPE, 
IN IN_CRGTS_USUARIO_INSERT SII.CGG_RES_GARANTIA_SOLICITUD.CRGTS_USUARIO_INSERT%TYPE, 
IN IN_CRGTS_USUARIO_UPDATE SII.CGG_RES_GARANTIA_SOLICITUD.CRGTS_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRGTS_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_GARANTIA_SOLICITUD','CRGTS_CODIGO','CRGTS') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRGTS_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_GARANTIA_SOLICITUD(
		CRGTS_CODIGO, 
		CRGRT_CODIGO, 
		CRTST_CODIGO, 
		CRGTS_ESTADO, 
		CRGTS_FECHA_INSERT, 
		CRGTS_USUARIO_INSERT, 
		CRGTS_FECHA_UPDATE, 
		CRGTS_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRGRT_CODIGO, 
		IN_CRTST_CODIGO, 
		IN_CRGTS_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRGTS_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRGTS_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

