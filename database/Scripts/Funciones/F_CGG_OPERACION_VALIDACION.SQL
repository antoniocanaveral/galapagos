/**
* FUNCION SII.F_CGG_OPERACION_VALIDACION_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_OPERACION_VALIDACION
* @param IN_COPVL_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE OPERACION
* @param IN_COPVL_DESCRIPCION DESCRIPCION DE LA OPERACION
* @param IN_COPVL_FORMULARIO FORMULARIO EN EL QUE EJECUTARIA LA OPERACION POR DEFECTO
* @param IN_COPVL_ESTADO ESTADO DEL REGISTRO
* @param IN_COPVL_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_COPVL_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_OPERACION_VALIDACION_INSERT(
IN IN_COPVL_CODIGO SII.CGG_OPERACION_VALIDACION.COPVL_CODIGO%TYPE, 
IN IN_COPVL_DESCRIPCION SII.CGG_OPERACION_VALIDACION.COPVL_DESCRIPCION%TYPE, 
IN IN_COPVL_FORMULARIO SII.CGG_OPERACION_VALIDACION.COPVL_FORMULARIO%TYPE, 
IN IN_COPVL_ESTADO SII.CGG_OPERACION_VALIDACION.COPVL_ESTADO%TYPE, 
IN IN_COPVL_USUARIO_INSERT SII.CGG_OPERACION_VALIDACION.COPVL_USUARIO_INSERT%TYPE, 
IN IN_COPVL_USUARIO_UPDATE SII.CGG_OPERACION_VALIDACION.COPVL_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_COPVL_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_OPERACION_VALIDACION','COPVL_CODIGO','COPVL') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_COPVL_CODIGO;
	END IF;
	INSERT INTO SII.CGG_OPERACION_VALIDACION(
		COPVL_CODIGO, 
		COPVL_DESCRIPCION, 
		COPVL_FORMULARIO, 
		COPVL_ESTADO, 
		COPVL_FECHA_INSERT, 
		COPVL_USUARIO_INSERT, 
		COPVL_FECHA_UPDATE, 
		COPVL_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_COPVL_DESCRIPCION, 
		IN_COPVL_FORMULARIO, 
		IN_COPVL_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_COPVL_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_COPVL_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

