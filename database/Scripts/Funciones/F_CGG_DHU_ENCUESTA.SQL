/**
* FUNCION SII.F_CGG_DHU_ENCUESTA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_DHU_ENCUESTA
* @param IN_CDENC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE EVALUACION
* @param IN_CDENC_NOMBRE NOMBRE DE LA EVALUACION
* @param IN_CDENC_OBSERVACION OBSERVACION DE LA EVALUACION
* @param IN_CDENC_ESTADO ESTADO DEL REGISTRO
* @param IN_CDENC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CDENC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_DHU_ENCUESTA_INSERT(
IN IN_CDENC_CODIGO SII.CGG_DHU_ENCUESTA.CDENC_CODIGO%TYPE, 
IN IN_CDENC_NOMBRE SII.CGG_DHU_ENCUESTA.CDENC_NOMBRE%TYPE, 
IN IN_CDENC_OBSERVACION SII.CGG_DHU_ENCUESTA.CDENC_OBSERVACION%TYPE, 
IN IN_CDENC_ESTADO SII.CGG_DHU_ENCUESTA.CDENC_ESTADO%TYPE, 
IN IN_CDENC_USUARIO_INSERT SII.CGG_DHU_ENCUESTA.CDENC_USUARIO_INSERT%TYPE, 
IN IN_CDENC_USUARIO_UPDATE SII.CGG_DHU_ENCUESTA.CDENC_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CDENC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_DHU_ENCUESTA','CDENC_CODIGO','CDENC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CDENC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_DHU_ENCUESTA(
		CDENC_CODIGO, 
		CDENC_NOMBRE, 
		CDENC_OBSERVACION, 
		CDENC_ESTADO, 
		CDENC_FECHA_INSERT, 
		CDENC_USUARIO_INSERT, 
		CDENC_FECHA_UPDATE, 
		CDENC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CDENC_NOMBRE, 
		IN_CDENC_OBSERVACION, 
		IN_CDENC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CDENC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CDENC_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

