* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_DHU_ENCUESTA_SELECT(
IN IN_USER_NAME varchar(50),
IN IN_START_INDEX int,
IN IN_LIMIT int,
IN IN_SORT_FIELD_NAME varchar,
IN IN_DIRECTION varchar,
IN IN_FIND_TEXT text
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CDENC_CODIGO, 
		CDENC_NOMBRE, 
		CDENC_OBSERVACION, 
		CDENC_ESTADO, 
		CDENC_FECHA_INSERT, 
		CDENC_USUARIO_INSERT, 
		CDENC_FECHA_UPDATE, 
		CDENC_USUARIO_UPDATE
	FROM SII.CGG_DHU_ENCUESTA
	WHERE CDENC_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CDENC_CODIGO IS NULL THEN '''' ELSE CDENC_CODIGO END ||'' ''||CASE WHEN CDENC_NOMBRE IS NULL THEN '''' ELSE CDENC_NOMBRE END ||'' ''||CASE WHEN CDENC_OBSERVACION IS NULL THEN '''' ELSE CDENC_OBSERVACION END ||'' ''||CASE WHEN CDENC_ESTADO IS NULL THEN FALSE ELSE CDENC_ESTADO END ||'' ''||CASE WHEN CDENC_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CDENC_FECHA_INSERT END ||'' ''||CASE WHEN CDENC_USUARIO_INSERT IS NULL THEN '''' ELSE CDENC_USUARIO_INSERT END ||'' ''||CASE WHEN CDENC_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CDENC_FECHA_UPDATE END ||'' ''||CASE WHEN CDENC_USUARIO_UPDATE IS NULL THEN '''' ELSE CDENC_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

