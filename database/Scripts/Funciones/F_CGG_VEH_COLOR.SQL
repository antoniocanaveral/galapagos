/**
* FUNCION SII.F_CGG_VEH_COLOR_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_VEH_COLOR
* @param IN_CVCLR_CODIGO IDENTIFICADOR UNICO DE REGISTRO DE COLOR
* @param IN_CVCLR_NOMBRE NOMBRE DESCRIPTIVO DEL NOMBRE.
* @param IN_CVCLR_OBSERVACION INFORMACION ADICIONAL
* @param IN_CVCLR_ESTADO ESTADO DEL REGISTRO
* @param IN_CVCLR_USUARIO_INSERT USUARIO QUE CREO EL REGISTRO.
* @param IN_CVCLR_USUARIO_UPDATE USUARIO QUE ACTUALIZO EL REGISTRO.
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_VEH_COLOR_INSERT(
IN IN_CVCLR_CODIGO SII.CGG_VEH_COLOR.CVCLR_CODIGO%TYPE, 
IN IN_CVCLR_NOMBRE SII.CGG_VEH_COLOR.CVCLR_NOMBRE%TYPE, 
IN IN_CVCLR_OBSERVACION SII.CGG_VEH_COLOR.CVCLR_OBSERVACION%TYPE, 
IN IN_CVCLR_ESTADO SII.CGG_VEH_COLOR.CVCLR_ESTADO%TYPE, 
IN IN_CVCLR_USUARIO_INSERT SII.CGG_VEH_COLOR.CVCLR_USUARIO_INSERT%TYPE, 
IN IN_CVCLR_USUARIO_UPDATE SII.CGG_VEH_COLOR.CVCLR_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CVCLR_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_VEH_COLOR','CVCLR_CODIGO','CVCLR') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CVCLR_CODIGO;
	END IF;
	INSERT INTO SII.CGG_VEH_COLOR(
		CVCLR_CODIGO, 
		CVCLR_NOMBRE, 
		CVCLR_OBSERVACION, 
		CVCLR_ESTADO, 
		CVCLR_USUARIO_INSERT, 
		CVCLR_FECHA_INSERT, 
		CVCLR_USUARIO_UPDATE, 
		CVCLR_FECHA_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CVCLR_NOMBRE, 
		IN_CVCLR_OBSERVACION, 
		IN_CVCLR_ESTADO, 
		IN_CVCLR_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CVCLR_USUARIO_UPDATE, 
		CURRENT_TIMESTAMP
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

