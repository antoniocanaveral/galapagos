IN IN_CDBCR_USUARIO_INSERT SII.CGG_DHU_BECA_CERTIFICADO.CDBCR_USUARIO_INSERT%TYPE, 
IN IN_CDBCR_USUARIO_UPDATE SII.CGG_DHU_BECA_CERTIFICADO.CDBCR_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CDBCR_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_DHU_BECA_CERTIFICADO','CDBCR_CODIGO','CDBCR') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CDBCR_CODIGO;
	END IF;
	INSERT INTO SII.CGG_DHU_BECA_CERTIFICADO(
		CDBCR_CODIGO, 
		CDTCA_CODIGO, 
		CDTBC_CODIGO, 
		CDBCR_OBLIGATORIO, 
		CDBCR_ESTADO, 
		CDBCR_FECHA_INSERT, 
		CDBCR_USUARIO_INSERT, 
		CDBCR_FECHA_UPDATE, 
		CDBCR_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CDTCA_CODIGO, 
		IN_CDTBC_CODIGO, 
		IN_CDBCR_OBLIGATORIO, 
		IN_CDBCR_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CDBCR_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CDBCR_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

