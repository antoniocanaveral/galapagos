* INGRESA UN REGISTRO EN LA TABLA SII.CGG_DHU_BECA_CERTIFICADO.
* @param IN_CDBCR_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE CERTIFICADO
* @param IN_CDTCA_CODIGO IDENTIFICATIVO UNICO DE REGISTRO TIPO DE CERTIFICADO ACADEMICO
* @param IN_CDTBC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TIPO DE BECA
