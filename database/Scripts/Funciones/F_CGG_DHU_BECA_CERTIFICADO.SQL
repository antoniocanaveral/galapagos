* @param IN_CDBCR_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CDBCR_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_DHU_BECA_CERTIFICADO_INSERT(
IN IN_CDBCR_CODIGO SII.CGG_DHU_BECA_CERTIFICADO.CDBCR_CODIGO%TYPE, 
IN IN_CDTCA_CODIGO SII.CGG_DHU_BECA_CERTIFICADO.CDTCA_CODIGO%TYPE, 
IN IN_CDTBC_CODIGO SII.CGG_DHU_BECA_CERTIFICADO.CDTBC_CODIGO%TYPE, 
