	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		ANT.CJAAC_CODIGO, 
		ANT.CJACU_CODIGO, 		
		ACR.CRPJR_CODIGO,
		(SELECT CRPJR_RAZON_SOCIAL FROM CGG_RES_PERSONA_JURIDICA PJA WHERE (PJA.CRPJR_CODIGO = ACR.CRPJR_CODIGO)) RAZON_SOCIAL,			
