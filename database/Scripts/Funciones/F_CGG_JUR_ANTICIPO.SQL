		ANT.CJAAC_MONTO, 
		ANT.CJAAC_VALOR_1, 
		ANT.CJAAC_ESTADO, 
		ANT.CJAAC_FECHA_INSERT, 
		ANT.CJAAC_USUARIO_INSERT, 
		ANT.CJAAC_FECHA_UPDATE, 
		ANT.CJAAC_USUARIO_UPDATE
	FROM SII.CGG_JUR_ANTICIPO ANT
	INNER JOIN CGG_JUR_ACUERDO ACR ON (ACR.CJACU_CODIGO= ANT.CJACU_CODIGO)
	WHERE CJAAC_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',	
	COALESCE(CJACU_ANIO, 0)||'' ''||
	COALESCE(CJACU_NUMERO, '''')||'' ''||
	COALESCE(CJACU_OBJETO, '''')||'' ''||
	COALESCE(CJACU_FECHA_SUSCRIPCION, CURRENT_TIMESTAMP)||'' ''||
	COALESCE(CJACU_FECHA_INICIO, CURRENT_TIMESTAMP)||'' ''||
	COALESCE(CJACU_ESTADO_EJECUCION, 0)||'' ''||
	COALESCE(CJACU_RESPONSABLE, '''')||'' ''||
	COALESCE(CJACU_TIPO, 0)||'' ''||
	COALESCE(CJACU_TIPO_PERSONA, 0)||'' ''||	
	COALESCE(CJAAC_PORCENTAJE, 0)||'' ''||
	COALESCE(CJAAC_OBSERVACION, '''')||'' ''||
	COALESCE(CJAAC_FECHA, CURRENT_TIMESTAMP)||'' ''||
	COALESCE(CJAAC_MONTO, 0)||'' ''||
	COALESCE(CJAAC_VALOR_1, '''')||'' ''||
	COALESCE(CJAAC_ESTADO, FALSE)||'' ''||
	COALESCE(CJAAC_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||
	COALESCE(CJAAC_USUARIO_INSERT, '''')||'' ''||
	COALESCE(CJAAC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||
	COALESCE(CJAAC_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
