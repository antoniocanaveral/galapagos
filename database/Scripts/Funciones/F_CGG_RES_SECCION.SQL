/**
* FUNCION SII.F_CGG_RES_SECCION_SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_RES_SECCION
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_SECCION_SELECT(
IN IN_USER_NAME VARCHAR,
IN IN_START_INDEX INT,
IN IN_LIMIT INT,
IN IN_SORT_FIELD_NAME VARCHAR,
IN IN_DIRECTION VARCHAR,
IN IN_FIND_TEXT TEXT
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CRSEC_CODIGO, 
		CRSEC_NOMBRE, 
		CRSEC_ESTADO, 
		CRSEC_FECHA_INSERT, 
		CRSEC_USUARIO_INSERT, 
		CRSEC_FECHA_UPDATE, 
		CRSEC_USUARIO_UPDATE
	FROM SII.CGG_RES_SECCION
	WHERE CRSEC_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CRSEC_NOMBRE, '''')||'' ''||COALESCE(CRSEC_ESTADO, FALSE)||'' ''||COALESCE(CRSEC_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CRSEC_USUARIO_INSERT, '''')||'' ''||COALESCE(CRSEC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CRSEC_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

