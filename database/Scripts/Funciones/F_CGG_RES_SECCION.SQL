/**
* FUNCION SII.F_CGG_RES_SECCION_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_SECCION
* @param IN_CRSEC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO SECCION
* @param IN_CRSEC_NOMBRE NOMBRE DEL DOCUMENTO DE RESPUESTA
* @param IN_CRSEC_ESTADO ESTADO DEL REGISTRO
* @param IN_CRSEC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRSEC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_SECCION_INSERT(
IN IN_CRSEC_CODIGO SII.CGG_RES_SECCION.CRSEC_CODIGO%TYPE,
IN IN_CRSEC_NOMBRE SII.CGG_RES_SECCION.CRSEC_NOMBRE%TYPE,
IN IN_CRSEC_ESTADO SII.CGG_RES_SECCION.CRSEC_ESTADO%TYPE,
IN IN_CRSEC_USUARIO_INSERT SII.CGG_RES_SECCION.CRSEC_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRSEC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_SECCION','CRSEC_CODIGO','CRSEC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRSEC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_SECCION(
		CRSEC_CODIGO, 
		CRSEC_NOMBRE, 
		CRSEC_ESTADO, 
		CRSEC_FECHA_INSERT, 
		CRSEC_USUARIO_INSERT, 
		CRSEC_FECHA_UPDATE, 
		CRSEC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRSEC_NOMBRE, 
		IN_CRSEC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRSEC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRSEC_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

