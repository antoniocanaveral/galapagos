/**
* FUNCION SII.F_CGG_SEC_ROL_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_SEC_ROL
* @param IN_CSROL_CODIGO IDENTIFICATIVO UNICO DE REGISTRO ROL
* @param IN_CSROL_NOMBRE NOMBRE DEL ROL
* @param IN_CSROL_ESTADO ESTADO DEL REGISTRO
* @param IN_CSROL_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CSROL_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_SEC_ROL_INSERT(
IN IN_CSROL_CODIGO SII.CGG_SEC_ROL.CSROL_CODIGO%TYPE, 
IN IN_CSROL_NOMBRE SII.CGG_SEC_ROL.CSROL_NOMBRE%TYPE, 
IN IN_CSROL_ESTADO SII.CGG_SEC_ROL.CSROL_ESTADO%TYPE, 
IN IN_CSROL_USUARIO_INSERT SII.CGG_SEC_ROL.CSROL_USUARIO_INSERT%TYPE, 
IN IN_CSROL_USUARIO_UPDATE SII.CGG_SEC_ROL.CSROL_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CSROL_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_SEC_ROL','CSROL_CODIGO','CSROL') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CSROL_CODIGO;
	END IF;
	INSERT INTO SII.CGG_SEC_ROL(
		CSROL_CODIGO, 
		CSROL_NOMBRE, 
		CSROL_ESTADO, 
		CSROL_FECHA_INSERT, 
		CSROL_USUARIO_INSERT, 
		CSROL_FECHA_UPDATE, 
		CSROL_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CSROL_NOMBRE, 
		IN_CSROL_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CSROL_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CSROL_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

