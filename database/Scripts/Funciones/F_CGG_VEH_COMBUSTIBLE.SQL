/**
* FUNCION SII.F_CGG_VEH_COMBUSTIBLE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_VEH_COMBUSTIBLE
* @param IN_CVCMB_CODIGO IDENTIFICADOR UNICO DE REGISTRO DE COMBUSTIBLE
* @param IN_CVCMB_NOMBRE IDENTIFICADOR DESCRIPTIVO DEL COMBUSTIBLE.
* @param IN_CVCMB_OBSERVACION INFORMACION ADICIONAL
* @param IN_CVCMB_ESTADO ESTADO DEL REGISTRO.
* @param IN_CVCMB_USUARIO_INSERT USUARIO QUE CREO EL REGISTRO.
* @param IN_CVCMB_USUARIO_UPDATE USUARIO QUE ACTUALIZO EL REGISTRO.
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_VEH_COMBUSTIBLE_INSERT(
IN IN_CVCMB_CODIGO SII.CGG_VEH_COMBUSTIBLE.CVCMB_CODIGO%TYPE,
IN IN_CVCMB_NOMBRE SII.CGG_VEH_COMBUSTIBLE.CVCMB_NOMBRE%TYPE,
IN IN_CVCMB_OBSERVACION SII.CGG_VEH_COMBUSTIBLE.CVCMB_OBSERVACION%TYPE,
IN IN_CVCMB_ESTADO SII.CGG_VEH_COMBUSTIBLE.CVCMB_ESTADO%TYPE,
IN IN_CVCMB_USUARIO_INSERT SII.CGG_VEH_COMBUSTIBLE.CVCMB_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CVCMB_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_VEH_COMBUSTIBLE','CVCMB_CODIGO','CVCMB') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CVCMB_CODIGO;
	END IF;
	INSERT INTO SII.CGG_VEH_COMBUSTIBLE(
		CVCMB_CODIGO, 
		CVCMB_NOMBRE, 
		CVCMB_OBSERVACION, 
		CVCMB_ESTADO, 
		CVCMB_USUARIO_INSERT, 
		CVCMB_FECHA_INSERT, 
		CVCMB_USUARIO_UPDATE, 
		CVCMB_FECHA_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CVCMB_NOMBRE, 
		IN_CVCMB_OBSERVACION, 
		IN_CVCMB_ESTADO, 
		IN_CVCMB_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CVCMB_USUARIO_INSERT, 
		CURRENT_TIMESTAMP
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

