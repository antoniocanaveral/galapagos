/**
* FUNCION SII.F_CGG_RES_EMBARCACION_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_EMBARCACION
* @param IN_CREMB_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CREMB_NOMBRE NOMBRE DE LA EMBARCACION
* @param IN_CREMB_ESTADO ESTADO DEL REGISTRO
* @param IN_CREMB_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CREMB_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_EMBARCACION_INSERT(
IN IN_CREMB_CODIGO SII.CGG_RES_EMBARCACION.CREMB_CODIGO%TYPE, 
IN IN_CREMB_NOMBRE SII.CGG_RES_EMBARCACION.CREMB_NOMBRE%TYPE, 
IN IN_CREMB_ESTADO SII.CGG_RES_EMBARCACION.CREMB_ESTADO%TYPE, 
IN IN_CREMB_USUARIO_INSERT SII.CGG_RES_EMBARCACION.CREMB_USUARIO_INSERT%TYPE, 
IN IN_CREMB_USUARIO_UPDATE SII.CGG_RES_EMBARCACION.CREMB_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
TMP_COUNT INT;
BEGIN
	SELECT COUNT(CREMB_CODIGO) INTO TMP_COUNT 
	FROM SII.CGG_RES_EMBARCACION 
	WHERE CREMB_ESTADO AND UPPER(CREMB_NOMBRE) = UPPER(IN_CREMB_NOMBRE);
	IF (TMP_COUNT > 0) THEN
		RAISE EXCEPTION 'El nombre de la embarcaci&oacute;n ya existe.';
	END IF;
	IF (UPPER(IN_CREMB_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_EMBARCACION','CREMB_CODIGO','CREMB') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CREMB_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_EMBARCACION(
		CREMB_CODIGO, 
		CREMB_NOMBRE, 
		CREMB_ESTADO, 
		CREMB_FECHA_INSERT, 
		CREMB_USUARIO_INSERT, 
		CREMB_FECHA_UPDATE, 
		CREMB_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CREMB_NOMBRE, 
		IN_CREMB_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CREMB_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CREMB_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

