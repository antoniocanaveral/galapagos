/*
* FUNCION SII.F_CGG_RES_EMBARCACION__SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_RES_EMBARCACION
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_EMBARCACION_SELECT(
IN IN_USER_NAME varchar(50),
IN IN_START_INDEX int,
IN IN_LIMIT int,
IN IN_SORT_FIELD_NAME varchar,
IN IN_DIRECTION varchar,
IN IN_FIND_TEXT text
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CREMB_CODIGO, 
		CREMB_NOMBRE, 
		CREMB_ESTADO, 
		CREMB_FECHA_INSERT, 
		CREMB_USUARIO_INSERT, 
		CREMB_FECHA_UPDATE, 
		CREMB_USUARIO_UPDATE
	FROM SII.CGG_RES_EMBARCACION
	WHERE CREMB_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CREMB_CODIGO IS NULL THEN '''' ELSE CREMB_CODIGO END ||'' ''||CASE WHEN CREMB_NOMBRE IS NULL THEN '''' ELSE CREMB_NOMBRE END ||'' ''||CASE WHEN CREMB_ESTADO IS NULL THEN FALSE ELSE CREMB_ESTADO END ||'' ''||CASE WHEN CREMB_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CREMB_FECHA_INSERT END ||'' ''||CASE WHEN CREMB_USUARIO_INSERT IS NULL THEN '''' ELSE CREMB_USUARIO_INSERT END ||'' ''||CASE WHEN CREMB_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CREMB_FECHA_UPDATE END ||'' ''||CASE WHEN CREMB_USUARIO_UPDATE IS NULL THEN '''' ELSE CREMB_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

