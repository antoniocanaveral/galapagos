/**
* FUNCION SII.F_CGG_VEH_ESTADO_VHMT_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_VEH_ESTADO_VHMT
* @param IN_CVSVM_CODIGO IDENTIFICATIVO UNICO DE REGISTRO ESTADO VEHICULO MOTOR
* @param IN_CVSVM_NOMBRE NOMBRE IDENTIFICATIVO DEL ESTADO DE UN MOTOR.
* @param IN_CVSVM_OBERVACION INFORMACION ADICIONAL
* @param IN_CVSVM_ESTADO ESTADO DEL REGISTRO
* @param IN_CVSVM_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CVSVM_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_VEH_ESTADO_VHMT_INSERT(
IN IN_CVSVM_CODIGO SII.CGG_VEH_ESTADO_VHMT.CVSVM_CODIGO%TYPE,
IN IN_CVSVM_NOMBRE SII.CGG_VEH_ESTADO_VHMT.CVSVM_NOMBRE%TYPE,
IN IN_CVSVM_OBERVACION SII.CGG_VEH_ESTADO_VHMT.CVSVM_OBERVACION%TYPE,
IN IN_CVSVM_ESTADO SII.CGG_VEH_ESTADO_VHMT.CVSVM_ESTADO%TYPE,
IN IN_CVSVM_USUARIO_INSERT SII.CGG_VEH_ESTADO_VHMT.CVSVM_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CVSVM_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_VEH_ESTADO_VHMT','CVSVM_CODIGO','CVSVM') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CVSVM_CODIGO;
	END IF;
	INSERT INTO SII.CGG_VEH_ESTADO_VHMT(
		CVSVM_CODIGO, 
		CVSVM_NOMBRE, 
		CVSVM_OBERVACION, 
		CVSVM_ESTADO, 
		CVSVM_FECHA_INSERT, 
		CVSVM_USUARIO_INSERT, 
		CVSVM_FECHA_UPDATE, 
		CVSVM_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CVSVM_NOMBRE, 
		IN_CVSVM_OBERVACION, 
		IN_CVSVM_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CVSVM_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CVSVM_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

