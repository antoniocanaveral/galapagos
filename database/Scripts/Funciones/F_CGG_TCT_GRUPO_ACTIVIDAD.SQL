/**
* FUNCION SII.F_CGG_TCT_GRUPO_ACTIVIDAD_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_GRUPO_ACTIVIDAD
* @param IN_CTGAC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CTGTR_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CTACT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ACTIVIDADES
* @param IN_CTGAC_ESTADO ESTADO DEL REGISTRO
* @param IN_CTGAC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTGAC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_GRUPO_ACTIVIDAD_INSERT(
IN IN_CTGAC_CODIGO SII.CGG_TCT_GRUPO_ACTIVIDAD.CTGAC_CODIGO%TYPE, 
IN IN_CTGTR_CODIGO SII.CGG_TCT_GRUPO_ACTIVIDAD.CTGTR_CODIGO%TYPE, 
IN IN_CTACT_CODIGO SII.CGG_TCT_GRUPO_ACTIVIDAD.CTACT_CODIGO%TYPE, 
IN IN_CTGAC_ESTADO SII.CGG_TCT_GRUPO_ACTIVIDAD.CTGAC_ESTADO%TYPE, 
IN IN_CTGAC_USUARIO_INSERT SII.CGG_TCT_GRUPO_ACTIVIDAD.CTGAC_USUARIO_INSERT%TYPE, 
IN IN_CTGAC_USUARIO_UPDATE SII.CGG_TCT_GRUPO_ACTIVIDAD.CTGAC_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CTGAC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_GRUPO_ACTIVIDAD','CTGAC_CODIGO','CTGAC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTGAC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_GRUPO_ACTIVIDAD(
		CTGAC_CODIGO, 
		CTGTR_CODIGO, 
		CTACT_CODIGO, 
		CTGAC_ESTADO, 
		CTGAC_FECHA_INSERT, 
		CTGAC_USUARIO_INSERT, 
		CTGAC_FECHA_UPDATE, 
		CTGAC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTGTR_CODIGO, 
		IN_CTACT_CODIGO, 
		IN_CTGAC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTGAC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTGAC_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

