		CRATE_FECHA_UPDATE, 
		CRATE_USUARIO_UPDATE
	FROM SII.CGG_RES_ADJUNTO_TEMPORAL
	WHERE CRATE_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CRATE_NOMBRE, '''')||'' ''||COALESCE(CRATE_TIPO_CONTENIDO, '''')||'' ''||COALESCE(CRATE_ESTADO, FALSE)||'' ''||COALESCE(CRATE_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CRATE_USUARIO_INSERT, '''')||'' ''||COALESCE(CRATE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CRATE_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

