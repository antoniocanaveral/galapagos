/**
* FUNCION SII.F_CGG_RES_ADJUNTO_TEMPORAL_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_ADJUNTO_TEMPORAL
* @param IN_CRATE_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ADJUNTO TEMPORAL
* @param IN_CRATE_NOMBRE NOMBRE DE ADJUNTO TEMPORAL
* @param IN_CRATE_TIPO_CONTENIDO CADENA DE TEXTO DEL TIPO DE CONTENIDO MIME DEL ADJUNTO TEMPORAL
* @param IN_CRATE_ARCHIVO CONTENIDO BINARIO DEL ARCHIVO TEMPORAL.
* @param IN_CRATE_ESTADO ESTADO DEL REGISTRO
* @param IN_CRATE_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRATE_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_ADJUNTO_TEMPORAL_INSERT(
IN IN_CRATE_CODIGO SII.CGG_RES_ADJUNTO_TEMPORAL.CRATE_CODIGO%TYPE, 
IN IN_CRATE_NOMBRE SII.CGG_RES_ADJUNTO_TEMPORAL.CRATE_NOMBRE%TYPE, 
IN IN_CRATE_TIPO_CONTENIDO SII.CGG_RES_ADJUNTO_TEMPORAL.CRATE_TIPO_CONTENIDO%TYPE, 
IN IN_CRATE_ARCHIVO SII.CGG_RES_ADJUNTO_TEMPORAL.CRATE_ARCHIVO%TYPE, 
IN IN_CRATE_ESTADO SII.CGG_RES_ADJUNTO_TEMPORAL.CRATE_ESTADO%TYPE, 
IN IN_CRATE_USUARIO_INSERT SII.CGG_RES_ADJUNTO_TEMPORAL.CRATE_USUARIO_INSERT%TYPE, 
IN IN_CRATE_USUARIO_UPDATE SII.CGG_RES_ADJUNTO_TEMPORAL.CRATE_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRATE_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_ADJUNTO_TEMPORAL','CRATE_CODIGO','CRATE') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRATE_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_ADJUNTO_TEMPORAL(
		CRATE_CODIGO, 
		CRATE_NOMBRE, 
		CRATE_TIPO_CONTENIDO, 
		CRATE_ARCHIVO, 
		CRATE_ESTADO, 
		CRATE_FECHA_INSERT, 
		CRATE_USUARIO_INSERT, 
		CRATE_FECHA_UPDATE, 
		CRATE_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRATE_NOMBRE, 
		IN_CRATE_TIPO_CONTENIDO, 
		IN_CRATE_ARCHIVO, 
		IN_CRATE_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRATE_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRATE_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

