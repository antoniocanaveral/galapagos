*/

CREATE OR REPLACE FUNCTION SII.F_RPT_UNION_CADENA(
IN IN_CTGTR_CODIGO VARCHAR
)RETURNS TEXT AS
$$
DECLARE
	OUT_RESULTADO TEXT;
    TMP_CTACT_NOMBRE RECORD;
    VAR_NUM NUMERIC;
BEGIN
	OUT_RESULTADO:=', ';
	VAR_NUM:=0;
	FOR TMP_CTACT_NOMBRE IN SELECT TACT.CTACT_NOMBRE 
