/**
* FUNCION SII.F_CGG_GEM_TIPO_CONTRATO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_GEM_TIPO_CONTRATO
* @param IN_CGTCN_CODIGO IDENTIFICATIVO UNICO DE REGISTRO TIPO CONTRATO
* @param IN_CGTCN_DESCRIPCION DESCRIPCION DEL TIPO DE CONTRATO DE TRABAJO
* @param IN_CGTCN_OBSERVACION OBSERVACION DEL TIPO DE CONTRATO DE TRABAJO
* @param IN_CGTCN_ESTADO ESTADO DEL REGISTRO
* @param IN_CGTCN_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CGTCN_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_GEM_TIPO_CONTRATO_INSERT(
IN IN_CGTCN_CODIGO SII.CGG_GEM_TIPO_CONTRATO.CGTCN_CODIGO%TYPE, 
IN IN_CGTCN_DESCRIPCION SII.CGG_GEM_TIPO_CONTRATO.CGTCN_DESCRIPCION%TYPE, 
IN IN_CGTCN_OBSERVACION SII.CGG_GEM_TIPO_CONTRATO.CGTCN_OBSERVACION%TYPE, 
IN IN_CGTCN_ESTADO SII.CGG_GEM_TIPO_CONTRATO.CGTCN_ESTADO%TYPE, 
IN IN_CGTCN_USUARIO_INSERT SII.CGG_GEM_TIPO_CONTRATO.CGTCN_USUARIO_INSERT%TYPE, 
IN IN_CGTCN_USUARIO_UPDATE SII.CGG_GEM_TIPO_CONTRATO.CGTCN_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
TMP_COUNT INT2;
BEGIN
	SELECT COUNT(CGTCN_DESCRIPCION) INTO TMP_COUNT
	FROM SII.CGG_GEM_TIPO_CONTRATO
	WHERE CGTCN_ESTADO = TRUE AND UPPER(CGTCN_DESCRIPCION) = UPPER(RTRIM(LTRIM(IN_CGTCN_DESCRIPCION)));
	
	IF (TMP_COUNT>0) THEN
		RAISE EXCEPTION 'No se puede ingresar varios tipos de contrato con el mismo nombre';
	END IF;
	
	IF (UPPER(IN_CGTCN_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_GEM_TIPO_CONTRATO','CGTCN_CODIGO','CGTCN') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CGTCN_CODIGO;
	END IF;
	INSERT INTO SII.CGG_GEM_TIPO_CONTRATO(
		CGTCN_CODIGO, 
		CGTCN_DESCRIPCION, 
		CGTCN_OBSERVACION, 
		CGTCN_ESTADO, 
		CGTCN_FECHA_INSERT, 
		CGTCN_USUARIO_INSERT, 
		CGTCN_FECHA_UPDATE, 
		CGTCN_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CGTCN_DESCRIPCION, 
		IN_CGTCN_OBSERVACION, 
		IN_CGTCN_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CGTCN_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CGTCN_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

