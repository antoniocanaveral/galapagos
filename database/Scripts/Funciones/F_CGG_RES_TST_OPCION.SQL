/**
* FUNCION SII.F_CGG_RES_TST_OPCION_SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_RES_TST_OPCION
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TST_OPCION_SELECT(
IN IN_USER_NAME VARCHAR,
IN IN_START_INDEX INT,
IN IN_LIMIT INT,
IN IN_SORT_FIELD_NAME VARCHAR,
IN IN_DIRECTION VARCHAR,
IN IN_FIND_TEXT TEXT
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CRTSO_CODIGO, 
		CRTSO_NOMBRE, 
		CRTSO_DESCRIPCION, 
		CRTSO_ESTADO, 
		CRTSO_FECHA_INSERT, 
		CRTSO_USUARIO_INSERT, 
		CRTSO_FECHA_UPDATE, 
		CRTSO_USUARIO_UPDATE
	FROM SII.CGG_RES_TST_OPCION
	WHERE CRTSO_ESTADO AND (LENGTH('||QUOTE_LITERAL(IN_FIND_TEXT)||') = 0 OR SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CRTSO_NOMBRE, '''')||'' ''||COALESCE(CRTSO_DESCRIPCION, '''')||'' ''||COALESCE(CRTSO_ESTADO, FALSE)||'' ''||COALESCE(CRTSO_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CRTSO_USUARIO_INSERT, '''')||'' ''||COALESCE(CRTSO_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CRTSO_USUARIO_UPDATE, '''')) = 1)
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

/**
* FUNCION SII.F_CGG_RES_TST_OPCION_COUNT
