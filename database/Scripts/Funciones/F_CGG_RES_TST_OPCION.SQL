/**
* FUNCION SII.F_CGG_RES_TST_OPCION_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_TST_OPCION
* @param IN_CRTSO_CODIGO IDENTIFICADOR UNICO DE REGISTRO DE TST OPCIONES
* @param IN_CRTSO_NOMBRE NOMBRE CORTO DE LA OPCION
* @param IN_CRTSO_DESCRIPCION DESCRIPCION DE LA OPCION DEL TST
* @param IN_CRTSO_ESTADO ESTADO DEL REGISTRO, SI ESTA VALIDO O NO
* @param IN_CRTSO_USUARIO_INSERT NOMBRE DEL USUARIO QUE CREO EL REGISTRO
* @param IN_CRTSO_USUARIO_UPDATE NOMBRE DEL USUARIO QUE HIZO LA ACTUALIZACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TST_OPCION_INSERT(
IN IN_CRTSO_CODIGO SII.CGG_RES_TST_OPCION.CRTSO_CODIGO%TYPE,
IN IN_CRTSO_NOMBRE SII.CGG_RES_TST_OPCION.CRTSO_NOMBRE%TYPE,
IN IN_CRTSO_DESCRIPCION SII.CGG_RES_TST_OPCION.CRTSO_DESCRIPCION%TYPE,
IN IN_CRTSO_ESTADO SII.CGG_RES_TST_OPCION.CRTSO_ESTADO%TYPE,
IN IN_CRTSO_USUARIO_INSERT SII.CGG_RES_TST_OPCION.CRTSO_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRTSO_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_TST_OPCION','CRTSO_CODIGO','CRTSO') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRTSO_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_TST_OPCION(
		CRTSO_CODIGO, 
		CRTSO_NOMBRE, 
		CRTSO_DESCRIPCION, 
		CRTSO_ESTADO, 
		CRTSO_FECHA_INSERT, 
		CRTSO_USUARIO_INSERT, 
		CRTSO_FECHA_UPDATE, 
		CRTSO_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRTSO_NOMBRE, 
		IN_CRTSO_DESCRIPCION, 
		IN_CRTSO_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRTSO_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRTSO_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

