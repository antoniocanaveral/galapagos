IN IN_CGGER_TITULO SII.CGG_GERENCIAL.CGGER_TITULO%TYPE,
IN IN_CGGER_URL SII.CGG_GERENCIAL.CGGER_URL%TYPE,
IN IN_CGGER_PARAMETROS SII.CGG_GERENCIAL.CGGER_PARAMETROS%TYPE,
IN IN_CGGER_DESCRIPCION SII.CGG_GERENCIAL.CGGER_DESCRIPCION%TYPE,
IN IN_CGGER_TIPO SII.CGG_GERENCIAL.CGGER_TIPO%TYPE,
