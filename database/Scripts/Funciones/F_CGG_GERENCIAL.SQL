IN IN_CGGER_USUARIO_INSERT SII.CGG_GERENCIAL.CGGER_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CGGER_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_GERENCIAL','CGGER_CODIGO','CGGER') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CGGER_CODIGO;
	END IF;
	INSERT INTO SII.CGG_GERENCIAL(
		CGGER_CODIGO, 
		CWMOD_CODIGO, 
		CGGER_NOMBRE, 
		CGGER_TITULO, 
		CGGER_URL, 
		CGGER_PARAMETROS, 
		CGGER_DESCRIPCION, 
		CGGER_TIPO, 
		CGGER_ESTADO, 
		CGGER_FECHA_INSERT, 
		CGGER_USUARIO_INSERT, 
		CGGER_FECHA_UPDATE, 
		CGGER_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CWMOD_CODIGO, 
		IN_CGGER_NOMBRE, 
		IN_CGGER_TITULO, 
		IN_CGGER_URL, 
		IN_CGGER_PARAMETROS, 
		IN_CGGER_DESCRIPCION, 
		IN_CGGER_TIPO, 
		IN_CGGER_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CGGER_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CGGER_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

