* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_GERENCIAL_INSERT(
IN IN_CGGER_CODIGO SII.CGG_GERENCIAL.CGGER_CODIGO%TYPE,
IN IN_CWMOD_CODIGO SII.CGG_GERENCIAL.CWMOD_CODIGO%TYPE,
