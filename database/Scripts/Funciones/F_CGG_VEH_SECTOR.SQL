/**
* FUNCION SII.F_CGG_VEH_SECTOR_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_VEH_SECTOR
* @param IN_CVSCT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE SECTOR
* @param IN_CVSCT_NOMBRE NOMBRE DESCRIPTIVO DEL SECTOR
* @param IN_CVSCT_DESCRIPCION DESCRIPCION DEL VEHICULO SECTOR
* @param IN_CVSCT_ESTADO ESTADO DEL REGISTRO
* @param IN_CVSCT_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CVSCT_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_VEH_SECTOR_INSERT(
IN IN_CVSCT_CODIGO SII.CGG_VEH_SECTOR.CVSCT_CODIGO%TYPE, 
IN IN_CVSCT_NOMBRE SII.CGG_VEH_SECTOR.CVSCT_NOMBRE%TYPE, 
IN IN_CVSCT_DESCRIPCION SII.CGG_VEH_SECTOR.CVSCT_DESCRIPCION%TYPE, 
IN IN_CVSCT_ESTADO SII.CGG_VEH_SECTOR.CVSCT_ESTADO%TYPE, 
IN IN_CVSCT_USUARIO_INSERT SII.CGG_VEH_SECTOR.CVSCT_USUARIO_INSERT%TYPE, 
IN IN_CVSCT_USUARIO_UPDATE SII.CGG_VEH_SECTOR.CVSCT_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CVSCT_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_VEH_SECTOR','CVSCT_CODIGO','CVSCT') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CVSCT_CODIGO;
	END IF;
	INSERT INTO SII.CGG_VEH_SECTOR(
		CVSCT_CODIGO, 
		CVSCT_NOMBRE, 
		CVSCT_DESCRIPCION, 
		CVSCT_ESTADO, 
		CVSCT_FECHA_INSERT, 
		CVSCT_USUARIO_INSERT, 
		CVSCT_FECHA_UPDATE, 
		CVSCT_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CVSCT_NOMBRE, 
		IN_CVSCT_DESCRIPCION, 
		IN_CVSCT_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CVSCT_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CVSCT_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

