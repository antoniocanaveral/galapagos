	WHERE CVSCT_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CVSCT_NOMBRE, '''')||'' ''||COALESCE(CVSCT_DESCRIPCION, '''')||'' ''||COALESCE(CVSCT_ESTADO, FALSE)||'' ''||COALESCE(CVSCT_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CVSCT_USUARIO_INSERT, '''')||'' ''||COALESCE(CVSCT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CVSCT_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

