/**
* FUNCION SII.F_CGG_WEB_MODULO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_WEB_MODULO
* @param IN_CWMOD_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CWMOD_NOMBRE NOMBRE DEL MODULO
* @param IN_CWMOD_DESCRIPCION DESCRIPCION ACERCA DEL MODULO
* @param IN_CWMOD_ESTADO ESTADO DEL REGISTRO
* @param IN_CWMOD_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CWMOD_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_WEB_MODULO_INSERT(
IN IN_CWMOD_CODIGO SII.CGG_WEB_MODULO.CWMOD_CODIGO%TYPE,
IN IN_CWMOD_NOMBRE SII.CGG_WEB_MODULO.CWMOD_NOMBRE%TYPE,
IN IN_CWMOD_DESCRIPCION SII.CGG_WEB_MODULO.CWMOD_DESCRIPCION%TYPE,
IN IN_CWMOD_ESTADO SII.CGG_WEB_MODULO.CWMOD_ESTADO%TYPE,
IN IN_CWMOD_USUARIO_INSERT SII.CGG_WEB_MODULO.CWMOD_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CWMOD_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_WEB_MODULO','CWMOD_CODIGO','CWMOD') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CWMOD_CODIGO;
	END IF;
	INSERT INTO SII.CGG_WEB_MODULO(
		CWMOD_CODIGO, 
		CWMOD_NOMBRE, 
		CWMOD_DESCRIPCION, 
		CWMOD_ESTADO, 
		CWMOD_FECHA_INSERT, 
		CWMOD_USUARIO_INSERT, 
		CWMOD_FECHA_UPDATE, 
		CWMOD_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CWMOD_NOMBRE, 
		IN_CWMOD_DESCRIPCION, 
		IN_CWMOD_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CWMOD_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CWMOD_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

