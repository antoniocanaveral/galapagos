/**
* FUNCION SII.F_CGG_WEB_MODULO_SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_WEB_MODULO
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_WEB_MODULO_SELECT(
IN IN_USER_NAME VARCHAR,
IN IN_START_INDEX INT,
IN IN_LIMIT INT,
IN IN_SORT_FIELD_NAME VARCHAR,
IN IN_DIRECTION VARCHAR,
IN IN_FIND_TEXT TEXT
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CWMOD_CODIGO, 
		CWMOD_NOMBRE, 
		CWMOD_DESCRIPCION, 
		CWMOD_ESTADO, 
		CWMOD_FECHA_INSERT, 
		CWMOD_USUARIO_INSERT, 
		CWMOD_FECHA_UPDATE, 
		CWMOD_USUARIO_UPDATE
	FROM SII.CGG_WEB_MODULO
	WHERE CWMOD_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CWMOD_NOMBRE, '''')||'' ''||COALESCE(CWMOD_DESCRIPCION, '''')||'' ''||COALESCE(CWMOD_ESTADO, FALSE)||'' ''||COALESCE(CWMOD_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CWMOD_USUARIO_INSERT, '''')||'' ''||COALESCE(CWMOD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CWMOD_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

