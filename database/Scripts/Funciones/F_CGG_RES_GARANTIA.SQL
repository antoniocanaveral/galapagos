/**
* FUNCION SII.F_CGG_RES_GARANTIA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_GARANTIA
* @param IN_CRGRT_CODIGO CODIGO IDENTIFICATIVO DE REGISTRO
* @param IN_CRGRT_DESCRPCION DESCRIPCION DEL TIPO DE RESIDENCIA
* @param IN_CRGRT_VALOR COSTO A PAGAR POR LA GARANTIA
* @param IN_CRGRT_ESTADO ESTADO DEL REGISTRO
* @param IN_CRGRT_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRGRT_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_GARANTIA_INSERT(
IN IN_CRGRT_CODIGO SII.CGG_RES_GARANTIA.CRGRT_CODIGO%TYPE, 
IN IN_CRGRT_DESCRPCION SII.CGG_RES_GARANTIA.CRGRT_DESCRPCION%TYPE, 
IN IN_CRGRT_VALOR SII.CGG_RES_GARANTIA.CRGRT_VALOR%TYPE, 
IN IN_CRGRT_ESTADO SII.CGG_RES_GARANTIA.CRGRT_ESTADO%TYPE, 
IN IN_CRGRT_USUARIO_INSERT SII.CGG_RES_GARANTIA.CRGRT_USUARIO_INSERT%TYPE, 
IN IN_CRGRT_USUARIO_UPDATE SII.CGG_RES_GARANTIA.CRGRT_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRGRT_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_GARANTIA','CRGRT_CODIGO','CRGRT') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRGRT_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_GARANTIA(
		CRGRT_CODIGO, 
		CRGRT_DESCRPCION, 
		CRGRT_VALOR, 
		CRGRT_ESTADO, 
		CRGRT_FECHA_INSERT, 
		CRGRT_USUARIO_INSERT, 
		CRGRT_FECHA_UPDATE, 
		CRGRT_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRGRT_DESCRPCION, 
		IN_CRGRT_VALOR, 
		IN_CRGRT_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRGRT_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRGRT_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

