IN IN_DIRECTION varchar,
IN IN_FIND_TEXT text
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CRGRT_CODIGO, 
		CRGRT_DESCRPCION, 
		CRGRT_VALOR, 
		CRGRT_ESTADO, 
		CRGRT_FECHA_INSERT, 
