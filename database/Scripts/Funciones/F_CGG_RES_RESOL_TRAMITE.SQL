/**
* FUNCION SII.F_CGG_RES_RESOL_TRAMITE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_RESOL_TRAMITE
* @param IN_CRRST_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE RESOLUCION TRAMITE
* @param IN_CRRES_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE RESOLUCIONES
* @param IN_CRTRA_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TRAMITE
* @param IN_CRRST_ESTADO ESTADO DEL REGISTRO
* @param IN_CRRST_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRRST_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_RESOL_TRAMITE_INSERT(
IN IN_CRRST_CODIGO SII.CGG_RES_RESOL_TRAMITE.CRRST_CODIGO%TYPE,
IN IN_CRRES_CODIGO SII.CGG_RES_RESOL_TRAMITE.CRRES_CODIGO%TYPE,
IN IN_CRTRA_CODIGO SII.CGG_RES_RESOL_TRAMITE.CRTRA_CODIGO%TYPE,
IN IN_CRRST_ESTADO SII.CGG_RES_RESOL_TRAMITE.CRRST_ESTADO%TYPE,
IN IN_CRRST_USUARIO_INSERT SII.CGG_RES_RESOL_TRAMITE.CRRST_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRRST_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_RESOL_TRAMITE','CRRST_CODIGO','CRRST') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRRST_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_RESOL_TRAMITE(
		CRRST_CODIGO, 
		CRRES_CODIGO, 
		CRTRA_CODIGO, 
		CRRST_ESTADO, 
		CRRST_FECHA_INSERT, 
		CRRST_USUARIO_INSERT, 
		CRRST_FECHA_UPDATE, 
		CRRST_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRRES_CODIGO, 
		IN_CRTRA_CODIGO, 
		IN_CRRST_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRRST_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRRST_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

