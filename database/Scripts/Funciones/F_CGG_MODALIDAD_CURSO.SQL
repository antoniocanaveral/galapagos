/**
* FUNCION SII.F_CGG_MODALIDAD_CURSO_SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_MODALIDAD_CURSO
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_MODALIDAD_CURSO_SELECT(
IN IN_USER_NAME VARCHAR,
IN IN_START_INDEX INT,
IN IN_LIMIT INT,
IN IN_SORT_FIELD_NAME VARCHAR,
IN IN_DIRECTION VARCHAR,
IN IN_FIND_TEXT TEXT
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CGMDC_CODIGO, 
		CGMDC_DESCRIPCION, 
		CGMDC_ESTADO, 
		CGMDC_FECHA_INSERT, 
		CGMDC_USUARIO_INSERT, 
		CGMDC_FECHA_UPDATE, 
		CGMDC_USUARIO_UPDATE
	FROM SII.CGG_MODALIDAD_CURSO
	WHERE CGMDC_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CGMDC_CODIGO IS NULL THEN '''' ELSE CGMDC_CODIGO END ||'' ''||CASE WHEN CGMDC_DESCRIPCION IS NULL THEN '''' ELSE CGMDC_DESCRIPCION END ||'' ''||CASE WHEN CGMDC_ESTADO IS NULL THEN FALSE ELSE CGMDC_ESTADO END ||'' ''||CASE WHEN CGMDC_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CGMDC_FECHA_INSERT END ||'' ''||CASE WHEN CGMDC_USUARIO_INSERT IS NULL THEN '''' ELSE CGMDC_USUARIO_INSERT END ||'' ''||CASE WHEN CGMDC_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CGMDC_FECHA_UPDATE END ||'' ''||CASE WHEN CGMDC_USUARIO_UPDATE IS NULL THEN '''' ELSE CGMDC_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

