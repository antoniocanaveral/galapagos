/**
* FUNCION SII.F_CGG_MODALIDAD_CURSO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_MODALIDAD_CURSO
* @param IN_CGMDC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE MODALIDAD DE CURSO
* @param IN_CGMDC_DESCRIPCION DESCRIPCION DE LA MODALIDAD DE CURSO
* @param IN_CGMDC_ESTADO ESTADO DEL REGISTRO
* @param IN_CGMDC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CGMDC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_MODALIDAD_CURSO_INSERT(
IN IN_CGMDC_CODIGO SII.CGG_MODALIDAD_CURSO.CGMDC_CODIGO%TYPE, 
IN IN_CGMDC_DESCRIPCION SII.CGG_MODALIDAD_CURSO.CGMDC_DESCRIPCION%TYPE, 
IN IN_CGMDC_ESTADO SII.CGG_MODALIDAD_CURSO.CGMDC_ESTADO%TYPE, 
IN IN_CGMDC_USUARIO_INSERT SII.CGG_MODALIDAD_CURSO.CGMDC_USUARIO_INSERT%TYPE, 
IN IN_CGMDC_USUARIO_UPDATE SII.CGG_MODALIDAD_CURSO.CGMDC_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CGMDC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_MODALIDAD_CURSO','CGMDC_CODIGO','CGMDC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CGMDC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_MODALIDAD_CURSO(
		CGMDC_CODIGO, 
		CGMDC_DESCRIPCION, 
		CGMDC_ESTADO, 
		CGMDC_FECHA_INSERT, 
		CGMDC_USUARIO_INSERT, 
		CGMDC_FECHA_UPDATE, 
		CGMDC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CGMDC_DESCRIPCION, 
		IN_CGMDC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CGMDC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CGMDC_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

