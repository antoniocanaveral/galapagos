/**
* FUNCION SII.F_CGG_TCT_ACTIVIDAD__SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_TCT_ACTIVIDAD
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_ACTIVIDAD_SELECT(
IN IN_USER_NAME varchar(50),
IN IN_START_INDEX int,
IN IN_LIMIT int,
IN IN_SORT_FIELD_NAME varchar,
IN IN_DIRECTION varchar,
IN IN_FIND_TEXT text
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CTACT_CODIGO, 
		CTACT_NOMBRE, 
		CTACT_ESTADO, 
		CTACT_FECHA_INSERT, 
		CTACT_USUARIO_INSERT, 
		CTACT_FECHA_UPDATE, 
		CTACT_USUARIO_UPDATE
	FROM SII.CGG_TCT_ACTIVIDAD
	WHERE CTACT_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CTACT_CODIGO IS NULL THEN '''' ELSE CTACT_CODIGO END ||'' ''||CASE WHEN CTACT_NOMBRE IS NULL THEN '''' ELSE CTACT_NOMBRE END ||'' ''||CASE WHEN CTACT_ESTADO IS NULL THEN FALSE ELSE CTACT_ESTADO END ||'' ''||CASE WHEN CTACT_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CTACT_FECHA_INSERT END ||'' ''||CASE WHEN CTACT_USUARIO_INSERT IS NULL THEN '''' ELSE CTACT_USUARIO_INSERT END ||'' ''||CASE WHEN CTACT_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CTACT_FECHA_UPDATE END ||'' ''||CASE WHEN CTACT_USUARIO_UPDATE IS NULL THEN '''' ELSE CTACT_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

