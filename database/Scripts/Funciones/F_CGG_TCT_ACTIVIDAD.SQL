/**
* FUNCION SII.F_CGG_TCT_ACTIVIDAD_UPDATE
* ACTUALIZA UN REGISTRO EN LA TABLA sii.CGG_TCT_ACTIVIDAD
* @param IN_CTACT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ACTIVIDADES
* @param IN_CTACT_NOMBRE NOMBRE DE LA ACTIVIDAD  A REALIZAR
* @param IN_CTACT_ESTADO ESTADO DEL REGISTRO
* @param IN_CTACT_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VOID
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_ACTIVIDAD_UPDATE(
IN IN_CTACT_CODIGO SII.CGG_TCT_ACTIVIDAD.CTACT_CODIGO%TYPE, 
IN IN_CTACT_NOMBRE SII.CGG_TCT_ACTIVIDAD.CTACT_NOMBRE%TYPE, 
IN IN_CTACT_ESTADO SII.CGG_TCT_ACTIVIDAD.CTACT_ESTADO%TYPE, 
IN IN_CTACT_USUARIO_UPDATE SII.CGG_TCT_ACTIVIDAD.CTACT_USUARIO_UPDATE%TYPE
)RETURNS VOID AS
$$
DECLARE
TMP_COUNT INT;
BEGIN
SELECT COUNT (UPPER(IN_CTACT_NOMBRE)) INTO TMP_COUNT FROM SII.CGG_TCT_ACTIVIDAD WHERE UPPER(CTACT_NOMBRE) = UPPER(IN_CTACT_NOMBRE) AND CTACT_ESTADO = TRUE;
	IF (TMP_COUNT > 0) THEN 
		RAISE EXCEPTION 'La Actividad ya se encuentra registrada.';
	END IF;
	UPDATE SII.CGG_TCT_ACTIVIDAD SET
		CTACT_NOMBRE = IN_CTACT_NOMBRE, 
		CTACT_ESTADO = IN_CTACT_ESTADO, 
		CTACT_FECHA_UPDATE = CURRENT_TIMESTAMP, 
		CTACT_USUARIO_UPDATE = IN_CTACT_USUARIO_UPDATE
	WHERE
		CTACT_CODIGO = IN_CTACT_CODIGO;
END$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

