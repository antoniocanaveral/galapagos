/**
* FUNCION SII.F_CGG_TCT_ACTIVIDAD_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_ACTIVIDAD
* @param IN_CTACT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ACTIVIDADES
* @param IN_CTACT_NOMBRE NOMBRE DE LA ACTIVIDAD  A REALIZAR
* @param IN_CTACT_ESTADO ESTADO DEL REGISTRO
* @param IN_CTACT_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTACT_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_ACTIVIDAD_INSERT(
IN IN_CTACT_CODIGO SII.CGG_TCT_ACTIVIDAD.CTACT_CODIGO%TYPE, 
IN IN_CTACT_NOMBRE SII.CGG_TCT_ACTIVIDAD.CTACT_NOMBRE%TYPE, 
IN IN_CTACT_ESTADO SII.CGG_TCT_ACTIVIDAD.CTACT_ESTADO%TYPE, 
IN IN_CTACT_USUARIO_INSERT SII.CGG_TCT_ACTIVIDAD.CTACT_USUARIO_INSERT%TYPE, 
IN IN_CTACT_USUARIO_UPDATE SII.CGG_TCT_ACTIVIDAD.CTACT_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
TMP_COUNT INT;
BEGIN
	SELECT COUNT (IN_CTACT_NOMBRE) INTO TMP_COUNT FROM SII.CGG_TCT_ACTIVIDAD WHERE UPPER(CTACT_NOMBRE) = UPPER(IN_CTACT_NOMBRE) AND CTACT_ESTADO = TRUE;
	IF (TMP_COUNT > 0) THEN 
		RAISE EXCEPTION 'La Actividad ya se encuentra registrada.';
	END IF;
	
	IF (UPPER(IN_CTACT_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_ACTIVIDAD','CTACT_CODIGO','CTACT') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTACT_CODIGO;
	END IF;
	
	INSERT INTO SII.CGG_TCT_ACTIVIDAD(
		CTACT_CODIGO, 
		CTACT_NOMBRE, 
		CTACT_ESTADO, 
		CTACT_FECHA_INSERT, 
		CTACT_USUARIO_INSERT, 
		CTACT_FECHA_UPDATE, 
		CTACT_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTACT_NOMBRE, 
		IN_CTACT_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTACT_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTACT_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

