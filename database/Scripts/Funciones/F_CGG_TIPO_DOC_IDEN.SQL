/**
* FUNCION SII.F_CGG_TIPO_DOC_IDEN_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TIPO_DOC_IDEN
* @param IN_CTDID_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TIPO DE DOCUMENTO DE IDENTIFICACION
* @param IN_CTDID_NOMBRE NOMBRE DEL DOCUMENTO
* @param IN_CTDID_ESTADO ESTADO DEL REGISTRO
* @param IN_CTDID_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTDID_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TIPO_DOC_IDEN_INSERT(
IN IN_CTDID_CODIGO SII.CGG_TIPO_DOC_IDEN.CTDID_CODIGO%TYPE, 
IN IN_CTDID_NOMBRE SII.CGG_TIPO_DOC_IDEN.CTDID_NOMBRE%TYPE, 
IN IN_CTDID_ESTADO SII.CGG_TIPO_DOC_IDEN.CTDID_ESTADO%TYPE, 
IN IN_CTDID_USUARIO_INSERT SII.CGG_TIPO_DOC_IDEN.CTDID_USUARIO_INSERT%TYPE, 
IN IN_CTDID_USUARIO_UPDATE SII.CGG_TIPO_DOC_IDEN.CTDID_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CTDID_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TIPO_DOC_IDEN','CTDID_CODIGO','CTDID') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTDID_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TIPO_DOC_IDEN(
		CTDID_CODIGO, 
		CTDID_NOMBRE, 
		CTDID_ESTADO, 
		CTDID_FECHA_INSERT, 
		CTDID_USUARIO_INSERT, 
		CTDID_FECHA_UPDATE, 
		CTDID_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTDID_NOMBRE, 
		IN_CTDID_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTDID_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTDID_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

