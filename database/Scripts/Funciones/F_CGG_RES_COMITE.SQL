/**
* FUNCION SII.F_CGG_RES_COMITE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_COMITE
* @param IN_CRCOM_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CRCOM_NOMBRE NOMBRE DEL COMITE
* @param IN_CRCOM_DESCRIPCION BREVE DESCRIPCION DEL COMITE 
* @param IN_CRCOM_ABREVIATURA ABREVIIATURA DEL NOMBRE DE COMITE UTILIZADO PARA EL NUMERO DE RESOLUCIONES.
* @param IN_CRCOM_ESTADO ESTADO DEL REGISTRO
* @param IN_CRCOM_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRCOM_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_COMITE_INSERT(
IN IN_CRCOM_CODIGO SII.CGG_RES_COMITE.CRCOM_CODIGO%TYPE,
IN IN_CRCOM_NOMBRE SII.CGG_RES_COMITE.CRCOM_NOMBRE%TYPE,
IN IN_CRCOM_DESCRIPCION SII.CGG_RES_COMITE.CRCOM_DESCRIPCION%TYPE,
IN IN_CRCOM_ABREVIATURA SII.CGG_RES_COMITE.CRCOM_ABREVIATURA%TYPE,
IN IN_CRCOM_ESTADO SII.CGG_RES_COMITE.CRCOM_ESTADO%TYPE,
IN IN_CRCOM_USUARIO_INSERT SII.CGG_RES_COMITE.CRCOM_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRCOM_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_COMITE','CRCOM_CODIGO','CRCOM') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRCOM_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_COMITE(
		CRCOM_CODIGO, 
		CRCOM_NOMBRE, 
		CRCOM_DESCRIPCION, 
		CRCOM_ABREVIATURA, 
		CRCOM_ESTADO, 
		CRCOM_FECHA_INSERT, 
		CRCOM_USUARIO_INSERT, 
		CRCOM_FECHA_UPDATE, 
		CRCOM_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRCOM_NOMBRE, 
		IN_CRCOM_DESCRIPCION, 
		IN_CRCOM_ABREVIATURA, 
		IN_CRCOM_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRCOM_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRCOM_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

