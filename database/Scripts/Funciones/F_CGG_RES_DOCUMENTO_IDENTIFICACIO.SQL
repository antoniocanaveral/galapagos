/*
* FUNCION SII.F_CGG_RES_DOCUMENTO_IDENTIFICACIO__SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_RES_DOCUMENTO_IDENTIFICACIO
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_DOCUMENTO_IDENTIFICACIO_SELECT(
IN IN_USER_NAME varchar(50),
IN IN_START_INDEX int,
IN IN_LIMIT int,
IN IN_SORT_FIELD_NAME varchar,
IN IN_DIRECTION varchar,
IN IN_FIND_TEXT text
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CRDID_CODIGO, 
		CRDID_DESCRIPCION, 
		CRDID_ESTADO, 
		CRDID_FECHA_INSERT, 
		CRDID_USUARIO_INSERT, 
		CRDID_FECHA_UPDATE, 
		CRDID_USUARIO_UPDATE
	FROM SII.CGG_RES_DOCUMENTO_IDENTIFICACIO
	WHERE CRDID_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CRDID_CODIGO IS NULL THEN '''' ELSE CRDID_CODIGO END ||'' ''||CASE WHEN CRDID_DESCRIPCION IS NULL THEN '''' ELSE CRDID_DESCRIPCION END ||'' ''||CASE WHEN CRDID_ESTADO IS NULL THEN FALSE ELSE CRDID_ESTADO END ||'' ''||CASE WHEN CRDID_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CRDID_FECHA_INSERT END ||'' ''||CASE WHEN CRDID_USUARIO_INSERT IS NULL THEN '''' ELSE CRDID_USUARIO_INSERT END ||'' ''||CASE WHEN CRDID_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CRDID_FECHA_UPDATE END ||'' ''||CASE WHEN CRDID_USUARIO_UPDATE IS NULL THEN '''' ELSE CRDID_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

