/**
* FUNCION SII.F_CGG_RES_DOCUMENTO_IDENTIFICACIO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_DOCUMENTO_IDENTIFICACIO
* @param IN_CRDID_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE DOCUMENTO DE IDENTIFICACION
* @param IN_CRDID_DESCRIPCION DESCRIPCION DEL DOCUMENTO DE IDENTIFICACION
* @param IN_CRDID_ESTADO ESTADO DEL REGISTRO
* @param IN_CRDID_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRDID_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_DOCUMENTO_IDENTIFICACIO_INSERT(
IN IN_CRDID_CODIGO SII.CGG_RES_DOCUMENTO_IDENTIFICACIO.CRDID_CODIGO%TYPE, 
IN IN_CRDID_DESCRIPCION SII.CGG_RES_DOCUMENTO_IDENTIFICACIO.CRDID_DESCRIPCION%TYPE, 
IN IN_CRDID_ESTADO SII.CGG_RES_DOCUMENTO_IDENTIFICACIO.CRDID_ESTADO%TYPE, 
IN IN_CRDID_USUARIO_INSERT SII.CGG_RES_DOCUMENTO_IDENTIFICACIO.CRDID_USUARIO_INSERT%TYPE, 
IN IN_CRDID_USUARIO_UPDATE SII.CGG_RES_DOCUMENTO_IDENTIFICACIO.CRDID_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRDID_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_DOCUMENTO_IDENTIFICACIO','CRDID_CODIGO','CRDID') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRDID_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_DOCUMENTO_IDENTIFICACIO(
		CRDID_CODIGO, 
		CRDID_DESCRIPCION, 
		CRDID_ESTADO, 
		CRDID_FECHA_INSERT, 
		CRDID_USUARIO_INSERT, 
		CRDID_FECHA_UPDATE, 
		CRDID_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRDID_DESCRIPCION, 
		IN_CRDID_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRDID_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRDID_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

