/**
* FUNCION SII.F_CGG_RES_CARGO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_CARGO
* @param IN_CRCRG_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CRCRG_NOMBRE NOMBRE DEL CARGO
* @param IN_CRCRG_ESTADO ESTADO DEL REGISTRO
* @param IN_CRCRG_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRCRG_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @param IN_CRSEC_CODIGO IDENTIFICATIVO UNICO DE SECCION.
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_CARGO_INSERT(
IN IN_CRCRG_CODIGO SII.CGG_RES_CARGO.CRCRG_CODIGO%TYPE,
IN IN_CRCRG_NOMBRE SII.CGG_RES_CARGO.CRCRG_NOMBRE%TYPE,
IN IN_CRCRG_ESTADO SII.CGG_RES_CARGO.CRCRG_ESTADO%TYPE,
IN IN_CRCRG_USUARIO_INSERT SII.CGG_RES_CARGO.CRCRG_USUARIO_INSERT%TYPE,
IN IN_CRSEC_CODIGO SII.CGG_RES_CARGO.CRSEC_CODIGO%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRCRG_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_CARGO','CRCRG_CODIGO','CRCRG') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRCRG_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_CARGO(
		CRCRG_CODIGO, 
		CRCRG_NOMBRE, 
		CRCRG_ESTADO, 
		CRCRG_FECHA_INSERT, 
		CRCRG_USUARIO_INSERT, 
		CRCRG_FECHA_UPDATE, 
		CRCRG_USUARIO_UPDATE, 
		CRSEC_CODIGO
	) VALUES (
		OUT_KEY, 
		IN_CRCRG_NOMBRE, 
		IN_CRCRG_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRCRG_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRCRG_USUARIO_INSERT, 
		IN_CRSEC_CODIGO
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

