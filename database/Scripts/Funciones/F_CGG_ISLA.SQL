/**
* FUNCION SII.F_CGG_ISLA_SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_ISLA.
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_ISLA_SELECT(
IN IN_USER_NAME VARCHAR,
IN IN_START_INDEX INT,
IN IN_LIMIT INT,
IN IN_SORT_FIELD_NAME VARCHAR,
IN IN_DIRECTION VARCHAR,
IN IN_FIND_TEXT TEXT
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CISLA_CODIGO, 
		CCTN_CODIGO, 
		CISLA_NOMBRE, 
		CISLA_ABREVIATURA, 
		CISLA_INDICE, 
		CISLA_ESTADO, 
		CISLA_FECHA_INSERT, 
		CISLA_USUARIO_INSERT, 
		CISLA_FECHA_UPDATE, 
		CISLA_USUARIO_UPDATE
	FROM SII.CGG_ISLA
	WHERE CISLA_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CISLA_NOMBRE, '''')||'' ''||COALESCE(CISLA_ABREVIATURA, '''')||'' ''||COALESCE(CISLA_INDICE, 0)||'' ''||COALESCE(CISLA_ESTADO, FALSE)||'' ''||COALESCE(CISLA_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CISLA_USUARIO_INSERT, '''')||'' ''||COALESCE(CISLA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CISLA_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

