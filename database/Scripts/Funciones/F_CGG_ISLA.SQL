/**
* FUNCION SII.F_CGG_ISLA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_ISLA.
* @param IN_CISLA_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ISLA O CIUDAD
* @param IN_CCTN_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CISLA_NOMBRE NOMBRE DE LA ISLA O DE CIUDAD
* @param IN_CISLA_ABREVIATURA ABREVIATURA DEL NOMBRE DE LA ISLA
* @param IN_CISLA_INDICE INDICE NUMERICO PARA GENERACION DE EXPEDIENTE DE PERSONA O NUMERO DE CARNET DE RESIDENCIA
* @param IN_CISLA_ESTADO ESTADO DEL REGISTRO
* @param IN_CISLA_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CISLA_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_ISLA_INSERT(
IN IN_CISLA_CODIGO SII.CGG_ISLA.CISLA_CODIGO%TYPE, 
IN IN_CCTN_CODIGO SII.CGG_ISLA.CCTN_CODIGO%TYPE, 
IN IN_CISLA_NOMBRE SII.CGG_ISLA.CISLA_NOMBRE%TYPE, 
IN IN_CISLA_ABREVIATURA SII.CGG_ISLA.CISLA_ABREVIATURA%TYPE, 
IN IN_CISLA_INDICE SII.CGG_ISLA.CISLA_INDICE%TYPE, 
IN IN_CISLA_ESTADO SII.CGG_ISLA.CISLA_ESTADO%TYPE, 
IN IN_CISLA_USUARIO_INSERT SII.CGG_ISLA.CISLA_USUARIO_INSERT%TYPE, 
IN IN_CISLA_USUARIO_UPDATE SII.CGG_ISLA.CISLA_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CISLA_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_ISLA','CISLA_CODIGO','CISLA') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CISLA_CODIGO;
	END IF;
	INSERT INTO SII.CGG_ISLA(
		CISLA_CODIGO, 
		CCTN_CODIGO, 
		CISLA_NOMBRE, 
		CISLA_ABREVIATURA, 
		CISLA_INDICE, 
		CISLA_ESTADO, 
		CISLA_FECHA_INSERT, 
		CISLA_USUARIO_INSERT, 
		CISLA_FECHA_UPDATE, 
		CISLA_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CCTN_CODIGO, 
		IN_CISLA_NOMBRE, 
		IN_CISLA_ABREVIATURA, 
		IN_CISLA_INDICE, 
		IN_CISLA_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CISLA_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CISLA_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

