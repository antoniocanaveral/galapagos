CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_GRUPO_HOSPEDAJE_INSERT(
IN IN_CTGHJ_CODIGO SII.CGG_TCT_GRUPO_HOSPEDAJE.CTGHJ_CODIGO%TYPE, 
IN IN_CTGTR_CODIGO SII.CGG_TCT_GRUPO_HOSPEDAJE.CTGTR_CODIGO%TYPE, 
