		SELECT SII.F_KEYGEN('CGG_TCT_GRUPO_HOSPEDAJE','CTGHJ_CODIGO','CTGHJ') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTGHJ_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_GRUPO_HOSPEDAJE(
		CTGHJ_CODIGO, 
		CTGTR_CODIGO, 
		CTTHJ_CODIGO, 
		CTGHJ_LUGAR_HOSPEDAJE, 
		CTGHJ_ESTADO, 
		CTGHJ_FECHA_INSERT, 
		CTGHJ_USUARIO_INSERT, 
		CTGHJ_FECHA_UPDATE, 
		CTGHJ_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTGTR_CODIGO, 
		IN_CTTHJ_CODIGO, 
		IN_CTGHJ_LUGAR_HOSPEDAJE, 
		IN_CTGHJ_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTGHJ_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTGHJ_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

