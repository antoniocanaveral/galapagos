/**
* FUNCION SII.TRG_KG_CGG_DHU_ASISTENTE_CURSO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_ASISTENTE_CURSO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_ASISTENTE_CURSO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDASC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDASC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDASC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDASC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDASC_CODIGO>'||COALESCE(OLD.CDASC_CODIGO, '')||'</CDASC_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CDCUR_CODIGO>'||COALESCE(OLD.CDCUR_CODIGO, '')||'</CDCUR_CODIGO>'||
			'<CSCTP_CODIGO>'||COALESCE(OLD.CSCTP_CODIGO, '')||'</CSCTP_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CDASC_ACTIVIDAD_REALIZADA>'||COALESCE(OLD.CDASC_ACTIVIDAD_REALIZADA, '')||'</CDASC_ACTIVIDAD_REALIZADA>'||
			'<CDASC_NOTA_FINAL>'||COALESCE(OLD.CDASC_NOTA_FINAL, 0)||'</CDASC_NOTA_FINAL>'||
			'<CDASC_NUMERO_CERTIFICADO>'||COALESCE(OLD.CDASC_NUMERO_CERTIFICADO, '')||'</CDASC_NUMERO_CERTIFICADO>'||
			'<CDASC_FECHA_EMISION_CERTIFICADO>'||COALESCE(OLD.CDASC_FECHA_EMISION_CERTIFICADO, CURRENT_TIMESTAMP)||'</CDASC_FECHA_EMISION_CERTIFICADO>'||
			'<CDASC_ESTADO_INSCRIPCION>'||COALESCE(OLD.CDASC_ESTADO_INSCRIPCION, 0)||'</CDASC_ESTADO_INSCRIPCION>'||
			'<CDASC_APROBADO>'||COALESCE(OLD.CDASC_APROBADO, 0)||'</CDASC_APROBADO>'||
			'<CDASC_ESTADO>'||COALESCE(OLD.CDASC_ESTADO, FALSE)||'</CDASC_ESTADO>'||
			'<CDASC_FECHA_INSERT>'||COALESCE(OLD.CDASC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDASC_FECHA_INSERT>'||
			'<CDASC_USUARIO_INSERT>'||COALESCE(OLD.CDASC_USUARIO_INSERT, '')||'</CDASC_USUARIO_INSERT>'||
			'<CDASC_FECHA_UPDATE>'||COALESCE(OLD.CDASC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDASC_FECHA_UPDATE>'||
			'<CDASC_USUARIO_UPDATE>'||COALESCE(OLD.CDASC_USUARIO_UPDATE, '')||'</CDASC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDASC_CODIGO>'||COALESCE(NEW.CDASC_CODIGO, '')||'</CDASC_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CDCUR_CODIGO>'||COALESCE(NEW.CDCUR_CODIGO, '')||'</CDCUR_CODIGO>'||
			'<CSCTP_CODIGO>'||COALESCE(NEW.CSCTP_CODIGO, '')||'</CSCTP_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CDASC_ACTIVIDAD_REALIZADA>'||COALESCE(NEW.CDASC_ACTIVIDAD_REALIZADA, '')||'</CDASC_ACTIVIDAD_REALIZADA>'||
			'<CDASC_NOTA_FINAL>'||COALESCE(NEW.CDASC_NOTA_FINAL, 0)||'</CDASC_NOTA_FINAL>'||
			'<CDASC_NUMERO_CERTIFICADO>'||COALESCE(NEW.CDASC_NUMERO_CERTIFICADO, '')||'</CDASC_NUMERO_CERTIFICADO>'||
			'<CDASC_FECHA_EMISION_CERTIFICADO>'||COALESCE(NEW.CDASC_FECHA_EMISION_CERTIFICADO, CURRENT_TIMESTAMP)||'</CDASC_FECHA_EMISION_CERTIFICADO>'||
			'<CDASC_ESTADO_INSCRIPCION>'||COALESCE(NEW.CDASC_ESTADO_INSCRIPCION, 0)||'</CDASC_ESTADO_INSCRIPCION>'||
			'<CDASC_APROBADO>'||COALESCE(NEW.CDASC_APROBADO, 0)||'</CDASC_APROBADO>'||
			'<CDASC_ESTADO>'||COALESCE(NEW.CDASC_ESTADO, FALSE)||'</CDASC_ESTADO>'||
			'<CDASC_FECHA_INSERT>'||COALESCE(NEW.CDASC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDASC_FECHA_INSERT>'||
			'<CDASC_USUARIO_INSERT>'||COALESCE(NEW.CDASC_USUARIO_INSERT, '')||'</CDASC_USUARIO_INSERT>'||
			'<CDASC_FECHA_UPDATE>'||COALESCE(NEW.CDASC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDASC_FECHA_UPDATE>'||
			'<CDASC_USUARIO_UPDATE>'||COALESCE(NEW.CDASC_USUARIO_UPDATE, '')||'</CDASC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_ASISTENTE_CURSO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_ASISTENTE_CURSO
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_ASISTENTE_CURSO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_ASISTENTE_CURSO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_ASISTENTE_CURSO();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_ASISTENTE_CURSO ON SII.CGG_DHU_ASISTENTE_CURSO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_ASISTENTE_CURSO DISABLE TRIGGER TRG_AUD_CGG_DHU_ASISTENTE_CURSO;

