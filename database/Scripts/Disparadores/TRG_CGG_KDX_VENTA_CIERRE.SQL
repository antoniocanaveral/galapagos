/**
* FUNCION SII.TRG_KG_CGG_KDX_VENTA_CIERRE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_VENTA_CIERRE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_VENTA_CIERRE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKVTC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKVTC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKVTC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKVTC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKVTC_CODIGO>'||COALESCE(OLD.CKVTC_CODIGO, '')||'</CKVTC_CODIGO>'||
			'<CKVNT_CODIGO>'||COALESCE(OLD.CKVNT_CODIGO, '')||'</CKVNT_CODIGO>'||
			'<CKCRR_CODIGO>'||COALESCE(OLD.CKCRR_CODIGO, '')||'</CKCRR_CODIGO>'||
			'<CKVTC_ESTADO>'||COALESCE(OLD.CKVTC_ESTADO, FALSE)||'</CKVTC_ESTADO>'||
			'<CKVTC_FECHA_INSERT>'||COALESCE(OLD.CKVTC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKVTC_FECHA_INSERT>'||
			'<CKVTC_USUARIO_INSERT>'||COALESCE(OLD.CKVTC_USUARIO_INSERT, '')||'</CKVTC_USUARIO_INSERT>'||
			'<CKVTC_FECHA_UPDATE>'||COALESCE(OLD.CKVTC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKVTC_FECHA_UPDATE>'||
			'<CKVTC_USUARIO_UPDATE>'||COALESCE(OLD.CKVTC_USUARIO_UPDATE, '')||'</CKVTC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKVTC_CODIGO>'||COALESCE(NEW.CKVTC_CODIGO, '')||'</CKVTC_CODIGO>'||
			'<CKVNT_CODIGO>'||COALESCE(NEW.CKVNT_CODIGO, '')||'</CKVNT_CODIGO>'||
			'<CKCRR_CODIGO>'||COALESCE(NEW.CKCRR_CODIGO, '')||'</CKCRR_CODIGO>'||
			'<CKVTC_ESTADO>'||COALESCE(NEW.CKVTC_ESTADO, FALSE)||'</CKVTC_ESTADO>'||
			'<CKVTC_FECHA_INSERT>'||COALESCE(NEW.CKVTC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKVTC_FECHA_INSERT>'||
			'<CKVTC_USUARIO_INSERT>'||COALESCE(NEW.CKVTC_USUARIO_INSERT, '')||'</CKVTC_USUARIO_INSERT>'||
			'<CKVTC_FECHA_UPDATE>'||COALESCE(NEW.CKVTC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKVTC_FECHA_UPDATE>'||
			'<CKVTC_USUARIO_UPDATE>'||COALESCE(NEW.CKVTC_USUARIO_UPDATE, '')||'</CKVTC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_VENTA_CIERRE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_VENTA_CIERRE
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_VENTA_CIERRE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_VENTA_CIERRE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_VENTA_CIERRE();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_VENTA_CIERRE ON SII.CGG_KDX_VENTA_CIERRE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_VENTA_CIERRE DISABLE TRIGGER TRG_AUD_CGG_KDX_VENTA_CIERRE;

