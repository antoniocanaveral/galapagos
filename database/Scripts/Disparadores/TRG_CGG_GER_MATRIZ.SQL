/**
* FUNCION SII.TRG_KG_CGG_GER_MATRIZ. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GER_MATRIZ
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GER_MATRIZ() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGGMA_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGGMA_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGGMA_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGGMA_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGGMA_CODIGO>'||COALESCE(OLD.CGGMA_CODIGO, '')||'</CGGMA_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(OLD.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CGGIN_CODIGO>'||COALESCE(OLD.CGGIN_CODIGO, '')||'</CGGIN_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CGGMA_MES>'||COALESCE(OLD.CGGMA_MES, '')||'</CGGMA_MES>'||
			'<CGGMA_ANIO>'||COALESCE(OLD.CGGMA_ANIO, '')||'</CGGMA_ANIO>'||
			'<CGGMA_VALOR>'||COALESCE(OLD.CGGMA_VALOR, 0)||'</CGGMA_VALOR>'||
			'<CGGMA_DESCRIPCION>'||COALESCE(OLD.CGGMA_DESCRIPCION, '')||'</CGGMA_DESCRIPCION>'||
			'<CGGMA_ACTUALIZACIONES>'||COALESCE(OLD.CGGMA_ACTUALIZACIONES, 0)||'</CGGMA_ACTUALIZACIONES>'||
			'<CGGMA_ESTADO>'||COALESCE(OLD.CGGMA_ESTADO, FALSE)||'</CGGMA_ESTADO>'||
			'<CGGMA_FECHA_INSERT>'||COALESCE(OLD.CGGMA_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGGMA_FECHA_INSERT>'||
			'<CGGMA_USUARIO_INSERT>'||COALESCE(OLD.CGGMA_USUARIO_INSERT, '')||'</CGGMA_USUARIO_INSERT>'||
			'<CGGMA_FECHA_UPDATE>'||COALESCE(OLD.CGGMA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGGMA_FECHA_UPDATE>'||
			'<CGGMA_USUARIO_UPDATE>'||COALESCE(OLD.CGGMA_USUARIO_UPDATE, '')||'</CGGMA_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGGMA_CODIGO>'||COALESCE(NEW.CGGMA_CODIGO, '')||'</CGGMA_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(NEW.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CGGIN_CODIGO>'||COALESCE(NEW.CGGIN_CODIGO, '')||'</CGGIN_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CGGMA_MES>'||COALESCE(NEW.CGGMA_MES, '')||'</CGGMA_MES>'||
			'<CGGMA_ANIO>'||COALESCE(NEW.CGGMA_ANIO, '')||'</CGGMA_ANIO>'||
			'<CGGMA_VALOR>'||COALESCE(NEW.CGGMA_VALOR, 0)||'</CGGMA_VALOR>'||
			'<CGGMA_DESCRIPCION>'||COALESCE(NEW.CGGMA_DESCRIPCION, '')||'</CGGMA_DESCRIPCION>'||
			'<CGGMA_ACTUALIZACIONES>'||COALESCE(NEW.CGGMA_ACTUALIZACIONES, 0)||'</CGGMA_ACTUALIZACIONES>'||
			'<CGGMA_ESTADO>'||COALESCE(NEW.CGGMA_ESTADO, FALSE)||'</CGGMA_ESTADO>'||
			'<CGGMA_FECHA_INSERT>'||COALESCE(NEW.CGGMA_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGGMA_FECHA_INSERT>'||
			'<CGGMA_USUARIO_INSERT>'||COALESCE(NEW.CGGMA_USUARIO_INSERT, '')||'</CGGMA_USUARIO_INSERT>'||
			'<CGGMA_FECHA_UPDATE>'||COALESCE(NEW.CGGMA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGGMA_FECHA_UPDATE>'||
			'<CGGMA_USUARIO_UPDATE>'||COALESCE(NEW.CGGMA_USUARIO_UPDATE, '')||'</CGGMA_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GER_MATRIZ. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GER_MATRIZ
*/
CREATE TRIGGER TRG_AUD_CGG_GER_MATRIZ AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GER_MATRIZ FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GER_MATRIZ();
COMMENT ON TRIGGER TRG_AUD_CGG_GER_MATRIZ ON SII.CGG_GER_MATRIZ IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GER_MATRIZ DISABLE TRIGGER TRG_AUD_CGG_GER_MATRIZ;

