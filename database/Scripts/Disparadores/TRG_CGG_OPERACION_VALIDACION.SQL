/**
* FUNCION SII.TRG_KG_CGG_OPERACION_VALIDACION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_OPERACION_VALIDACION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_OPERACION_VALIDACION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.COPVL_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.COPVL_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.COPVL_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.COPVL_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<COPVL_CODIGO>'||COALESCE(OLD.COPVL_CODIGO, '')||'</COPVL_CODIGO>'||
			'<COPVL_DESCRIPCION>'||COALESCE(OLD.COPVL_DESCRIPCION, '')||'</COPVL_DESCRIPCION>'||
			'<COPVL_FORMULARIO>'||COALESCE(OLD.COPVL_FORMULARIO, '')||'</COPVL_FORMULARIO>'||
			'<COPVL_ESTADO>'||COALESCE(OLD.COPVL_ESTADO, FALSE)||'</COPVL_ESTADO>'||
			'<COPVL_FECHA_INSERT>'||COALESCE(OLD.COPVL_FECHA_INSERT, CURRENT_TIMESTAMP)||'</COPVL_FECHA_INSERT>'||
			'<COPVL_USUARIO_INSERT>'||COALESCE(OLD.COPVL_USUARIO_INSERT, '')||'</COPVL_USUARIO_INSERT>'||
			'<COPVL_FECHA_UPDATE>'||COALESCE(OLD.COPVL_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</COPVL_FECHA_UPDATE>'||
			'<COPVL_USUARIO_UPDATE>'||COALESCE(OLD.COPVL_USUARIO_UPDATE, '')||'</COPVL_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<COPVL_CODIGO>'||COALESCE(NEW.COPVL_CODIGO, '')||'</COPVL_CODIGO>'||
			'<COPVL_DESCRIPCION>'||COALESCE(NEW.COPVL_DESCRIPCION, '')||'</COPVL_DESCRIPCION>'||
			'<COPVL_FORMULARIO>'||COALESCE(NEW.COPVL_FORMULARIO, '')||'</COPVL_FORMULARIO>'||
			'<COPVL_ESTADO>'||COALESCE(NEW.COPVL_ESTADO, FALSE)||'</COPVL_ESTADO>'||
			'<COPVL_FECHA_INSERT>'||COALESCE(NEW.COPVL_FECHA_INSERT, CURRENT_TIMESTAMP)||'</COPVL_FECHA_INSERT>'||
			'<COPVL_USUARIO_INSERT>'||COALESCE(NEW.COPVL_USUARIO_INSERT, '')||'</COPVL_USUARIO_INSERT>'||
			'<COPVL_FECHA_UPDATE>'||COALESCE(NEW.COPVL_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</COPVL_FECHA_UPDATE>'||
			'<COPVL_USUARIO_UPDATE>'||COALESCE(NEW.COPVL_USUARIO_UPDATE, '')||'</COPVL_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_OPERACION_VALIDACION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_OPERACION_VALIDACION
*/
CREATE TRIGGER TRG_AUD_CGG_OPERACION_VALIDACION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_OPERACION_VALIDACION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_OPERACION_VALIDACION();
COMMENT ON TRIGGER TRG_AUD_CGG_OPERACION_VALIDACION ON SII.CGG_OPERACION_VALIDACION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_OPERACION_VALIDACION DISABLE TRIGGER TRG_AUD_CGG_OPERACION_VALIDACION;

