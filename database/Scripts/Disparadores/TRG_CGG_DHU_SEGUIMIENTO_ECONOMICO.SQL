/**
* FUNCION SII.TRG_KG_CGG_DHU_SEGUIMIENTO_ECONOMICO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_SEGUIMIENTO_ECONOMICO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_SEGUIMIENTO_ECONOMICO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDSEC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDSEC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDSEC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDSEC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDSEC_CODIGO>'||COALESCE(OLD.CDSEC_CODIGO, '')||'</CDSEC_CODIGO>'||
			'<CDTCP_CODIGO>'||COALESCE(OLD.CDTCP_CODIGO, '')||'</CDTCP_CODIGO>'||
			'<CDACD_CODIGO>'||COALESCE(OLD.CDACD_CODIGO, '')||'</CDACD_CODIGO>'||
			'<CDSEC_CODIGO_TRANSFERENCIA>'||COALESCE(OLD.CDSEC_CODIGO_TRANSFERENCIA, '')||'</CDSEC_CODIGO_TRANSFERENCIA>'||
			'<CDSEC_VALOR>'||COALESCE(OLD.CDSEC_VALOR, 0)||'</CDSEC_VALOR>'||
			'<CDSEC_FECHA_TRANSFERENCIA>'||COALESCE(OLD.CDSEC_FECHA_TRANSFERENCIA, CURRENT_TIMESTAMP)||'</CDSEC_FECHA_TRANSFERENCIA>'||
			'<CDSEC_CODIGO_TRANSF_IECE>'||COALESCE(OLD.CDSEC_CODIGO_TRANSF_IECE, '')||'</CDSEC_CODIGO_TRANSF_IECE>'||
			'<CDSEC_VALOR_IECE>'||COALESCE(OLD.CDSEC_VALOR_IECE, 0)||'</CDSEC_VALOR_IECE>'||
			'<CDSEC_FECHA_TRANSF_IECE>'||COALESCE(OLD.CDSEC_FECHA_TRANSF_IECE, CURRENT_TIMESTAMP)||'</CDSEC_FECHA_TRANSF_IECE>'||
			'<CDSEC_CIERRE_DESEMBOLSO>'||COALESCE(OLD.CDSEC_CIERRE_DESEMBOLSO, FALSE)||'</CDSEC_CIERRE_DESEMBOLSO>'||
			'<CDSEC_OBSERVACION>'||COALESCE(OLD.CDSEC_OBSERVACION, '')||'</CDSEC_OBSERVACION>'||
			'<CDSEC_ESTADO>'||COALESCE(OLD.CDSEC_ESTADO, FALSE)||'</CDSEC_ESTADO>'||
			'<CDSEC_FECHA_INSERT>'||COALESCE(OLD.CDSEC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDSEC_FECHA_INSERT>'||
			'<CDSEC_USUARIO_INSERT>'||COALESCE(OLD.CDSEC_USUARIO_INSERT, '')||'</CDSEC_USUARIO_INSERT>'||
			'<CDSEC_FECHA_UPDATE>'||COALESCE(OLD.CDSEC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDSEC_FECHA_UPDATE>'||
			'<CDSEC_USUARIO_UPDATE>'||COALESCE(OLD.CDSEC_USUARIO_UPDATE, '')||'</CDSEC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDSEC_CODIGO>'||COALESCE(NEW.CDSEC_CODIGO, '')||'</CDSEC_CODIGO>'||
			'<CDTCP_CODIGO>'||COALESCE(NEW.CDTCP_CODIGO, '')||'</CDTCP_CODIGO>'||
			'<CDACD_CODIGO>'||COALESCE(NEW.CDACD_CODIGO, '')||'</CDACD_CODIGO>'||
			'<CDSEC_CODIGO_TRANSFERENCIA>'||COALESCE(NEW.CDSEC_CODIGO_TRANSFERENCIA, '')||'</CDSEC_CODIGO_TRANSFERENCIA>'||
			'<CDSEC_VALOR>'||COALESCE(NEW.CDSEC_VALOR, 0)||'</CDSEC_VALOR>'||
			'<CDSEC_FECHA_TRANSFERENCIA>'||COALESCE(NEW.CDSEC_FECHA_TRANSFERENCIA, CURRENT_TIMESTAMP)||'</CDSEC_FECHA_TRANSFERENCIA>'||
			'<CDSEC_CODIGO_TRANSF_IECE>'||COALESCE(NEW.CDSEC_CODIGO_TRANSF_IECE, '')||'</CDSEC_CODIGO_TRANSF_IECE>'||
			'<CDSEC_VALOR_IECE>'||COALESCE(NEW.CDSEC_VALOR_IECE, 0)||'</CDSEC_VALOR_IECE>'||
			'<CDSEC_FECHA_TRANSF_IECE>'||COALESCE(NEW.CDSEC_FECHA_TRANSF_IECE, CURRENT_TIMESTAMP)||'</CDSEC_FECHA_TRANSF_IECE>'||
			'<CDSEC_CIERRE_DESEMBOLSO>'||COALESCE(NEW.CDSEC_CIERRE_DESEMBOLSO, FALSE)||'</CDSEC_CIERRE_DESEMBOLSO>'||
			'<CDSEC_OBSERVACION>'||COALESCE(NEW.CDSEC_OBSERVACION, '')||'</CDSEC_OBSERVACION>'||
			'<CDSEC_ESTADO>'||COALESCE(NEW.CDSEC_ESTADO, FALSE)||'</CDSEC_ESTADO>'||
			'<CDSEC_FECHA_INSERT>'||COALESCE(NEW.CDSEC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDSEC_FECHA_INSERT>'||
			'<CDSEC_USUARIO_INSERT>'||COALESCE(NEW.CDSEC_USUARIO_INSERT, '')||'</CDSEC_USUARIO_INSERT>'||
			'<CDSEC_FECHA_UPDATE>'||COALESCE(NEW.CDSEC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDSEC_FECHA_UPDATE>'||
			'<CDSEC_USUARIO_UPDATE>'||COALESCE(NEW.CDSEC_USUARIO_UPDATE, '')||'</CDSEC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_SEGUIMIENTO_ECONOMICO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_SEGUIMIENTO_ECONOMICO
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_ECONOMICO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_SEGUIMIENTO_ECONOMICO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_SEGUIMIENTO_ECONOMICO();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_ECONOMICO ON SII.CGG_DHU_SEGUIMIENTO_ECONOMICO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_SEGUIMIENTO_ECONOMICO DISABLE TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_ECONOMICO;

