/**
* FUNCION SII.TRG_KG_CGG_RES_NOVEDAD_NOTIFICACION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_NOVEDAD_NOTIFICACION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_NOVEDAD_NOTIFICACION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRNOV_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRNOV_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRNOV_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRNOV_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRNOV_CODIGO>'||COALESCE(OLD.CRNOV_CODIGO, '')||'</CRNOV_CODIGO>'||
			'<CRSEG_CODIGO>'||COALESCE(OLD.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CRNOV_FECHA_REGISTRO>'||COALESCE(OLD.CRNOV_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CRNOV_FECHA_REGISTRO>'||
			'<CRNOV_DESCRIPCION>'||COALESCE(OLD.CRNOV_DESCRIPCION, '')||'</CRNOV_DESCRIPCION>'||
			'<CRNOV_TIPO>'||COALESCE(OLD.CRNOV_TIPO, 0)||'</CRNOV_TIPO>'||
			'<CRNOV_ESTADO>'||COALESCE(OLD.CRNOV_ESTADO, FALSE)||'</CRNOV_ESTADO>'||
			'<CRNOV_FECHA_INSERT>'||COALESCE(OLD.CRNOV_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRNOV_FECHA_INSERT>'||
			'<CRNOV_USUARIO_INSERT>'||COALESCE(OLD.CRNOV_USUARIO_INSERT, '')||'</CRNOV_USUARIO_INSERT>'||
			'<CRNOV_FECHA_UPDATE>'||COALESCE(OLD.CRNOV_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRNOV_FECHA_UPDATE>'||
			'<CRNOV_USUARIO_UPDATE>'||COALESCE(OLD.CRNOV_USUARIO_UPDATE, '')||'</CRNOV_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRNOV_CODIGO>'||COALESCE(NEW.CRNOV_CODIGO, '')||'</CRNOV_CODIGO>'||
			'<CRSEG_CODIGO>'||COALESCE(NEW.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CRNOV_FECHA_REGISTRO>'||COALESCE(NEW.CRNOV_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CRNOV_FECHA_REGISTRO>'||
			'<CRNOV_DESCRIPCION>'||COALESCE(NEW.CRNOV_DESCRIPCION, '')||'</CRNOV_DESCRIPCION>'||
			'<CRNOV_TIPO>'||COALESCE(NEW.CRNOV_TIPO, 0)||'</CRNOV_TIPO>'||
			'<CRNOV_ESTADO>'||COALESCE(NEW.CRNOV_ESTADO, FALSE)||'</CRNOV_ESTADO>'||
			'<CRNOV_FECHA_INSERT>'||COALESCE(NEW.CRNOV_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRNOV_FECHA_INSERT>'||
			'<CRNOV_USUARIO_INSERT>'||COALESCE(NEW.CRNOV_USUARIO_INSERT, '')||'</CRNOV_USUARIO_INSERT>'||
			'<CRNOV_FECHA_UPDATE>'||COALESCE(NEW.CRNOV_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRNOV_FECHA_UPDATE>'||
			'<CRNOV_USUARIO_UPDATE>'||COALESCE(NEW.CRNOV_USUARIO_UPDATE, '')||'</CRNOV_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_NOVEDAD_NOTIFICACION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_NOVEDAD_NOTIFICACION
*/
CREATE TRIGGER TRG_AUD_CGG_RES_NOVEDAD_NOTIFICACION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_NOVEDAD_NOTIFICACION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_NOVEDAD_NOTIFICACION();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_NOVEDAD_NOTIFICACION ON SII.CGG_RES_NOVEDAD_NOTIFICACION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_NOVEDAD_NOTIFICACION DISABLE TRIGGER TRG_AUD_CGG_RES_NOVEDAD_NOTIFICACION;

