/**
* FUNCION SII.TRG_KG_CGG_RES_VOTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_VOTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_VOTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRVTO_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRVTO_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRVTO_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRVTO_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRVTO_CODIGO>'||COALESCE(OLD.CRVTO_CODIGO, '')||'</CRVTO_CODIGO>'||
			'<CRSMB_CODIGO>'||COALESCE(OLD.CRSMB_CODIGO, '')||'</CRSMB_CODIGO>'||
			'<CRRES_CODIGO>'||COALESCE(OLD.CRRES_CODIGO, '')||'</CRRES_CODIGO>'||
			'<CRVTO_VOTACION>'||COALESCE(OLD.CRVTO_VOTACION, 0)||'</CRVTO_VOTACION>'||
			'<CRVTO_OBSERVACION>'||COALESCE(OLD.CRVTO_OBSERVACION, '')||'</CRVTO_OBSERVACION>'||
			'<CRVTO_ESTADO>'||COALESCE(OLD.CRVTO_ESTADO, FALSE)||'</CRVTO_ESTADO>'||
			'<CRVTO_FECHA_INSERT>'||COALESCE(OLD.CRVTO_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRVTO_FECHA_INSERT>'||
			'<CRVTO_USUARIO_INSERT>'||COALESCE(OLD.CRVTO_USUARIO_INSERT, '')||'</CRVTO_USUARIO_INSERT>'||
			'<CRVTO_FECHA_UPDATE>'||COALESCE(OLD.CRVTO_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRVTO_FECHA_UPDATE>'||
			'<CRVTO_USUARIO_UPDATE>'||COALESCE(OLD.CRVTO_USUARIO_UPDATE, '')||'</CRVTO_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRVTO_CODIGO>'||COALESCE(NEW.CRVTO_CODIGO, '')||'</CRVTO_CODIGO>'||
			'<CRSMB_CODIGO>'||COALESCE(NEW.CRSMB_CODIGO, '')||'</CRSMB_CODIGO>'||
			'<CRRES_CODIGO>'||COALESCE(NEW.CRRES_CODIGO, '')||'</CRRES_CODIGO>'||
			'<CRVTO_VOTACION>'||COALESCE(NEW.CRVTO_VOTACION, 0)||'</CRVTO_VOTACION>'||
			'<CRVTO_OBSERVACION>'||COALESCE(NEW.CRVTO_OBSERVACION, '')||'</CRVTO_OBSERVACION>'||
			'<CRVTO_ESTADO>'||COALESCE(NEW.CRVTO_ESTADO, FALSE)||'</CRVTO_ESTADO>'||
			'<CRVTO_FECHA_INSERT>'||COALESCE(NEW.CRVTO_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRVTO_FECHA_INSERT>'||
			'<CRVTO_USUARIO_INSERT>'||COALESCE(NEW.CRVTO_USUARIO_INSERT, '')||'</CRVTO_USUARIO_INSERT>'||
			'<CRVTO_FECHA_UPDATE>'||COALESCE(NEW.CRVTO_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRVTO_FECHA_UPDATE>'||
			'<CRVTO_USUARIO_UPDATE>'||COALESCE(NEW.CRVTO_USUARIO_UPDATE, '')||'</CRVTO_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_VOTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_VOTO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_VOTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_VOTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_VOTO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_VOTO ON SII.CGG_RES_VOTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_VOTO DISABLE TRIGGER TRG_AUD_CGG_RES_VOTO;

