/**
* FUNCION SII.TRG_KG_CGG_SEC_USUARIO_CONTACTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SEC_USUARIO_CONTACTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_SEC_USUARIO_CONTACTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CSUCT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSUCT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CSUCT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSUCT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CSUCT_CODIGO>'||COALESCE(OLD.CSUCT_CODIGO, '')||'</CSUCT_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRTCO_CODIGO>'||COALESCE(OLD.CRTCO_CODIGO, '')||'</CRTCO_CODIGO>'||
			'<CSUCT_CONTACTO>'||COALESCE(OLD.CSUCT_CONTACTO, '')||'</CSUCT_CONTACTO>'||
			'<CSUCT_OBSERVACION>'||COALESCE(OLD.CSUCT_OBSERVACION, '')||'</CSUCT_OBSERVACION>'||
			'<CSUCT_ESTADO>'||COALESCE(OLD.CSUCT_ESTADO, FALSE)||'</CSUCT_ESTADO>'||
			'<CSUCT_FECHA_INSERT>'||COALESCE(OLD.CSUCT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSUCT_FECHA_INSERT>'||
			'<CSUCT_USUARIO_INSERT>'||COALESCE(OLD.CSUCT_USUARIO_INSERT, '')||'</CSUCT_USUARIO_INSERT>'||
			'<CSUCT_FECHA_UPDATE>'||COALESCE(OLD.CSUCT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSUCT_FECHA_UPDATE>'||
			'<CSUCT_USUARIO_UPDATE>'||COALESCE(OLD.CSUCT_USUARIO_UPDATE, '')||'</CSUCT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CSUCT_CODIGO>'||COALESCE(NEW.CSUCT_CODIGO, '')||'</CSUCT_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRTCO_CODIGO>'||COALESCE(NEW.CRTCO_CODIGO, '')||'</CRTCO_CODIGO>'||
			'<CSUCT_CONTACTO>'||COALESCE(NEW.CSUCT_CONTACTO, '')||'</CSUCT_CONTACTO>'||
			'<CSUCT_OBSERVACION>'||COALESCE(NEW.CSUCT_OBSERVACION, '')||'</CSUCT_OBSERVACION>'||
			'<CSUCT_ESTADO>'||COALESCE(NEW.CSUCT_ESTADO, FALSE)||'</CSUCT_ESTADO>'||
			'<CSUCT_FECHA_INSERT>'||COALESCE(NEW.CSUCT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSUCT_FECHA_INSERT>'||
			'<CSUCT_USUARIO_INSERT>'||COALESCE(NEW.CSUCT_USUARIO_INSERT, '')||'</CSUCT_USUARIO_INSERT>'||
			'<CSUCT_FECHA_UPDATE>'||COALESCE(NEW.CSUCT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSUCT_FECHA_UPDATE>'||
			'<CSUCT_USUARIO_UPDATE>'||COALESCE(NEW.CSUCT_USUARIO_UPDATE, '')||'</CSUCT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_SEC_USUARIO_CONTACTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SEC_USUARIO_CONTACTO
*/
CREATE TRIGGER TRG_AUD_CGG_SEC_USUARIO_CONTACTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_SEC_USUARIO_CONTACTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_SEC_USUARIO_CONTACTO();
COMMENT ON TRIGGER TRG_AUD_CGG_SEC_USUARIO_CONTACTO ON SII.CGG_SEC_USUARIO_CONTACTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_SEC_USUARIO_CONTACTO DISABLE TRIGGER TRG_AUD_CGG_SEC_USUARIO_CONTACTO;

