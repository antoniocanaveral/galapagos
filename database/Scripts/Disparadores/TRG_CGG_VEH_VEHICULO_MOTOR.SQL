/**
* FUNCION SII.TRG_KG_CGG_VEH_VEHICULO_MOTOR. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_VEHICULO_MOTOR
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_VEH_VEHICULO_MOTOR() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CVVMT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVVMT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CVVMT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVVMT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CVVMT_CODIGO>'||COALESCE(OLD.CVVMT_CODIGO, '')||'</CVVMT_CODIGO>'||
			'<CVVEH_CODIGO>'||COALESCE(OLD.CVVEH_CODIGO, '')||'</CVVEH_CODIGO>'||
			'<CVMTR_CODIGO>'||COALESCE(OLD.CVMTR_CODIGO, '')||'</CVMTR_CODIGO>'||
			'<CVVMT_FECHA>'||COALESCE(OLD.CVVMT_FECHA, CURRENT_TIMESTAMP)||'</CVVMT_FECHA>'||
			'<CVVMT_OBSERVACION>'||COALESCE(OLD.CVVMT_OBSERVACION, '')||'</CVVMT_OBSERVACION>'||
			'<CVVMT_ESTADO>'||COALESCE(OLD.CVVMT_ESTADO, FALSE)||'</CVVMT_ESTADO>'||
			'<CVVMT_FECHA_INSERT>'||COALESCE(OLD.CVVMT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVVMT_FECHA_INSERT>'||
			'<CVVMT_USUARIO_INSERT>'||COALESCE(OLD.CVVMT_USUARIO_INSERT, '')||'</CVVMT_USUARIO_INSERT>'||
			'<CVVMT_FECHA_UPDATE>'||COALESCE(OLD.CVVMT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVVMT_FECHA_UPDATE>'||
			'<CVVMT_USUARIO_UPDATE>'||COALESCE(OLD.CVVMT_USUARIO_UPDATE, '')||'</CVVMT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CVVMT_CODIGO>'||COALESCE(NEW.CVVMT_CODIGO, '')||'</CVVMT_CODIGO>'||
			'<CVVEH_CODIGO>'||COALESCE(NEW.CVVEH_CODIGO, '')||'</CVVEH_CODIGO>'||
			'<CVMTR_CODIGO>'||COALESCE(NEW.CVMTR_CODIGO, '')||'</CVMTR_CODIGO>'||
			'<CVVMT_FECHA>'||COALESCE(NEW.CVVMT_FECHA, CURRENT_TIMESTAMP)||'</CVVMT_FECHA>'||
			'<CVVMT_OBSERVACION>'||COALESCE(NEW.CVVMT_OBSERVACION, '')||'</CVVMT_OBSERVACION>'||
			'<CVVMT_ESTADO>'||COALESCE(NEW.CVVMT_ESTADO, FALSE)||'</CVVMT_ESTADO>'||
			'<CVVMT_FECHA_INSERT>'||COALESCE(NEW.CVVMT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVVMT_FECHA_INSERT>'||
			'<CVVMT_USUARIO_INSERT>'||COALESCE(NEW.CVVMT_USUARIO_INSERT, '')||'</CVVMT_USUARIO_INSERT>'||
			'<CVVMT_FECHA_UPDATE>'||COALESCE(NEW.CVVMT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVVMT_FECHA_UPDATE>'||
			'<CVVMT_USUARIO_UPDATE>'||COALESCE(NEW.CVVMT_USUARIO_UPDATE, '')||'</CVVMT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_VEH_VEHICULO_MOTOR. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_VEHICULO_MOTOR
*/
CREATE TRIGGER TRG_AUD_CGG_VEH_VEHICULO_MOTOR AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_VEH_VEHICULO_MOTOR FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_VEH_VEHICULO_MOTOR();
COMMENT ON TRIGGER TRG_AUD_CGG_VEH_VEHICULO_MOTOR ON SII.CGG_VEH_VEHICULO_MOTOR IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_VEH_VEHICULO_MOTOR DISABLE TRIGGER TRG_AUD_CGG_VEH_VEHICULO_MOTOR;

