/**
* FUNCION SII.TRG_KG_CGG_RES_PERSONA_CONTACTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_PERSONA_CONTACTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_PERSONA_CONTACTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRPRC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRPRC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRPRC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRPRC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRPRC_CODIGO>'||COALESCE(OLD.CRPRC_CODIGO, '')||'</CRPRC_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRTCO_CODIGO>'||COALESCE(OLD.CRTCO_CODIGO, '')||'</CRTCO_CODIGO>'||
			'<CRPRC_DESCRIPCION>'||COALESCE(OLD.CRPRC_DESCRIPCION, '')||'</CRPRC_DESCRIPCION>'||
			'<CRPRC_CONTACTO>'||COALESCE(OLD.CRPRC_CONTACTO, '')||'</CRPRC_CONTACTO>'||
			'<CRPRC_ESTADO>'||COALESCE(OLD.CRPRC_ESTADO, FALSE)||'</CRPRC_ESTADO>'||
			'<CRPRC_FECHA_INSERT>'||COALESCE(OLD.CRPRC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRPRC_FECHA_INSERT>'||
			'<CRPRC_USUARIO_INSERT>'||COALESCE(OLD.CRPRC_USUARIO_INSERT, '')||'</CRPRC_USUARIO_INSERT>'||
			'<CRPRC_FECHA_UPDATE>'||COALESCE(OLD.CRPRC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRPRC_FECHA_UPDATE>'||
			'<CRPRC_USUARIO_UPDATE>'||COALESCE(OLD.CRPRC_USUARIO_UPDATE, '')||'</CRPRC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRPRC_CODIGO>'||COALESCE(NEW.CRPRC_CODIGO, '')||'</CRPRC_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRTCO_CODIGO>'||COALESCE(NEW.CRTCO_CODIGO, '')||'</CRTCO_CODIGO>'||
			'<CRPRC_DESCRIPCION>'||COALESCE(NEW.CRPRC_DESCRIPCION, '')||'</CRPRC_DESCRIPCION>'||
			'<CRPRC_CONTACTO>'||COALESCE(NEW.CRPRC_CONTACTO, '')||'</CRPRC_CONTACTO>'||
			'<CRPRC_ESTADO>'||COALESCE(NEW.CRPRC_ESTADO, FALSE)||'</CRPRC_ESTADO>'||
			'<CRPRC_FECHA_INSERT>'||COALESCE(NEW.CRPRC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRPRC_FECHA_INSERT>'||
			'<CRPRC_USUARIO_INSERT>'||COALESCE(NEW.CRPRC_USUARIO_INSERT, '')||'</CRPRC_USUARIO_INSERT>'||
			'<CRPRC_FECHA_UPDATE>'||COALESCE(NEW.CRPRC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRPRC_FECHA_UPDATE>'||
			'<CRPRC_USUARIO_UPDATE>'||COALESCE(NEW.CRPRC_USUARIO_UPDATE, '')||'</CRPRC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_PERSONA_CONTACTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_PERSONA_CONTACTO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_PERSONA_CONTACTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_PERSONA_CONTACTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_PERSONA_CONTACTO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_PERSONA_CONTACTO ON SII.CGG_RES_PERSONA_CONTACTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_PERSONA_CONTACTO DISABLE TRIGGER TRG_AUD_CGG_RES_PERSONA_CONTACTO;

