/**
* FUNCION SII.TRG_KG_CGG_ESPECIALIDAD. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_ESPECIALIDAD
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_ESPECIALIDAD() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGESP_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGESP_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGESP_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGESP_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGESP_CODIGO>'||COALESCE(OLD.CGESP_CODIGO, '')||'</CGESP_CODIGO>'||
			'<CGNES_CODIGO>'||COALESCE(OLD.CGNES_CODIGO, '')||'</CGNES_CODIGO>'||
			'<CGESP_NOMBRE>'||COALESCE(OLD.CGESP_NOMBRE, '')||'</CGESP_NOMBRE>'||
			'<CGESP_DESCRIPCION>'||COALESCE(OLD.CGESP_DESCRIPCION, '')||'</CGESP_DESCRIPCION>'||
			'<CGESP_ESTADO>'||COALESCE(OLD.CGESP_ESTADO, FALSE)||'</CGESP_ESTADO>'||
			'<CGESP_FECHA_INSERT>'||COALESCE(OLD.CGESP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGESP_FECHA_INSERT>'||
			'<CGESP_USUARIO_INSERT>'||COALESCE(OLD.CGESP_USUARIO_INSERT, '')||'</CGESP_USUARIO_INSERT>'||
			'<CGESP_FECHA_UPDATE>'||COALESCE(OLD.CGESP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGESP_FECHA_UPDATE>'||
			'<CGESP_USUARIO_UPDATE>'||COALESCE(OLD.CGESP_USUARIO_UPDATE, '')||'</CGESP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGESP_CODIGO>'||COALESCE(NEW.CGESP_CODIGO, '')||'</CGESP_CODIGO>'||
			'<CGNES_CODIGO>'||COALESCE(NEW.CGNES_CODIGO, '')||'</CGNES_CODIGO>'||
			'<CGESP_NOMBRE>'||COALESCE(NEW.CGESP_NOMBRE, '')||'</CGESP_NOMBRE>'||
			'<CGESP_DESCRIPCION>'||COALESCE(NEW.CGESP_DESCRIPCION, '')||'</CGESP_DESCRIPCION>'||
			'<CGESP_ESTADO>'||COALESCE(NEW.CGESP_ESTADO, FALSE)||'</CGESP_ESTADO>'||
			'<CGESP_FECHA_INSERT>'||COALESCE(NEW.CGESP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGESP_FECHA_INSERT>'||
			'<CGESP_USUARIO_INSERT>'||COALESCE(NEW.CGESP_USUARIO_INSERT, '')||'</CGESP_USUARIO_INSERT>'||
			'<CGESP_FECHA_UPDATE>'||COALESCE(NEW.CGESP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGESP_FECHA_UPDATE>'||
			'<CGESP_USUARIO_UPDATE>'||COALESCE(NEW.CGESP_USUARIO_UPDATE, '')||'</CGESP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_ESPECIALIDAD. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_ESPECIALIDAD
*/
CREATE TRIGGER TRG_AUD_CGG_ESPECIALIDAD AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_ESPECIALIDAD FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_ESPECIALIDAD();
COMMENT ON TRIGGER TRG_AUD_CGG_ESPECIALIDAD ON SII.CGG_ESPECIALIDAD IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_ESPECIALIDAD DISABLE TRIGGER TRG_AUD_CGG_ESPECIALIDAD;

