/**
* FUNCION SII.TRG_KG_CGG_GEM_IDIOMA_PERSONA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_IDIOMA_PERSONA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GEM_IDIOMA_PERSONA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGIDP_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGIDP_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGIDP_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGIDP_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGIDP_CODIGO>'||COALESCE(OLD.CGIDP_CODIGO, '')||'</CGIDP_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGIDM_CODIGO>'||COALESCE(OLD.CGIDM_CODIGO, '')||'</CGIDM_CODIGO>'||
			'<CGIDP_ESCRITURA>'||COALESCE(OLD.CGIDP_ESCRITURA, 0)||'</CGIDP_ESCRITURA>'||
			'<CGIDP_LECTURA>'||COALESCE(OLD.CGIDP_LECTURA, 0)||'</CGIDP_LECTURA>'||
			'<CGIDP_HABLAR>'||COALESCE(OLD.CGIDP_HABLAR, 0)||'</CGIDP_HABLAR>'||
			'<CGIDP_ESCUCHAR>'||COALESCE(OLD.CGIDP_ESCUCHAR, 0)||'</CGIDP_ESCUCHAR>'||
			'<CGIDP_ESTADO>'||COALESCE(OLD.CGIDP_ESTADO, FALSE)||'</CGIDP_ESTADO>'||
			'<CGIDP_FECHA_INSERT>'||COALESCE(OLD.CGIDP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGIDP_FECHA_INSERT>'||
			'<CGIDP_USUARIO_INSERT>'||COALESCE(OLD.CGIDP_USUARIO_INSERT, '')||'</CGIDP_USUARIO_INSERT>'||
			'<CGIDP_FECHA_UPDATE>'||COALESCE(OLD.CGIDP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGIDP_FECHA_UPDATE>'||
			'<CGIDP_USUARIO_UPDATE>'||COALESCE(OLD.CGIDP_USUARIO_UPDATE, '')||'</CGIDP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGIDP_CODIGO>'||COALESCE(NEW.CGIDP_CODIGO, '')||'</CGIDP_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGIDM_CODIGO>'||COALESCE(NEW.CGIDM_CODIGO, '')||'</CGIDM_CODIGO>'||
			'<CGIDP_ESCRITURA>'||COALESCE(NEW.CGIDP_ESCRITURA, 0)||'</CGIDP_ESCRITURA>'||
			'<CGIDP_LECTURA>'||COALESCE(NEW.CGIDP_LECTURA, 0)||'</CGIDP_LECTURA>'||
			'<CGIDP_HABLAR>'||COALESCE(NEW.CGIDP_HABLAR, 0)||'</CGIDP_HABLAR>'||
			'<CGIDP_ESCUCHAR>'||COALESCE(NEW.CGIDP_ESCUCHAR, 0)||'</CGIDP_ESCUCHAR>'||
			'<CGIDP_ESTADO>'||COALESCE(NEW.CGIDP_ESTADO, FALSE)||'</CGIDP_ESTADO>'||
			'<CGIDP_FECHA_INSERT>'||COALESCE(NEW.CGIDP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGIDP_FECHA_INSERT>'||
			'<CGIDP_USUARIO_INSERT>'||COALESCE(NEW.CGIDP_USUARIO_INSERT, '')||'</CGIDP_USUARIO_INSERT>'||
			'<CGIDP_FECHA_UPDATE>'||COALESCE(NEW.CGIDP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGIDP_FECHA_UPDATE>'||
			'<CGIDP_USUARIO_UPDATE>'||COALESCE(NEW.CGIDP_USUARIO_UPDATE, '')||'</CGIDP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GEM_IDIOMA_PERSONA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_IDIOMA_PERSONA
*/
CREATE TRIGGER TRG_AUD_CGG_GEM_IDIOMA_PERSONA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GEM_IDIOMA_PERSONA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GEM_IDIOMA_PERSONA();
COMMENT ON TRIGGER TRG_AUD_CGG_GEM_IDIOMA_PERSONA ON SII.CGG_GEM_IDIOMA_PERSONA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GEM_IDIOMA_PERSONA DISABLE TRIGGER TRG_AUD_CGG_GEM_IDIOMA_PERSONA;

