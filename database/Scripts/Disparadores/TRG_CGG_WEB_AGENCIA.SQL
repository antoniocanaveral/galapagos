/**
* FUNCION SII.TRG_KG_CGG_WEB_AGENCIA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_WEB_AGENCIA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_WEB_AGENCIA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CWAGE_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CWAGE_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CWAGE_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CWAGE_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CWAGE_CODIGO>'||COALESCE(OLD.CWAGE_CODIGO, '')||'</CWAGE_CODIGO>'||
			'<CWMOD_CODIGO>'||COALESCE(OLD.CWMOD_CODIGO, '')||'</CWMOD_CODIGO>'||
			'<CWAGE_NOMBRE>'||COALESCE(OLD.CWAGE_NOMBRE, '')||'</CWAGE_NOMBRE>'||
			'<CWAGE_DESCRIPCION>'||COALESCE(OLD.CWAGE_DESCRIPCION, '')||'</CWAGE_DESCRIPCION>'||
			'<CWAGE_ENCARGADO>'||COALESCE(OLD.CWAGE_ENCARGADO, '')||'</CWAGE_ENCARGADO>'||
			'<CWAGE_ESTADO>'||COALESCE(OLD.CWAGE_ESTADO, FALSE)||'</CWAGE_ESTADO>'||
			'<CWAGE_FECHA_INSERT>'||COALESCE(OLD.CWAGE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CWAGE_FECHA_INSERT>'||
			'<CWAGE_USUARIO_INSERT>'||COALESCE(OLD.CWAGE_USUARIO_INSERT, '')||'</CWAGE_USUARIO_INSERT>'||
			'<CWAGE_FECHA_UPDATE>'||COALESCE(OLD.CWAGE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CWAGE_FECHA_UPDATE>'||
			'<CWAGE_USUARIO_UPDATE>'||COALESCE(OLD.CWAGE_USUARIO_UPDATE, '')||'</CWAGE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CWAGE_CODIGO>'||COALESCE(NEW.CWAGE_CODIGO, '')||'</CWAGE_CODIGO>'||
			'<CWMOD_CODIGO>'||COALESCE(NEW.CWMOD_CODIGO, '')||'</CWMOD_CODIGO>'||
			'<CWAGE_NOMBRE>'||COALESCE(NEW.CWAGE_NOMBRE, '')||'</CWAGE_NOMBRE>'||
			'<CWAGE_DESCRIPCION>'||COALESCE(NEW.CWAGE_DESCRIPCION, '')||'</CWAGE_DESCRIPCION>'||
			'<CWAGE_ENCARGADO>'||COALESCE(NEW.CWAGE_ENCARGADO, '')||'</CWAGE_ENCARGADO>'||
			'<CWAGE_ESTADO>'||COALESCE(NEW.CWAGE_ESTADO, FALSE)||'</CWAGE_ESTADO>'||
			'<CWAGE_FECHA_INSERT>'||COALESCE(NEW.CWAGE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CWAGE_FECHA_INSERT>'||
			'<CWAGE_USUARIO_INSERT>'||COALESCE(NEW.CWAGE_USUARIO_INSERT, '')||'</CWAGE_USUARIO_INSERT>'||
			'<CWAGE_FECHA_UPDATE>'||COALESCE(NEW.CWAGE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CWAGE_FECHA_UPDATE>'||
			'<CWAGE_USUARIO_UPDATE>'||COALESCE(NEW.CWAGE_USUARIO_UPDATE, '')||'</CWAGE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_WEB_AGENCIA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_WEB_AGENCIA
*/
CREATE TRIGGER TRG_AUD_CGG_WEB_AGENCIA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_WEB_AGENCIA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_WEB_AGENCIA();
COMMENT ON TRIGGER TRG_AUD_CGG_WEB_AGENCIA ON SII.CGG_WEB_AGENCIA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_WEB_AGENCIA DISABLE TRIGGER TRG_AUD_CGG_WEB_AGENCIA;

