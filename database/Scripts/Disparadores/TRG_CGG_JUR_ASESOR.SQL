/**
* FUNCION SII.TRG_KG_CGG_JUR_ASESOR. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_ASESOR
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_JUR_ASESOR() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CJASE_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJASE_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CJASE_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJASE_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CJASE_CODIGO>'||COALESCE(OLD.CJASE_CODIGO, '')||'</CJASE_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CJPJU_CODIGO>'||COALESCE(OLD.CJPJU_CODIGO, '')||'</CJPJU_CODIGO>'||
			'<CJASE_ACTIVO>'||COALESCE(OLD.CJASE_ACTIVO, 0)||'</CJASE_ACTIVO>'||
			'<CJASE_ESTADO>'||COALESCE(OLD.CJASE_ESTADO, FALSE)||'</CJASE_ESTADO>'||
			'<CJASE_FECHA_INSERT>'||COALESCE(OLD.CJASE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJASE_FECHA_INSERT>'||
			'<CJASE_USUARIO_INSERT>'||COALESCE(OLD.CJASE_USUARIO_INSERT, '')||'</CJASE_USUARIO_INSERT>'||
			'<CJASE_FECHA_UPDATE>'||COALESCE(OLD.CJASE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJASE_FECHA_UPDATE>'||
			'<CJASE_USUARIO_UPDATE>'||COALESCE(OLD.CJASE_USUARIO_UPDATE, '')||'</CJASE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CJASE_CODIGO>'||COALESCE(NEW.CJASE_CODIGO, '')||'</CJASE_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CJPJU_CODIGO>'||COALESCE(NEW.CJPJU_CODIGO, '')||'</CJPJU_CODIGO>'||
			'<CJASE_ACTIVO>'||COALESCE(NEW.CJASE_ACTIVO, 0)||'</CJASE_ACTIVO>'||
			'<CJASE_ESTADO>'||COALESCE(NEW.CJASE_ESTADO, FALSE)||'</CJASE_ESTADO>'||
			'<CJASE_FECHA_INSERT>'||COALESCE(NEW.CJASE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJASE_FECHA_INSERT>'||
			'<CJASE_USUARIO_INSERT>'||COALESCE(NEW.CJASE_USUARIO_INSERT, '')||'</CJASE_USUARIO_INSERT>'||
			'<CJASE_FECHA_UPDATE>'||COALESCE(NEW.CJASE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJASE_FECHA_UPDATE>'||
			'<CJASE_USUARIO_UPDATE>'||COALESCE(NEW.CJASE_USUARIO_UPDATE, '')||'</CJASE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_JUR_ASESOR. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_ASESOR
*/
CREATE TRIGGER TRG_AUD_CGG_JUR_ASESOR AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_JUR_ASESOR FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_JUR_ASESOR();
COMMENT ON TRIGGER TRG_AUD_CGG_JUR_ASESOR ON SII.CGG_JUR_ASESOR IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_JUR_ASESOR DISABLE TRIGGER TRG_AUD_CGG_JUR_ASESOR;

