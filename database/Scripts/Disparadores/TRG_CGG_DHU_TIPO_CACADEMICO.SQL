/**
* FUNCION SII.TRG_KG_CGG_DHU_TIPO_CACADEMICO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_TIPO_CACADEMICO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_TIPO_CACADEMICO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDTCA_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDTCA_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDTCA_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDTCA_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDTCA_CODIGO>'||COALESCE(OLD.CDTCA_CODIGO, '')||'</CDTCA_CODIGO>'||
			'<CDTCA_NOMBRE>'||COALESCE(OLD.CDTCA_NOMBRE, '')||'</CDTCA_NOMBRE>'||
			'<CDTCA_OBSERVACION>'||COALESCE(OLD.CDTCA_OBSERVACION, '')||'</CDTCA_OBSERVACION>'||
			'<CDTCA_ESTADO>'||COALESCE(OLD.CDTCA_ESTADO, FALSE)||'</CDTCA_ESTADO>'||
			'<CDTCA_FECHA_INSERT>'||COALESCE(OLD.CDTCA_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDTCA_FECHA_INSERT>'||
			'<CDTCA_USUARIO_INSERT>'||COALESCE(OLD.CDTCA_USUARIO_INSERT, '')||'</CDTCA_USUARIO_INSERT>'||
			'<CDTCA_FECHA_UPDATE>'||COALESCE(OLD.CDTCA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDTCA_FECHA_UPDATE>'||
			'<CDTCA_USUARIO_UPDATE>'||COALESCE(OLD.CDTCA_USUARIO_UPDATE, '')||'</CDTCA_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDTCA_CODIGO>'||COALESCE(NEW.CDTCA_CODIGO, '')||'</CDTCA_CODIGO>'||
			'<CDTCA_NOMBRE>'||COALESCE(NEW.CDTCA_NOMBRE, '')||'</CDTCA_NOMBRE>'||
			'<CDTCA_OBSERVACION>'||COALESCE(NEW.CDTCA_OBSERVACION, '')||'</CDTCA_OBSERVACION>'||
			'<CDTCA_ESTADO>'||COALESCE(NEW.CDTCA_ESTADO, FALSE)||'</CDTCA_ESTADO>'||
			'<CDTCA_FECHA_INSERT>'||COALESCE(NEW.CDTCA_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDTCA_FECHA_INSERT>'||
			'<CDTCA_USUARIO_INSERT>'||COALESCE(NEW.CDTCA_USUARIO_INSERT, '')||'</CDTCA_USUARIO_INSERT>'||
			'<CDTCA_FECHA_UPDATE>'||COALESCE(NEW.CDTCA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDTCA_FECHA_UPDATE>'||
			'<CDTCA_USUARIO_UPDATE>'||COALESCE(NEW.CDTCA_USUARIO_UPDATE, '')||'</CDTCA_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_TIPO_CACADEMICO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_TIPO_CACADEMICO
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_TIPO_CACADEMICO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_TIPO_CACADEMICO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_TIPO_CACADEMICO();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_TIPO_CACADEMICO ON SII.CGG_DHU_TIPO_CACADEMICO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_TIPO_CACADEMICO DISABLE TRIGGER TRG_AUD_CGG_DHU_TIPO_CACADEMICO;

