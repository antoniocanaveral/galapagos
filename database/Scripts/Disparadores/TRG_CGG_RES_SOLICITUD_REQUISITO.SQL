/**
* FUNCION SII.TRG_KG_CGG_RES_SOLICITUD_REQUISITO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_SOLICITUD_REQUISITO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_SOLICITUD_REQUISITO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRSRQ_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRSRQ_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRSRQ_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRSRQ_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRSRQ_CODIGO>'||COALESCE(OLD.CRSRQ_CODIGO, '')||'</CRSRQ_CODIGO>'||
			'<CRREQ_CODIGO>'||COALESCE(OLD.CRREQ_CODIGO, '')||'</CRREQ_CODIGO>'||
			'<CRTST_CODIGO>'||COALESCE(OLD.CRTST_CODIGO, '')||'</CRTST_CODIGO>'||
			'<CRSRQ_DESCRIPCION>'||COALESCE(OLD.CRSRQ_DESCRIPCION, '')||'</CRSRQ_DESCRIPCION>'||
			'<CRSRQ_REQUERIDO>'||COALESCE(OLD.CRSRQ_REQUERIDO, FALSE)||'</CRSRQ_REQUERIDO>'||
			'<CRSRQ_PARTICIPANTE>'||COALESCE(OLD.CRSRQ_PARTICIPANTE, 0)||'</CRSRQ_PARTICIPANTE>'||
			'<CRSRQ_ESTADO>'||COALESCE(OLD.CRSRQ_ESTADO, FALSE)||'</CRSRQ_ESTADO>'||
			'<CRSRQ_FECHA_INSERT>'||COALESCE(OLD.CRSRQ_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRSRQ_FECHA_INSERT>'||
			'<CRSRQ_USUARIO_INSERT>'||COALESCE(OLD.CRSRQ_USUARIO_INSERT, '')||'</CRSRQ_USUARIO_INSERT>'||
			'<CRSRQ_FECHA_UPDATE>'||COALESCE(OLD.CRSRQ_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRSRQ_FECHA_UPDATE>'||
			'<CRSRQ_USUARIO_UPDATE>'||COALESCE(OLD.CRSRQ_USUARIO_UPDATE, '')||'</CRSRQ_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRSRQ_CODIGO>'||COALESCE(NEW.CRSRQ_CODIGO, '')||'</CRSRQ_CODIGO>'||
			'<CRREQ_CODIGO>'||COALESCE(NEW.CRREQ_CODIGO, '')||'</CRREQ_CODIGO>'||
			'<CRTST_CODIGO>'||COALESCE(NEW.CRTST_CODIGO, '')||'</CRTST_CODIGO>'||
			'<CRSRQ_DESCRIPCION>'||COALESCE(NEW.CRSRQ_DESCRIPCION, '')||'</CRSRQ_DESCRIPCION>'||
			'<CRSRQ_REQUERIDO>'||COALESCE(NEW.CRSRQ_REQUERIDO, FALSE)||'</CRSRQ_REQUERIDO>'||
			'<CRSRQ_PARTICIPANTE>'||COALESCE(NEW.CRSRQ_PARTICIPANTE, 0)||'</CRSRQ_PARTICIPANTE>'||
			'<CRSRQ_ESTADO>'||COALESCE(NEW.CRSRQ_ESTADO, FALSE)||'</CRSRQ_ESTADO>'||
			'<CRSRQ_FECHA_INSERT>'||COALESCE(NEW.CRSRQ_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRSRQ_FECHA_INSERT>'||
			'<CRSRQ_USUARIO_INSERT>'||COALESCE(NEW.CRSRQ_USUARIO_INSERT, '')||'</CRSRQ_USUARIO_INSERT>'||
			'<CRSRQ_FECHA_UPDATE>'||COALESCE(NEW.CRSRQ_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRSRQ_FECHA_UPDATE>'||
			'<CRSRQ_USUARIO_UPDATE>'||COALESCE(NEW.CRSRQ_USUARIO_UPDATE, '')||'</CRSRQ_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_SOLICITUD_REQUISITO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_SOLICITUD_REQUISITO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_SOLICITUD_REQUISITO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_SOLICITUD_REQUISITO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_SOLICITUD_REQUISITO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_SOLICITUD_REQUISITO ON SII.CGG_RES_SOLICITUD_REQUISITO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_SOLICITUD_REQUISITO DISABLE TRIGGER TRG_AUD_CGG_RES_SOLICITUD_REQUISITO;

