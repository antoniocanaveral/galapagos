/**
* FUNCION SII.TRG_KG_CGG_KDX_ASIGNACION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_ASIGNACION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_ASIGNACION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKASG_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKASG_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKASG_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKASG_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKASG_CODIGO>'||COALESCE(OLD.CKASG_CODIGO, '')||'</CKASG_CODIGO>'||
			'<CKTRN_CODIGO>'||COALESCE(OLD.CKTRN_CODIGO, '')||'</CKTRN_CODIGO>'||
			'<CKUPV_CODIGO>'||COALESCE(OLD.CKUPV_CODIGO, '')||'</CKUPV_CODIGO>'||
			'<CGG_CKUPV_CODIGO>'||COALESCE(OLD.CGG_CKUPV_CODIGO, '')||'</CGG_CKUPV_CODIGO>'||
			'<CGG_CKASG_CODIGO>'||COALESCE(OLD.CGG_CKASG_CODIGO, '')||'</CGG_CKASG_CODIGO>'||
			'<CKASG_NUMERO_ASIGNACION>'||COALESCE(OLD.CKASG_NUMERO_ASIGNACION, '')||'</CKASG_NUMERO_ASIGNACION>'||
			'<CKASG_FECHA_RECEPCION>'||COALESCE(OLD.CKASG_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CKASG_FECHA_RECEPCION>'||
			'<CKASG_CANTIDAD>'||COALESCE(OLD.CKASG_CANTIDAD, 0)||'</CKASG_CANTIDAD>'||
			'<CKASG_PRECIO_UNITARIO>'||COALESCE(OLD.CKASG_PRECIO_UNITARIO, 0)||'</CKASG_PRECIO_UNITARIO>'||
			'<CKASG_SERIE_INICIO>'||COALESCE(OLD.CKASG_SERIE_INICIO, 0)||'</CKASG_SERIE_INICIO>'||
			'<CKASG_SERIE_FIN>'||COALESCE(OLD.CKASG_SERIE_FIN, 0)||'</CKASG_SERIE_FIN>'||
			'<CKASG_OBSERVACION>'||COALESCE(OLD.CKASG_OBSERVACION, '')||'</CKASG_OBSERVACION>'||
			'<CKASG_CANTIDAD_DISPONIBLE>'||COALESCE(OLD.CKASG_CANTIDAD_DISPONIBLE, 0)||'</CKASG_CANTIDAD_DISPONIBLE>'||
			'<CKASG_ESTADO_ASIGNACION>'||COALESCE(OLD.CKASG_ESTADO_ASIGNACION, 0)||'</CKASG_ESTADO_ASIGNACION>'||
			'<CKASG_ESTADO>'||COALESCE(OLD.CKASG_ESTADO, FALSE)||'</CKASG_ESTADO>'||
			'<CKASG_FECHA_INSERT>'||COALESCE(OLD.CKASG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKASG_FECHA_INSERT>'||
			'<CKASG_USUARIO_INSERT>'||COALESCE(OLD.CKASG_USUARIO_INSERT, '')||'</CKASG_USUARIO_INSERT>'||
			'<CKASG_FECHA_UPDATE>'||COALESCE(OLD.CKASG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKASG_FECHA_UPDATE>'||
			'<CKASG_USUARIO_UPDATE>'||COALESCE(OLD.CKASG_USUARIO_UPDATE, '')||'</CKASG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKASG_CODIGO>'||COALESCE(NEW.CKASG_CODIGO, '')||'</CKASG_CODIGO>'||
			'<CKTRN_CODIGO>'||COALESCE(NEW.CKTRN_CODIGO, '')||'</CKTRN_CODIGO>'||
			'<CKUPV_CODIGO>'||COALESCE(NEW.CKUPV_CODIGO, '')||'</CKUPV_CODIGO>'||
			'<CGG_CKUPV_CODIGO>'||COALESCE(NEW.CGG_CKUPV_CODIGO, '')||'</CGG_CKUPV_CODIGO>'||
			'<CGG_CKASG_CODIGO>'||COALESCE(NEW.CGG_CKASG_CODIGO, '')||'</CGG_CKASG_CODIGO>'||
			'<CKASG_NUMERO_ASIGNACION>'||COALESCE(NEW.CKASG_NUMERO_ASIGNACION, '')||'</CKASG_NUMERO_ASIGNACION>'||
			'<CKASG_FECHA_RECEPCION>'||COALESCE(NEW.CKASG_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CKASG_FECHA_RECEPCION>'||
			'<CKASG_CANTIDAD>'||COALESCE(NEW.CKASG_CANTIDAD, 0)||'</CKASG_CANTIDAD>'||
			'<CKASG_PRECIO_UNITARIO>'||COALESCE(NEW.CKASG_PRECIO_UNITARIO, 0)||'</CKASG_PRECIO_UNITARIO>'||
			'<CKASG_SERIE_INICIO>'||COALESCE(NEW.CKASG_SERIE_INICIO, 0)||'</CKASG_SERIE_INICIO>'||
			'<CKASG_SERIE_FIN>'||COALESCE(NEW.CKASG_SERIE_FIN, 0)||'</CKASG_SERIE_FIN>'||
			'<CKASG_OBSERVACION>'||COALESCE(NEW.CKASG_OBSERVACION, '')||'</CKASG_OBSERVACION>'||
			'<CKASG_CANTIDAD_DISPONIBLE>'||COALESCE(NEW.CKASG_CANTIDAD_DISPONIBLE, 0)||'</CKASG_CANTIDAD_DISPONIBLE>'||
			'<CKASG_ESTADO_ASIGNACION>'||COALESCE(NEW.CKASG_ESTADO_ASIGNACION, 0)||'</CKASG_ESTADO_ASIGNACION>'||
			'<CKASG_ESTADO>'||COALESCE(NEW.CKASG_ESTADO, FALSE)||'</CKASG_ESTADO>'||
			'<CKASG_FECHA_INSERT>'||COALESCE(NEW.CKASG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKASG_FECHA_INSERT>'||
			'<CKASG_USUARIO_INSERT>'||COALESCE(NEW.CKASG_USUARIO_INSERT, '')||'</CKASG_USUARIO_INSERT>'||
			'<CKASG_FECHA_UPDATE>'||COALESCE(NEW.CKASG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKASG_FECHA_UPDATE>'||
			'<CKASG_USUARIO_UPDATE>'||COALESCE(NEW.CKASG_USUARIO_UPDATE, '')||'</CKASG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_ASIGNACION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_ASIGNACION
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_ASIGNACION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_ASIGNACION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_ASIGNACION();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_ASIGNACION ON SII.CGG_KDX_ASIGNACION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_ASIGNACION DISABLE TRIGGER TRG_AUD_CGG_KDX_ASIGNACION;

