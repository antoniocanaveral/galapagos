/**
* FUNCION SII.TRG_KG_CGG_JUR_HISTORIAL_ACUERDO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_HISTORIAL_ACUERDO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_JUR_HISTORIAL_ACUERDO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CJHAC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJHAC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CJHAC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJHAC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CJHAC_CODIGO>'||COALESCE(OLD.CJHAC_CODIGO, '')||'</CJHAC_CODIGO>'||
			'<CJACU_CODIGO>'||COALESCE(OLD.CJACU_CODIGO, '')||'</CJACU_CODIGO>'||
			'<CJHAC_DESCRIPCION>'||COALESCE(OLD.CJHAC_DESCRIPCION, '')||'</CJHAC_DESCRIPCION>'||
			'<CJHAC_RESPONSABLE>'||COALESCE(OLD.CJHAC_RESPONSABLE, '')||'</CJHAC_RESPONSABLE>'||
			'<CJHAC_FECHA>'||COALESCE(OLD.CJHAC_FECHA, CURRENT_TIMESTAMP)||'</CJHAC_FECHA>'||
			'<CJHAC_ESTADO>'||COALESCE(OLD.CJHAC_ESTADO, FALSE)||'</CJHAC_ESTADO>'||
			'<CJHAC_FECHA_INSERT>'||COALESCE(OLD.CJHAC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJHAC_FECHA_INSERT>'||
			'<CJHAC_USUARIO_INSERT>'||COALESCE(OLD.CJHAC_USUARIO_INSERT, '')||'</CJHAC_USUARIO_INSERT>'||
			'<CJHAC_FECHA_UPDATE>'||COALESCE(OLD.CJHAC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJHAC_FECHA_UPDATE>'||
			'<CJHAC_USUARIO_UPDATE>'||COALESCE(OLD.CJHAC_USUARIO_UPDATE, '')||'</CJHAC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CJHAC_CODIGO>'||COALESCE(NEW.CJHAC_CODIGO, '')||'</CJHAC_CODIGO>'||
			'<CJACU_CODIGO>'||COALESCE(NEW.CJACU_CODIGO, '')||'</CJACU_CODIGO>'||
			'<CJHAC_DESCRIPCION>'||COALESCE(NEW.CJHAC_DESCRIPCION, '')||'</CJHAC_DESCRIPCION>'||
			'<CJHAC_RESPONSABLE>'||COALESCE(NEW.CJHAC_RESPONSABLE, '')||'</CJHAC_RESPONSABLE>'||
			'<CJHAC_FECHA>'||COALESCE(NEW.CJHAC_FECHA, CURRENT_TIMESTAMP)||'</CJHAC_FECHA>'||
			'<CJHAC_ESTADO>'||COALESCE(NEW.CJHAC_ESTADO, FALSE)||'</CJHAC_ESTADO>'||
			'<CJHAC_FECHA_INSERT>'||COALESCE(NEW.CJHAC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJHAC_FECHA_INSERT>'||
			'<CJHAC_USUARIO_INSERT>'||COALESCE(NEW.CJHAC_USUARIO_INSERT, '')||'</CJHAC_USUARIO_INSERT>'||
			'<CJHAC_FECHA_UPDATE>'||COALESCE(NEW.CJHAC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJHAC_FECHA_UPDATE>'||
			'<CJHAC_USUARIO_UPDATE>'||COALESCE(NEW.CJHAC_USUARIO_UPDATE, '')||'</CJHAC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_JUR_HISTORIAL_ACUERDO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_HISTORIAL_ACUERDO
*/
CREATE TRIGGER TRG_AUD_CGG_JUR_HISTORIAL_ACUERDO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_JUR_HISTORIAL_ACUERDO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_JUR_HISTORIAL_ACUERDO();
COMMENT ON TRIGGER TRG_AUD_CGG_JUR_HISTORIAL_ACUERDO ON SII.CGG_JUR_HISTORIAL_ACUERDO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_JUR_HISTORIAL_ACUERDO DISABLE TRIGGER TRG_AUD_CGG_JUR_HISTORIAL_ACUERDO;

