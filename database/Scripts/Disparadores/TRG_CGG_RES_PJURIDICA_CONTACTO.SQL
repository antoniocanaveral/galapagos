/**
* FUNCION SII.TRG_KG_CGG_RES_PJURIDICA_CONTACTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_PJURIDICA_CONTACTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_PJURIDICA_CONTACTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRPJC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRPJC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRPJC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRPJC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRPJC_CODIGO>'||COALESCE(OLD.CRPJC_CODIGO, '')||'</CRPJC_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(OLD.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRTCO_CODIGO>'||COALESCE(OLD.CRTCO_CODIGO, '')||'</CRTCO_CODIGO>'||
			'<CRPJC_DESCRIPCION>'||COALESCE(OLD.CRPJC_DESCRIPCION, '')||'</CRPJC_DESCRIPCION>'||
			'<CRPJC_CONTACTO>'||COALESCE(OLD.CRPJC_CONTACTO, '')||'</CRPJC_CONTACTO>'||
			'<CRPJC_PRINCIPAL>'||COALESCE(OLD.CRPJC_PRINCIPAL, FALSE)||'</CRPJC_PRINCIPAL>'||
			'<CRPJC_ESTADO>'||COALESCE(OLD.CRPJC_ESTADO, FALSE)||'</CRPJC_ESTADO>'||
			'<CRPJC_FECHA_INSERT>'||COALESCE(OLD.CRPJC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRPJC_FECHA_INSERT>'||
			'<CRPJC_USUARIO_INSERT>'||COALESCE(OLD.CRPJC_USUARIO_INSERT, '')||'</CRPJC_USUARIO_INSERT>'||
			'<CRPJC_FECHA_UPDATE>'||COALESCE(OLD.CRPJC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRPJC_FECHA_UPDATE>'||
			'<CRPJC_USUARIO_UPDATE>'||COALESCE(OLD.CRPJC_USUARIO_UPDATE, '')||'</CRPJC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRPJC_CODIGO>'||COALESCE(NEW.CRPJC_CODIGO, '')||'</CRPJC_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRTCO_CODIGO>'||COALESCE(NEW.CRTCO_CODIGO, '')||'</CRTCO_CODIGO>'||
			'<CRPJC_DESCRIPCION>'||COALESCE(NEW.CRPJC_DESCRIPCION, '')||'</CRPJC_DESCRIPCION>'||
			'<CRPJC_CONTACTO>'||COALESCE(NEW.CRPJC_CONTACTO, '')||'</CRPJC_CONTACTO>'||
			'<CRPJC_PRINCIPAL>'||COALESCE(NEW.CRPJC_PRINCIPAL, FALSE)||'</CRPJC_PRINCIPAL>'||
			'<CRPJC_ESTADO>'||COALESCE(NEW.CRPJC_ESTADO, FALSE)||'</CRPJC_ESTADO>'||
			'<CRPJC_FECHA_INSERT>'||COALESCE(NEW.CRPJC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRPJC_FECHA_INSERT>'||
			'<CRPJC_USUARIO_INSERT>'||COALESCE(NEW.CRPJC_USUARIO_INSERT, '')||'</CRPJC_USUARIO_INSERT>'||
			'<CRPJC_FECHA_UPDATE>'||COALESCE(NEW.CRPJC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRPJC_FECHA_UPDATE>'||
			'<CRPJC_USUARIO_UPDATE>'||COALESCE(NEW.CRPJC_USUARIO_UPDATE, '')||'</CRPJC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_PJURIDICA_CONTACTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_PJURIDICA_CONTACTO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_PJURIDICA_CONTACTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_PJURIDICA_CONTACTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_PJURIDICA_CONTACTO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_PJURIDICA_CONTACTO ON SII.CGG_RES_PJURIDICA_CONTACTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_PJURIDICA_CONTACTO DISABLE TRIGGER TRG_AUD_CGG_RES_PJURIDICA_CONTACTO;

