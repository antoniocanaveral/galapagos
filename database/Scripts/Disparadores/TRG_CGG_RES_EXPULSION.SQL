/**
* FUNCION SII.TRG_KG_CGG_RES_EXPULSION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_EXPULSION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_EXPULSION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CREXP_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CREXP_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CREXP_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CREXP_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CREXP_CODIGO>'||COALESCE(OLD.CREXP_CODIGO, '')||'</CREXP_CODIGO>'||
			'<CRMOV_CODIGO>'||COALESCE(OLD.CRMOV_CODIGO, '')||'</CRMOV_CODIGO>'||
			'<CRNOT_CODIGO>'||COALESCE(OLD.CRNOT_CODIGO, '')||'</CRNOT_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CROSG_CODIGO>'||COALESCE(OLD.CROSG_CODIGO, '')||'</CROSG_CODIGO>'||
			'<CTFSL_CODIGO>'||COALESCE(OLD.CTFSL_CODIGO, '')||'</CTFSL_CODIGO>'||
			'<CREXP_FECHA_EXPULSION>'||COALESCE(OLD.CREXP_FECHA_EXPULSION, CURRENT_TIMESTAMP)||'</CREXP_FECHA_EXPULSION>'||
			'<CREXP_TIPO_PAGO>'||COALESCE(OLD.CREXP_TIPO_PAGO, 0)||'</CREXP_TIPO_PAGO>'||
			'<CREXP_VALOR_VUELO>'||COALESCE(OLD.CREXP_VALOR_VUELO, 0)||'</CREXP_VALOR_VUELO>'||
			'<CREXP_OBSERVACION>'||COALESCE(OLD.CREXP_OBSERVACION, '')||'</CREXP_OBSERVACION>'||
			'<CREXP_ESTADO>'||COALESCE(OLD.CREXP_ESTADO, FALSE)||'</CREXP_ESTADO>'||
			'<CREXP_FECHA_INSERT>'||COALESCE(OLD.CREXP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CREXP_FECHA_INSERT>'||
			'<CREXP_USUARIO_INSERT>'||COALESCE(OLD.CREXP_USUARIO_INSERT, '')||'</CREXP_USUARIO_INSERT>'||
			'<CREXP_FECHA_UPDATE>'||COALESCE(OLD.CREXP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CREXP_FECHA_UPDATE>'||
			'<CREXP_USUARIO_UPDATE>'||COALESCE(OLD.CREXP_USUARIO_UPDATE, '')||'</CREXP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CREXP_CODIGO>'||COALESCE(NEW.CREXP_CODIGO, '')||'</CREXP_CODIGO>'||
			'<CRMOV_CODIGO>'||COALESCE(NEW.CRMOV_CODIGO, '')||'</CRMOV_CODIGO>'||
			'<CRNOT_CODIGO>'||COALESCE(NEW.CRNOT_CODIGO, '')||'</CRNOT_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CROSG_CODIGO>'||COALESCE(NEW.CROSG_CODIGO, '')||'</CROSG_CODIGO>'||
			'<CTFSL_CODIGO>'||COALESCE(NEW.CTFSL_CODIGO, '')||'</CTFSL_CODIGO>'||
			'<CREXP_FECHA_EXPULSION>'||COALESCE(NEW.CREXP_FECHA_EXPULSION, CURRENT_TIMESTAMP)||'</CREXP_FECHA_EXPULSION>'||
			'<CREXP_TIPO_PAGO>'||COALESCE(NEW.CREXP_TIPO_PAGO, 0)||'</CREXP_TIPO_PAGO>'||
			'<CREXP_VALOR_VUELO>'||COALESCE(NEW.CREXP_VALOR_VUELO, 0)||'</CREXP_VALOR_VUELO>'||
			'<CREXP_OBSERVACION>'||COALESCE(NEW.CREXP_OBSERVACION, '')||'</CREXP_OBSERVACION>'||
			'<CREXP_ESTADO>'||COALESCE(NEW.CREXP_ESTADO, FALSE)||'</CREXP_ESTADO>'||
			'<CREXP_FECHA_INSERT>'||COALESCE(NEW.CREXP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CREXP_FECHA_INSERT>'||
			'<CREXP_USUARIO_INSERT>'||COALESCE(NEW.CREXP_USUARIO_INSERT, '')||'</CREXP_USUARIO_INSERT>'||
			'<CREXP_FECHA_UPDATE>'||COALESCE(NEW.CREXP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CREXP_FECHA_UPDATE>'||
			'<CREXP_USUARIO_UPDATE>'||COALESCE(NEW.CREXP_USUARIO_UPDATE, '')||'</CREXP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_EXPULSION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_EXPULSION
*/
CREATE TRIGGER TRG_AUD_CGG_RES_EXPULSION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_EXPULSION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_EXPULSION();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_EXPULSION ON SII.CGG_RES_EXPULSION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_EXPULSION DISABLE TRIGGER TRG_AUD_CGG_RES_EXPULSION;

