/**
* FUNCION SII.TRG_KG_CGG_KDX_COMPRA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_COMPRA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_COMPRA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKCMP_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKCMP_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKCMP_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKCMP_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKCMP_CODIGO>'||COALESCE(OLD.CKCMP_CODIGO, '')||'</CKCMP_CODIGO>'||
			'<CKCMP_LOTE>'||COALESCE(OLD.CKCMP_LOTE, 0)||'</CKCMP_LOTE>'||
			'<CKCMP_FECHA_INGRESO>'||COALESCE(OLD.CKCMP_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CKCMP_FECHA_INGRESO>'||
			'<CKCMP_SERIE>'||COALESCE(OLD.CKCMP_SERIE, '')||'</CKCMP_SERIE>'||
			'<CKCMP_PROCEDENCIA>'||COALESCE(OLD.CKCMP_PROCEDENCIA, '')||'</CKCMP_PROCEDENCIA>'||
			'<CKCMP_CANTIDAD>'||COALESCE(OLD.CKCMP_CANTIDAD, 0)||'</CKCMP_CANTIDAD>'||
			'<CKCMP_FACTURA>'||COALESCE(OLD.CKCMP_FACTURA, '')||'</CKCMP_FACTURA>'||
			'<CKCMP_TOTAL>'||COALESCE(OLD.CKCMP_TOTAL, '')||'</CKCMP_TOTAL>'||
			'<CKCMP_ESTADO>'||COALESCE(OLD.CKCMP_ESTADO, FALSE)||'</CKCMP_ESTADO>'||
			'<CKCMP_FECHA_INSERT>'||COALESCE(OLD.CKCMP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKCMP_FECHA_INSERT>'||
			'<CKCMP_USUARIO_INSERT>'||COALESCE(OLD.CKCMP_USUARIO_INSERT, '')||'</CKCMP_USUARIO_INSERT>'||
			'<CKCMP_FECHA_UPDATE>'||COALESCE(OLD.CKCMP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKCMP_FECHA_UPDATE>'||
			'<CKCMP_USUARIO_UPDATE>'||COALESCE(OLD.CKCMP_USUARIO_UPDATE, '')||'</CKCMP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKCMP_CODIGO>'||COALESCE(NEW.CKCMP_CODIGO, '')||'</CKCMP_CODIGO>'||
			'<CKCMP_LOTE>'||COALESCE(NEW.CKCMP_LOTE, 0)||'</CKCMP_LOTE>'||
			'<CKCMP_FECHA_INGRESO>'||COALESCE(NEW.CKCMP_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CKCMP_FECHA_INGRESO>'||
			'<CKCMP_SERIE>'||COALESCE(NEW.CKCMP_SERIE, '')||'</CKCMP_SERIE>'||
			'<CKCMP_PROCEDENCIA>'||COALESCE(NEW.CKCMP_PROCEDENCIA, '')||'</CKCMP_PROCEDENCIA>'||
			'<CKCMP_CANTIDAD>'||COALESCE(NEW.CKCMP_CANTIDAD, 0)||'</CKCMP_CANTIDAD>'||
			'<CKCMP_FACTURA>'||COALESCE(NEW.CKCMP_FACTURA, '')||'</CKCMP_FACTURA>'||
			'<CKCMP_TOTAL>'||COALESCE(NEW.CKCMP_TOTAL, '')||'</CKCMP_TOTAL>'||
			'<CKCMP_ESTADO>'||COALESCE(NEW.CKCMP_ESTADO, FALSE)||'</CKCMP_ESTADO>'||
			'<CKCMP_FECHA_INSERT>'||COALESCE(NEW.CKCMP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKCMP_FECHA_INSERT>'||
			'<CKCMP_USUARIO_INSERT>'||COALESCE(NEW.CKCMP_USUARIO_INSERT, '')||'</CKCMP_USUARIO_INSERT>'||
			'<CKCMP_FECHA_UPDATE>'||COALESCE(NEW.CKCMP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKCMP_FECHA_UPDATE>'||
			'<CKCMP_USUARIO_UPDATE>'||COALESCE(NEW.CKCMP_USUARIO_UPDATE, '')||'</CKCMP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_COMPRA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_COMPRA
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_COMPRA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_COMPRA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_COMPRA();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_COMPRA ON SII.CGG_KDX_COMPRA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_COMPRA DISABLE TRIGGER TRG_AUD_CGG_KDX_COMPRA;

