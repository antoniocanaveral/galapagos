/**
* FUNCION SII.TRG_KG_CGG_TCT_GRUPO_TURISTA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_GRUPO_TURISTA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_TCT_GRUPO_TURISTA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CTGTR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTGTR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CTGTR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTGTR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CTGTR_CODIGO>'||COALESCE(OLD.CTGTR_CODIGO, '')||'</CTGTR_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CTADC_CODIGO>'||COALESCE(OLD.CTADC_CODIGO, '')||'</CTADC_CODIGO>'||
			'<CTGTR_NUMERO>'||COALESCE(OLD.CTGTR_NUMERO, 0)||'</CTGTR_NUMERO>'||
			'<CTGTR_FECHA_RESERVA>'||COALESCE(OLD.CTGTR_FECHA_RESERVA, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_RESERVA>'||
			'<CTGTR_FECHA_INGRESO>'||COALESCE(OLD.CTGTR_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_INGRESO>'||
			'<CTGTR_FECHA_SALIDA>'||COALESCE(OLD.CTGTR_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_SALIDA>'||
			'<CTGTR_ESTADO>'||COALESCE(OLD.CTGTR_ESTADO, FALSE)||'</CTGTR_ESTADO>'||
			'<CTGTR_FECHA_INSERT>'||COALESCE(OLD.CTGTR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_INSERT>'||
			'<CTGTR_USUARIO_INSERT>'||COALESCE(OLD.CTGTR_USUARIO_INSERT, '')||'</CTGTR_USUARIO_INSERT>'||
			'<CTGTR_FECHA_UPDATE>'||COALESCE(OLD.CTGTR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_UPDATE>'||
			'<CTGTR_USUARIO_UPDATE>'||COALESCE(OLD.CTGTR_USUARIO_UPDATE, '')||'</CTGTR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CTGTR_CODIGO>'||COALESCE(NEW.CTGTR_CODIGO, '')||'</CTGTR_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CTADC_CODIGO>'||COALESCE(NEW.CTADC_CODIGO, '')||'</CTADC_CODIGO>'||
			'<CTGTR_NUMERO>'||COALESCE(NEW.CTGTR_NUMERO, 0)||'</CTGTR_NUMERO>'||
			'<CTGTR_FECHA_RESERVA>'||COALESCE(NEW.CTGTR_FECHA_RESERVA, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_RESERVA>'||
			'<CTGTR_FECHA_INGRESO>'||COALESCE(NEW.CTGTR_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_INGRESO>'||
			'<CTGTR_FECHA_SALIDA>'||COALESCE(NEW.CTGTR_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_SALIDA>'||
			'<CTGTR_ESTADO>'||COALESCE(NEW.CTGTR_ESTADO, FALSE)||'</CTGTR_ESTADO>'||
			'<CTGTR_FECHA_INSERT>'||COALESCE(NEW.CTGTR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_INSERT>'||
			'<CTGTR_USUARIO_INSERT>'||COALESCE(NEW.CTGTR_USUARIO_INSERT, '')||'</CTGTR_USUARIO_INSERT>'||
			'<CTGTR_FECHA_UPDATE>'||COALESCE(NEW.CTGTR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTGTR_FECHA_UPDATE>'||
			'<CTGTR_USUARIO_UPDATE>'||COALESCE(NEW.CTGTR_USUARIO_UPDATE, '')||'</CTGTR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_TCT_GRUPO_TURISTA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_GRUPO_TURISTA
*/
CREATE TRIGGER TRG_AUD_CGG_TCT_GRUPO_TURISTA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_TCT_GRUPO_TURISTA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_TCT_GRUPO_TURISTA();
COMMENT ON TRIGGER TRG_AUD_CGG_TCT_GRUPO_TURISTA ON SII.CGG_TCT_GRUPO_TURISTA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_TCT_GRUPO_TURISTA DISABLE TRIGGER TRG_AUD_CGG_TCT_GRUPO_TURISTA;

