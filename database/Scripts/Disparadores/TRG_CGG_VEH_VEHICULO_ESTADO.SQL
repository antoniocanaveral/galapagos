/**
* FUNCION SII.TRG_KG_CGG_VEH_VEHICULO_ESTADO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_VEHICULO_ESTADO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_VEH_VEHICULO_ESTADO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CVSVH_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVSVH_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CVSVH_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVSVH_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CVSVH_CODIGO>'||COALESCE(OLD.CVSVH_CODIGO, '')||'</CVSVH_CODIGO>'||
			'<CVVEH_CODIGO>'||COALESCE(OLD.CVVEH_CODIGO, '')||'</CVVEH_CODIGO>'||
			'<CVSVM_CODIGO>'||COALESCE(OLD.CVSVM_CODIGO, '')||'</CVSVM_CODIGO>'||
			'<CVSVH_FECHA>'||COALESCE(OLD.CVSVH_FECHA, CURRENT_TIMESTAMP)||'</CVSVH_FECHA>'||
			'<CVSVH_OBSERVACION>'||COALESCE(OLD.CVSVH_OBSERVACION, '')||'</CVSVH_OBSERVACION>'||
			'<CVSVH_ESTADO>'||COALESCE(OLD.CVSVH_ESTADO, FALSE)||'</CVSVH_ESTADO>'||
			'<CVSVH_FECHA_INSERT>'||COALESCE(OLD.CVSVH_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVSVH_FECHA_INSERT>'||
			'<CVSVH_USUARIO_INSERT>'||COALESCE(OLD.CVSVH_USUARIO_INSERT, '')||'</CVSVH_USUARIO_INSERT>'||
			'<CVSVH_FECHA_UPDATE>'||COALESCE(OLD.CVSVH_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVSVH_FECHA_UPDATE>'||
			'<CVSVH_USUARIO_UPDATE>'||COALESCE(OLD.CVSVH_USUARIO_UPDATE, '')||'</CVSVH_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CVSVH_CODIGO>'||COALESCE(NEW.CVSVH_CODIGO, '')||'</CVSVH_CODIGO>'||
			'<CVVEH_CODIGO>'||COALESCE(NEW.CVVEH_CODIGO, '')||'</CVVEH_CODIGO>'||
			'<CVSVM_CODIGO>'||COALESCE(NEW.CVSVM_CODIGO, '')||'</CVSVM_CODIGO>'||
			'<CVSVH_FECHA>'||COALESCE(NEW.CVSVH_FECHA, CURRENT_TIMESTAMP)||'</CVSVH_FECHA>'||
			'<CVSVH_OBSERVACION>'||COALESCE(NEW.CVSVH_OBSERVACION, '')||'</CVSVH_OBSERVACION>'||
			'<CVSVH_ESTADO>'||COALESCE(NEW.CVSVH_ESTADO, FALSE)||'</CVSVH_ESTADO>'||
			'<CVSVH_FECHA_INSERT>'||COALESCE(NEW.CVSVH_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVSVH_FECHA_INSERT>'||
			'<CVSVH_USUARIO_INSERT>'||COALESCE(NEW.CVSVH_USUARIO_INSERT, '')||'</CVSVH_USUARIO_INSERT>'||
			'<CVSVH_FECHA_UPDATE>'||COALESCE(NEW.CVSVH_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVSVH_FECHA_UPDATE>'||
			'<CVSVH_USUARIO_UPDATE>'||COALESCE(NEW.CVSVH_USUARIO_UPDATE, '')||'</CVSVH_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_VEH_VEHICULO_ESTADO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_VEHICULO_ESTADO
*/
CREATE TRIGGER TRG_AUD_CGG_VEH_VEHICULO_ESTADO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_VEH_VEHICULO_ESTADO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_VEH_VEHICULO_ESTADO();
COMMENT ON TRIGGER TRG_AUD_CGG_VEH_VEHICULO_ESTADO ON SII.CGG_VEH_VEHICULO_ESTADO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_VEH_VEHICULO_ESTADO DISABLE TRIGGER TRG_AUD_CGG_VEH_VEHICULO_ESTADO;

