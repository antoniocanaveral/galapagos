/**
* FUNCION SII.TRG_KG_CGG_RES_INFORME_PAREJA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_INFORME_PAREJA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_INFORME_PAREJA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRINF_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRINF_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRINF_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRINF_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRINF_CODIGO>'||COALESCE(OLD.CRINF_CODIGO, '')||'</CRINF_CODIGO>'||
			'<CROFM_CODIGO>'||COALESCE(OLD.CROFM_CODIGO, '')||'</CROFM_CODIGO>'||
			'<CRINF_FECHA_ENTREGA>'||COALESCE(OLD.CRINF_FECHA_ENTREGA, CURRENT_TIMESTAMP)||'</CRINF_FECHA_ENTREGA>'||
			'<CRINF_EXTRACTO>'||COALESCE(OLD.CRINF_EXTRACTO, '')||'</CRINF_EXTRACTO>'||
			'<CRINF_NUMERO_DOCUMENTO>'||COALESCE(OLD.CRINF_NUMERO_DOCUMENTO, '')||'</CRINF_NUMERO_DOCUMENTO>'||
			'<CRINF_OBSERVACION>'||COALESCE(OLD.CRINF_OBSERVACION, '')||'</CRINF_OBSERVACION>'||
			'<CRINF_ESTADO>'||COALESCE(OLD.CRINF_ESTADO, FALSE)||'</CRINF_ESTADO>'||
			'<CRINF_FECHA_INSERT>'||COALESCE(OLD.CRINF_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRINF_FECHA_INSERT>'||
			'<CRINF_USUARIO_INSERT>'||COALESCE(OLD.CRINF_USUARIO_INSERT, '')||'</CRINF_USUARIO_INSERT>'||
			'<CRINF_FECHA_UPDATE>'||COALESCE(OLD.CRINF_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRINF_FECHA_UPDATE>'||
			'<CRINF_USUARIO_UPDATE>'||COALESCE(OLD.CRINF_USUARIO_UPDATE, '')||'</CRINF_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRINF_CODIGO>'||COALESCE(NEW.CRINF_CODIGO, '')||'</CRINF_CODIGO>'||
			'<CROFM_CODIGO>'||COALESCE(NEW.CROFM_CODIGO, '')||'</CROFM_CODIGO>'||
			'<CRINF_FECHA_ENTREGA>'||COALESCE(NEW.CRINF_FECHA_ENTREGA, CURRENT_TIMESTAMP)||'</CRINF_FECHA_ENTREGA>'||
			'<CRINF_EXTRACTO>'||COALESCE(NEW.CRINF_EXTRACTO, '')||'</CRINF_EXTRACTO>'||
			'<CRINF_NUMERO_DOCUMENTO>'||COALESCE(NEW.CRINF_NUMERO_DOCUMENTO, '')||'</CRINF_NUMERO_DOCUMENTO>'||
			'<CRINF_OBSERVACION>'||COALESCE(NEW.CRINF_OBSERVACION, '')||'</CRINF_OBSERVACION>'||
			'<CRINF_ESTADO>'||COALESCE(NEW.CRINF_ESTADO, FALSE)||'</CRINF_ESTADO>'||
			'<CRINF_FECHA_INSERT>'||COALESCE(NEW.CRINF_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRINF_FECHA_INSERT>'||
			'<CRINF_USUARIO_INSERT>'||COALESCE(NEW.CRINF_USUARIO_INSERT, '')||'</CRINF_USUARIO_INSERT>'||
			'<CRINF_FECHA_UPDATE>'||COALESCE(NEW.CRINF_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRINF_FECHA_UPDATE>'||
			'<CRINF_USUARIO_UPDATE>'||COALESCE(NEW.CRINF_USUARIO_UPDATE, '')||'</CRINF_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_INFORME_PAREJA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_INFORME_PAREJA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_INFORME_PAREJA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_INFORME_PAREJA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_INFORME_PAREJA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_INFORME_PAREJA ON SII.CGG_RES_INFORME_PAREJA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_INFORME_PAREJA DISABLE TRIGGER TRG_AUD_CGG_RES_INFORME_PAREJA;

