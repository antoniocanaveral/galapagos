/**
* FUNCION SII.TRG_KG_CGG_RES_ADJUNTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_ADJUNTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_ADJUNTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRADJ_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRADJ_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRADJ_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRADJ_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRADJ_CODIGO>'||COALESCE(OLD.CRADJ_CODIGO, '')||'</CRADJ_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRINF_CODIGO>'||COALESCE(OLD.CRINF_CODIGO, '')||'</CRINF_CODIGO>'||
			'<CRNOT_CODIGO>'||COALESCE(OLD.CRNOT_CODIGO, '')||'</CRNOT_CODIGO>'||
			'<CRRES_CODIGO>'||COALESCE(OLD.CRRES_CODIGO, '')||'</CRRES_CODIGO>'||
			'<CRDPT_CODIGO>'||COALESCE(OLD.CRDPT_CODIGO, '')||'</CRDPT_CODIGO>'||
			'<CRSEG_CODIGO>'||COALESCE(OLD.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CRTPT_CODIGO>'||COALESCE(OLD.CRTPT_CODIGO, '')||'</CRTPT_CODIGO>'||
			'<CRSSC_CODIGO>'||COALESCE(OLD.CRSSC_CODIGO, '')||'</CRSSC_CODIGO>'||
			'<CRRQT_CODIGO>'||COALESCE(OLD.CRRQT_CODIGO, '')||'</CRRQT_CODIGO>'||
			'<CVVEH_CODIGO>'||COALESCE(OLD.CVVEH_CODIGO, '')||'</CVVEH_CODIGO>'||
			'<CRADJ_CONTENIDO>'||COALESCE(OLD.CRADJ_CONTENIDO, '')||'</CRADJ_CONTENIDO>'||
			'<CRADJ_NOMBRE_ADJUNTO>'||COALESCE(OLD.CRADJ_NOMBRE_ADJUNTO, '')||'</CRADJ_NOMBRE_ADJUNTO>'||
			'<CRADJ_OBSERVACION>'||COALESCE(OLD.CRADJ_OBSERVACION, '')||'</CRADJ_OBSERVACION>'||
			'<CRADJ_ARCHIVO_ADJUNTO>'||COALESCE(encode(OLD.CRADJ_ARCHIVO_ADJUNTO, 'base64'), '')||'</CRADJ_ARCHIVO_ADJUNTO>'||
			'<CRADJ_FECHA_REGISTRO>'||COALESCE(OLD.CRADJ_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CRADJ_FECHA_REGISTRO>'||
			'<CRADJ_ESTADO>'||COALESCE(OLD.CRADJ_ESTADO, FALSE)||'</CRADJ_ESTADO>'||
			'<CRADJ_FECHA_INSERT>'||COALESCE(OLD.CRADJ_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRADJ_FECHA_INSERT>'||
			'<CRADJ_USUARIO_INSERT>'||COALESCE(OLD.CRADJ_USUARIO_INSERT, '')||'</CRADJ_USUARIO_INSERT>'||
			'<CRADJ_FECHA_UPDATE>'||COALESCE(OLD.CRADJ_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRADJ_FECHA_UPDATE>'||
			'<CRADJ_USUARIO_UPDATE>'||COALESCE(OLD.CRADJ_USUARIO_UPDATE, '')||'</CRADJ_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRADJ_CODIGO>'||COALESCE(NEW.CRADJ_CODIGO, '')||'</CRADJ_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRINF_CODIGO>'||COALESCE(NEW.CRINF_CODIGO, '')||'</CRINF_CODIGO>'||
			'<CRNOT_CODIGO>'||COALESCE(NEW.CRNOT_CODIGO, '')||'</CRNOT_CODIGO>'||
			'<CRRES_CODIGO>'||COALESCE(NEW.CRRES_CODIGO, '')||'</CRRES_CODIGO>'||
			'<CRDPT_CODIGO>'||COALESCE(NEW.CRDPT_CODIGO, '')||'</CRDPT_CODIGO>'||
			'<CRSEG_CODIGO>'||COALESCE(NEW.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CRTPT_CODIGO>'||COALESCE(NEW.CRTPT_CODIGO, '')||'</CRTPT_CODIGO>'||
			'<CRSSC_CODIGO>'||COALESCE(NEW.CRSSC_CODIGO, '')||'</CRSSC_CODIGO>'||
			'<CRRQT_CODIGO>'||COALESCE(NEW.CRRQT_CODIGO, '')||'</CRRQT_CODIGO>'||
			'<CVVEH_CODIGO>'||COALESCE(NEW.CVVEH_CODIGO, '')||'</CVVEH_CODIGO>'||
			'<CRADJ_CONTENIDO>'||COALESCE(NEW.CRADJ_CONTENIDO, '')||'</CRADJ_CONTENIDO>'||
			'<CRADJ_NOMBRE_ADJUNTO>'||COALESCE(NEW.CRADJ_NOMBRE_ADJUNTO, '')||'</CRADJ_NOMBRE_ADJUNTO>'||
			'<CRADJ_OBSERVACION>'||COALESCE(NEW.CRADJ_OBSERVACION, '')||'</CRADJ_OBSERVACION>'||
			'<CRADJ_ARCHIVO_ADJUNTO>'||COALESCE(encode(NEW.CRADJ_ARCHIVO_ADJUNTO, 'base64'), '')||'</CRADJ_ARCHIVO_ADJUNTO>'||
			'<CRADJ_FECHA_REGISTRO>'||COALESCE(NEW.CRADJ_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CRADJ_FECHA_REGISTRO>'||
			'<CRADJ_ESTADO>'||COALESCE(NEW.CRADJ_ESTADO, FALSE)||'</CRADJ_ESTADO>'||
			'<CRADJ_FECHA_INSERT>'||COALESCE(NEW.CRADJ_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRADJ_FECHA_INSERT>'||
			'<CRADJ_USUARIO_INSERT>'||COALESCE(NEW.CRADJ_USUARIO_INSERT, '')||'</CRADJ_USUARIO_INSERT>'||
			'<CRADJ_FECHA_UPDATE>'||COALESCE(NEW.CRADJ_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRADJ_FECHA_UPDATE>'||
			'<CRADJ_USUARIO_UPDATE>'||COALESCE(NEW.CRADJ_USUARIO_UPDATE, '')||'</CRADJ_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_ADJUNTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_ADJUNTO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_ADJUNTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_ADJUNTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_ADJUNTO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_ADJUNTO ON SII.CGG_RES_ADJUNTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_ADJUNTO DISABLE TRIGGER TRG_AUD_CGG_RES_ADJUNTO;

