/**
* FUNCION SII.TRG_KG_CGG_RES_MATRIMONIO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_MATRIMONIO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_MATRIMONIO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRMTR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRMTR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRMTR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRMTR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRMTR_CODIGO>'||COALESCE(OLD.CRMTR_CODIGO, '')||'</CRMTR_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(OLD.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CRMTR_FECHA_MATRIMONIO>'||COALESCE(OLD.CRMTR_FECHA_MATRIMONIO, CURRENT_TIMESTAMP)||'</CRMTR_FECHA_MATRIMONIO>'||
			'<CRMTR_TIPO>'||COALESCE(OLD.CRMTR_TIPO, 0)||'</CRMTR_TIPO>'||
			'<CRMTR_ESTADO_SEGUIMIENTO>'||COALESCE(OLD.CRMTR_ESTADO_SEGUIMIENTO, 0)||'</CRMTR_ESTADO_SEGUIMIENTO>'||
			'<CRMTR_ESTADO>'||COALESCE(OLD.CRMTR_ESTADO, FALSE)||'</CRMTR_ESTADO>'||
			'<CRMTR_FECHA_INSERT>'||COALESCE(OLD.CRMTR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRMTR_FECHA_INSERT>'||
			'<CRMTR_USUARIO_INSERT>'||COALESCE(OLD.CRMTR_USUARIO_INSERT, '')||'</CRMTR_USUARIO_INSERT>'||
			'<CRMTR_FECHA_UPDATE>'||COALESCE(OLD.CRMTR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRMTR_FECHA_UPDATE>'||
			'<CRMTR_USUARIO_UPDATE>'||COALESCE(OLD.CRMTR_USUARIO_UPDATE, '')||'</CRMTR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRMTR_CODIGO>'||COALESCE(NEW.CRMTR_CODIGO, '')||'</CRMTR_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(NEW.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CRMTR_FECHA_MATRIMONIO>'||COALESCE(NEW.CRMTR_FECHA_MATRIMONIO, CURRENT_TIMESTAMP)||'</CRMTR_FECHA_MATRIMONIO>'||
			'<CRMTR_TIPO>'||COALESCE(NEW.CRMTR_TIPO, 0)||'</CRMTR_TIPO>'||
			'<CRMTR_ESTADO_SEGUIMIENTO>'||COALESCE(NEW.CRMTR_ESTADO_SEGUIMIENTO, 0)||'</CRMTR_ESTADO_SEGUIMIENTO>'||
			'<CRMTR_ESTADO>'||COALESCE(NEW.CRMTR_ESTADO, FALSE)||'</CRMTR_ESTADO>'||
			'<CRMTR_FECHA_INSERT>'||COALESCE(NEW.CRMTR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRMTR_FECHA_INSERT>'||
			'<CRMTR_USUARIO_INSERT>'||COALESCE(NEW.CRMTR_USUARIO_INSERT, '')||'</CRMTR_USUARIO_INSERT>'||
			'<CRMTR_FECHA_UPDATE>'||COALESCE(NEW.CRMTR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRMTR_FECHA_UPDATE>'||
			'<CRMTR_USUARIO_UPDATE>'||COALESCE(NEW.CRMTR_USUARIO_UPDATE, '')||'</CRMTR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_MATRIMONIO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_MATRIMONIO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_MATRIMONIO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_MATRIMONIO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_MATRIMONIO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_MATRIMONIO ON SII.CGG_RES_MATRIMONIO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_MATRIMONIO DISABLE TRIGGER TRG_AUD_CGG_RES_MATRIMONIO;

