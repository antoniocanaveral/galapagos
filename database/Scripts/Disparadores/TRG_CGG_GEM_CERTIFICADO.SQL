/**
* FUNCION SII.TRG_KG_CGG_GEM_CERTIFICADO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_CERTIFICADO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GEM_CERTIFICADO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGCER_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGCER_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGCER_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGCER_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGCER_CODIGO>'||COALESCE(OLD.CGCER_CODIGO, '')||'</CGCER_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGCER_INSTITUCION>'||COALESCE(OLD.CGCER_INSTITUCION, '')||'</CGCER_INSTITUCION>'||
			'<CGCER_NOMBRE>'||COALESCE(OLD.CGCER_NOMBRE, '')||'</CGCER_NOMBRE>'||
			'<CGCER_DESCRIPCION>'||COALESCE(OLD.CGCER_DESCRIPCION, '')||'</CGCER_DESCRIPCION>'||
			'<CGCER_DURACION>'||COALESCE(OLD.CGCER_DURACION, 0)||'</CGCER_DURACION>'||
			'<CGCER_FECHA_INICIO>'||COALESCE(OLD.CGCER_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CGCER_FECHA_INICIO>'||
			'<CGCER_FECHA_FIN>'||COALESCE(OLD.CGCER_FECHA_FIN, CURRENT_TIMESTAMP)||'</CGCER_FECHA_FIN>'||
			'<CGCER_TIPO_ADJUNTO>'||COALESCE(OLD.CGCER_TIPO_ADJUNTO, '')||'</CGCER_TIPO_ADJUNTO>'||
			'<CGCER_NOMBRE_ADJUNTO>'||COALESCE(OLD.CGCER_NOMBRE_ADJUNTO, '')||'</CGCER_NOMBRE_ADJUNTO>'||
			'<CGCER_ADJUNTO>'||COALESCE(encode(OLD.CGCER_ADJUNTO, 'base64'), '')||'</CGCER_ADJUNTO>'||
			'<CGCER_OBSERVACION>'||COALESCE(OLD.CGCER_OBSERVACION, '')||'</CGCER_OBSERVACION>'||
			'<CGCER_TIPO>'||COALESCE(OLD.CGCER_TIPO, 0)||'</CGCER_TIPO>'||
			'<CGCER_ESTADO>'||COALESCE(OLD.CGCER_ESTADO, FALSE)||'</CGCER_ESTADO>'||
			'<CGCER_FECHA_INSERT>'||COALESCE(OLD.CGCER_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGCER_FECHA_INSERT>'||
			'<CGCER_USUARIO_INSERT>'||COALESCE(OLD.CGCER_USUARIO_INSERT, '')||'</CGCER_USUARIO_INSERT>'||
			'<CGCER_FECHA_UPDATE>'||COALESCE(OLD.CGCER_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGCER_FECHA_UPDATE>'||
			'<CGCER_USUARIO_UPDATE>'||COALESCE(OLD.CGCER_USUARIO_UPDATE, '')||'</CGCER_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGCER_CODIGO>'||COALESCE(NEW.CGCER_CODIGO, '')||'</CGCER_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGCER_INSTITUCION>'||COALESCE(NEW.CGCER_INSTITUCION, '')||'</CGCER_INSTITUCION>'||
			'<CGCER_NOMBRE>'||COALESCE(NEW.CGCER_NOMBRE, '')||'</CGCER_NOMBRE>'||
			'<CGCER_DESCRIPCION>'||COALESCE(NEW.CGCER_DESCRIPCION, '')||'</CGCER_DESCRIPCION>'||
			'<CGCER_DURACION>'||COALESCE(NEW.CGCER_DURACION, 0)||'</CGCER_DURACION>'||
			'<CGCER_FECHA_INICIO>'||COALESCE(NEW.CGCER_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CGCER_FECHA_INICIO>'||
			'<CGCER_FECHA_FIN>'||COALESCE(NEW.CGCER_FECHA_FIN, CURRENT_TIMESTAMP)||'</CGCER_FECHA_FIN>'||
			'<CGCER_TIPO_ADJUNTO>'||COALESCE(NEW.CGCER_TIPO_ADJUNTO, '')||'</CGCER_TIPO_ADJUNTO>'||
			'<CGCER_NOMBRE_ADJUNTO>'||COALESCE(NEW.CGCER_NOMBRE_ADJUNTO, '')||'</CGCER_NOMBRE_ADJUNTO>'||
			'<CGCER_ADJUNTO>'||COALESCE(encode(NEW.CGCER_ADJUNTO, 'base64'), '')||'</CGCER_ADJUNTO>'||
			'<CGCER_OBSERVACION>'||COALESCE(NEW.CGCER_OBSERVACION, '')||'</CGCER_OBSERVACION>'||
			'<CGCER_TIPO>'||COALESCE(NEW.CGCER_TIPO, 0)||'</CGCER_TIPO>'||
			'<CGCER_ESTADO>'||COALESCE(NEW.CGCER_ESTADO, FALSE)||'</CGCER_ESTADO>'||
			'<CGCER_FECHA_INSERT>'||COALESCE(NEW.CGCER_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGCER_FECHA_INSERT>'||
			'<CGCER_USUARIO_INSERT>'||COALESCE(NEW.CGCER_USUARIO_INSERT, '')||'</CGCER_USUARIO_INSERT>'||
			'<CGCER_FECHA_UPDATE>'||COALESCE(NEW.CGCER_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGCER_FECHA_UPDATE>'||
			'<CGCER_USUARIO_UPDATE>'||COALESCE(NEW.CGCER_USUARIO_UPDATE, '')||'</CGCER_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GEM_CERTIFICADO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_CERTIFICADO
*/
CREATE TRIGGER TRG_AUD_CGG_GEM_CERTIFICADO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GEM_CERTIFICADO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GEM_CERTIFICADO();
COMMENT ON TRIGGER TRG_AUD_CGG_GEM_CERTIFICADO ON SII.CGG_GEM_CERTIFICADO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GEM_CERTIFICADO DISABLE TRIGGER TRG_AUD_CGG_GEM_CERTIFICADO;

