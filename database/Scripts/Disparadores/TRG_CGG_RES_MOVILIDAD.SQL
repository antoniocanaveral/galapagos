/**
* FUNCION SII.TRG_KG_CGG_RES_MOVILIDAD. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_MOVILIDAD
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_MOVILIDAD() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRMOV_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRMOV_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRMOV_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRMOV_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRMOV_CODIGO>'||COALESCE(OLD.CRMOV_CODIGO, '')||'</CRMOV_CODIGO>'||
			'<CRALN_CODIGO>'||COALESCE(OLD.CRALN_CODIGO, '')||'</CRALN_CODIGO>'||
			'<CARPT_CODIGO>'||COALESCE(OLD.CARPT_CODIGO, '')||'</CARPT_CODIGO>'||
			'<CGG_CARPT_CODIGO>'||COALESCE(OLD.CGG_CARPT_CODIGO, '')||'</CGG_CARPT_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CTREG_CODIGO>'||COALESCE(OLD.CTREG_CODIGO, '')||'</CTREG_CODIGO>'||
			'<CRRSD_CODIGO>'||COALESCE(OLD.CRRSD_CODIGO, '')||'</CRRSD_CODIGO>'||
			'<CRMOV_FECHA_VIAJE>'||COALESCE(OLD.CRMOV_FECHA_VIAJE, CURRENT_TIMESTAMP)||'</CRMOV_FECHA_VIAJE>'||
			'<CRMOV_TIPO_OPERACION>'||COALESCE(OLD.CRMOV_TIPO_OPERACION, 0)||'</CRMOV_TIPO_OPERACION>'||
			'<CRMOV_NUMERO_VUELO>'||COALESCE(OLD.CRMOV_NUMERO_VUELO, '')||'</CRMOV_NUMERO_VUELO>'||
			'<CRMOV_OBSERVACION>'||COALESCE(OLD.CRMOV_OBSERVACION, '')||'</CRMOV_OBSERVACION>'||
			'<CRMOV_TIPO_SALIDA>'||COALESCE(OLD.CRMOV_TIPO_SALIDA, 0)||'</CRMOV_TIPO_SALIDA>'||
			'<CRMOV_FILTRO_INTERNO>'||COALESCE(OLD.CRMOV_FILTRO_INTERNO, FALSE)||'</CRMOV_FILTRO_INTERNO>'||
			'<CRMOV_SOPORTE>'||COALESCE(OLD.CRMOV_SOPORTE, FALSE)||'</CRMOV_SOPORTE>'||
			'<CRMOV_ESTADO>'||COALESCE(OLD.CRMOV_ESTADO, FALSE)||'</CRMOV_ESTADO>'||
			'<CRMOV_FECHA_INSERT>'||COALESCE(OLD.CRMOV_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRMOV_FECHA_INSERT>'||
			'<CRMOV_USUARIO_INSERT>'||COALESCE(OLD.CRMOV_USUARIO_INSERT, '')||'</CRMOV_USUARIO_INSERT>'||
			'<CRMOV_FECHA_UPDATE>'||COALESCE(OLD.CRMOV_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRMOV_FECHA_UPDATE>'||
			'<CRMOV_USUARIO_UPDATE>'||COALESCE(OLD.CRMOV_USUARIO_UPDATE, '')||'</CRMOV_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRMOV_CODIGO>'||COALESCE(NEW.CRMOV_CODIGO, '')||'</CRMOV_CODIGO>'||
			'<CRALN_CODIGO>'||COALESCE(NEW.CRALN_CODIGO, '')||'</CRALN_CODIGO>'||
			'<CARPT_CODIGO>'||COALESCE(NEW.CARPT_CODIGO, '')||'</CARPT_CODIGO>'||
			'<CGG_CARPT_CODIGO>'||COALESCE(NEW.CGG_CARPT_CODIGO, '')||'</CGG_CARPT_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CTREG_CODIGO>'||COALESCE(NEW.CTREG_CODIGO, '')||'</CTREG_CODIGO>'||
			'<CRRSD_CODIGO>'||COALESCE(NEW.CRRSD_CODIGO, '')||'</CRRSD_CODIGO>'||
			'<CRMOV_FECHA_VIAJE>'||COALESCE(NEW.CRMOV_FECHA_VIAJE, CURRENT_TIMESTAMP)||'</CRMOV_FECHA_VIAJE>'||
			'<CRMOV_TIPO_OPERACION>'||COALESCE(NEW.CRMOV_TIPO_OPERACION, 0)||'</CRMOV_TIPO_OPERACION>'||
			'<CRMOV_NUMERO_VUELO>'||COALESCE(NEW.CRMOV_NUMERO_VUELO, '')||'</CRMOV_NUMERO_VUELO>'||
			'<CRMOV_OBSERVACION>'||COALESCE(NEW.CRMOV_OBSERVACION, '')||'</CRMOV_OBSERVACION>'||
			'<CRMOV_TIPO_SALIDA>'||COALESCE(NEW.CRMOV_TIPO_SALIDA, 0)||'</CRMOV_TIPO_SALIDA>'||
			'<CRMOV_FILTRO_INTERNO>'||COALESCE(NEW.CRMOV_FILTRO_INTERNO, FALSE)||'</CRMOV_FILTRO_INTERNO>'||
			'<CRMOV_SOPORTE>'||COALESCE(NEW.CRMOV_SOPORTE, FALSE)||'</CRMOV_SOPORTE>'||
			'<CRMOV_ESTADO>'||COALESCE(NEW.CRMOV_ESTADO, FALSE)||'</CRMOV_ESTADO>'||
			'<CRMOV_FECHA_INSERT>'||COALESCE(NEW.CRMOV_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRMOV_FECHA_INSERT>'||
			'<CRMOV_USUARIO_INSERT>'||COALESCE(NEW.CRMOV_USUARIO_INSERT, '')||'</CRMOV_USUARIO_INSERT>'||
			'<CRMOV_FECHA_UPDATE>'||COALESCE(NEW.CRMOV_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRMOV_FECHA_UPDATE>'||
			'<CRMOV_USUARIO_UPDATE>'||COALESCE(NEW.CRMOV_USUARIO_UPDATE, '')||'</CRMOV_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_MOVILIDAD. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_MOVILIDAD
*/
CREATE TRIGGER TRG_AUD_CGG_RES_MOVILIDAD AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_MOVILIDAD FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_MOVILIDAD();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_MOVILIDAD ON SII.CGG_RES_MOVILIDAD IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_MOVILIDAD DISABLE TRIGGER TRG_AUD_CGG_RES_MOVILIDAD;

