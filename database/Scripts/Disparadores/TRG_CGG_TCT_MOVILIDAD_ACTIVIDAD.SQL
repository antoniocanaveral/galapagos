/**
* FUNCION SII.TRG_KG_CGG_TCT_MOVILIDAD_ACTIVIDAD. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_MOVILIDAD_ACTIVIDAD
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_TCT_MOVILIDAD_ACTIVIDAD() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CTMVC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTMVC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CTMVC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTMVC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CTMVC_CODIGO>'||COALESCE(OLD.CTMVC_CODIGO, '')||'</CTMVC_CODIGO>'||
			'<CTACT_CODIGO>'||COALESCE(OLD.CTACT_CODIGO, '')||'</CTACT_CODIGO>'||
			'<CRMOV_CODIGO>'||COALESCE(OLD.CRMOV_CODIGO, '')||'</CRMOV_CODIGO>'||
			'<CTMVC_ESTADO>'||COALESCE(OLD.CTMVC_ESTADO, FALSE)||'</CTMVC_ESTADO>'||
			'<CTMVC_FECHA_INSERT>'||COALESCE(OLD.CTMVC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTMVC_FECHA_INSERT>'||
			'<CTMVC_USUARIO_INSERT>'||COALESCE(OLD.CTMVC_USUARIO_INSERT, '')||'</CTMVC_USUARIO_INSERT>'||
			'<CTMVC_FECHA_UPDATE>'||COALESCE(OLD.CTMVC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTMVC_FECHA_UPDATE>'||
			'<CTMVC_USUARIO_UPDATE>'||COALESCE(OLD.CTMVC_USUARIO_UPDATE, '')||'</CTMVC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CTMVC_CODIGO>'||COALESCE(NEW.CTMVC_CODIGO, '')||'</CTMVC_CODIGO>'||
			'<CTACT_CODIGO>'||COALESCE(NEW.CTACT_CODIGO, '')||'</CTACT_CODIGO>'||
			'<CRMOV_CODIGO>'||COALESCE(NEW.CRMOV_CODIGO, '')||'</CRMOV_CODIGO>'||
			'<CTMVC_ESTADO>'||COALESCE(NEW.CTMVC_ESTADO, FALSE)||'</CTMVC_ESTADO>'||
			'<CTMVC_FECHA_INSERT>'||COALESCE(NEW.CTMVC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTMVC_FECHA_INSERT>'||
			'<CTMVC_USUARIO_INSERT>'||COALESCE(NEW.CTMVC_USUARIO_INSERT, '')||'</CTMVC_USUARIO_INSERT>'||
			'<CTMVC_FECHA_UPDATE>'||COALESCE(NEW.CTMVC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTMVC_FECHA_UPDATE>'||
			'<CTMVC_USUARIO_UPDATE>'||COALESCE(NEW.CTMVC_USUARIO_UPDATE, '')||'</CTMVC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_TCT_MOVILIDAD_ACTIVIDAD. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_MOVILIDAD_ACTIVIDAD
*/
CREATE TRIGGER TRG_AUD_CGG_TCT_MOVILIDAD_ACTIVIDAD AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_TCT_MOVILIDAD_ACTIVIDAD FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_TCT_MOVILIDAD_ACTIVIDAD();
COMMENT ON TRIGGER TRG_AUD_CGG_TCT_MOVILIDAD_ACTIVIDAD ON SII.CGG_TCT_MOVILIDAD_ACTIVIDAD IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_TCT_MOVILIDAD_ACTIVIDAD DISABLE TRIGGER TRG_AUD_CGG_TCT_MOVILIDAD_ACTIVIDAD;

