/**
* FUNCION SII.TRG_KG_CGG_REGLA_VALIDACION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_REGLA_VALIDACION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_REGLA_VALIDACION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRVAL_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRVAL_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRVAL_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRVAL_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRVAL_CODIGO>'||COALESCE(OLD.CRVAL_CODIGO, '')||'</CRVAL_CODIGO>'||
			'<CGCNF_CODIGO>'||COALESCE(OLD.CGCNF_CODIGO, '')||'</CGCNF_CODIGO>'||
			'<CRVAL_NOMBRE>'||COALESCE(OLD.CRVAL_NOMBRE, '')||'</CRVAL_NOMBRE>'||
			'<CRVAL_DESCRIPCION>'||COALESCE(OLD.CRVAL_DESCRIPCION, '')||'</CRVAL_DESCRIPCION>'||
			'<CRVAL_FUNCION_VALIDACION>'||COALESCE(OLD.CRVAL_FUNCION_VALIDACION, '')||'</CRVAL_FUNCION_VALIDACION>'||
			'<CRVAL_OPERADOR_COMPARADOR>'||COALESCE(OLD.CRVAL_OPERADOR_COMPARADOR, '')||'</CRVAL_OPERADOR_COMPARADOR>'||
			'<CRVAL_VALOR_LIBRE>'||COALESCE(OLD.CRVAL_VALOR_LIBRE, '')||'</CRVAL_VALOR_LIBRE>'||
			'<CRVAL_RESULTADO_ACEPTACION>'||COALESCE(OLD.CRVAL_RESULTADO_ACEPTACION, '')||'</CRVAL_RESULTADO_ACEPTACION>'||
			'<CRVAL_SUGERENCIA>'||COALESCE(OLD.CRVAL_SUGERENCIA, '')||'</CRVAL_SUGERENCIA>'||
			'<CRVAL_TIPO>'||COALESCE(OLD.CRVAL_TIPO, 0)||'</CRVAL_TIPO>'||
			'<CRVAR_FECHA_INICIO>'||COALESCE(OLD.CRVAR_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CRVAR_FECHA_INICIO>'||
			'<CRVAR_FECHA_FIN>'||COALESCE(OLD.CRVAR_FECHA_FIN, CURRENT_TIMESTAMP)||'</CRVAR_FECHA_FIN>'||
			'<CRVAL_VALOR_1>'||COALESCE(OLD.CRVAL_VALOR_1, '')||'</CRVAL_VALOR_1>'||
			'<CRVAL_VALOR_2>'||COALESCE(OLD.CRVAL_VALOR_2, '')||'</CRVAL_VALOR_2>'||
			'<CRVAL_ESTADO>'||COALESCE(OLD.CRVAL_ESTADO, FALSE)||'</CRVAL_ESTADO>'||
			'<CRVAL_FECHA_INSERT>'||COALESCE(OLD.CRVAL_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRVAL_FECHA_INSERT>'||
			'<CRVAL_USUARIO_INSERT>'||COALESCE(OLD.CRVAL_USUARIO_INSERT, '')||'</CRVAL_USUARIO_INSERT>'||
			'<CRVAL_FECHA_UPDATE>'||COALESCE(OLD.CRVAL_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRVAL_FECHA_UPDATE>'||
			'<CRVAL_USUARIO_UPDATE>'||COALESCE(OLD.CRVAL_USUARIO_UPDATE, '')||'</CRVAL_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRVAL_CODIGO>'||COALESCE(NEW.CRVAL_CODIGO, '')||'</CRVAL_CODIGO>'||
			'<CGCNF_CODIGO>'||COALESCE(NEW.CGCNF_CODIGO, '')||'</CGCNF_CODIGO>'||
			'<CRVAL_NOMBRE>'||COALESCE(NEW.CRVAL_NOMBRE, '')||'</CRVAL_NOMBRE>'||
			'<CRVAL_DESCRIPCION>'||COALESCE(NEW.CRVAL_DESCRIPCION, '')||'</CRVAL_DESCRIPCION>'||
			'<CRVAL_FUNCION_VALIDACION>'||COALESCE(NEW.CRVAL_FUNCION_VALIDACION, '')||'</CRVAL_FUNCION_VALIDACION>'||
			'<CRVAL_OPERADOR_COMPARADOR>'||COALESCE(NEW.CRVAL_OPERADOR_COMPARADOR, '')||'</CRVAL_OPERADOR_COMPARADOR>'||
			'<CRVAL_VALOR_LIBRE>'||COALESCE(NEW.CRVAL_VALOR_LIBRE, '')||'</CRVAL_VALOR_LIBRE>'||
			'<CRVAL_RESULTADO_ACEPTACION>'||COALESCE(NEW.CRVAL_RESULTADO_ACEPTACION, '')||'</CRVAL_RESULTADO_ACEPTACION>'||
			'<CRVAL_SUGERENCIA>'||COALESCE(NEW.CRVAL_SUGERENCIA, '')||'</CRVAL_SUGERENCIA>'||
			'<CRVAL_TIPO>'||COALESCE(NEW.CRVAL_TIPO, 0)||'</CRVAL_TIPO>'||
			'<CRVAR_FECHA_INICIO>'||COALESCE(NEW.CRVAR_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CRVAR_FECHA_INICIO>'||
			'<CRVAR_FECHA_FIN>'||COALESCE(NEW.CRVAR_FECHA_FIN, CURRENT_TIMESTAMP)||'</CRVAR_FECHA_FIN>'||
			'<CRVAL_VALOR_1>'||COALESCE(NEW.CRVAL_VALOR_1, '')||'</CRVAL_VALOR_1>'||
			'<CRVAL_VALOR_2>'||COALESCE(NEW.CRVAL_VALOR_2, '')||'</CRVAL_VALOR_2>'||
			'<CRVAL_ESTADO>'||COALESCE(NEW.CRVAL_ESTADO, FALSE)||'</CRVAL_ESTADO>'||
			'<CRVAL_FECHA_INSERT>'||COALESCE(NEW.CRVAL_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRVAL_FECHA_INSERT>'||
			'<CRVAL_USUARIO_INSERT>'||COALESCE(NEW.CRVAL_USUARIO_INSERT, '')||'</CRVAL_USUARIO_INSERT>'||
			'<CRVAL_FECHA_UPDATE>'||COALESCE(NEW.CRVAL_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRVAL_FECHA_UPDATE>'||
			'<CRVAL_USUARIO_UPDATE>'||COALESCE(NEW.CRVAL_USUARIO_UPDATE, '')||'</CRVAL_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_REGLA_VALIDACION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_REGLA_VALIDACION
*/
CREATE TRIGGER TRG_AUD_CGG_REGLA_VALIDACION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_REGLA_VALIDACION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_REGLA_VALIDACION();
COMMENT ON TRIGGER TRG_AUD_CGG_REGLA_VALIDACION ON SII.CGG_REGLA_VALIDACION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_REGLA_VALIDACION DISABLE TRIGGER TRG_AUD_CGG_REGLA_VALIDACION;

