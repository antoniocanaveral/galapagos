/**
* FUNCION SII.TRG_KG_CGG_GEM_REQUISITO_VACANTE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_REQUISITO_VACANTE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GEM_REQUISITO_VACANTE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGRVC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGRVC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGRVC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGRVC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGRVC_CODIGO>'||COALESCE(OLD.CGRVC_CODIGO, '')||'</CGRVC_CODIGO>'||
			'<CGVCN_CODIGO>'||COALESCE(OLD.CGVCN_CODIGO, '')||'</CGVCN_CODIGO>'||
			'<CGRVC_REQUISITO>'||COALESCE(OLD.CGRVC_REQUISITO, '')||'</CGRVC_REQUISITO>'||
			'<CGRVC_OBLIGATORIO>'||COALESCE(OLD.CGRVC_OBLIGATORIO, FALSE)||'</CGRVC_OBLIGATORIO>'||
			'<CGRVC_ESTADO>'||COALESCE(OLD.CGRVC_ESTADO, FALSE)||'</CGRVC_ESTADO>'||
			'<CGRVC_FECHA_INSERT>'||COALESCE(OLD.CGRVC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGRVC_FECHA_INSERT>'||
			'<CGRVC_USUARIO_INSERT>'||COALESCE(OLD.CGRVC_USUARIO_INSERT, '')||'</CGRVC_USUARIO_INSERT>'||
			'<CGRVC_FECHA_UPDATE>'||COALESCE(OLD.CGRVC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGRVC_FECHA_UPDATE>'||
			'<CGRVC_USUARIO_UPDATE>'||COALESCE(OLD.CGRVC_USUARIO_UPDATE, '')||'</CGRVC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGRVC_CODIGO>'||COALESCE(NEW.CGRVC_CODIGO, '')||'</CGRVC_CODIGO>'||
			'<CGVCN_CODIGO>'||COALESCE(NEW.CGVCN_CODIGO, '')||'</CGVCN_CODIGO>'||
			'<CGRVC_REQUISITO>'||COALESCE(NEW.CGRVC_REQUISITO, '')||'</CGRVC_REQUISITO>'||
			'<CGRVC_OBLIGATORIO>'||COALESCE(NEW.CGRVC_OBLIGATORIO, FALSE)||'</CGRVC_OBLIGATORIO>'||
			'<CGRVC_ESTADO>'||COALESCE(NEW.CGRVC_ESTADO, FALSE)||'</CGRVC_ESTADO>'||
			'<CGRVC_FECHA_INSERT>'||COALESCE(NEW.CGRVC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGRVC_FECHA_INSERT>'||
			'<CGRVC_USUARIO_INSERT>'||COALESCE(NEW.CGRVC_USUARIO_INSERT, '')||'</CGRVC_USUARIO_INSERT>'||
			'<CGRVC_FECHA_UPDATE>'||COALESCE(NEW.CGRVC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGRVC_FECHA_UPDATE>'||
			'<CGRVC_USUARIO_UPDATE>'||COALESCE(NEW.CGRVC_USUARIO_UPDATE, '')||'</CGRVC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GEM_REQUISITO_VACANTE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_REQUISITO_VACANTE
*/
CREATE TRIGGER TRG_AUD_CGG_GEM_REQUISITO_VACANTE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GEM_REQUISITO_VACANTE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GEM_REQUISITO_VACANTE();
COMMENT ON TRIGGER TRG_AUD_CGG_GEM_REQUISITO_VACANTE ON SII.CGG_GEM_REQUISITO_VACANTE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GEM_REQUISITO_VACANTE DISABLE TRIGGER TRG_AUD_CGG_GEM_REQUISITO_VACANTE;

