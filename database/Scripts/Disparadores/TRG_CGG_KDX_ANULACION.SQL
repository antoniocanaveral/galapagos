	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKANL_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKANL_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKANL_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKANL_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKANL_CODIGO>'||COALESCE(OLD.CKANL_CODIGO, '')||'</CKANL_CODIGO>'||
			'<CKASG_CODIGO>'||COALESCE(OLD.CKASG_CODIGO, '')||'</CKASG_CODIGO>'||
			'<CKUPV_CODIGO>'||COALESCE(OLD.CKUPV_CODIGO, '')||'</CKUPV_CODIGO>'||
			'<CKANL_NUMERO_ANULACION>'||COALESCE(OLD.CKANL_NUMERO_ANULACION, '')||'</CKANL_NUMERO_ANULACION>'||
			'<CKANL_FECHA_ANULACION>'||COALESCE(OLD.CKANL_FECHA_ANULACION, CURRENT_TIMESTAMP)||'</CKANL_FECHA_ANULACION>'||
			'<CKANL_CANTIDAD>'||COALESCE(OLD.CKANL_CANTIDAD, 0)||'</CKANL_CANTIDAD>'||
			'<CKANL_PRECIO_UNITARIO>'||COALESCE(OLD.CKANL_PRECIO_UNITARIO, 0)||'</CKANL_PRECIO_UNITARIO>'||
			'<CKANL_PRECIO_TOTAL>'||COALESCE(OLD.CKANL_PRECIO_TOTAL, 0)||'</CKANL_PRECIO_TOTAL>'||
			'<CKANL_SERIE_INICIO>'||COALESCE(OLD.CKANL_SERIE_INICIO, 0)||'</CKANL_SERIE_INICIO>'||
			'<CKANL_SERIE_FIN>'||COALESCE(OLD.CKANL_SERIE_FIN, 0)||'</CKANL_SERIE_FIN>'||
			'<CKANL_OBSERVACION>'||COALESCE(OLD.CKANL_OBSERVACION, '')||'</CKANL_OBSERVACION>'||
			'<CKANL_JUSTIFICACION>'||COALESCE(OLD.CKANL_JUSTIFICACION, '')||'</CKANL_JUSTIFICACION>'||
			'<CKANL_TIPO_ANULACION>'||COALESCE(OLD.CKANL_TIPO_ANULACION, 0)||'</CKANL_TIPO_ANULACION>'||
			'<CKANL_ESTADO_ANULACION>'||COALESCE(OLD.CKANL_ESTADO_ANULACION, 0)||'</CKANL_ESTADO_ANULACION>'||
			'<CKANL_ESTADO>'||COALESCE(OLD.CKANL_ESTADO, FALSE)||'</CKANL_ESTADO>'||
			'<CKANL_FECHA_INSERT>'||COALESCE(OLD.CKANL_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKANL_FECHA_INSERT>'||
			'<CKANL_USUARIO_INSERT>'||COALESCE(OLD.CKANL_USUARIO_INSERT, '')||'</CKANL_USUARIO_INSERT>'||
			'<CKANL_FECHA_UPDATE>'||COALESCE(OLD.CKANL_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKANL_FECHA_UPDATE>'||
			'<CKANL_USUARIO_UPDATE>'||COALESCE(OLD.CKANL_USUARIO_UPDATE, '')||'</CKANL_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKANL_CODIGO>'||COALESCE(NEW.CKANL_CODIGO, '')||'</CKANL_CODIGO>'||
			'<CKASG_CODIGO>'||COALESCE(NEW.CKASG_CODIGO, '')||'</CKASG_CODIGO>'||
			'<CKUPV_CODIGO>'||COALESCE(NEW.CKUPV_CODIGO, '')||'</CKUPV_CODIGO>'||
			'<CKANL_NUMERO_ANULACION>'||COALESCE(NEW.CKANL_NUMERO_ANULACION, '')||'</CKANL_NUMERO_ANULACION>'||
			'<CKANL_FECHA_ANULACION>'||COALESCE(NEW.CKANL_FECHA_ANULACION, CURRENT_TIMESTAMP)||'</CKANL_FECHA_ANULACION>'||
			'<CKANL_CANTIDAD>'||COALESCE(NEW.CKANL_CANTIDAD, 0)||'</CKANL_CANTIDAD>'||
			'<CKANL_PRECIO_UNITARIO>'||COALESCE(NEW.CKANL_PRECIO_UNITARIO, 0)||'</CKANL_PRECIO_UNITARIO>'||
			'<CKANL_PRECIO_TOTAL>'||COALESCE(NEW.CKANL_PRECIO_TOTAL, 0)||'</CKANL_PRECIO_TOTAL>'||
			'<CKANL_SERIE_INICIO>'||COALESCE(NEW.CKANL_SERIE_INICIO, 0)||'</CKANL_SERIE_INICIO>'||
			'<CKANL_SERIE_FIN>'||COALESCE(NEW.CKANL_SERIE_FIN, 0)||'</CKANL_SERIE_FIN>'||
			'<CKANL_OBSERVACION>'||COALESCE(NEW.CKANL_OBSERVACION, '')||'</CKANL_OBSERVACION>'||
			'<CKANL_JUSTIFICACION>'||COALESCE(NEW.CKANL_JUSTIFICACION, '')||'</CKANL_JUSTIFICACION>'||
			'<CKANL_TIPO_ANULACION>'||COALESCE(NEW.CKANL_TIPO_ANULACION, 0)||'</CKANL_TIPO_ANULACION>'||
			'<CKANL_ESTADO_ANULACION>'||COALESCE(NEW.CKANL_ESTADO_ANULACION, 0)||'</CKANL_ESTADO_ANULACION>'||
			'<CKANL_ESTADO>'||COALESCE(NEW.CKANL_ESTADO, FALSE)||'</CKANL_ESTADO>'||
			'<CKANL_FECHA_INSERT>'||COALESCE(NEW.CKANL_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKANL_FECHA_INSERT>'||
			'<CKANL_USUARIO_INSERT>'||COALESCE(NEW.CKANL_USUARIO_INSERT, '')||'</CKANL_USUARIO_INSERT>'||
			'<CKANL_FECHA_UPDATE>'||COALESCE(NEW.CKANL_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKANL_FECHA_UPDATE>'||
			'<CKANL_USUARIO_UPDATE>'||COALESCE(NEW.CKANL_USUARIO_UPDATE, '')||'</CKANL_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_ANULACION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_ANULACION
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_ANULACION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_ANULACION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_ANULACION();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_ANULACION ON SII.CGG_KDX_ANULACION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
