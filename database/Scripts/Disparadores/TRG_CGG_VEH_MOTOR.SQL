/**
* FUNCION SII.TRG_KG_CGG_VEH_MOTOR. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_MOTOR
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_VEH_MOTOR() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CVMTR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVMTR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CVMTR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVMTR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CVMTR_CODIGO>'||COALESCE(OLD.CVMTR_CODIGO, '')||'</CVMTR_CODIGO>'||
			'<CVCLN_CODIGO>'||COALESCE(OLD.CVCLN_CODIGO, '')||'</CVCLN_CODIGO>'||
			'<CVMRC_CODIGO>'||COALESCE(OLD.CVMRC_CODIGO, '')||'</CVMRC_CODIGO>'||
			'<CVCMB_CODIGO>'||COALESCE(OLD.CVCMB_CODIGO, '')||'</CVCMB_CODIGO>'||
			'<CVMTR_SERIE>'||COALESCE(OLD.CVMTR_SERIE, '')||'</CVMTR_SERIE>'||
			'<CVMTR_ANIO_FABRICACION>'||COALESCE(OLD.CVMTR_ANIO_FABRICACION, 0)||'</CVMTR_ANIO_FABRICACION>'||
			'<CVMTR_ESTADO_FUNCIONAL>'||COALESCE(OLD.CVMTR_ESTADO_FUNCIONAL, FALSE)||'</CVMTR_ESTADO_FUNCIONAL>'||
			'<CVMTR_TONELAJE>'||COALESCE(OLD.CVMTR_TONELAJE, 0)||'</CVMTR_TONELAJE>'||
			'<CVMTR_INGRESO>'||COALESCE(OLD.CVMTR_INGRESO, FALSE)||'</CVMTR_INGRESO>'||
			'<CVMTR_FECHA_INGRESO>'||COALESCE(OLD.CVMTR_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CVMTR_FECHA_INGRESO>'||
			'<CVMTR_SALIO>'||COALESCE(OLD.CVMTR_SALIO, FALSE)||'</CVMTR_SALIO>'||
			'<CVMTR_FECHA_SALIDA>'||COALESCE(OLD.CVMTR_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CVMTR_FECHA_SALIDA>'||
			'<CVMTR_OBSERVACION>'||COALESCE(OLD.CVMTR_OBSERVACION, '')||'</CVMTR_OBSERVACION>'||
			'<CVMTR_ESTADO>'||COALESCE(OLD.CVMTR_ESTADO, FALSE)||'</CVMTR_ESTADO>'||
			'<CVMTR_FECHA_INSERT>'||COALESCE(OLD.CVMTR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVMTR_FECHA_INSERT>'||
			'<CVMTR_USUARIO_INSERT>'||COALESCE(OLD.CVMTR_USUARIO_INSERT, '')||'</CVMTR_USUARIO_INSERT>'||
			'<CVMTR_FECHA_UPDATE>'||COALESCE(OLD.CVMTR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVMTR_FECHA_UPDATE>'||
			'<CVMTR_USUARIO_UPDATE>'||COALESCE(OLD.CVMTR_USUARIO_UPDATE, '')||'</CVMTR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CVMTR_CODIGO>'||COALESCE(NEW.CVMTR_CODIGO, '')||'</CVMTR_CODIGO>'||
			'<CVCLN_CODIGO>'||COALESCE(NEW.CVCLN_CODIGO, '')||'</CVCLN_CODIGO>'||
			'<CVMRC_CODIGO>'||COALESCE(NEW.CVMRC_CODIGO, '')||'</CVMRC_CODIGO>'||
			'<CVCMB_CODIGO>'||COALESCE(NEW.CVCMB_CODIGO, '')||'</CVCMB_CODIGO>'||
			'<CVMTR_SERIE>'||COALESCE(NEW.CVMTR_SERIE, '')||'</CVMTR_SERIE>'||
			'<CVMTR_ANIO_FABRICACION>'||COALESCE(NEW.CVMTR_ANIO_FABRICACION, 0)||'</CVMTR_ANIO_FABRICACION>'||
			'<CVMTR_ESTADO_FUNCIONAL>'||COALESCE(NEW.CVMTR_ESTADO_FUNCIONAL, FALSE)||'</CVMTR_ESTADO_FUNCIONAL>'||
			'<CVMTR_TONELAJE>'||COALESCE(NEW.CVMTR_TONELAJE, 0)||'</CVMTR_TONELAJE>'||
			'<CVMTR_INGRESO>'||COALESCE(NEW.CVMTR_INGRESO, FALSE)||'</CVMTR_INGRESO>'||
			'<CVMTR_FECHA_INGRESO>'||COALESCE(NEW.CVMTR_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CVMTR_FECHA_INGRESO>'||
			'<CVMTR_SALIO>'||COALESCE(NEW.CVMTR_SALIO, FALSE)||'</CVMTR_SALIO>'||
			'<CVMTR_FECHA_SALIDA>'||COALESCE(NEW.CVMTR_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CVMTR_FECHA_SALIDA>'||
			'<CVMTR_OBSERVACION>'||COALESCE(NEW.CVMTR_OBSERVACION, '')||'</CVMTR_OBSERVACION>'||
			'<CVMTR_ESTADO>'||COALESCE(NEW.CVMTR_ESTADO, FALSE)||'</CVMTR_ESTADO>'||
			'<CVMTR_FECHA_INSERT>'||COALESCE(NEW.CVMTR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVMTR_FECHA_INSERT>'||
			'<CVMTR_USUARIO_INSERT>'||COALESCE(NEW.CVMTR_USUARIO_INSERT, '')||'</CVMTR_USUARIO_INSERT>'||
			'<CVMTR_FECHA_UPDATE>'||COALESCE(NEW.CVMTR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVMTR_FECHA_UPDATE>'||
			'<CVMTR_USUARIO_UPDATE>'||COALESCE(NEW.CVMTR_USUARIO_UPDATE, '')||'</CVMTR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_VEH_MOTOR. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_MOTOR
*/
CREATE TRIGGER TRG_AUD_CGG_VEH_MOTOR AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_VEH_MOTOR FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_VEH_MOTOR();
COMMENT ON TRIGGER TRG_AUD_CGG_VEH_MOTOR ON SII.CGG_VEH_MOTOR IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_VEH_MOTOR DISABLE TRIGGER TRG_AUD_CGG_VEH_MOTOR;

