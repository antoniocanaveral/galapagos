/**
* FUNCION SII.TRG_KG_CGG_DHU_SEGUIMIENTO_PROFESIONAL. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_SEGUIMIENTO_PROFESIONAL
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_SEGUIMIENTO_PROFESIONAL() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDSPR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDSPR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDSPR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDSPR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDSPR_CODIGO>'||COALESCE(OLD.CDSPR_CODIGO, '')||'</CDSPR_CODIGO>'||
			'<CDBEC_CODIGO>'||COALESCE(OLD.CDBEC_CODIGO, '')||'</CDBEC_CODIGO>'||
			'<CDSPR_ANIO>'||COALESCE(OLD.CDSPR_ANIO, 0)||'</CDSPR_ANIO>'||
			'<CDSPR_FECHA_INGRESO>'||COALESCE(OLD.CDSPR_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CDSPR_FECHA_INGRESO>'||
			'<CDSPR_INSTITUCION>'||COALESCE(OLD.CDSPR_INSTITUCION, '')||'</CDSPR_INSTITUCION>'||
			'<CDSPR_CARGO>'||COALESCE(OLD.CDSPR_CARGO, '')||'</CDSPR_CARGO>'||
			'<CDSPR_FECHA_SALIDA>'||COALESCE(OLD.CDSPR_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CDSPR_FECHA_SALIDA>'||
			'<CDSPR_OBSERVACIONES>'||COALESCE(OLD.CDSPR_OBSERVACIONES, '')||'</CDSPR_OBSERVACIONES>'||
			'<CDSPR_ESTADO>'||COALESCE(OLD.CDSPR_ESTADO, FALSE)||'</CDSPR_ESTADO>'||
			'<CDSPR_FECHA_INSERT>'||COALESCE(OLD.CDSPR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDSPR_FECHA_INSERT>'||
			'<CDSPR_USUARIO_INSERT>'||COALESCE(OLD.CDSPR_USUARIO_INSERT, '')||'</CDSPR_USUARIO_INSERT>'||
			'<CDSPR_FECHA_UPDATE>'||COALESCE(OLD.CDSPR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDSPR_FECHA_UPDATE>'||
			'<CDSPR_USUARIO_UPDATE>'||COALESCE(OLD.CDSPR_USUARIO_UPDATE, '')||'</CDSPR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDSPR_CODIGO>'||COALESCE(NEW.CDSPR_CODIGO, '')||'</CDSPR_CODIGO>'||
			'<CDBEC_CODIGO>'||COALESCE(NEW.CDBEC_CODIGO, '')||'</CDBEC_CODIGO>'||
			'<CDSPR_ANIO>'||COALESCE(NEW.CDSPR_ANIO, 0)||'</CDSPR_ANIO>'||
			'<CDSPR_FECHA_INGRESO>'||COALESCE(NEW.CDSPR_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CDSPR_FECHA_INGRESO>'||
			'<CDSPR_INSTITUCION>'||COALESCE(NEW.CDSPR_INSTITUCION, '')||'</CDSPR_INSTITUCION>'||
			'<CDSPR_CARGO>'||COALESCE(NEW.CDSPR_CARGO, '')||'</CDSPR_CARGO>'||
			'<CDSPR_FECHA_SALIDA>'||COALESCE(NEW.CDSPR_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CDSPR_FECHA_SALIDA>'||
			'<CDSPR_OBSERVACIONES>'||COALESCE(NEW.CDSPR_OBSERVACIONES, '')||'</CDSPR_OBSERVACIONES>'||
			'<CDSPR_ESTADO>'||COALESCE(NEW.CDSPR_ESTADO, FALSE)||'</CDSPR_ESTADO>'||
			'<CDSPR_FECHA_INSERT>'||COALESCE(NEW.CDSPR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDSPR_FECHA_INSERT>'||
			'<CDSPR_USUARIO_INSERT>'||COALESCE(NEW.CDSPR_USUARIO_INSERT, '')||'</CDSPR_USUARIO_INSERT>'||
			'<CDSPR_FECHA_UPDATE>'||COALESCE(NEW.CDSPR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDSPR_FECHA_UPDATE>'||
			'<CDSPR_USUARIO_UPDATE>'||COALESCE(NEW.CDSPR_USUARIO_UPDATE, '')||'</CDSPR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_SEGUIMIENTO_PROFESIONAL. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_SEGUIMIENTO_PROFESIONAL
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_PROFESIONAL AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_SEGUIMIENTO_PROFESIONAL FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_SEGUIMIENTO_PROFESIONAL();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_PROFESIONAL ON SII.CGG_DHU_SEGUIMIENTO_PROFESIONAL IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_SEGUIMIENTO_PROFESIONAL DISABLE TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_PROFESIONAL;

