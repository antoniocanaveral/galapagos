/**
* FUNCION SII.TRG_KG_CGG_KDX_TRANSFERENCIA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_TRANSFERENCIA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_TRANSFERENCIA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKTRN_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKTRN_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKTRN_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKTRN_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKTRN_CODIGO>'||COALESCE(OLD.CKTRN_CODIGO, '')||'</CKTRN_CODIGO>'||
			'<CKPVT_CODIGO>'||COALESCE(OLD.CKPVT_CODIGO, '')||'</CKPVT_CODIGO>'||
			'<CKESP_CODIGO>'||COALESCE(OLD.CKESP_CODIGO, '')||'</CKESP_CODIGO>'||
			'<CKTRN_NUMERO_TRANSFERENCIA>'||COALESCE(OLD.CKTRN_NUMERO_TRANSFERENCIA, '')||'</CKTRN_NUMERO_TRANSFERENCIA>'||
			'<CKTRN_FECHA_RECEPCION>'||COALESCE(OLD.CKTRN_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CKTRN_FECHA_RECEPCION>'||
			'<CKTRN_CANTIDAD>'||COALESCE(OLD.CKTRN_CANTIDAD, 0)||'</CKTRN_CANTIDAD>'||
			'<CKTRN_PRECIO_UNITARIO>'||COALESCE(OLD.CKTRN_PRECIO_UNITARIO, 0)||'</CKTRN_PRECIO_UNITARIO>'||
			'<CKTRN_SERIE_INICIO>'||COALESCE(OLD.CKTRN_SERIE_INICIO, 0)||'</CKTRN_SERIE_INICIO>'||
			'<CKTRN_SERIE_FIN>'||COALESCE(OLD.CKTRN_SERIE_FIN, 0)||'</CKTRN_SERIE_FIN>'||
			'<CKTRN_OBSERVACION>'||COALESCE(OLD.CKTRN_OBSERVACION, '')||'</CKTRN_OBSERVACION>'||
			'<CKTRN_CANTIDAD_DISPONIBLE>'||COALESCE(OLD.CKTRN_CANTIDAD_DISPONIBLE, 0)||'</CKTRN_CANTIDAD_DISPONIBLE>'||
			'<CKTRN_ESTADO_TRANSFERENCIA>'||COALESCE(OLD.CKTRN_ESTADO_TRANSFERENCIA, 0)||'</CKTRN_ESTADO_TRANSFERENCIA>'||
			'<CKTRN_ESTADO>'||COALESCE(OLD.CKTRN_ESTADO, FALSE)||'</CKTRN_ESTADO>'||
			'<CKTRN_FECHA_INSERT>'||COALESCE(OLD.CKTRN_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKTRN_FECHA_INSERT>'||
			'<CKTRN_USUARIO_INSERT>'||COALESCE(OLD.CKTRN_USUARIO_INSERT, '')||'</CKTRN_USUARIO_INSERT>'||
			'<CKTRN_FECHA_UPDATE>'||COALESCE(OLD.CKTRN_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKTRN_FECHA_UPDATE>'||
			'<CKTRN_USUARIO_UPDATE>'||COALESCE(OLD.CKTRN_USUARIO_UPDATE, '')||'</CKTRN_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKTRN_CODIGO>'||COALESCE(NEW.CKTRN_CODIGO, '')||'</CKTRN_CODIGO>'||
			'<CKPVT_CODIGO>'||COALESCE(NEW.CKPVT_CODIGO, '')||'</CKPVT_CODIGO>'||
			'<CKESP_CODIGO>'||COALESCE(NEW.CKESP_CODIGO, '')||'</CKESP_CODIGO>'||
			'<CKTRN_NUMERO_TRANSFERENCIA>'||COALESCE(NEW.CKTRN_NUMERO_TRANSFERENCIA, '')||'</CKTRN_NUMERO_TRANSFERENCIA>'||
			'<CKTRN_FECHA_RECEPCION>'||COALESCE(NEW.CKTRN_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CKTRN_FECHA_RECEPCION>'||
			'<CKTRN_CANTIDAD>'||COALESCE(NEW.CKTRN_CANTIDAD, 0)||'</CKTRN_CANTIDAD>'||
			'<CKTRN_PRECIO_UNITARIO>'||COALESCE(NEW.CKTRN_PRECIO_UNITARIO, 0)||'</CKTRN_PRECIO_UNITARIO>'||
			'<CKTRN_SERIE_INICIO>'||COALESCE(NEW.CKTRN_SERIE_INICIO, 0)||'</CKTRN_SERIE_INICIO>'||
			'<CKTRN_SERIE_FIN>'||COALESCE(NEW.CKTRN_SERIE_FIN, 0)||'</CKTRN_SERIE_FIN>'||
			'<CKTRN_OBSERVACION>'||COALESCE(NEW.CKTRN_OBSERVACION, '')||'</CKTRN_OBSERVACION>'||
			'<CKTRN_CANTIDAD_DISPONIBLE>'||COALESCE(NEW.CKTRN_CANTIDAD_DISPONIBLE, 0)||'</CKTRN_CANTIDAD_DISPONIBLE>'||
			'<CKTRN_ESTADO_TRANSFERENCIA>'||COALESCE(NEW.CKTRN_ESTADO_TRANSFERENCIA, 0)||'</CKTRN_ESTADO_TRANSFERENCIA>'||
			'<CKTRN_ESTADO>'||COALESCE(NEW.CKTRN_ESTADO, FALSE)||'</CKTRN_ESTADO>'||
			'<CKTRN_FECHA_INSERT>'||COALESCE(NEW.CKTRN_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKTRN_FECHA_INSERT>'||
			'<CKTRN_USUARIO_INSERT>'||COALESCE(NEW.CKTRN_USUARIO_INSERT, '')||'</CKTRN_USUARIO_INSERT>'||
			'<CKTRN_FECHA_UPDATE>'||COALESCE(NEW.CKTRN_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKTRN_FECHA_UPDATE>'||
			'<CKTRN_USUARIO_UPDATE>'||COALESCE(NEW.CKTRN_USUARIO_UPDATE, '')||'</CKTRN_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_TRANSFERENCIA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_TRANSFERENCIA
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_TRANSFERENCIA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_TRANSFERENCIA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_TRANSFERENCIA();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_TRANSFERENCIA ON SII.CGG_KDX_TRANSFERENCIA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_TRANSFERENCIA DISABLE TRIGGER TRG_AUD_CGG_KDX_TRANSFERENCIA;

