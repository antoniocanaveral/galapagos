/**
* FUNCION SII.TRG_KG_CGG_GER_INDICADOR. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GER_INDICADOR
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GER_INDICADOR() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGGIN_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGGIN_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGGIN_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGGIN_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGGIN_CODIGO>'||COALESCE(OLD.CGGIN_CODIGO, '')||'</CGGIN_CODIGO>'||
			'<CGGIT_CODIGO>'||COALESCE(OLD.CGGIT_CODIGO, '')||'</CGGIT_CODIGO>'||
			'<CGGIN_NOMBRE>'||COALESCE(OLD.CGGIN_NOMBRE, '')||'</CGGIN_NOMBRE>'||
			'<CGGIN_DESCRIPCION>'||COALESCE(OLD.CGGIN_DESCRIPCION, '')||'</CGGIN_DESCRIPCION>'||
			'<CGGIN_ESTADO>'||COALESCE(OLD.CGGIN_ESTADO, FALSE)||'</CGGIN_ESTADO>'||
			'<CGGIN_FECHA_INSERT>'||COALESCE(OLD.CGGIN_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGGIN_FECHA_INSERT>'||
			'<CGGIN_USUARIO_INSERT>'||COALESCE(OLD.CGGIN_USUARIO_INSERT, '')||'</CGGIN_USUARIO_INSERT>'||
			'<CGGIN_FECHA_UPDATE>'||COALESCE(OLD.CGGIN_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGGIN_FECHA_UPDATE>'||
			'<CGGIN_USUARIO_UPDATE>'||COALESCE(OLD.CGGIN_USUARIO_UPDATE, '')||'</CGGIN_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGGIN_CODIGO>'||COALESCE(NEW.CGGIN_CODIGO, '')||'</CGGIN_CODIGO>'||
			'<CGGIT_CODIGO>'||COALESCE(NEW.CGGIT_CODIGO, '')||'</CGGIT_CODIGO>'||
			'<CGGIN_NOMBRE>'||COALESCE(NEW.CGGIN_NOMBRE, '')||'</CGGIN_NOMBRE>'||
			'<CGGIN_DESCRIPCION>'||COALESCE(NEW.CGGIN_DESCRIPCION, '')||'</CGGIN_DESCRIPCION>'||
			'<CGGIN_ESTADO>'||COALESCE(NEW.CGGIN_ESTADO, FALSE)||'</CGGIN_ESTADO>'||
			'<CGGIN_FECHA_INSERT>'||COALESCE(NEW.CGGIN_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGGIN_FECHA_INSERT>'||
			'<CGGIN_USUARIO_INSERT>'||COALESCE(NEW.CGGIN_USUARIO_INSERT, '')||'</CGGIN_USUARIO_INSERT>'||
			'<CGGIN_FECHA_UPDATE>'||COALESCE(NEW.CGGIN_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGGIN_FECHA_UPDATE>'||
			'<CGGIN_USUARIO_UPDATE>'||COALESCE(NEW.CGGIN_USUARIO_UPDATE, '')||'</CGGIN_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GER_INDICADOR. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GER_INDICADOR
*/
CREATE TRIGGER TRG_AUD_CGG_GER_INDICADOR AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GER_INDICADOR FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GER_INDICADOR();
COMMENT ON TRIGGER TRG_AUD_CGG_GER_INDICADOR ON SII.CGG_GER_INDICADOR IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GER_INDICADOR DISABLE TRIGGER TRG_AUD_CGG_GER_INDICADOR;

