/**
* FUNCION SII.TRG_KG_CGG_DHU_ADJUNTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_ADJUNTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_ADJUNTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDAD_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDAD_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDAD_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDAD_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDAD_CODIGO>'||COALESCE(OLD.CDAD_CODIGO, '')||'</CDAD_CODIGO>'||
			'<CDBEC_CODIGO>'||COALESCE(OLD.CDBEC_CODIGO, '')||'</CDBEC_CODIGO>'||
			'<CDAD_DESCRIPCION>'||COALESCE(OLD.CDAD_DESCRIPCION, '')||'</CDAD_DESCRIPCION>'||
			'<CDAD_OBSERVACION>'||COALESCE(OLD.CDAD_OBSERVACION, '')||'</CDAD_OBSERVACION>'||
			'<CDAD_NOMBRE_ADJUNTO>'||COALESCE(OLD.CDAD_NOMBRE_ADJUNTO, '')||'</CDAD_NOMBRE_ADJUNTO>'||
			'<CDAD_ADJUNTO>'||COALESCE(encode(OLD.CDAD_ADJUNTO, 'base64'), '')||'</CDAD_ADJUNTO>'||
			'<CDAD_TIPO>'||COALESCE(OLD.CDAD_TIPO, 0)||'</CDAD_TIPO>'||
			'<CDAD_DESCRIPCION_CONTENIDO>'||COALESCE(OLD.CDAD_DESCRIPCION_CONTENIDO, '')||'</CDAD_DESCRIPCION_CONTENIDO>'||
			'<CDAD_ESTADO>'||COALESCE(OLD.CDAD_ESTADO, FALSE)||'</CDAD_ESTADO>'||
			'<CDAD_FECHA_INSERT>'||COALESCE(OLD.CDAD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDAD_FECHA_INSERT>'||
			'<CDAD_USUARIO_INSERT>'||COALESCE(OLD.CDAD_USUARIO_INSERT, '')||'</CDAD_USUARIO_INSERT>'||
			'<CDAD_FECHA_UPDATE>'||COALESCE(OLD.CDAD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDAD_FECHA_UPDATE>'||
			'<CDAD_USUARIO_UPDATE>'||COALESCE(OLD.CDAD_USUARIO_UPDATE, '')||'</CDAD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDAD_CODIGO>'||COALESCE(NEW.CDAD_CODIGO, '')||'</CDAD_CODIGO>'||
			'<CDBEC_CODIGO>'||COALESCE(NEW.CDBEC_CODIGO, '')||'</CDBEC_CODIGO>'||
			'<CDAD_DESCRIPCION>'||COALESCE(NEW.CDAD_DESCRIPCION, '')||'</CDAD_DESCRIPCION>'||
			'<CDAD_OBSERVACION>'||COALESCE(NEW.CDAD_OBSERVACION, '')||'</CDAD_OBSERVACION>'||
			'<CDAD_NOMBRE_ADJUNTO>'||COALESCE(NEW.CDAD_NOMBRE_ADJUNTO, '')||'</CDAD_NOMBRE_ADJUNTO>'||
			'<CDAD_ADJUNTO>'||COALESCE(encode(NEW.CDAD_ADJUNTO, 'base64'), '')||'</CDAD_ADJUNTO>'||
			'<CDAD_TIPO>'||COALESCE(NEW.CDAD_TIPO, 0)||'</CDAD_TIPO>'||
			'<CDAD_DESCRIPCION_CONTENIDO>'||COALESCE(NEW.CDAD_DESCRIPCION_CONTENIDO, '')||'</CDAD_DESCRIPCION_CONTENIDO>'||
			'<CDAD_ESTADO>'||COALESCE(NEW.CDAD_ESTADO, FALSE)||'</CDAD_ESTADO>'||
			'<CDAD_FECHA_INSERT>'||COALESCE(NEW.CDAD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDAD_FECHA_INSERT>'||
			'<CDAD_USUARIO_INSERT>'||COALESCE(NEW.CDAD_USUARIO_INSERT, '')||'</CDAD_USUARIO_INSERT>'||
			'<CDAD_FECHA_UPDATE>'||COALESCE(NEW.CDAD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDAD_FECHA_UPDATE>'||
			'<CDAD_USUARIO_UPDATE>'||COALESCE(NEW.CDAD_USUARIO_UPDATE, '')||'</CDAD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_ADJUNTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_ADJUNTO
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_ADJUNTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_ADJUNTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_ADJUNTO();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_ADJUNTO ON SII.CGG_DHU_ADJUNTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_ADJUNTO DISABLE TRIGGER TRG_AUD_CGG_DHU_ADJUNTO;

