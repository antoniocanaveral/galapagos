/**
* FUNCION SII.TRG_KG_CGG_SUSTITUCION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SUSTITUCION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_SUSTITUCION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CSUST_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSUST_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CSUST_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSUST_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CSUST_CODIGO>'||COALESCE(OLD.CSUST_CODIGO, '')||'</CSUST_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CGG_CUSU_CODIGO>'||COALESCE(OLD.CGG_CUSU_CODIGO, '')||'</CGG_CUSU_CODIGO>'||
			'<CSUST_MODALIDAD>'||COALESCE(OLD.CSUST_MODALIDAD, 0)||'</CSUST_MODALIDAD>'||
			'<CSUST_FECHA_INICIO>'||COALESCE(OLD.CSUST_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CSUST_FECHA_INICIO>'||
			'<CSUST_FECHA_FIN>'||COALESCE(OLD.CSUST_FECHA_FIN, CURRENT_TIMESTAMP)||'</CSUST_FECHA_FIN>'||
			'<CSUST_ESTADO_ENCARGO>'||COALESCE(OLD.CSUST_ESTADO_ENCARGO, FALSE)||'</CSUST_ESTADO_ENCARGO>'||
			'<CSUST_ESTADO>'||COALESCE(OLD.CSUST_ESTADO, FALSE)||'</CSUST_ESTADO>'||
			'<CSUST_FECHA_INSERT>'||COALESCE(OLD.CSUST_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSUST_FECHA_INSERT>'||
			'<CSUST_USUARIO_INSERT>'||COALESCE(OLD.CSUST_USUARIO_INSERT, '')||'</CSUST_USUARIO_INSERT>'||
			'<CSUST_FECHA_UPDATE>'||COALESCE(OLD.CSUST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSUST_FECHA_UPDATE>'||
			'<CSUST_USUARIO_UPDATE>'||COALESCE(OLD.CSUST_USUARIO_UPDATE, '')||'</CSUST_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CSUST_CODIGO>'||COALESCE(NEW.CSUST_CODIGO, '')||'</CSUST_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CGG_CUSU_CODIGO>'||COALESCE(NEW.CGG_CUSU_CODIGO, '')||'</CGG_CUSU_CODIGO>'||
			'<CSUST_MODALIDAD>'||COALESCE(NEW.CSUST_MODALIDAD, 0)||'</CSUST_MODALIDAD>'||
			'<CSUST_FECHA_INICIO>'||COALESCE(NEW.CSUST_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CSUST_FECHA_INICIO>'||
			'<CSUST_FECHA_FIN>'||COALESCE(NEW.CSUST_FECHA_FIN, CURRENT_TIMESTAMP)||'</CSUST_FECHA_FIN>'||
			'<CSUST_ESTADO_ENCARGO>'||COALESCE(NEW.CSUST_ESTADO_ENCARGO, FALSE)||'</CSUST_ESTADO_ENCARGO>'||
			'<CSUST_ESTADO>'||COALESCE(NEW.CSUST_ESTADO, FALSE)||'</CSUST_ESTADO>'||
			'<CSUST_FECHA_INSERT>'||COALESCE(NEW.CSUST_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSUST_FECHA_INSERT>'||
			'<CSUST_USUARIO_INSERT>'||COALESCE(NEW.CSUST_USUARIO_INSERT, '')||'</CSUST_USUARIO_INSERT>'||
			'<CSUST_FECHA_UPDATE>'||COALESCE(NEW.CSUST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSUST_FECHA_UPDATE>'||
			'<CSUST_USUARIO_UPDATE>'||COALESCE(NEW.CSUST_USUARIO_UPDATE, '')||'</CSUST_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_SUSTITUCION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SUSTITUCION
*/
CREATE TRIGGER TRG_AUD_CGG_SUSTITUCION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_SUSTITUCION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_SUSTITUCION();
COMMENT ON TRIGGER TRG_AUD_CGG_SUSTITUCION ON SII.CGG_SUSTITUCION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_SUSTITUCION DISABLE TRIGGER TRG_AUD_CGG_SUSTITUCION;

