/**
* FUNCION SII.TRG_KG_CGG_INSTITUCION_EDUCATIVA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_INSTITUCION_EDUCATIVA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_INSTITUCION_EDUCATIVA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGIED_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGIED_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGIED_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGIED_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGIED_CODIGO>'||COALESCE(OLD.CGIED_CODIGO, '')||'</CGIED_CODIGO>'||
			'<CPAIS_CODIGO>'||COALESCE(OLD.CPAIS_CODIGO, '')||'</CPAIS_CODIGO>'||
			'<CGIED_NOMBRE>'||COALESCE(OLD.CGIED_NOMBRE, '')||'</CGIED_NOMBRE>'||
			'<CGIED_DESCRIPCION>'||COALESCE(OLD.CGIED_DESCRIPCION, '')||'</CGIED_DESCRIPCION>'||
			'<CGIED_DIRECCION>'||COALESCE(OLD.CGIED_DIRECCION, '')||'</CGIED_DIRECCION>'||
			'<CGIED_ESTADO>'||COALESCE(OLD.CGIED_ESTADO, FALSE)||'</CGIED_ESTADO>'||
			'<CGIED_FECHA_INSERT>'||COALESCE(OLD.CGIED_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGIED_FECHA_INSERT>'||
			'<CGIED_USUARIO_INSERT>'||COALESCE(OLD.CGIED_USUARIO_INSERT, '')||'</CGIED_USUARIO_INSERT>'||
			'<CGIED_FECHA_UPDATE>'||COALESCE(OLD.CGIED_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGIED_FECHA_UPDATE>'||
			'<CGIED_USUARIO_UPDATE>'||COALESCE(OLD.CGIED_USUARIO_UPDATE, '')||'</CGIED_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGIED_CODIGO>'||COALESCE(NEW.CGIED_CODIGO, '')||'</CGIED_CODIGO>'||
			'<CPAIS_CODIGO>'||COALESCE(NEW.CPAIS_CODIGO, '')||'</CPAIS_CODIGO>'||
			'<CGIED_NOMBRE>'||COALESCE(NEW.CGIED_NOMBRE, '')||'</CGIED_NOMBRE>'||
			'<CGIED_DESCRIPCION>'||COALESCE(NEW.CGIED_DESCRIPCION, '')||'</CGIED_DESCRIPCION>'||
			'<CGIED_DIRECCION>'||COALESCE(NEW.CGIED_DIRECCION, '')||'</CGIED_DIRECCION>'||
			'<CGIED_ESTADO>'||COALESCE(NEW.CGIED_ESTADO, FALSE)||'</CGIED_ESTADO>'||
			'<CGIED_FECHA_INSERT>'||COALESCE(NEW.CGIED_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGIED_FECHA_INSERT>'||
			'<CGIED_USUARIO_INSERT>'||COALESCE(NEW.CGIED_USUARIO_INSERT, '')||'</CGIED_USUARIO_INSERT>'||
			'<CGIED_FECHA_UPDATE>'||COALESCE(NEW.CGIED_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGIED_FECHA_UPDATE>'||
			'<CGIED_USUARIO_UPDATE>'||COALESCE(NEW.CGIED_USUARIO_UPDATE, '')||'</CGIED_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_INSTITUCION_EDUCATIVA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_INSTITUCION_EDUCATIVA
*/
CREATE TRIGGER TRG_AUD_CGG_INSTITUCION_EDUCATIVA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_INSTITUCION_EDUCATIVA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_INSTITUCION_EDUCATIVA();
COMMENT ON TRIGGER TRG_AUD_CGG_INSTITUCION_EDUCATIVA ON SII.CGG_INSTITUCION_EDUCATIVA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_INSTITUCION_EDUCATIVA DISABLE TRIGGER TRG_AUD_CGG_INSTITUCION_EDUCATIVA;

