/**
* FUNCION SII.TRG_KG_CGG_RES_ESTADO_TRAMITE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_ESTADO_TRAMITE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_ESTADO_TRAMITE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRETT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRETT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRETT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRETT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRETT_CODIGO>'||COALESCE(OLD.CRETT_CODIGO, '')||'</CRETT_CODIGO>'||
			'<CRETT_NOMBRE>'||COALESCE(OLD.CRETT_NOMBRE, '')||'</CRETT_NOMBRE>'||
			'<CRETT_ABREVIATURA>'||COALESCE(OLD.CRETT_ABREVIATURA, '')||'</CRETT_ABREVIATURA>'||
			'<CRETT_ESTADO>'||COALESCE(OLD.CRETT_ESTADO, FALSE)||'</CRETT_ESTADO>'||
			'<CRETT_FECHA_INSERT>'||COALESCE(OLD.CRETT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRETT_FECHA_INSERT>'||
			'<CRETT_USUARIO_INSERT>'||COALESCE(OLD.CRETT_USUARIO_INSERT, '')||'</CRETT_USUARIO_INSERT>'||
			'<CRETT_FECHA_UPDATE>'||COALESCE(OLD.CRETT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRETT_FECHA_UPDATE>'||
			'<CRETT_USUARIO_UPDATE>'||COALESCE(OLD.CRETT_USUARIO_UPDATE, '')||'</CRETT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRETT_CODIGO>'||COALESCE(NEW.CRETT_CODIGO, '')||'</CRETT_CODIGO>'||
			'<CRETT_NOMBRE>'||COALESCE(NEW.CRETT_NOMBRE, '')||'</CRETT_NOMBRE>'||
			'<CRETT_ABREVIATURA>'||COALESCE(NEW.CRETT_ABREVIATURA, '')||'</CRETT_ABREVIATURA>'||
			'<CRETT_ESTADO>'||COALESCE(NEW.CRETT_ESTADO, FALSE)||'</CRETT_ESTADO>'||
			'<CRETT_FECHA_INSERT>'||COALESCE(NEW.CRETT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRETT_FECHA_INSERT>'||
			'<CRETT_USUARIO_INSERT>'||COALESCE(NEW.CRETT_USUARIO_INSERT, '')||'</CRETT_USUARIO_INSERT>'||
			'<CRETT_FECHA_UPDATE>'||COALESCE(NEW.CRETT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRETT_FECHA_UPDATE>'||
			'<CRETT_USUARIO_UPDATE>'||COALESCE(NEW.CRETT_USUARIO_UPDATE, '')||'</CRETT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_ESTADO_TRAMITE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_ESTADO_TRAMITE
*/
CREATE TRIGGER TRG_AUD_CGG_RES_ESTADO_TRAMITE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_ESTADO_TRAMITE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_ESTADO_TRAMITE();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_ESTADO_TRAMITE ON SII.CGG_RES_ESTADO_TRAMITE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_ESTADO_TRAMITE DISABLE TRIGGER TRG_AUD_CGG_RES_ESTADO_TRAMITE;

