/**
* FUNCION SII.TRG_KG_CGG_VEH_MARCA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_MARCA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_VEH_MARCA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CVMRC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVMRC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CVMRC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVMRC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CVMRC_CODIGO>'||COALESCE(OLD.CVMRC_CODIGO, '')||'</CVMRC_CODIGO>'||
			'<CVMRC_NOMBRE>'||COALESCE(OLD.CVMRC_NOMBRE, '')||'</CVMRC_NOMBRE>'||
			'<CVMRC_OBSERVACION>'||COALESCE(OLD.CVMRC_OBSERVACION, '')||'</CVMRC_OBSERVACION>'||
			'<CVMRC_ESTADO>'||COALESCE(OLD.CVMRC_ESTADO, FALSE)||'</CVMRC_ESTADO>'||
			'<CVMRC_USUARIO_INSERT>'||COALESCE(OLD.CVMRC_USUARIO_INSERT, '')||'</CVMRC_USUARIO_INSERT>'||
			'<CVMRC_FECHA_INSERT>'||COALESCE(OLD.CVMRC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVMRC_FECHA_INSERT>'||
			'<CVMRC_USUARIO_UPDATE>'||COALESCE(OLD.CVMRC_USUARIO_UPDATE, '')||'</CVMRC_USUARIO_UPDATE>'||
			'<CVMRC_FECHA_UPDATE>'||COALESCE(OLD.CVMRC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVMRC_FECHA_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CVMRC_CODIGO>'||COALESCE(NEW.CVMRC_CODIGO, '')||'</CVMRC_CODIGO>'||
			'<CVMRC_NOMBRE>'||COALESCE(NEW.CVMRC_NOMBRE, '')||'</CVMRC_NOMBRE>'||
			'<CVMRC_OBSERVACION>'||COALESCE(NEW.CVMRC_OBSERVACION, '')||'</CVMRC_OBSERVACION>'||
			'<CVMRC_ESTADO>'||COALESCE(NEW.CVMRC_ESTADO, FALSE)||'</CVMRC_ESTADO>'||
			'<CVMRC_USUARIO_INSERT>'||COALESCE(NEW.CVMRC_USUARIO_INSERT, '')||'</CVMRC_USUARIO_INSERT>'||
			'<CVMRC_FECHA_INSERT>'||COALESCE(NEW.CVMRC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVMRC_FECHA_INSERT>'||
			'<CVMRC_USUARIO_UPDATE>'||COALESCE(NEW.CVMRC_USUARIO_UPDATE, '')||'</CVMRC_USUARIO_UPDATE>'||
			'<CVMRC_FECHA_UPDATE>'||COALESCE(NEW.CVMRC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVMRC_FECHA_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_VEH_MARCA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_MARCA
*/
CREATE TRIGGER TRG_AUD_CGG_VEH_MARCA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_VEH_MARCA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_VEH_MARCA();
COMMENT ON TRIGGER TRG_AUD_CGG_VEH_MARCA ON SII.CGG_VEH_MARCA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_VEH_MARCA DISABLE TRIGGER TRG_AUD_CGG_VEH_MARCA;

