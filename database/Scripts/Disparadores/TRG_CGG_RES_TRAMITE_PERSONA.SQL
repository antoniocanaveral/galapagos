/**
* FUNCION SII.TRG_KG_CGG_RES_TRAMITE_PERSONA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TRAMITE_PERSONA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_TRAMITE_PERSONA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRTRP_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTRP_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRTRP_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTRP_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRTRP_CODIGO>'||COALESCE(OLD.CRTRP_CODIGO, '')||'</CRTRP_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(OLD.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CRTPR_FECHA_INGRESO>'||COALESCE(OLD.CRTPR_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CRTPR_FECHA_INGRESO>'||
			'<CRTPR_FECHA_SALIDA>'||COALESCE(OLD.CRTPR_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CRTPR_FECHA_SALIDA>'||
			'<CRTPR_ESTADO_TRAMITE>'||COALESCE(OLD.CRTPR_ESTADO_TRAMITE, FALSE)||'</CRTPR_ESTADO_TRAMITE>'||
			'<CRTPR_ESTADO>'||COALESCE(OLD.CRTPR_ESTADO, FALSE)||'</CRTPR_ESTADO>'||
			'<CRTPR_FECHA_INSERT>'||COALESCE(OLD.CRTPR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTPR_FECHA_INSERT>'||
			'<CRTPR_USUARIO_INSERT>'||COALESCE(OLD.CRTPR_USUARIO_INSERT, '')||'</CRTPR_USUARIO_INSERT>'||
			'<CRTPR_FECHA_UPDATE>'||COALESCE(OLD.CRTPR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTPR_FECHA_UPDATE>'||
			'<CRTPR_USUARIO_UPDATE>'||COALESCE(OLD.CRTPR_USUARIO_UPDATE, CURRENT_TIMESTAMP)||'</CRTPR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRTRP_CODIGO>'||COALESCE(NEW.CRTRP_CODIGO, '')||'</CRTRP_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(NEW.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CRTPR_FECHA_INGRESO>'||COALESCE(NEW.CRTPR_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CRTPR_FECHA_INGRESO>'||
			'<CRTPR_FECHA_SALIDA>'||COALESCE(NEW.CRTPR_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CRTPR_FECHA_SALIDA>'||
			'<CRTPR_ESTADO_TRAMITE>'||COALESCE(NEW.CRTPR_ESTADO_TRAMITE, FALSE)||'</CRTPR_ESTADO_TRAMITE>'||
			'<CRTPR_ESTADO>'||COALESCE(NEW.CRTPR_ESTADO, FALSE)||'</CRTPR_ESTADO>'||
			'<CRTPR_FECHA_INSERT>'||COALESCE(NEW.CRTPR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTPR_FECHA_INSERT>'||
			'<CRTPR_USUARIO_INSERT>'||COALESCE(NEW.CRTPR_USUARIO_INSERT, '')||'</CRTPR_USUARIO_INSERT>'||
			'<CRTPR_FECHA_UPDATE>'||COALESCE(NEW.CRTPR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTPR_FECHA_UPDATE>'||
			'<CRTPR_USUARIO_UPDATE>'||COALESCE(NEW.CRTPR_USUARIO_UPDATE, CURRENT_TIMESTAMP)||'</CRTPR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	SELECT SII.F_CGG_SEC_AUDITORIA_INSERT(
		'KEYGEN'::VARCHAR,
		CURRENT_TIMESTAMP::TIMESTAMP WITH TIME ZONE,
		TMP_USUARIO::VARCHAR,
		TMP_IP::TEXT,
		TMP_SESSION_ID::TEXT,
		TG_TABLE_NAME::VARCHAR,
		TG_OP::TEXT,
		TMP_DATO_ANTIGUO::TEXT,
		TMP_DATO_NUEVO::TEXT
	)INTO TMP_CODIGO;
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_TRAMITE_PERSONA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TRAMITE_PERSONA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_TRAMITE_PERSONA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_TRAMITE_PERSONA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_TRAMITE_PERSONA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_TRAMITE_PERSONA ON SII.CGG_RES_TRAMITE_PERSONA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_TRAMITE_PERSONA DISABLE TRIGGER TRG_AUD_CGG_RES_TRAMITE_PERSONA;

