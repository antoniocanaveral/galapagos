/**
* FUNCION SII.TRG_KG_CGG_RES_CARNET. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_CARNET
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_CARNET() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRCNT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRCNT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRCNT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRCNT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRCNT_CODIGO>'||COALESCE(OLD.CRCNT_CODIGO, '')||'</CRCNT_CODIGO>'||
			'<CRCOM_CODIGO>'||COALESCE(OLD.CRCOM_CODIGO, '')||'</CRCOM_CODIGO>'||
			'<CRADJ_CODIGO>'||COALESCE(OLD.CRADJ_CODIGO, '')||'</CRADJ_CODIGO>'||
			'<CRRSD_CODIGO>'||COALESCE(OLD.CRRSD_CODIGO, '')||'</CRRSD_CODIGO>'||
			'<CRCPJ_CODIGO>'||COALESCE(OLD.CRCPJ_CODIGO, '')||'</CRCPJ_CODIGO>'||
			'<CRCNT_SERIE>'||COALESCE(OLD.CRCNT_SERIE, '')||'</CRCNT_SERIE>'||
			'<CRCNT_MOTIVO>'||COALESCE(OLD.CRCNT_MOTIVO, 0)||'</CRCNT_MOTIVO>'||
			'<CRCNT_FECHA_EMISION>'||COALESCE(OLD.CRCNT_FECHA_EMISION, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_EMISION>'||
			'<CRCNT_FECHA_APROBACION>'||COALESCE(OLD.CRCNT_FECHA_APROBACION, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_APROBACION>'||
			'<CRCNT_FECHA_CADUCIDAD>'||COALESCE(OLD.CRCNT_FECHA_CADUCIDAD, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_CADUCIDAD>'||
			'<CRCNT_FECHA_DEPOSITO>'||COALESCE(OLD.CRCNT_FECHA_DEPOSITO, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_DEPOSITO>'||
			'<CRCNT_CARNET_IMPRESO>'||COALESCE(OLD.CRCNT_CARNET_IMPRESO, FALSE)||'</CRCNT_CARNET_IMPRESO>'||
			'<CRCNT_NUMERO_COMP>'||COALESCE(OLD.CRCNT_NUMERO_COMP, '')||'</CRCNT_NUMERO_COMP>'||
			'<CRCNT_VALOR>'||COALESCE(OLD.CRCNT_VALOR, 0)||'</CRCNT_VALOR>'||
			'<CRCNT_OBSERVACION>'||COALESCE(OLD.CRCNT_OBSERVACION, '')||'</CRCNT_OBSERVACION>'||
			'<CRCNT_ESTADO>'||COALESCE(OLD.CRCNT_ESTADO, FALSE)||'</CRCNT_ESTADO>'||
			'<CRCNT_FECHA_INSERT>'||COALESCE(OLD.CRCNT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_INSERT>'||
			'<CRCNT_USUARIO_INSERT>'||COALESCE(OLD.CRCNT_USUARIO_INSERT, '')||'</CRCNT_USUARIO_INSERT>'||
			'<CRCNT_FECHA_UPDATE>'||COALESCE(OLD.CRCNT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_UPDATE>'||
			'<CRCNT_USUARIO_UPDATE>'||COALESCE(OLD.CRCNT_USUARIO_UPDATE, '')||'</CRCNT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRCNT_CODIGO>'||COALESCE(NEW.CRCNT_CODIGO, '')||'</CRCNT_CODIGO>'||
			'<CRCOM_CODIGO>'||COALESCE(NEW.CRCOM_CODIGO, '')||'</CRCOM_CODIGO>'||
			'<CRADJ_CODIGO>'||COALESCE(NEW.CRADJ_CODIGO, '')||'</CRADJ_CODIGO>'||
			'<CRRSD_CODIGO>'||COALESCE(NEW.CRRSD_CODIGO, '')||'</CRRSD_CODIGO>'||
			'<CRCPJ_CODIGO>'||COALESCE(NEW.CRCPJ_CODIGO, '')||'</CRCPJ_CODIGO>'||
			'<CRCNT_SERIE>'||COALESCE(NEW.CRCNT_SERIE, '')||'</CRCNT_SERIE>'||
			'<CRCNT_MOTIVO>'||COALESCE(NEW.CRCNT_MOTIVO, 0)||'</CRCNT_MOTIVO>'||
			'<CRCNT_FECHA_EMISION>'||COALESCE(NEW.CRCNT_FECHA_EMISION, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_EMISION>'||
			'<CRCNT_FECHA_APROBACION>'||COALESCE(NEW.CRCNT_FECHA_APROBACION, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_APROBACION>'||
			'<CRCNT_FECHA_CADUCIDAD>'||COALESCE(NEW.CRCNT_FECHA_CADUCIDAD, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_CADUCIDAD>'||
			'<CRCNT_FECHA_DEPOSITO>'||COALESCE(NEW.CRCNT_FECHA_DEPOSITO, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_DEPOSITO>'||
			'<CRCNT_CARNET_IMPRESO>'||COALESCE(NEW.CRCNT_CARNET_IMPRESO, FALSE)||'</CRCNT_CARNET_IMPRESO>'||
			'<CRCNT_NUMERO_COMP>'||COALESCE(NEW.CRCNT_NUMERO_COMP, '')||'</CRCNT_NUMERO_COMP>'||
			'<CRCNT_VALOR>'||COALESCE(NEW.CRCNT_VALOR, 0)||'</CRCNT_VALOR>'||
			'<CRCNT_OBSERVACION>'||COALESCE(NEW.CRCNT_OBSERVACION, '')||'</CRCNT_OBSERVACION>'||
			'<CRCNT_ESTADO>'||COALESCE(NEW.CRCNT_ESTADO, FALSE)||'</CRCNT_ESTADO>'||
			'<CRCNT_FECHA_INSERT>'||COALESCE(NEW.CRCNT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_INSERT>'||
			'<CRCNT_USUARIO_INSERT>'||COALESCE(NEW.CRCNT_USUARIO_INSERT, '')||'</CRCNT_USUARIO_INSERT>'||
			'<CRCNT_FECHA_UPDATE>'||COALESCE(NEW.CRCNT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRCNT_FECHA_UPDATE>'||
			'<CRCNT_USUARIO_UPDATE>'||COALESCE(NEW.CRCNT_USUARIO_UPDATE, '')||'</CRCNT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_CARNET. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_CARNET
*/
CREATE TRIGGER TRG_AUD_CGG_RES_CARNET AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_CARNET FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_CARNET();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_CARNET ON SII.CGG_RES_CARNET IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_CARNET DISABLE TRIGGER TRG_AUD_CGG_RES_CARNET;

