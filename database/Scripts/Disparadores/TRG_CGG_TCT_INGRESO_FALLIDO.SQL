/**
* FUNCION SII.TRG_KG_CGG_TCT_INGRESO_FALLIDO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_INGRESO_FALLIDO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_TCT_INGRESO_FALLIDO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CTIGF_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTIGF_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CTIGF_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTIGF_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CTIGF_CODIGO>'||COALESCE(OLD.CTIGF_CODIGO, '')||'</CTIGF_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CARPT_CODIGO>'||COALESCE(OLD.CARPT_CODIGO, '')||'</CARPT_CODIGO>'||
			'<CTIGF_FECHA_INTENTO>'||COALESCE(OLD.CTIGF_FECHA_INTENTO, CURRENT_TIMESTAMP)||'</CTIGF_FECHA_INTENTO>'||
			'<CTIGF_TIPO_INGRESO_FALLIDO>'||COALESCE(OLD.CTIGF_TIPO_INGRESO_FALLIDO, 0)||'</CTIGF_TIPO_INGRESO_FALLIDO>'||
			'<CTIGF_DESCRIPCION>'||COALESCE(OLD.CTIGF_DESCRIPCION, '')||'</CTIGF_DESCRIPCION>'||
			'<CTIGF_ESTADO>'||COALESCE(OLD.CTIGF_ESTADO, FALSE)||'</CTIGF_ESTADO>'||
			'<CTIGF_FECHA_INSERT>'||COALESCE(OLD.CTIGF_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTIGF_FECHA_INSERT>'||
			'<CTIGF_USUARIO_INSERT>'||COALESCE(OLD.CTIGF_USUARIO_INSERT, '')||'</CTIGF_USUARIO_INSERT>'||
			'<CTIGF_FECHA_UPDATE>'||COALESCE(OLD.CTIGF_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTIGF_FECHA_UPDATE>'||
			'<CTIGF_USUARIO_UPDATE>'||COALESCE(OLD.CTIGF_USUARIO_UPDATE, '')||'</CTIGF_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CTIGF_CODIGO>'||COALESCE(NEW.CTIGF_CODIGO, '')||'</CTIGF_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CARPT_CODIGO>'||COALESCE(NEW.CARPT_CODIGO, '')||'</CARPT_CODIGO>'||
			'<CTIGF_FECHA_INTENTO>'||COALESCE(NEW.CTIGF_FECHA_INTENTO, CURRENT_TIMESTAMP)||'</CTIGF_FECHA_INTENTO>'||
			'<CTIGF_TIPO_INGRESO_FALLIDO>'||COALESCE(NEW.CTIGF_TIPO_INGRESO_FALLIDO, 0)||'</CTIGF_TIPO_INGRESO_FALLIDO>'||
			'<CTIGF_DESCRIPCION>'||COALESCE(NEW.CTIGF_DESCRIPCION, '')||'</CTIGF_DESCRIPCION>'||
			'<CTIGF_ESTADO>'||COALESCE(NEW.CTIGF_ESTADO, FALSE)||'</CTIGF_ESTADO>'||
			'<CTIGF_FECHA_INSERT>'||COALESCE(NEW.CTIGF_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTIGF_FECHA_INSERT>'||
			'<CTIGF_USUARIO_INSERT>'||COALESCE(NEW.CTIGF_USUARIO_INSERT, '')||'</CTIGF_USUARIO_INSERT>'||
			'<CTIGF_FECHA_UPDATE>'||COALESCE(NEW.CTIGF_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTIGF_FECHA_UPDATE>'||
			'<CTIGF_USUARIO_UPDATE>'||COALESCE(NEW.CTIGF_USUARIO_UPDATE, '')||'</CTIGF_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_TCT_INGRESO_FALLIDO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_INGRESO_FALLIDO
*/
CREATE TRIGGER TRG_AUD_CGG_TCT_INGRESO_FALLIDO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_TCT_INGRESO_FALLIDO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_TCT_INGRESO_FALLIDO();
COMMENT ON TRIGGER TRG_AUD_CGG_TCT_INGRESO_FALLIDO ON SII.CGG_TCT_INGRESO_FALLIDO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_TCT_INGRESO_FALLIDO DISABLE TRIGGER TRG_AUD_CGG_TCT_INGRESO_FALLIDO;

