/**
* FUNCION SII.TRG_KG_CGG_TCT_ADJUNTO_CSV. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_ADJUNTO_CSV
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_TCT_ADJUNTO_CSV() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CTADC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTADC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CTADC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTADC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CTADC_CODIGO>'||COALESCE(OLD.CTADC_CODIGO, '')||'</CTADC_CODIGO>'||
			'<CTADC_NOMBRE_ADJUNTO>'||COALESCE(OLD.CTADC_NOMBRE_ADJUNTO, '')||'</CTADC_NOMBRE_ADJUNTO>'||
			'<CTADC_DESCRIPCION_ADJUNTO>'||COALESCE(OLD.CTADC_DESCRIPCION_ADJUNTO, '')||'</CTADC_DESCRIPCION_ADJUNTO>'||
			'<CTADC_FECHA_REGISTRO>'||COALESCE(OLD.CTADC_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CTADC_FECHA_REGISTRO>'||
			'<CTADC_ADJUNTO>'||COALESCE(encode(OLD.CTADC_ADJUNTO, 'base64'), '')||'</CTADC_ADJUNTO>'||
			'<CTADC_ESTADO>'||COALESCE(OLD.CTADC_ESTADO, FALSE)||'</CTADC_ESTADO>'||
			'<CTADC_FECHA_INSERT>'||COALESCE(OLD.CTADC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTADC_FECHA_INSERT>'||
			'<CTADC_USUARIO_INSERT>'||COALESCE(OLD.CTADC_USUARIO_INSERT, '')||'</CTADC_USUARIO_INSERT>'||
			'<CTADC_FECHA_UPDATE>'||COALESCE(OLD.CTADC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTADC_FECHA_UPDATE>'||
			'<CTADC_USUARIO_UPDATE>'||COALESCE(OLD.CTADC_USUARIO_UPDATE, '')||'</CTADC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CTADC_CODIGO>'||COALESCE(NEW.CTADC_CODIGO, '')||'</CTADC_CODIGO>'||
			'<CTADC_NOMBRE_ADJUNTO>'||COALESCE(NEW.CTADC_NOMBRE_ADJUNTO, '')||'</CTADC_NOMBRE_ADJUNTO>'||
			'<CTADC_DESCRIPCION_ADJUNTO>'||COALESCE(NEW.CTADC_DESCRIPCION_ADJUNTO, '')||'</CTADC_DESCRIPCION_ADJUNTO>'||
			'<CTADC_FECHA_REGISTRO>'||COALESCE(NEW.CTADC_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CTADC_FECHA_REGISTRO>'||
			'<CTADC_ADJUNTO>'||COALESCE(encode(NEW.CTADC_ADJUNTO, 'base64'), '')||'</CTADC_ADJUNTO>'||
			'<CTADC_ESTADO>'||COALESCE(NEW.CTADC_ESTADO, FALSE)||'</CTADC_ESTADO>'||
			'<CTADC_FECHA_INSERT>'||COALESCE(NEW.CTADC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTADC_FECHA_INSERT>'||
			'<CTADC_USUARIO_INSERT>'||COALESCE(NEW.CTADC_USUARIO_INSERT, '')||'</CTADC_USUARIO_INSERT>'||
			'<CTADC_FECHA_UPDATE>'||COALESCE(NEW.CTADC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTADC_FECHA_UPDATE>'||
			'<CTADC_USUARIO_UPDATE>'||COALESCE(NEW.CTADC_USUARIO_UPDATE, '')||'</CTADC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_TCT_ADJUNTO_CSV. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_ADJUNTO_CSV
*/
CREATE TRIGGER TRG_AUD_CGG_TCT_ADJUNTO_CSV AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_TCT_ADJUNTO_CSV FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_TCT_ADJUNTO_CSV();
COMMENT ON TRIGGER TRG_AUD_CGG_TCT_ADJUNTO_CSV ON SII.CGG_TCT_ADJUNTO_CSV IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_TCT_ADJUNTO_CSV DISABLE TRIGGER TRG_AUD_CGG_TCT_ADJUNTO_CSV;

