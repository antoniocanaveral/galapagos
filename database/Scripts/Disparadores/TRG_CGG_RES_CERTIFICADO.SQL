/**
* FUNCION SII.TRG_KG_CGG_RES_CERTIFICADO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_CERTIFICADO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_CERTIFICADO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRCRT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRCRT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRCRT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRCRT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRCRT_CODIGO>'||COALESCE(OLD.CRCRT_CODIGO, '')||'</CRCRT_CODIGO>'||
			'<CRRSD_CODIGO>'||COALESCE(OLD.CRRSD_CODIGO, '')||'</CRRSD_CODIGO>'||
			'<CRCRT_FECHA_EMISION>'||COALESCE(OLD.CRCRT_FECHA_EMISION, CURRENT_TIMESTAMP)||'</CRCRT_FECHA_EMISION>'||
			'<CRCRT_NUMERO>'||COALESCE(OLD.CRCRT_NUMERO, '')||'</CRCRT_NUMERO>'||
			'<CRCRT_CONTENIDO>'||COALESCE(OLD.CRCRT_CONTENIDO, '')||'</CRCRT_CONTENIDO>'||
			'<CRCRT_ANIO>'||COALESCE(OLD.CRCRT_ANIO, 0)||'</CRCRT_ANIO>'||
			'<CRCRT_ESTADO>'||COALESCE(OLD.CRCRT_ESTADO, FALSE)||'</CRCRT_ESTADO>'||
			'<CRCRT_FECHA_INSERT>'||COALESCE(OLD.CRCRT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRCRT_FECHA_INSERT>'||
			'<CRCRT_USUARIO_INSERT>'||COALESCE(OLD.CRCRT_USUARIO_INSERT, '')||'</CRCRT_USUARIO_INSERT>'||
			'<CRCRT_FECHA_UPDATE>'||COALESCE(OLD.CRCRT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRCRT_FECHA_UPDATE>'||
			'<CRCRT_USUARIO_UPDATE>'||COALESCE(OLD.CRCRT_USUARIO_UPDATE, '')||'</CRCRT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRCRT_CODIGO>'||COALESCE(NEW.CRCRT_CODIGO, '')||'</CRCRT_CODIGO>'||
			'<CRRSD_CODIGO>'||COALESCE(NEW.CRRSD_CODIGO, '')||'</CRRSD_CODIGO>'||
			'<CRCRT_FECHA_EMISION>'||COALESCE(NEW.CRCRT_FECHA_EMISION, CURRENT_TIMESTAMP)||'</CRCRT_FECHA_EMISION>'||
			'<CRCRT_NUMERO>'||COALESCE(NEW.CRCRT_NUMERO, '')||'</CRCRT_NUMERO>'||
			'<CRCRT_CONTENIDO>'||COALESCE(NEW.CRCRT_CONTENIDO, '')||'</CRCRT_CONTENIDO>'||
			'<CRCRT_ANIO>'||COALESCE(NEW.CRCRT_ANIO, 0)||'</CRCRT_ANIO>'||
			'<CRCRT_ESTADO>'||COALESCE(NEW.CRCRT_ESTADO, FALSE)||'</CRCRT_ESTADO>'||
			'<CRCRT_FECHA_INSERT>'||COALESCE(NEW.CRCRT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRCRT_FECHA_INSERT>'||
			'<CRCRT_USUARIO_INSERT>'||COALESCE(NEW.CRCRT_USUARIO_INSERT, '')||'</CRCRT_USUARIO_INSERT>'||
			'<CRCRT_FECHA_UPDATE>'||COALESCE(NEW.CRCRT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRCRT_FECHA_UPDATE>'||
			'<CRCRT_USUARIO_UPDATE>'||COALESCE(NEW.CRCRT_USUARIO_UPDATE, '')||'</CRCRT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_CERTIFICADO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_CERTIFICADO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_CERTIFICADO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_CERTIFICADO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_CERTIFICADO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_CERTIFICADO ON SII.CGG_RES_CERTIFICADO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_CERTIFICADO DISABLE TRIGGER TRG_AUD_CGG_RES_CERTIFICADO;

