			'<CRSEG_VECES_REVISION>'||COALESCE(NEW.CRSEG_VECES_REVISION, 0)||'</CRSEG_VECES_REVISION>'||
			'<CRSEG_NUMERO>'||COALESCE(NEW.CRSEG_NUMERO, 0)||'</CRSEG_NUMERO>'||
			'<CRSEG_ESTADO>'||COALESCE(NEW.CRSEG_ESTADO, FALSE)||'</CRSEG_ESTADO>'||
			'<CRSEG_FECHA_INSERT>'||COALESCE(NEW.CRSEG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_INSERT>'||
			'<CRSEG_USUARIO_INSERT>'||COALESCE(NEW.CRSEG_USUARIO_INSERT, '')||'</CRSEG_USUARIO_INSERT>'||
			'<CRSEG_FECHA_UPDATE>'||COALESCE(NEW.CRSEG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_UPDATE>'||
			'<CRSEG_USUARIO_UPDATE>'||COALESCE(NEW.CRSEG_USUARIO_UPDATE, '')||'</CRSEG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_SEGUIMIENTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_SEGUIMIENTO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_SEGUIMIENTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_SEGUIMIENTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_SEGUIMIENTO();
