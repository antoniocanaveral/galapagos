			'<CRSEG_ESTADO_ATENCION>'||COALESCE(OLD.CRSEG_ESTADO_ATENCION, 0)||'</CRSEG_ESTADO_ATENCION>'||
			'<CRSEG_OBSERVACION>'||COALESCE(OLD.CRSEG_OBSERVACION, '')||'</CRSEG_OBSERVACION>'||
			'<CRSEG_TIPO_RESPUESTA>'||COALESCE(OLD.CRSEG_TIPO_RESPUESTA, 0)||'</CRSEG_TIPO_RESPUESTA>'||
			'<CRSEG_TIPO_ACTIVIDAD>'||COALESCE(OLD.CRSEG_TIPO_ACTIVIDAD, '')||'</CRSEG_TIPO_ACTIVIDAD>'||
			'<CRSEG_FECHA_RECEPCION>'||COALESCE(OLD.CRSEG_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_RECEPCION>'||
			'<CRSEG_FECHA_REVISION>'||COALESCE(OLD.CRSEG_FECHA_REVISION, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_REVISION>'||
			'<CRSEG_FECHA_DESPACHO>'||COALESCE(OLD.CRSEG_FECHA_DESPACHO, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_DESPACHO>'||
			'<CRSEG_ESTADO_HIJO>'||COALESCE(OLD.CRSEG_ESTADO_HIJO, 0)||'</CRSEG_ESTADO_HIJO>'||
			'<CRSEG_FECHA_REAPERTURA>'||COALESCE(OLD.CRSEG_FECHA_REAPERTURA, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_REAPERTURA>'||
			'<CRSEG_VECES_REVISION>'||COALESCE(OLD.CRSEG_VECES_REVISION, 0)||'</CRSEG_VECES_REVISION>'||
			'<CRSEG_NUMERO>'||COALESCE(OLD.CRSEG_NUMERO, 0)||'</CRSEG_NUMERO>'||
			'<CRSEG_ESTADO>'||COALESCE(OLD.CRSEG_ESTADO, FALSE)||'</CRSEG_ESTADO>'||
			'<CRSEG_FECHA_INSERT>'||COALESCE(OLD.CRSEG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_INSERT>'||
			'<CRSEG_USUARIO_INSERT>'||COALESCE(OLD.CRSEG_USUARIO_INSERT, '')||'</CRSEG_USUARIO_INSERT>'||
			'<CRSEG_FECHA_UPDATE>'||COALESCE(OLD.CRSEG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_UPDATE>'||
			'<CRSEG_USUARIO_UPDATE>'||COALESCE(OLD.CRSEG_USUARIO_UPDATE, '')||'</CRSEG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRSEG_CODIGO>'||COALESCE(NEW.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CGG_CRSEG_CODIGO>'||COALESCE(NEW.CGG_CRSEG_CODIGO, '')||'</CGG_CRSEG_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(NEW.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CRFAS_CODIGO>'||COALESCE(NEW.CRFAS_CODIGO, '')||'</CRFAS_CODIGO>'||
			'<CGG_CRFAS_CODIGO>'||COALESCE(NEW.CGG_CRFAS_CODIGO, '')||'</CGG_CRFAS_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRSEG_DESCRIPCION>'||COALESCE(NEW.CRSEG_DESCRIPCION, '')||'</CRSEG_DESCRIPCION>'||
			'<CRSEG_ESTADO_ATENCION>'||COALESCE(NEW.CRSEG_ESTADO_ATENCION, 0)||'</CRSEG_ESTADO_ATENCION>'||
