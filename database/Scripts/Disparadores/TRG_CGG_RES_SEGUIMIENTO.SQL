			'<CRSEG_TIPO_ACTIVIDAD>'||COALESCE(NEW.CRSEG_TIPO_ACTIVIDAD, '')||'</CRSEG_TIPO_ACTIVIDAD>'||
			'<CRSEG_FECHA_RECEPCION>'||COALESCE(NEW.CRSEG_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_RECEPCION>'||
			'<CRSEG_FECHA_REVISION>'||COALESCE(NEW.CRSEG_FECHA_REVISION, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_REVISION>'||
			'<CRSEG_FECHA_DESPACHO>'||COALESCE(NEW.CRSEG_FECHA_DESPACHO, CURRENT_TIMESTAMP)||'</CRSEG_FECHA_DESPACHO>'||
			'<CRSEG_ESTADO_HIJO>'||COALESCE(NEW.CRSEG_ESTADO_HIJO, 0)||'</CRSEG_ESTADO_HIJO>'||
