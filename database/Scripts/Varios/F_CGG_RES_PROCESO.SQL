	/**
	* FUNCION SII.F_CGG_RES_PROCESO_SELECT_BY_TIPO_SOLICITUD_TRAMITE
	* SELECCIONA UN REGISTRO EN LA TABLA SII.CGG_RES_PROCESO CORRESPONDIENTE A UN TIPO DE SOLICITUD DE TRAMITE.
	* @param IN_CRTST_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TST
	* @return REFCURSOR
	*/
	CREATE OR REPLACE FUNCTION SII.F_CGG_RES_PROCESO_SELECT_BY_TIPO_SOLICITUD_TRAMITE(
	IN IN_CRTST_CODIGO VARCHAR
	)RETURNS SETOF REFCURSOR AS
	$$
	DECLARE
		TMP_REF REFCURSOR;
	BEGIN
		OPEN TMP_REF FOR
		SELECT 
			PR.CRPRO_CODIGO,
			PR.CRPRO_NOMBRE,
			PR.CRPRO_ESTADO 
		FROM CGG_RES_PROCESO PR
		INNER JOIN CGG_RES_TIPO_SOLICITUD_TRAMITE TST ON (PR.CRPRO_CODIGO = TST.CRPRO_CODIGO)
		WHERE TST.CRTST_CODIGO = IN_CRTST_CODIGO;
		RETURN NEXT TMP_REF;
	END
	$$
	LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;


