/**
* FUNCION SII.F_CGG_SECTOR_PRODUCTIVO_SELECT_PARENT
* SELECCIONA TODOS LOS REGISTROS DE LA TABLA SII.CGG_SECTOR_PRODUCTIVO.
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_SECTOR_PRODUCTIVO_SELECT_PARENT(
IN IN_USER_NAME VARCHAR
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR
	SELECT
		CSCTP_CODIGO, 
		CGG_CSCTP_CODIGO, 
		CSCTP_NOMBRE, 
		CSCTP_DESCRIPCION, 
		CSCTP_ESTADO, 
		CSCTP_FECHA_INSERT, 
		CSCTP_USUARIO_INSERT, 
		CSCTP_FECHA_UPDATE, 
		CSCTP_USUARIO_UPDATE
	FROM SII.CGG_SECTOR_PRODUCTIVO
	WHERE CSCTP_ESTADO = TRUE
	AND CGG_CSCTP_CODIGO IS NULL;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

