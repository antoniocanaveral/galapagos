/**
* FUNCION SII.F_SELECT_OBJ_ONLY
* SELECCIONA LOS OBJETOs SEGUN EL TIPO.
* @param IN_CSOBJ_TIPO TIPO DE OBJETO
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_SELECT_OBJ_ONLY(
IN IN_CSOBJ_TIPO INT
)RETURNS REFCURSOR AS
$$
DECLARE
OUT_CURSOR REFCURSOR;
BEGIN
	OPEN OUT_CURSOR FOR
		SELECT DISTINCT CSOBJ_TIPO, TRIM(CSOBJ_RUTA,'/.js') CSOBJ_RUTA FROM CGG_SEC_OBJETO
		WHERE CSOBJ_TIPO = IN_CSOBJ_TIPO;
	RETURN OUT_CURSOR;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;