/**
* FUNCION SII.F_CGG_KDX_COMPRA_SELECT_ESPECIE
* SELECCIONA N REGISTROS DE LA TABLA SII.F_CGG_KDX_COMPRA.
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_KDX_COMPRA_SELECT_ESPECIE(
IN IN_USER_NAME VARCHAR,
IN IN_START_INDEX INT,
IN IN_LIMIT INT,
IN IN_SORT_FIELD_NAME VARCHAR,
IN IN_DIRECTION VARCHAR,
IN IN_FIND_TEXT TEXT
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CKCMP_CODIGO, 
		CKCMP_LOTE, 
		CKCMP_FECHA_INGRESO,
		(SELECT ESP.CKESP_NOMBRE
		 FROM SII.CGG_KDX_COMPRA_DETALLE CMD 
		 INNER JOIN CGG_KDX_ESPECIE ESP ON (ESP.CKESP_CODIGO = CMD.CKESP_CODIGO)
		 WHERE CMP.CKCMP_CODIGO = CMD.CKCMP_CODIGO 
		 LIMIT 1) CKESP_NOMBRE,
		CKCMP_SERIE, 
		CKCMP_PROCEDENCIA, 
		CKCMP_CANTIDAD, 
		CKCMP_FACTURA, 
		CKCMP_TOTAL, 
		CKCMP_ESTADO, 
		CKCMP_FECHA_INSERT, 
		CKCMP_USUARIO_INSERT, 
		CKCMP_FECHA_UPDATE, 
		CKCMP_USUARIO_UPDATE
	FROM SII.CGG_KDX_COMPRA CMP	
	WHERE CKCMP_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CKCMP_LOTE, 0)||'' ''||COALESCE(CKCMP_FECHA_INGRESO, CURRENT_TIMESTAMP)||'' ''||COALESCE(CKCMP_SERIE, '''')||'' ''||COALESCE(CKCMP_PROCEDENCIA, '''')||'' ''||COALESCE(CKCMP_CANTIDAD, 0)||'' ''||COALESCE(CKCMP_FACTURA, '''')||'' ''||COALESCE(CKCMP_TOTAL, '''')||'' ''||COALESCE(CKCMP_ESTADO, FALSE)||'' ''||COALESCE(CKCMP_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CKCMP_USUARIO_INSERT, '''')||'' ''||COALESCE(CKCMP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CKCMP_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

