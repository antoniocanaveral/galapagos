/**
* FUNCION SII.TRG_KG_CGG_DHU_SEGUIMIENTO_ACADEMICO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_SEGUIMIENTO_ACADEMICO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_SEGUIMIENTO_ACADEMICO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDACD_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDACD_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDACD_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDACD_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDBEC_CODIGO>'||COALESCE(OLD.CDBEC_CODIGO, '')||'</CDBEC_CODIGO>'||
			'<CDACD_CODIGO>'||COALESCE(OLD.CDACD_CODIGO, '')||'</CDACD_CODIGO>'||
			'<CDACD_NUMERO_CICLO>'||COALESCE(OLD.CDACD_NUMERO_CICLO, 0)||'</CDACD_NUMERO_CICLO>'||
			'<CDACD_FECHA_INICIO>'||COALESCE(OLD.CDACD_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CDACD_FECHA_INICIO>'||
			'<CDACD_FECHA_FIN>'||COALESCE(OLD.CDACD_FECHA_FIN, CURRENT_TIMESTAMP)||'</CDACD_FECHA_FIN>'||
			'<CDACD_OBSERVACION>'||COALESCE(OLD.CDACD_OBSERVACION, '')||'</CDACD_OBSERVACION>'||
			'<CDACD_ESTADO_APROBACION>'||COALESCE(OLD.CDACD_ESTADO_APROBACION, 0)||'</CDACD_ESTADO_APROBACION>'||
			'<CDACD_ESTADO>'||COALESCE(OLD.CDACD_ESTADO, FALSE)||'</CDACD_ESTADO>'||
			'<CDACD_FECHA_INSERT>'||COALESCE(OLD.CDACD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDACD_FECHA_INSERT>'||
			'<CDACD_USUARIO_INSERT>'||COALESCE(OLD.CDACD_USUARIO_INSERT, '')||'</CDACD_USUARIO_INSERT>'||
			'<CDACD_FECHA_UPDATE>'||COALESCE(OLD.CDACD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDACD_FECHA_UPDATE>'||
			'<CDACD_USUARIO_UPDATE>'||COALESCE(OLD.CDACD_USUARIO_UPDATE, '')||'</CDACD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDBEC_CODIGO>'||COALESCE(NEW.CDBEC_CODIGO, '')||'</CDBEC_CODIGO>'||
			'<CDACD_CODIGO>'||COALESCE(NEW.CDACD_CODIGO, '')||'</CDACD_CODIGO>'||
			'<CDACD_NUMERO_CICLO>'||COALESCE(NEW.CDACD_NUMERO_CICLO, 0)||'</CDACD_NUMERO_CICLO>'||
			'<CDACD_FECHA_INICIO>'||COALESCE(NEW.CDACD_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CDACD_FECHA_INICIO>'||
			'<CDACD_FECHA_FIN>'||COALESCE(NEW.CDACD_FECHA_FIN, CURRENT_TIMESTAMP)||'</CDACD_FECHA_FIN>'||
			'<CDACD_OBSERVACION>'||COALESCE(NEW.CDACD_OBSERVACION, '')||'</CDACD_OBSERVACION>'||
			'<CDACD_ESTADO_APROBACION>'||COALESCE(NEW.CDACD_ESTADO_APROBACION, 0)||'</CDACD_ESTADO_APROBACION>'||
			'<CDACD_ESTADO>'||COALESCE(NEW.CDACD_ESTADO, FALSE)||'</CDACD_ESTADO>'||
			'<CDACD_FECHA_INSERT>'||COALESCE(NEW.CDACD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDACD_FECHA_INSERT>'||
			'<CDACD_USUARIO_INSERT>'||COALESCE(NEW.CDACD_USUARIO_INSERT, '')||'</CDACD_USUARIO_INSERT>'||
			'<CDACD_FECHA_UPDATE>'||COALESCE(NEW.CDACD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDACD_FECHA_UPDATE>'||
			'<CDACD_USUARIO_UPDATE>'||COALESCE(NEW.CDACD_USUARIO_UPDATE, '')||'</CDACD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_SEGUIMIENTO_ACADEMICO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_SEGUIMIENTO_ACADEMICO
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_ACADEMICO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_SEGUIMIENTO_ACADEMICO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_SEGUIMIENTO_ACADEMICO();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_ACADEMICO ON SII.CGG_DHU_SEGUIMIENTO_ACADEMICO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_SEGUIMIENTO_ACADEMICO DISABLE TRIGGER TRG_AUD_CGG_DHU_SEGUIMIENTO_ACADEMICO;

