/**
* FUNCION SII.TRG_KG_CGG_RES_DENUNCIA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_DENUNCIA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_DENUNCIA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRDEN_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRDEN_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRDEN_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRDEN_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRDEN_CODIGO>'||COALESCE(OLD.CRDEN_CODIGO, '')||'</CRDEN_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRDEN_ANIO>'||COALESCE(OLD.CRDEN_ANIO, '')||'</CRDEN_ANIO>'||
			'<CRDEN_NUMERO>'||COALESCE(OLD.CRDEN_NUMERO, '')||'</CRDEN_NUMERO>'||
			'<CRDEN_FECHA_RECEPCION>'||COALESCE(OLD.CRDEN_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CRDEN_FECHA_RECEPCION>'||
			'<CRDEN_NUMERO_DOC_IDENT>'||COALESCE(OLD.CRDEN_NUMERO_DOC_IDENT, '')||'</CRDEN_NUMERO_DOC_IDENT>'||
			'<CRDEN_NOMBRES>'||COALESCE(OLD.CRDEN_NOMBRES, '')||'</CRDEN_NOMBRES>'||
			'<CRDEN_APELLIDOS>'||COALESCE(OLD.CRDEN_APELLIDOS, '')||'</CRDEN_APELLIDOS>'||
			'<CRDEN_DESCRIPCION>'||COALESCE(OLD.CRDEN_DESCRIPCION, '')||'</CRDEN_DESCRIPCION>'||
			'<CRDEN_CONTACTO>'||COALESCE(OLD.CRDEN_CONTACTO, '')||'</CRDEN_CONTACTO>'||
			'<CRDEN_TIPO>'||COALESCE(OLD.CRDEN_TIPO, 0)||'</CRDEN_TIPO>'||
			'<CRDEN_DENUNCIA>'||COALESCE(OLD.CRDEN_DENUNCIA, FALSE)||'</CRDEN_DENUNCIA>'||
			'<CRDEN_SEGUIMIENTO>'||COALESCE(OLD.CRDEN_SEGUIMIENTO, '')||'</CRDEN_SEGUIMIENTO>'||
			'<CRDEN_ESTADO_DENUNCIA>'||COALESCE(OLD.CRDEN_ESTADO_DENUNCIA, 0)||'</CRDEN_ESTADO_DENUNCIA>'||
			'<CRDEN_ESTADO>'||COALESCE(OLD.CRDEN_ESTADO, FALSE)||'</CRDEN_ESTADO>'||
			'<CRDEN_FECHA_INSERT>'||COALESCE(OLD.CRDEN_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRDEN_FECHA_INSERT>'||
			'<CRDEN_USUARIO_INSERT>'||COALESCE(OLD.CRDEN_USUARIO_INSERT, '')||'</CRDEN_USUARIO_INSERT>'||
			'<CRDEN_FECHA_UPDATE>'||COALESCE(OLD.CRDEN_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRDEN_FECHA_UPDATE>'||
			'<CRDEN_USUARIO_UPDATE>'||COALESCE(OLD.CRDEN_USUARIO_UPDATE, '')||'</CRDEN_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRDEN_CODIGO>'||COALESCE(NEW.CRDEN_CODIGO, '')||'</CRDEN_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRDEN_ANIO>'||COALESCE(NEW.CRDEN_ANIO, '')||'</CRDEN_ANIO>'||
			'<CRDEN_NUMERO>'||COALESCE(NEW.CRDEN_NUMERO, '')||'</CRDEN_NUMERO>'||
			'<CRDEN_FECHA_RECEPCION>'||COALESCE(NEW.CRDEN_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CRDEN_FECHA_RECEPCION>'||
			'<CRDEN_NUMERO_DOC_IDENT>'||COALESCE(NEW.CRDEN_NUMERO_DOC_IDENT, '')||'</CRDEN_NUMERO_DOC_IDENT>'||
			'<CRDEN_NOMBRES>'||COALESCE(NEW.CRDEN_NOMBRES, '')||'</CRDEN_NOMBRES>'||
			'<CRDEN_APELLIDOS>'||COALESCE(NEW.CRDEN_APELLIDOS, '')||'</CRDEN_APELLIDOS>'||
			'<CRDEN_DESCRIPCION>'||COALESCE(NEW.CRDEN_DESCRIPCION, '')||'</CRDEN_DESCRIPCION>'||
			'<CRDEN_CONTACTO>'||COALESCE(NEW.CRDEN_CONTACTO, '')||'</CRDEN_CONTACTO>'||
			'<CRDEN_TIPO>'||COALESCE(NEW.CRDEN_TIPO, 0)||'</CRDEN_TIPO>'||
			'<CRDEN_DENUNCIA>'||COALESCE(NEW.CRDEN_DENUNCIA, FALSE)||'</CRDEN_DENUNCIA>'||
			'<CRDEN_SEGUIMIENTO>'||COALESCE(NEW.CRDEN_SEGUIMIENTO, '')||'</CRDEN_SEGUIMIENTO>'||
			'<CRDEN_ESTADO_DENUNCIA>'||COALESCE(NEW.CRDEN_ESTADO_DENUNCIA, 0)||'</CRDEN_ESTADO_DENUNCIA>'||
			'<CRDEN_ESTADO>'||COALESCE(NEW.CRDEN_ESTADO, FALSE)||'</CRDEN_ESTADO>'||
			'<CRDEN_FECHA_INSERT>'||COALESCE(NEW.CRDEN_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRDEN_FECHA_INSERT>'||
			'<CRDEN_USUARIO_INSERT>'||COALESCE(NEW.CRDEN_USUARIO_INSERT, '')||'</CRDEN_USUARIO_INSERT>'||
			'<CRDEN_FECHA_UPDATE>'||COALESCE(NEW.CRDEN_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRDEN_FECHA_UPDATE>'||
			'<CRDEN_USUARIO_UPDATE>'||COALESCE(NEW.CRDEN_USUARIO_UPDATE, '')||'</CRDEN_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_DENUNCIA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_DENUNCIA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_DENUNCIA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_DENUNCIA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_DENUNCIA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_DENUNCIA ON SII.CGG_RES_DENUNCIA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_DENUNCIA DISABLE TRIGGER TRG_AUD_CGG_RES_DENUNCIA;

