/**
* FUNCION SII.TRG_KG_CGG_RES_OBSERVADO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_OBSERVADO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_OBSERVADO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CROBS_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CROBS_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CROBS_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CROBS_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CROBS_CODIGO>'||COALESCE(OLD.CROBS_CODIGO, '')||'</CROBS_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CROBS_FECHA_OBSERVACION>'||COALESCE(OLD.CROBS_FECHA_OBSERVACION, CURRENT_TIMESTAMP)||'</CROBS_FECHA_OBSERVACION>'||
			'<CROBS_DESCRIPCION>'||COALESCE(OLD.CROBS_DESCRIPCION, '')||'</CROBS_DESCRIPCION>'||
			'<CROBS_RESUELTO>'||COALESCE(OLD.CROBS_RESUELTO, 0)||'</CROBS_RESUELTO>'||
			'<CROBS_FECHA_RESUELTO>'||COALESCE(OLD.CROBS_FECHA_RESUELTO, CURRENT_TIMESTAMP)||'</CROBS_FECHA_RESUELTO>'||
			'<CROBS_ESTADO>'||COALESCE(OLD.CROBS_ESTADO, FALSE)||'</CROBS_ESTADO>'||
			'<CROBS_FECHA_INSERT>'||COALESCE(OLD.CROBS_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CROBS_FECHA_INSERT>'||
			'<CROBS_USUARIO_INSERT>'||COALESCE(OLD.CROBS_USUARIO_INSERT, '')||'</CROBS_USUARIO_INSERT>'||
			'<CROBS_FECHA_UPDATE>'||COALESCE(OLD.CROBS_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CROBS_FECHA_UPDATE>'||
			'<CROBS_USUARIO_UPDATE>'||COALESCE(OLD.CROBS_USUARIO_UPDATE, '')||'</CROBS_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CROBS_CODIGO>'||COALESCE(NEW.CROBS_CODIGO, '')||'</CROBS_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CROBS_FECHA_OBSERVACION>'||COALESCE(NEW.CROBS_FECHA_OBSERVACION, CURRENT_TIMESTAMP)||'</CROBS_FECHA_OBSERVACION>'||
			'<CROBS_DESCRIPCION>'||COALESCE(NEW.CROBS_DESCRIPCION, '')||'</CROBS_DESCRIPCION>'||
			'<CROBS_RESUELTO>'||COALESCE(NEW.CROBS_RESUELTO, 0)||'</CROBS_RESUELTO>'||
			'<CROBS_FECHA_RESUELTO>'||COALESCE(NEW.CROBS_FECHA_RESUELTO, CURRENT_TIMESTAMP)||'</CROBS_FECHA_RESUELTO>'||
			'<CROBS_ESTADO>'||COALESCE(NEW.CROBS_ESTADO, FALSE)||'</CROBS_ESTADO>'||
			'<CROBS_FECHA_INSERT>'||COALESCE(NEW.CROBS_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CROBS_FECHA_INSERT>'||
			'<CROBS_USUARIO_INSERT>'||COALESCE(NEW.CROBS_USUARIO_INSERT, '')||'</CROBS_USUARIO_INSERT>'||
			'<CROBS_FECHA_UPDATE>'||COALESCE(NEW.CROBS_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CROBS_FECHA_UPDATE>'||
			'<CROBS_USUARIO_UPDATE>'||COALESCE(NEW.CROBS_USUARIO_UPDATE, '')||'</CROBS_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_OBSERVADO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_OBSERVADO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_OBSERVADO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_OBSERVADO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_OBSERVADO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_OBSERVADO ON SII.CGG_RES_OBSERVADO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_OBSERVADO DISABLE TRIGGER TRG_AUD_CGG_RES_OBSERVADO;

