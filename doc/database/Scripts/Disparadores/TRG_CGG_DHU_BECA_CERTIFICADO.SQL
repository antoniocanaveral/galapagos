/**
* FUNCION SII.TRG_KG_CGG_DHU_BECA_CERTIFICADO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_BECA_CERTIFICADO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_BECA_CERTIFICADO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDBCR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDBCR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDBCR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDBCR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDBCR_CODIGO>'||COALESCE(OLD.CDBCR_CODIGO, '')||'</CDBCR_CODIGO>'||
			'<CDTCA_CODIGO>'||COALESCE(OLD.CDTCA_CODIGO, '')||'</CDTCA_CODIGO>'||
			'<CDTBC_CODIGO>'||COALESCE(OLD.CDTBC_CODIGO, '')||'</CDTBC_CODIGO>'||
			'<CDBCR_OBLIGATORIO>'||COALESCE(OLD.CDBCR_OBLIGATORIO, FALSE)||'</CDBCR_OBLIGATORIO>'||
			'<CDBCR_ESTADO>'||COALESCE(OLD.CDBCR_ESTADO, FALSE)||'</CDBCR_ESTADO>'||
			'<CDBCR_FECHA_INSERT>'||COALESCE(OLD.CDBCR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDBCR_FECHA_INSERT>'||
			'<CDBCR_USUARIO_INSERT>'||COALESCE(OLD.CDBCR_USUARIO_INSERT, '')||'</CDBCR_USUARIO_INSERT>'||
			'<CDBCR_FECHA_UPDATE>'||COALESCE(OLD.CDBCR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDBCR_FECHA_UPDATE>'||
			'<CDBCR_USUARIO_UPDATE>'||COALESCE(OLD.CDBCR_USUARIO_UPDATE, '')||'</CDBCR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDBCR_CODIGO>'||COALESCE(NEW.CDBCR_CODIGO, '')||'</CDBCR_CODIGO>'||
			'<CDTCA_CODIGO>'||COALESCE(NEW.CDTCA_CODIGO, '')||'</CDTCA_CODIGO>'||
			'<CDTBC_CODIGO>'||COALESCE(NEW.CDTBC_CODIGO, '')||'</CDTBC_CODIGO>'||
			'<CDBCR_OBLIGATORIO>'||COALESCE(NEW.CDBCR_OBLIGATORIO, FALSE)||'</CDBCR_OBLIGATORIO>'||
			'<CDBCR_ESTADO>'||COALESCE(NEW.CDBCR_ESTADO, FALSE)||'</CDBCR_ESTADO>'||
			'<CDBCR_FECHA_INSERT>'||COALESCE(NEW.CDBCR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDBCR_FECHA_INSERT>'||
			'<CDBCR_USUARIO_INSERT>'||COALESCE(NEW.CDBCR_USUARIO_INSERT, '')||'</CDBCR_USUARIO_INSERT>'||
			'<CDBCR_FECHA_UPDATE>'||COALESCE(NEW.CDBCR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDBCR_FECHA_UPDATE>'||
			'<CDBCR_USUARIO_UPDATE>'||COALESCE(NEW.CDBCR_USUARIO_UPDATE, '')||'</CDBCR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_BECA_CERTIFICADO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_BECA_CERTIFICADO
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_BECA_CERTIFICADO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_BECA_CERTIFICADO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_BECA_CERTIFICADO();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_BECA_CERTIFICADO ON SII.CGG_DHU_BECA_CERTIFICADO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_BECA_CERTIFICADO DISABLE TRIGGER TRG_AUD_CGG_DHU_BECA_CERTIFICADO;

