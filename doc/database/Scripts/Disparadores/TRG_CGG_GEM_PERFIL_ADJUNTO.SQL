/**
* FUNCION SII.TRG_KG_CGG_GEM_PERFIL_ADJUNTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_PERFIL_ADJUNTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GEM_PERFIL_ADJUNTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGPFA_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGPFA_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGPFA_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGPFA_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGPFA_CODIGO>'||COALESCE(OLD.CGPFA_CODIGO, '')||'</CGPFA_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGPFA_DESCRIPCION>'||COALESCE(OLD.CGPFA_DESCRIPCION, '')||'</CGPFA_DESCRIPCION>'||
			'<CGPFA_NOMBRE_ADJUNTO>'||COALESCE(OLD.CGPFA_NOMBRE_ADJUNTO, '')||'</CGPFA_NOMBRE_ADJUNTO>'||
			'<CGPFA_TIPO_ADJUNTO>'||COALESCE(OLD.CGPFA_TIPO_ADJUNTO, '')||'</CGPFA_TIPO_ADJUNTO>'||
			'<CGPFA_ADJUNTO>'||COALESCE(encode(OLD.CGPFA_ADJUNTO, 'base64'), '')||'</CGPFA_ADJUNTO>'||
			'<CGPFA_TIPO>'||COALESCE(OLD.CGPFA_TIPO, 0)||'</CGPFA_TIPO>'||
			'<CGPFA_ESTADO>'||COALESCE(OLD.CGPFA_ESTADO, FALSE)||'</CGPFA_ESTADO>'||
			'<CGPFA_FECHA_INSERT>'||COALESCE(OLD.CGPFA_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGPFA_FECHA_INSERT>'||
			'<CGPFA_USUARIO_INSERT>'||COALESCE(OLD.CGPFA_USUARIO_INSERT, '')||'</CGPFA_USUARIO_INSERT>'||
			'<CGPFA_FECHA_UPDATE>'||COALESCE(OLD.CGPFA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGPFA_FECHA_UPDATE>'||
			'<CGPFA_USUARIO_UPDATE>'||COALESCE(OLD.CGPFA_USUARIO_UPDATE, '')||'</CGPFA_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGPFA_CODIGO>'||COALESCE(NEW.CGPFA_CODIGO, '')||'</CGPFA_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGPFA_DESCRIPCION>'||COALESCE(NEW.CGPFA_DESCRIPCION, '')||'</CGPFA_DESCRIPCION>'||
			'<CGPFA_NOMBRE_ADJUNTO>'||COALESCE(NEW.CGPFA_NOMBRE_ADJUNTO, '')||'</CGPFA_NOMBRE_ADJUNTO>'||
			'<CGPFA_TIPO_ADJUNTO>'||COALESCE(NEW.CGPFA_TIPO_ADJUNTO, '')||'</CGPFA_TIPO_ADJUNTO>'||
			'<CGPFA_ADJUNTO>'||COALESCE(encode(NEW.CGPFA_ADJUNTO, 'base64'), '')||'</CGPFA_ADJUNTO>'||
			'<CGPFA_TIPO>'||COALESCE(NEW.CGPFA_TIPO, 0)||'</CGPFA_TIPO>'||
			'<CGPFA_ESTADO>'||COALESCE(NEW.CGPFA_ESTADO, FALSE)||'</CGPFA_ESTADO>'||
			'<CGPFA_FECHA_INSERT>'||COALESCE(NEW.CGPFA_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGPFA_FECHA_INSERT>'||
			'<CGPFA_USUARIO_INSERT>'||COALESCE(NEW.CGPFA_USUARIO_INSERT, '')||'</CGPFA_USUARIO_INSERT>'||
			'<CGPFA_FECHA_UPDATE>'||COALESCE(NEW.CGPFA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGPFA_FECHA_UPDATE>'||
			'<CGPFA_USUARIO_UPDATE>'||COALESCE(NEW.CGPFA_USUARIO_UPDATE, '')||'</CGPFA_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GEM_PERFIL_ADJUNTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_PERFIL_ADJUNTO
*/
CREATE TRIGGER TRG_AUD_CGG_GEM_PERFIL_ADJUNTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GEM_PERFIL_ADJUNTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GEM_PERFIL_ADJUNTO();
COMMENT ON TRIGGER TRG_AUD_CGG_GEM_PERFIL_ADJUNTO ON SII.CGG_GEM_PERFIL_ADJUNTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GEM_PERFIL_ADJUNTO DISABLE TRIGGER TRG_AUD_CGG_GEM_PERFIL_ADJUNTO;

