/**
* FUNCION SII.TRG_KG_CGG_RES_ADJUNTO_PERSONAL. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_ADJUNTO_PERSONAL
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_ADJUNTO_PERSONAL() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRADP_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRADP_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRADP_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRADP_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRADP_CODIGO>'||COALESCE(OLD.CRADP_CODIGO, '')||'</CRADP_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRADP_FECHA_REGISTRO>'||COALESCE(OLD.CRADP_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CRADP_FECHA_REGISTRO>'||
			'<CRADP_DESCRIPCION>'||COALESCE(OLD.CRADP_DESCRIPCION, '')||'</CRADP_DESCRIPCION>'||
			'<CRADP_NOMBRE_ADJUNTO>'||COALESCE(OLD.CRADP_NOMBRE_ADJUNTO, '')||'</CRADP_NOMBRE_ADJUNTO>'||
			'<CRADP_ADJUNTO>'||COALESCE(encode(OLD.CRADP_ADJUNTO, 'base64'), '')||'</CRADP_ADJUNTO>'||
			'<CRADP_ESTADO>'||COALESCE(OLD.CRADP_ESTADO, FALSE)||'</CRADP_ESTADO>'||
			'<CRADP_FECHA_INSERT>'||COALESCE(OLD.CRADP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRADP_FECHA_INSERT>'||
			'<CRADP_USUARIO_INSERT>'||COALESCE(OLD.CRADP_USUARIO_INSERT, '')||'</CRADP_USUARIO_INSERT>'||
			'<CRADP_FECHA_UPDATE>'||COALESCE(OLD.CRADP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRADP_FECHA_UPDATE>'||
			'<CRADP_USUARIO_UPDATE>'||COALESCE(OLD.CRADP_USUARIO_UPDATE, '')||'</CRADP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRADP_CODIGO>'||COALESCE(NEW.CRADP_CODIGO, '')||'</CRADP_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRADP_FECHA_REGISTRO>'||COALESCE(NEW.CRADP_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CRADP_FECHA_REGISTRO>'||
			'<CRADP_DESCRIPCION>'||COALESCE(NEW.CRADP_DESCRIPCION, '')||'</CRADP_DESCRIPCION>'||
			'<CRADP_NOMBRE_ADJUNTO>'||COALESCE(NEW.CRADP_NOMBRE_ADJUNTO, '')||'</CRADP_NOMBRE_ADJUNTO>'||
			'<CRADP_ADJUNTO>'||COALESCE(encode(NEW.CRADP_ADJUNTO, 'base64'), '')||'</CRADP_ADJUNTO>'||
			'<CRADP_ESTADO>'||COALESCE(NEW.CRADP_ESTADO, FALSE)||'</CRADP_ESTADO>'||
			'<CRADP_FECHA_INSERT>'||COALESCE(NEW.CRADP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRADP_FECHA_INSERT>'||
			'<CRADP_USUARIO_INSERT>'||COALESCE(NEW.CRADP_USUARIO_INSERT, '')||'</CRADP_USUARIO_INSERT>'||
			'<CRADP_FECHA_UPDATE>'||COALESCE(NEW.CRADP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRADP_FECHA_UPDATE>'||
			'<CRADP_USUARIO_UPDATE>'||COALESCE(NEW.CRADP_USUARIO_UPDATE, '')||'</CRADP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_ADJUNTO_PERSONAL. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_ADJUNTO_PERSONAL
*/
CREATE TRIGGER TRG_AUD_CGG_RES_ADJUNTO_PERSONAL AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_ADJUNTO_PERSONAL FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_ADJUNTO_PERSONAL();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_ADJUNTO_PERSONAL ON SII.CGG_RES_ADJUNTO_PERSONAL IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_ADJUNTO_PERSONAL DISABLE TRIGGER TRG_AUD_CGG_RES_ADJUNTO_PERSONAL;

