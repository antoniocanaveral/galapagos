/**
* FUNCION SII.TRG_KG_CGG_RES_TRAMITE_GARANTIA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TRAMITE_GARANTIA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_TRAMITE_GARANTIA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRTGR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTGR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRTGR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTGR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRTGR_CODIGO>'||COALESCE(OLD.CRTGR_CODIGO, '')||'</CRTGR_CODIGO>'||
			'<CRDPT_CODIGO>'||COALESCE(OLD.CRDPT_CODIGO, '')||'</CRDPT_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(OLD.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CRGRT_CODIGO>'||COALESCE(OLD.CRGRT_CODIGO, '')||'</CRGRT_CODIGO>'||
			'<CRTGR_VALOR>'||COALESCE(OLD.CRTGR_VALOR, 0)||'</CRTGR_VALOR>'||
			'<CRTGR_FECHA_SOLI_DEVOL>'||COALESCE(OLD.CRTGR_FECHA_SOLI_DEVOL, CURRENT_TIMESTAMP)||'</CRTGR_FECHA_SOLI_DEVOL>'||
			'<CRTGR_FECHA_DEVOLUCION>'||COALESCE(OLD.CRTGR_FECHA_DEVOLUCION, CURRENT_TIMESTAMP)||'</CRTGR_FECHA_DEVOLUCION>'||
			'<CRTGR_DEVUELTO>'||COALESCE(OLD.CRTGR_DEVUELTO, FALSE)||'</CRTGR_DEVUELTO>'||
			'<CRTGR_ESTADO_ASIGNACION>'||COALESCE(OLD.CRTGR_ESTADO_ASIGNACION, 0)||'</CRTGR_ESTADO_ASIGNACION>'||
			'<CRTGR_ESTADO>'||COALESCE(OLD.CRTGR_ESTADO, FALSE)||'</CRTGR_ESTADO>'||
			'<CRTGR_FECHA_INSERT>'||COALESCE(OLD.CRTGR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTGR_FECHA_INSERT>'||
			'<CRTGR_USUARIO_INSERT>'||COALESCE(OLD.CRTGR_USUARIO_INSERT, '')||'</CRTGR_USUARIO_INSERT>'||
			'<CRTGR_FECHA_UPDATE>'||COALESCE(OLD.CRTGR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTGR_FECHA_UPDATE>'||
			'<CRTGR_USUARIO_UPDATE>'||COALESCE(OLD.CRTGR_USUARIO_UPDATE, '')||'</CRTGR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRTGR_CODIGO>'||COALESCE(NEW.CRTGR_CODIGO, '')||'</CRTGR_CODIGO>'||
			'<CRDPT_CODIGO>'||COALESCE(NEW.CRDPT_CODIGO, '')||'</CRDPT_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(NEW.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CRGRT_CODIGO>'||COALESCE(NEW.CRGRT_CODIGO, '')||'</CRGRT_CODIGO>'||
			'<CRTGR_VALOR>'||COALESCE(NEW.CRTGR_VALOR, 0)||'</CRTGR_VALOR>'||
			'<CRTGR_FECHA_SOLI_DEVOL>'||COALESCE(NEW.CRTGR_FECHA_SOLI_DEVOL, CURRENT_TIMESTAMP)||'</CRTGR_FECHA_SOLI_DEVOL>'||
			'<CRTGR_FECHA_DEVOLUCION>'||COALESCE(NEW.CRTGR_FECHA_DEVOLUCION, CURRENT_TIMESTAMP)||'</CRTGR_FECHA_DEVOLUCION>'||
			'<CRTGR_DEVUELTO>'||COALESCE(NEW.CRTGR_DEVUELTO, FALSE)||'</CRTGR_DEVUELTO>'||
			'<CRTGR_ESTADO_ASIGNACION>'||COALESCE(NEW.CRTGR_ESTADO_ASIGNACION, 0)||'</CRTGR_ESTADO_ASIGNACION>'||
			'<CRTGR_ESTADO>'||COALESCE(NEW.CRTGR_ESTADO, FALSE)||'</CRTGR_ESTADO>'||
			'<CRTGR_FECHA_INSERT>'||COALESCE(NEW.CRTGR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTGR_FECHA_INSERT>'||
			'<CRTGR_USUARIO_INSERT>'||COALESCE(NEW.CRTGR_USUARIO_INSERT, '')||'</CRTGR_USUARIO_INSERT>'||
			'<CRTGR_FECHA_UPDATE>'||COALESCE(NEW.CRTGR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTGR_FECHA_UPDATE>'||
			'<CRTGR_USUARIO_UPDATE>'||COALESCE(NEW.CRTGR_USUARIO_UPDATE, '')||'</CRTGR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_TRAMITE_GARANTIA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TRAMITE_GARANTIA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_TRAMITE_GARANTIA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_TRAMITE_GARANTIA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_TRAMITE_GARANTIA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_TRAMITE_GARANTIA ON SII.CGG_RES_TRAMITE_GARANTIA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_TRAMITE_GARANTIA DISABLE TRIGGER TRG_AUD_CGG_RES_TRAMITE_GARANTIA;

