/**
* FUNCION SII.TRG_KG_CGG_JUR_ANTICIPO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_ANTICIPO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_JUR_ANTICIPO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CJAAC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJAAC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CJAAC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJAAC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CJAAC_CODIGO>'||COALESCE(OLD.CJAAC_CODIGO, '')||'</CJAAC_CODIGO>'||
			'<CJACU_CODIGO>'||COALESCE(OLD.CJACU_CODIGO, '')||'</CJACU_CODIGO>'||
			'<CJAAC_PORCENTAJE>'||COALESCE(OLD.CJAAC_PORCENTAJE, 0)||'</CJAAC_PORCENTAJE>'||
			'<CJAAC_OBSERVACION>'||COALESCE(OLD.CJAAC_OBSERVACION, '')||'</CJAAC_OBSERVACION>'||
			'<CJAAC_FECHA>'||COALESCE(OLD.CJAAC_FECHA, CURRENT_TIMESTAMP)||'</CJAAC_FECHA>'||
			'<CJAAC_MONTO>'||COALESCE(OLD.CJAAC_MONTO, 0)||'</CJAAC_MONTO>'||
			'<CJAAC_VALOR_1>'||COALESCE(OLD.CJAAC_VALOR_1, '')||'</CJAAC_VALOR_1>'||
			'<CJAAC_ESTADO>'||COALESCE(OLD.CJAAC_ESTADO, FALSE)||'</CJAAC_ESTADO>'||
			'<CJAAC_FECHA_INSERT>'||COALESCE(OLD.CJAAC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJAAC_FECHA_INSERT>'||
			'<CJAAC_USUARIO_INSERT>'||COALESCE(OLD.CJAAC_USUARIO_INSERT, '')||'</CJAAC_USUARIO_INSERT>'||
			'<CJAAC_FECHA_UPDATE>'||COALESCE(OLD.CJAAC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJAAC_FECHA_UPDATE>'||
			'<CJAAC_USUARIO_UPDATE>'||COALESCE(OLD.CJAAC_USUARIO_UPDATE, '')||'</CJAAC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CJAAC_CODIGO>'||COALESCE(NEW.CJAAC_CODIGO, '')||'</CJAAC_CODIGO>'||
			'<CJACU_CODIGO>'||COALESCE(NEW.CJACU_CODIGO, '')||'</CJACU_CODIGO>'||
			'<CJAAC_PORCENTAJE>'||COALESCE(NEW.CJAAC_PORCENTAJE, 0)||'</CJAAC_PORCENTAJE>'||
			'<CJAAC_OBSERVACION>'||COALESCE(NEW.CJAAC_OBSERVACION, '')||'</CJAAC_OBSERVACION>'||
			'<CJAAC_FECHA>'||COALESCE(NEW.CJAAC_FECHA, CURRENT_TIMESTAMP)||'</CJAAC_FECHA>'||
			'<CJAAC_MONTO>'||COALESCE(NEW.CJAAC_MONTO, 0)||'</CJAAC_MONTO>'||
			'<CJAAC_VALOR_1>'||COALESCE(NEW.CJAAC_VALOR_1, '')||'</CJAAC_VALOR_1>'||
			'<CJAAC_ESTADO>'||COALESCE(NEW.CJAAC_ESTADO, FALSE)||'</CJAAC_ESTADO>'||
			'<CJAAC_FECHA_INSERT>'||COALESCE(NEW.CJAAC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJAAC_FECHA_INSERT>'||
			'<CJAAC_USUARIO_INSERT>'||COALESCE(NEW.CJAAC_USUARIO_INSERT, '')||'</CJAAC_USUARIO_INSERT>'||
			'<CJAAC_FECHA_UPDATE>'||COALESCE(NEW.CJAAC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJAAC_FECHA_UPDATE>'||
			'<CJAAC_USUARIO_UPDATE>'||COALESCE(NEW.CJAAC_USUARIO_UPDATE, '')||'</CJAAC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_JUR_ANTICIPO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_ANTICIPO
*/
CREATE TRIGGER TRG_AUD_CGG_JUR_ANTICIPO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_JUR_ANTICIPO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_JUR_ANTICIPO();
COMMENT ON TRIGGER TRG_AUD_CGG_JUR_ANTICIPO ON SII.CGG_JUR_ANTICIPO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_JUR_ANTICIPO DISABLE TRIGGER TRG_AUD_CGG_JUR_ANTICIPO;

