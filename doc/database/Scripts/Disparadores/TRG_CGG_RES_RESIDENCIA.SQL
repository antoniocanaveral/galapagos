/**
* FUNCION SII.TRG_KG_CGG_RES_RESIDENCIA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_RESIDENCIA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_RESIDENCIA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRRSD_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRRSD_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRRSD_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRRSD_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRRSD_CODIGO>'||COALESCE(OLD.CRRSD_CODIGO, '')||'</CRRSD_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRTST_CODIGO>'||COALESCE(OLD.CRTST_CODIGO, '')||'</CRTST_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(OLD.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CGG_CRTRA_CODIGO>'||COALESCE(OLD.CGG_CRTRA_CODIGO, '')||'</CGG_CRTRA_CODIGO>'||
			'<CRRSD_NUMERO>'||COALESCE(OLD.CRRSD_NUMERO, 0)||'</CRRSD_NUMERO>'||
			'<CRRSD_MODALIDAD>'||COALESCE(OLD.CRRSD_MODALIDAD, 0)||'</CRRSD_MODALIDAD>'||
			'<CRRSD_FECHA_INICIO>'||COALESCE(OLD.CRRSD_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_INICIO>'||
			'<CRRSD_FECHA_CADUCIDAD>'||COALESCE(OLD.CRRSD_FECHA_CADUCIDAD, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_CADUCIDAD>'||
			'<CRRSD_REVOCADA>'||COALESCE(OLD.CRRSD_REVOCADA, FALSE)||'</CRRSD_REVOCADA>'||
			'<CRRSD_FECHA_REVOCATORIA>'||COALESCE(OLD.CRRSD_FECHA_REVOCATORIA, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_REVOCATORIA>'||
			'<CRRSD_MOTIVO_REVOCATORIA>'||COALESCE(OLD.CRRSD_MOTIVO_REVOCATORIA, '')||'</CRRSD_MOTIVO_REVOCATORIA>'||
			'<CRRSD_VIGENTE>'||COALESCE(OLD.CRRSD_VIGENTE, FALSE)||'</CRRSD_VIGENTE>'||
			'<CRRSD_TRAMITE_PENDIENTE>'||COALESCE(OLD.CRRSD_TRAMITE_PENDIENTE, FALSE)||'</CRRSD_TRAMITE_PENDIENTE>'||
			'<CRRSD_ESTADO>'||COALESCE(OLD.CRRSD_ESTADO, FALSE)||'</CRRSD_ESTADO>'||
			'<CRRSD_FECHA_INSERT>'||COALESCE(OLD.CRRSD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_INSERT>'||
			'<CRRSD_USUARIO_INSERT>'||COALESCE(OLD.CRRSD_USUARIO_INSERT, '')||'</CRRSD_USUARIO_INSERT>'||
			'<CRRSD_FECHA_UPDATE>'||COALESCE(OLD.CRRSD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_UPDATE>'||
			'<CRRSD_USUARIO_UPDATE>'||COALESCE(OLD.CRRSD_USUARIO_UPDATE, '')||'</CRRSD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRRSD_CODIGO>'||COALESCE(NEW.CRRSD_CODIGO, '')||'</CRRSD_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRTST_CODIGO>'||COALESCE(NEW.CRTST_CODIGO, '')||'</CRTST_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(NEW.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CGG_CRTRA_CODIGO>'||COALESCE(NEW.CGG_CRTRA_CODIGO, '')||'</CGG_CRTRA_CODIGO>'||
			'<CRRSD_NUMERO>'||COALESCE(NEW.CRRSD_NUMERO, 0)||'</CRRSD_NUMERO>'||
			'<CRRSD_MODALIDAD>'||COALESCE(NEW.CRRSD_MODALIDAD, 0)||'</CRRSD_MODALIDAD>'||
			'<CRRSD_FECHA_INICIO>'||COALESCE(NEW.CRRSD_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_INICIO>'||
			'<CRRSD_FECHA_CADUCIDAD>'||COALESCE(NEW.CRRSD_FECHA_CADUCIDAD, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_CADUCIDAD>'||
			'<CRRSD_REVOCADA>'||COALESCE(NEW.CRRSD_REVOCADA, FALSE)||'</CRRSD_REVOCADA>'||
			'<CRRSD_FECHA_REVOCATORIA>'||COALESCE(NEW.CRRSD_FECHA_REVOCATORIA, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_REVOCATORIA>'||
			'<CRRSD_MOTIVO_REVOCATORIA>'||COALESCE(NEW.CRRSD_MOTIVO_REVOCATORIA, '')||'</CRRSD_MOTIVO_REVOCATORIA>'||
			'<CRRSD_VIGENTE>'||COALESCE(NEW.CRRSD_VIGENTE, FALSE)||'</CRRSD_VIGENTE>'||
			'<CRRSD_TRAMITE_PENDIENTE>'||COALESCE(NEW.CRRSD_TRAMITE_PENDIENTE, FALSE)||'</CRRSD_TRAMITE_PENDIENTE>'||
			'<CRRSD_ESTADO>'||COALESCE(NEW.CRRSD_ESTADO, FALSE)||'</CRRSD_ESTADO>'||
			'<CRRSD_FECHA_INSERT>'||COALESCE(NEW.CRRSD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_INSERT>'||
			'<CRRSD_USUARIO_INSERT>'||COALESCE(NEW.CRRSD_USUARIO_INSERT, '')||'</CRRSD_USUARIO_INSERT>'||
			'<CRRSD_FECHA_UPDATE>'||COALESCE(NEW.CRRSD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRRSD_FECHA_UPDATE>'||
			'<CRRSD_USUARIO_UPDATE>'||COALESCE(NEW.CRRSD_USUARIO_UPDATE, '')||'</CRRSD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_RESIDENCIA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_RESIDENCIA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_RESIDENCIA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_RESIDENCIA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_RESIDENCIA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_RESIDENCIA ON SII.CGG_RES_RESIDENCIA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_RESIDENCIA DISABLE TRIGGER TRG_AUD_CGG_RES_RESIDENCIA;

