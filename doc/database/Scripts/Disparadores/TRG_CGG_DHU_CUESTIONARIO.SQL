/**
* FUNCION SII.TRG_KG_CGG_DHU_CUESTIONARIO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_CUESTIONARIO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_CUESTIONARIO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDCST_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDCST_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDCST_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDCST_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDCST_CODIGO>'||COALESCE(OLD.CDCST_CODIGO, '')||'</CDCST_CODIGO>'||
			'<CDPRG_CODIGO>'||COALESCE(OLD.CDPRG_CODIGO, '')||'</CDPRG_CODIGO>'||
			'<CDENC_CODIGO>'||COALESCE(OLD.CDENC_CODIGO, '')||'</CDENC_CODIGO>'||
			'<CDCST_ESTADO>'||COALESCE(OLD.CDCST_ESTADO, FALSE)||'</CDCST_ESTADO>'||
			'<CDCST_FECHA_INSERT>'||COALESCE(OLD.CDCST_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDCST_FECHA_INSERT>'||
			'<CDCST_USUARIO_INSERT>'||COALESCE(OLD.CDCST_USUARIO_INSERT, '')||'</CDCST_USUARIO_INSERT>'||
			'<CDCST_FECHA_UPDATE>'||COALESCE(OLD.CDCST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDCST_FECHA_UPDATE>'||
			'<CDCST_USUARIO_UPDATE>'||COALESCE(OLD.CDCST_USUARIO_UPDATE, '')||'</CDCST_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDCST_CODIGO>'||COALESCE(NEW.CDCST_CODIGO, '')||'</CDCST_CODIGO>'||
			'<CDPRG_CODIGO>'||COALESCE(NEW.CDPRG_CODIGO, '')||'</CDPRG_CODIGO>'||
			'<CDENC_CODIGO>'||COALESCE(NEW.CDENC_CODIGO, '')||'</CDENC_CODIGO>'||
			'<CDCST_ESTADO>'||COALESCE(NEW.CDCST_ESTADO, FALSE)||'</CDCST_ESTADO>'||
			'<CDCST_FECHA_INSERT>'||COALESCE(NEW.CDCST_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDCST_FECHA_INSERT>'||
			'<CDCST_USUARIO_INSERT>'||COALESCE(NEW.CDCST_USUARIO_INSERT, '')||'</CDCST_USUARIO_INSERT>'||
			'<CDCST_FECHA_UPDATE>'||COALESCE(NEW.CDCST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDCST_FECHA_UPDATE>'||
			'<CDCST_USUARIO_UPDATE>'||COALESCE(NEW.CDCST_USUARIO_UPDATE, '')||'</CDCST_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_CUESTIONARIO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_CUESTIONARIO
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_CUESTIONARIO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_CUESTIONARIO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_CUESTIONARIO();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_CUESTIONARIO ON SII.CGG_DHU_CUESTIONARIO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_CUESTIONARIO DISABLE TRIGGER TRG_AUD_CGG_DHU_CUESTIONARIO;

