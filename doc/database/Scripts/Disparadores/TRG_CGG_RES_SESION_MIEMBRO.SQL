/**
* FUNCION SII.TRG_KG_CGG_RES_SESION_MIEMBRO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_SESION_MIEMBRO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_SESION_MIEMBRO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRSMB_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRSMB_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRSMB_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRSMB_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRSMB_CODIGO>'||COALESCE(OLD.CRSMB_CODIGO, '')||'</CRSMB_CODIGO>'||
			'<CRSSC_CODIGO>'||COALESCE(OLD.CRSSC_CODIGO, '')||'</CRSSC_CODIGO>'||
			'<CRMBC_CODIGO>'||COALESCE(OLD.CRMBC_CODIGO, '')||'</CRMBC_CODIGO>'||
			'<CGG_CRMBC_CODIGO>'||COALESCE(OLD.CGG_CRMBC_CODIGO, '')||'</CGG_CRMBC_CODIGO>'||
			'<CRSMB_PRESIDENTE>'||COALESCE(OLD.CRSMB_PRESIDENTE, FALSE)||'</CRSMB_PRESIDENTE>'||
			'<CRSMB_PRESENTE>'||COALESCE(OLD.CRSMB_PRESENTE, FALSE)||'</CRSMB_PRESENTE>'||
			'<CRSMB_ESTADO>'||COALESCE(OLD.CRSMB_ESTADO, FALSE)||'</CRSMB_ESTADO>'||
			'<CRSMB_FECHA_INSERT>'||COALESCE(OLD.CRSMB_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRSMB_FECHA_INSERT>'||
			'<CRSMB_USUARIO_INSERT>'||COALESCE(OLD.CRSMB_USUARIO_INSERT, '')||'</CRSMB_USUARIO_INSERT>'||
			'<CRSMB_FECHA_UPDATE>'||COALESCE(OLD.CRSMB_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRSMB_FECHA_UPDATE>'||
			'<CRSMB_USUARIO_UPDATE>'||COALESCE(OLD.CRSMB_USUARIO_UPDATE, '')||'</CRSMB_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRSMB_CODIGO>'||COALESCE(NEW.CRSMB_CODIGO, '')||'</CRSMB_CODIGO>'||
			'<CRSSC_CODIGO>'||COALESCE(NEW.CRSSC_CODIGO, '')||'</CRSSC_CODIGO>'||
			'<CRMBC_CODIGO>'||COALESCE(NEW.CRMBC_CODIGO, '')||'</CRMBC_CODIGO>'||
			'<CGG_CRMBC_CODIGO>'||COALESCE(NEW.CGG_CRMBC_CODIGO, '')||'</CGG_CRMBC_CODIGO>'||
			'<CRSMB_PRESIDENTE>'||COALESCE(NEW.CRSMB_PRESIDENTE, FALSE)||'</CRSMB_PRESIDENTE>'||
			'<CRSMB_PRESENTE>'||COALESCE(NEW.CRSMB_PRESENTE, FALSE)||'</CRSMB_PRESENTE>'||
			'<CRSMB_ESTADO>'||COALESCE(NEW.CRSMB_ESTADO, FALSE)||'</CRSMB_ESTADO>'||
			'<CRSMB_FECHA_INSERT>'||COALESCE(NEW.CRSMB_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRSMB_FECHA_INSERT>'||
			'<CRSMB_USUARIO_INSERT>'||COALESCE(NEW.CRSMB_USUARIO_INSERT, '')||'</CRSMB_USUARIO_INSERT>'||
			'<CRSMB_FECHA_UPDATE>'||COALESCE(NEW.CRSMB_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRSMB_FECHA_UPDATE>'||
			'<CRSMB_USUARIO_UPDATE>'||COALESCE(NEW.CRSMB_USUARIO_UPDATE, '')||'</CRSMB_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_SESION_MIEMBRO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_SESION_MIEMBRO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_SESION_MIEMBRO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_SESION_MIEMBRO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_SESION_MIEMBRO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_SESION_MIEMBRO ON SII.CGG_RES_SESION_MIEMBRO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_SESION_MIEMBRO DISABLE TRIGGER TRG_AUD_CGG_RES_SESION_MIEMBRO;

