/**
* FUNCION SII.TRG_KG_CGG_DHU_PREGUNTA_RESPUESTA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_PREGUNTA_RESPUESTA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_PREGUNTA_RESPUESTA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDPRS_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDPRS_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDPRS_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDPRS_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDPRS_CODIGO>'||COALESCE(OLD.CDPRS_CODIGO, '')||'</CDPRS_CODIGO>'||
			'<CDPRG_CODIGO>'||COALESCE(OLD.CDPRG_CODIGO, '')||'</CDPRG_CODIGO>'||
			'<CDRES_CODIGO>'||COALESCE(OLD.CDRES_CODIGO, '')||'</CDRES_CODIGO>'||
			'<CDPRS_VALORACION>'||COALESCE(OLD.CDPRS_VALORACION, 0)||'</CDPRS_VALORACION>'||
			'<CDPRS_ESTADO>'||COALESCE(OLD.CDPRS_ESTADO, FALSE)||'</CDPRS_ESTADO>'||
			'<CDPRS_FECHA_INSERT>'||COALESCE(OLD.CDPRS_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDPRS_FECHA_INSERT>'||
			'<CDPRS_USUARIO_INSERT>'||COALESCE(OLD.CDPRS_USUARIO_INSERT, '')||'</CDPRS_USUARIO_INSERT>'||
			'<CDPRS_FECHA_UPDATE>'||COALESCE(OLD.CDPRS_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDPRS_FECHA_UPDATE>'||
			'<CDPRS_USUARIO_UPDATE>'||COALESCE(OLD.CDPRS_USUARIO_UPDATE, '')||'</CDPRS_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDPRS_CODIGO>'||COALESCE(NEW.CDPRS_CODIGO, '')||'</CDPRS_CODIGO>'||
			'<CDPRG_CODIGO>'||COALESCE(NEW.CDPRG_CODIGO, '')||'</CDPRG_CODIGO>'||
			'<CDRES_CODIGO>'||COALESCE(NEW.CDRES_CODIGO, '')||'</CDRES_CODIGO>'||
			'<CDPRS_VALORACION>'||COALESCE(NEW.CDPRS_VALORACION, 0)||'</CDPRS_VALORACION>'||
			'<CDPRS_ESTADO>'||COALESCE(NEW.CDPRS_ESTADO, FALSE)||'</CDPRS_ESTADO>'||
			'<CDPRS_FECHA_INSERT>'||COALESCE(NEW.CDPRS_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDPRS_FECHA_INSERT>'||
			'<CDPRS_USUARIO_INSERT>'||COALESCE(NEW.CDPRS_USUARIO_INSERT, '')||'</CDPRS_USUARIO_INSERT>'||
			'<CDPRS_FECHA_UPDATE>'||COALESCE(NEW.CDPRS_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDPRS_FECHA_UPDATE>'||
			'<CDPRS_USUARIO_UPDATE>'||COALESCE(NEW.CDPRS_USUARIO_UPDATE, '')||'</CDPRS_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_PREGUNTA_RESPUESTA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_PREGUNTA_RESPUESTA
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_PREGUNTA_RESPUESTA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_PREGUNTA_RESPUESTA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_PREGUNTA_RESPUESTA();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_PREGUNTA_RESPUESTA ON SII.CGG_DHU_PREGUNTA_RESPUESTA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_PREGUNTA_RESPUESTA DISABLE TRIGGER TRG_AUD_CGG_DHU_PREGUNTA_RESPUESTA;

