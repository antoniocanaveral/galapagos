/**
* FUNCION SII.TRG_KG_CGG_BUZON_CORREO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_BUZON_CORREO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_BUZON_CORREO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CBZC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CBZC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CBZC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CBZC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CBZC_CODIGO>'||COALESCE(OLD.CBZC_CODIGO, '')||'</CBZC_CODIGO>'||
			'<CBZC_DESTINATARIO>'||COALESCE(OLD.CBZC_DESTINATARIO, '')||'</CBZC_DESTINATARIO>'||
			'<CBZC_ASUNTO>'||COALESCE(OLD.CBZC_ASUNTO, '')||'</CBZC_ASUNTO>'||
			'<CBZC_MENSAJE>'||COALESCE(OLD.CBZC_MENSAJE, '')||'</CBZC_MENSAJE>'||
			'<CBZC_TIPO_CONTENIDO>'||COALESCE(OLD.CBZC_TIPO_CONTENIDO, '')||'</CBZC_TIPO_CONTENIDO>'||
			'<CBZC_NUMERO_INTENTOS>'||COALESCE(OLD.CBZC_NUMERO_INTENTOS, 0)||'</CBZC_NUMERO_INTENTOS>'||
			'<CBZC_ENVIADO>'||COALESCE(OLD.CBZC_ENVIADO, FALSE)||'</CBZC_ENVIADO>'||
			'<CBZC_ESTADO>'||COALESCE(OLD.CBZC_ESTADO, FALSE)||'</CBZC_ESTADO>'||
			'<CBZC_FECHA_INSERT>'||COALESCE(OLD.CBZC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CBZC_FECHA_INSERT>'||
			'<CBZC_USUARIO_INSERT>'||COALESCE(OLD.CBZC_USUARIO_INSERT, '')||'</CBZC_USUARIO_INSERT>'||
			'<CBZC_FECHA_UPDATE>'||COALESCE(OLD.CBZC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CBZC_FECHA_UPDATE>'||
			'<CBZC_USUARIO_UPDATE>'||COALESCE(OLD.CBZC_USUARIO_UPDATE, '')||'</CBZC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CBZC_CODIGO>'||COALESCE(NEW.CBZC_CODIGO, '')||'</CBZC_CODIGO>'||
			'<CBZC_DESTINATARIO>'||COALESCE(NEW.CBZC_DESTINATARIO, '')||'</CBZC_DESTINATARIO>'||
			'<CBZC_ASUNTO>'||COALESCE(NEW.CBZC_ASUNTO, '')||'</CBZC_ASUNTO>'||
			'<CBZC_MENSAJE>'||COALESCE(NEW.CBZC_MENSAJE, '')||'</CBZC_MENSAJE>'||
			'<CBZC_TIPO_CONTENIDO>'||COALESCE(NEW.CBZC_TIPO_CONTENIDO, '')||'</CBZC_TIPO_CONTENIDO>'||
			'<CBZC_NUMERO_INTENTOS>'||COALESCE(NEW.CBZC_NUMERO_INTENTOS, 0)||'</CBZC_NUMERO_INTENTOS>'||
			'<CBZC_ENVIADO>'||COALESCE(NEW.CBZC_ENVIADO, FALSE)||'</CBZC_ENVIADO>'||
			'<CBZC_ESTADO>'||COALESCE(NEW.CBZC_ESTADO, FALSE)||'</CBZC_ESTADO>'||
			'<CBZC_FECHA_INSERT>'||COALESCE(NEW.CBZC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CBZC_FECHA_INSERT>'||
			'<CBZC_USUARIO_INSERT>'||COALESCE(NEW.CBZC_USUARIO_INSERT, '')||'</CBZC_USUARIO_INSERT>'||
			'<CBZC_FECHA_UPDATE>'||COALESCE(NEW.CBZC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CBZC_FECHA_UPDATE>'||
			'<CBZC_USUARIO_UPDATE>'||COALESCE(NEW.CBZC_USUARIO_UPDATE, '')||'</CBZC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_BUZON_CORREO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_BUZON_CORREO
*/
CREATE TRIGGER TRG_AUD_CGG_BUZON_CORREO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_BUZON_CORREO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_BUZON_CORREO();
COMMENT ON TRIGGER TRG_AUD_CGG_BUZON_CORREO ON SII.CGG_BUZON_CORREO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_BUZON_CORREO DISABLE TRIGGER TRG_AUD_CGG_BUZON_CORREO;

