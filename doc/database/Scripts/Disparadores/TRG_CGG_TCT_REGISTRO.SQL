/**
* FUNCION SII.TRG_KG_CGG_TCT_REGISTRO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_REGISTRO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_TCT_REGISTRO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CTREG_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTREG_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CTREG_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CTREG_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CTREG_CODIGO>'||COALESCE(OLD.CTREG_CODIGO, '')||'</CTREG_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CTGTR_CODIGO>'||COALESCE(OLD.CTGTR_CODIGO, '')||'</CTGTR_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CGG_CUSU_CODIGO>'||COALESCE(OLD.CGG_CUSU_CODIGO, '')||'</CGG_CUSU_CODIGO>'||
			'<CARPT_CODIGO>'||COALESCE(OLD.CARPT_CODIGO, '')||'</CARPT_CODIGO>'||
			'<CGG_CARPT_CODIGO>'||COALESCE(OLD.CGG_CARPT_CODIGO, '')||'</CGG_CARPT_CODIGO>'||
			'<CRALN_CODIGO>'||COALESCE(OLD.CRALN_CODIGO, '')||'</CRALN_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(OLD.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CTREG_NUMERO>'||COALESCE(OLD.CTREG_NUMERO, 0)||'</CTREG_NUMERO>'||
			'<CTREG_FECHA_PREREGISTRO>'||COALESCE(OLD.CTREG_FECHA_PREREGISTRO, CURRENT_TIMESTAMP)||'</CTREG_FECHA_PREREGISTRO>'||
			'<CTREG_FECHA_INGRESO>'||COALESCE(OLD.CTREG_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CTREG_FECHA_INGRESO>'||
			'<CTREG_CODIGO_BARRAS>'||COALESCE(OLD.CTREG_CODIGO_BARRAS, '')||'</CTREG_CODIGO_BARRAS>'||
			'<CTREG_FECHA_SALIDA>'||COALESCE(OLD.CTREG_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CTREG_FECHA_SALIDA>'||
			'<CTREG_IMPRESION_ESPECIE>'||COALESCE(OLD.CTREG_IMPRESION_ESPECIE, CURRENT_TIMESTAMP)||'</CTREG_IMPRESION_ESPECIE>'||
			'<CTREG_ESTADO_REGISTRO>'||COALESCE(OLD.CTREG_ESTADO_REGISTRO, 0)||'</CTREG_ESTADO_REGISTRO>'||
			'<CTREG_OBSERVACION>'||COALESCE(OLD.CTREG_OBSERVACION, '')||'</CTREG_OBSERVACION>'||
			'<CTREG_ESTADO>'||COALESCE(OLD.CTREG_ESTADO, FALSE)||'</CTREG_ESTADO>'||
			'<CTREG_FECHA_INSERT>'||COALESCE(OLD.CTREG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTREG_FECHA_INSERT>'||
			'<CTREG_USUARIO_INSERT>'||COALESCE(OLD.CTREG_USUARIO_INSERT, '')||'</CTREG_USUARIO_INSERT>'||
			'<CTREG_FECHA_UPDATE>'||COALESCE(OLD.CTREG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTREG_FECHA_UPDATE>'||
			'<CTREG_USUARIO_UPDATE>'||COALESCE(OLD.CTREG_USUARIO_UPDATE, '')||'</CTREG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CTREG_CODIGO>'||COALESCE(NEW.CTREG_CODIGO, '')||'</CTREG_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CTGTR_CODIGO>'||COALESCE(NEW.CTGTR_CODIGO, '')||'</CTGTR_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CGG_CUSU_CODIGO>'||COALESCE(NEW.CGG_CUSU_CODIGO, '')||'</CGG_CUSU_CODIGO>'||
			'<CARPT_CODIGO>'||COALESCE(NEW.CARPT_CODIGO, '')||'</CARPT_CODIGO>'||
			'<CGG_CARPT_CODIGO>'||COALESCE(NEW.CGG_CARPT_CODIGO, '')||'</CGG_CARPT_CODIGO>'||
			'<CRALN_CODIGO>'||COALESCE(NEW.CRALN_CODIGO, '')||'</CRALN_CODIGO>'||
			'<CRTRA_CODIGO>'||COALESCE(NEW.CRTRA_CODIGO, '')||'</CRTRA_CODIGO>'||
			'<CTREG_NUMERO>'||COALESCE(NEW.CTREG_NUMERO, 0)||'</CTREG_NUMERO>'||
			'<CTREG_FECHA_PREREGISTRO>'||COALESCE(NEW.CTREG_FECHA_PREREGISTRO, CURRENT_TIMESTAMP)||'</CTREG_FECHA_PREREGISTRO>'||
			'<CTREG_FECHA_INGRESO>'||COALESCE(NEW.CTREG_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CTREG_FECHA_INGRESO>'||
			'<CTREG_CODIGO_BARRAS>'||COALESCE(NEW.CTREG_CODIGO_BARRAS, '')||'</CTREG_CODIGO_BARRAS>'||
			'<CTREG_FECHA_SALIDA>'||COALESCE(NEW.CTREG_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CTREG_FECHA_SALIDA>'||
			'<CTREG_IMPRESION_ESPECIE>'||COALESCE(NEW.CTREG_IMPRESION_ESPECIE, CURRENT_TIMESTAMP)||'</CTREG_IMPRESION_ESPECIE>'||
			'<CTREG_ESTADO_REGISTRO>'||COALESCE(NEW.CTREG_ESTADO_REGISTRO, 0)||'</CTREG_ESTADO_REGISTRO>'||
			'<CTREG_OBSERVACION>'||COALESCE(NEW.CTREG_OBSERVACION, '')||'</CTREG_OBSERVACION>'||
			'<CTREG_ESTADO>'||COALESCE(NEW.CTREG_ESTADO, FALSE)||'</CTREG_ESTADO>'||
			'<CTREG_FECHA_INSERT>'||COALESCE(NEW.CTREG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CTREG_FECHA_INSERT>'||
			'<CTREG_USUARIO_INSERT>'||COALESCE(NEW.CTREG_USUARIO_INSERT, '')||'</CTREG_USUARIO_INSERT>'||
			'<CTREG_FECHA_UPDATE>'||COALESCE(NEW.CTREG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CTREG_FECHA_UPDATE>'||
			'<CTREG_USUARIO_UPDATE>'||COALESCE(NEW.CTREG_USUARIO_UPDATE, '')||'</CTREG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_TCT_REGISTRO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_TCT_REGISTRO
*/
CREATE TRIGGER TRG_AUD_CGG_TCT_REGISTRO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_TCT_REGISTRO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_TCT_REGISTRO();
COMMENT ON TRIGGER TRG_AUD_CGG_TCT_REGISTRO ON SII.CGG_TCT_REGISTRO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_TCT_REGISTRO DISABLE TRIGGER TRG_AUD_CGG_TCT_REGISTRO;

