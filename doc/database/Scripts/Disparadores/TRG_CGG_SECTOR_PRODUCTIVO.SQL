/**
* FUNCION SII.TRG_KG_CGG_SECTOR_PRODUCTIVO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SECTOR_PRODUCTIVO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_SECTOR_PRODUCTIVO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CSCTP_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSCTP_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CSCTP_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSCTP_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CSCTP_CODIGO>'||COALESCE(OLD.CSCTP_CODIGO, '')||'</CSCTP_CODIGO>'||
			'<CGG_CSCTP_CODIGO>'||COALESCE(OLD.CGG_CSCTP_CODIGO, '')||'</CGG_CSCTP_CODIGO>'||
			'<CSCTP_NOMBRE>'||COALESCE(OLD.CSCTP_NOMBRE, '')||'</CSCTP_NOMBRE>'||
			'<CSCTP_DESCRIPCION>'||COALESCE(OLD.CSCTP_DESCRIPCION, '')||'</CSCTP_DESCRIPCION>'||
			'<CSCTP_ESTADO>'||COALESCE(OLD.CSCTP_ESTADO, FALSE)||'</CSCTP_ESTADO>'||
			'<CSCTP_FECHA_INSERT>'||COALESCE(OLD.CSCTP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSCTP_FECHA_INSERT>'||
			'<CSCTP_USUARIO_INSERT>'||COALESCE(OLD.CSCTP_USUARIO_INSERT, '')||'</CSCTP_USUARIO_INSERT>'||
			'<CSCTP_FECHA_UPDATE>'||COALESCE(OLD.CSCTP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSCTP_FECHA_UPDATE>'||
			'<CSCTP_USUARIO_UPDATE>'||COALESCE(OLD.CSCTP_USUARIO_UPDATE, '')||'</CSCTP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CSCTP_CODIGO>'||COALESCE(NEW.CSCTP_CODIGO, '')||'</CSCTP_CODIGO>'||
			'<CGG_CSCTP_CODIGO>'||COALESCE(NEW.CGG_CSCTP_CODIGO, '')||'</CGG_CSCTP_CODIGO>'||
			'<CSCTP_NOMBRE>'||COALESCE(NEW.CSCTP_NOMBRE, '')||'</CSCTP_NOMBRE>'||
			'<CSCTP_DESCRIPCION>'||COALESCE(NEW.CSCTP_DESCRIPCION, '')||'</CSCTP_DESCRIPCION>'||
			'<CSCTP_ESTADO>'||COALESCE(NEW.CSCTP_ESTADO, FALSE)||'</CSCTP_ESTADO>'||
			'<CSCTP_FECHA_INSERT>'||COALESCE(NEW.CSCTP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSCTP_FECHA_INSERT>'||
			'<CSCTP_USUARIO_INSERT>'||COALESCE(NEW.CSCTP_USUARIO_INSERT, '')||'</CSCTP_USUARIO_INSERT>'||
			'<CSCTP_FECHA_UPDATE>'||COALESCE(NEW.CSCTP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSCTP_FECHA_UPDATE>'||
			'<CSCTP_USUARIO_UPDATE>'||COALESCE(NEW.CSCTP_USUARIO_UPDATE, '')||'</CSCTP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_SECTOR_PRODUCTIVO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SECTOR_PRODUCTIVO
*/
CREATE TRIGGER TRG_AUD_CGG_SECTOR_PRODUCTIVO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_SECTOR_PRODUCTIVO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_SECTOR_PRODUCTIVO();
COMMENT ON TRIGGER TRG_AUD_CGG_SECTOR_PRODUCTIVO ON SII.CGG_SECTOR_PRODUCTIVO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_SECTOR_PRODUCTIVO DISABLE TRIGGER TRG_AUD_CGG_SECTOR_PRODUCTIVO;

