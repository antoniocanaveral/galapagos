/**
* FUNCION SII.TRG_KG_CGG_VEH_HISTORIAL. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_HISTORIAL
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_VEH_HISTORIAL() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CVHST_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVHST_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CVHST_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVHST_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CVHST_CODIGO>'||COALESCE(OLD.CVHST_CODIGO, '')||'</CVHST_CODIGO>'||
			'<CVVEH_CODIGO>'||COALESCE(OLD.CVVEH_CODIGO, '')||'</CVVEH_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(OLD.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CVHST_FECHA_TRASPASO>'||COALESCE(OLD.CVHST_FECHA_TRASPASO, CURRENT_TIMESTAMP)||'</CVHST_FECHA_TRASPASO>'||
			'<CVHST_PROPIETARIO>'||COALESCE(OLD.CVHST_PROPIETARIO, FALSE)||'</CVHST_PROPIETARIO>'||
			'<CVHST_CONCEPTO>'||COALESCE(OLD.CVHST_CONCEPTO, '')||'</CVHST_CONCEPTO>'||
			'<CVHST_OBSERVACIONES>'||COALESCE(OLD.CVHST_OBSERVACIONES, '')||'</CVHST_OBSERVACIONES>'||
			'<CVHST_ESTADO>'||COALESCE(OLD.CVHST_ESTADO, FALSE)||'</CVHST_ESTADO>'||
			'<CVHST_FECHA_INSERT>'||COALESCE(OLD.CVHST_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVHST_FECHA_INSERT>'||
			'<CVHST_USUARIO_INSERT>'||COALESCE(OLD.CVHST_USUARIO_INSERT, '')||'</CVHST_USUARIO_INSERT>'||
			'<CVHST_FECHA_UPDATE>'||COALESCE(OLD.CVHST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVHST_FECHA_UPDATE>'||
			'<CVHST_USUARIO_UPDATE>'||COALESCE(OLD.CVHST_USUARIO_UPDATE, '')||'</CVHST_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CVHST_CODIGO>'||COALESCE(NEW.CVHST_CODIGO, '')||'</CVHST_CODIGO>'||
			'<CVVEH_CODIGO>'||COALESCE(NEW.CVVEH_CODIGO, '')||'</CVVEH_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CVHST_FECHA_TRASPASO>'||COALESCE(NEW.CVHST_FECHA_TRASPASO, CURRENT_TIMESTAMP)||'</CVHST_FECHA_TRASPASO>'||
			'<CVHST_PROPIETARIO>'||COALESCE(NEW.CVHST_PROPIETARIO, FALSE)||'</CVHST_PROPIETARIO>'||
			'<CVHST_CONCEPTO>'||COALESCE(NEW.CVHST_CONCEPTO, '')||'</CVHST_CONCEPTO>'||
			'<CVHST_OBSERVACIONES>'||COALESCE(NEW.CVHST_OBSERVACIONES, '')||'</CVHST_OBSERVACIONES>'||
			'<CVHST_ESTADO>'||COALESCE(NEW.CVHST_ESTADO, FALSE)||'</CVHST_ESTADO>'||
			'<CVHST_FECHA_INSERT>'||COALESCE(NEW.CVHST_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVHST_FECHA_INSERT>'||
			'<CVHST_USUARIO_INSERT>'||COALESCE(NEW.CVHST_USUARIO_INSERT, '')||'</CVHST_USUARIO_INSERT>'||
			'<CVHST_FECHA_UPDATE>'||COALESCE(NEW.CVHST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVHST_FECHA_UPDATE>'||
			'<CVHST_USUARIO_UPDATE>'||COALESCE(NEW.CVHST_USUARIO_UPDATE, '')||'</CVHST_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_VEH_HISTORIAL. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_HISTORIAL
*/
CREATE TRIGGER TRG_AUD_CGG_VEH_HISTORIAL AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_VEH_HISTORIAL FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_VEH_HISTORIAL();
COMMENT ON TRIGGER TRG_AUD_CGG_VEH_HISTORIAL ON SII.CGG_VEH_HISTORIAL IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_VEH_HISTORIAL DISABLE TRIGGER TRG_AUD_CGG_VEH_HISTORIAL;

