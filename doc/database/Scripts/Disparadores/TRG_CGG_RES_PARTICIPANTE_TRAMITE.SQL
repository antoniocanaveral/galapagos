/**
* FUNCION SII.TRG_KG_CGG_RES_PARTICIPANTE_TRAMITE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_PARTICIPANTE_TRAMITE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_PARTICIPANTE_TRAMITE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRPTR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRPTR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRPTR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRPTR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRPTR_CODIGO>'||COALESCE(OLD.CRPTR_CODIGO, '')||'</CRPTR_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRTPT_CODIGO>'||COALESCE(OLD.CRTPT_CODIGO, '')||'</CRTPT_CODIGO>'||
			'<CRPTR_ESTADO>'||COALESCE(OLD.CRPTR_ESTADO, FALSE)||'</CRPTR_ESTADO>'||
			'<CRPTR_FECHA_INSERT>'||COALESCE(OLD.CRPTR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRPTR_FECHA_INSERT>'||
			'<CRPTR_USUARIO_INSERT>'||COALESCE(OLD.CRPTR_USUARIO_INSERT, '')||'</CRPTR_USUARIO_INSERT>'||
			'<CRPTR_FECHA_UPDATE>'||COALESCE(OLD.CRPTR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRPTR_FECHA_UPDATE>'||
			'<CRPTR_USUARIO_UPDATE>'||COALESCE(OLD.CRPTR_USUARIO_UPDATE, '')||'</CRPTR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRPTR_CODIGO>'||COALESCE(NEW.CRPTR_CODIGO, '')||'</CRPTR_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRTPT_CODIGO>'||COALESCE(NEW.CRTPT_CODIGO, '')||'</CRTPT_CODIGO>'||
			'<CRPTR_ESTADO>'||COALESCE(NEW.CRPTR_ESTADO, FALSE)||'</CRPTR_ESTADO>'||
			'<CRPTR_FECHA_INSERT>'||COALESCE(NEW.CRPTR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRPTR_FECHA_INSERT>'||
			'<CRPTR_USUARIO_INSERT>'||COALESCE(NEW.CRPTR_USUARIO_INSERT, '')||'</CRPTR_USUARIO_INSERT>'||
			'<CRPTR_FECHA_UPDATE>'||COALESCE(NEW.CRPTR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRPTR_FECHA_UPDATE>'||
			'<CRPTR_USUARIO_UPDATE>'||COALESCE(NEW.CRPTR_USUARIO_UPDATE, '')||'</CRPTR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_PARTICIPANTE_TRAMITE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_PARTICIPANTE_TRAMITE
*/
CREATE TRIGGER TRG_AUD_CGG_RES_PARTICIPANTE_TRAMITE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_PARTICIPANTE_TRAMITE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_PARTICIPANTE_TRAMITE();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_PARTICIPANTE_TRAMITE ON SII.CGG_RES_PARTICIPANTE_TRAMITE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_PARTICIPANTE_TRAMITE DISABLE TRIGGER TRG_AUD_CGG_RES_PARTICIPANTE_TRAMITE;

