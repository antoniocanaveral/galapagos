/**
* FUNCION SII.TRG_KG_CGG_RES_INFORME_TESTIGO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_INFORME_TESTIGO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_INFORME_TESTIGO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRINT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRINT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRINT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRINT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRINT_CODIGO>'||COALESCE(OLD.CRINT_CODIGO, '')||'</CRINT_CODIGO>'||
			'<CRMTR_CODIGO>'||COALESCE(OLD.CRMTR_CODIGO, '')||'</CRMTR_CODIGO>'||
			'<CRINT_NUMERO>'||COALESCE(OLD.CRINT_NUMERO, '')||'</CRINT_NUMERO>'||
			'<CRINT_OBSERVACION>'||COALESCE(OLD.CRINT_OBSERVACION, '')||'</CRINT_OBSERVACION>'||
			'<CRINT_ESTADO>'||COALESCE(OLD.CRINT_ESTADO, FALSE)||'</CRINT_ESTADO>'||
			'<CRINT_FECHA_INSERT>'||COALESCE(OLD.CRINT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRINT_FECHA_INSERT>'||
			'<CRINT_USUARIO_INSERT>'||COALESCE(OLD.CRINT_USUARIO_INSERT, '')||'</CRINT_USUARIO_INSERT>'||
			'<CRINT_FECHA_UPDATE>'||COALESCE(OLD.CRINT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRINT_FECHA_UPDATE>'||
			'<CRINT_USUARIO_UPDATE>'||COALESCE(OLD.CRINT_USUARIO_UPDATE, '')||'</CRINT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRINT_CODIGO>'||COALESCE(NEW.CRINT_CODIGO, '')||'</CRINT_CODIGO>'||
			'<CRMTR_CODIGO>'||COALESCE(NEW.CRMTR_CODIGO, '')||'</CRMTR_CODIGO>'||
			'<CRINT_NUMERO>'||COALESCE(NEW.CRINT_NUMERO, '')||'</CRINT_NUMERO>'||
			'<CRINT_OBSERVACION>'||COALESCE(NEW.CRINT_OBSERVACION, '')||'</CRINT_OBSERVACION>'||
			'<CRINT_ESTADO>'||COALESCE(NEW.CRINT_ESTADO, FALSE)||'</CRINT_ESTADO>'||
			'<CRINT_FECHA_INSERT>'||COALESCE(NEW.CRINT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRINT_FECHA_INSERT>'||
			'<CRINT_USUARIO_INSERT>'||COALESCE(NEW.CRINT_USUARIO_INSERT, '')||'</CRINT_USUARIO_INSERT>'||
			'<CRINT_FECHA_UPDATE>'||COALESCE(NEW.CRINT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRINT_FECHA_UPDATE>'||
			'<CRINT_USUARIO_UPDATE>'||COALESCE(NEW.CRINT_USUARIO_UPDATE, '')||'</CRINT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_INFORME_TESTIGO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_INFORME_TESTIGO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_INFORME_TESTIGO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_INFORME_TESTIGO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_INFORME_TESTIGO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_INFORME_TESTIGO ON SII.CGG_RES_INFORME_TESTIGO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_INFORME_TESTIGO DISABLE TRIGGER TRG_AUD_CGG_RES_INFORME_TESTIGO;

