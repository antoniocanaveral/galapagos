/**
* FUNCION SII.TRG_KG_CGG_GEM_EXPERIENCIA_LABORAL. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_EXPERIENCIA_LABORAL
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GEM_EXPERIENCIA_LABORAL() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGEXL_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGEXL_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGEXL_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGEXL_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGEXL_CODIGO>'||COALESCE(OLD.CGEXL_CODIGO, '')||'</CGEXL_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGEXL_FECHA_INICIO>'||COALESCE(OLD.CGEXL_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CGEXL_FECHA_INICIO>'||
			'<CGEXL_FECHA_FIN>'||COALESCE(OLD.CGEXL_FECHA_FIN, CURRENT_TIMESTAMP)||'</CGEXL_FECHA_FIN>'||
			'<CGEXL_RAZON_SOCIAL>'||COALESCE(OLD.CGEXL_RAZON_SOCIAL, '')||'</CGEXL_RAZON_SOCIAL>'||
			'<CGEXL_CIUDAD>'||COALESCE(OLD.CGEXL_CIUDAD, '')||'</CGEXL_CIUDAD>'||
			'<CGEXL_CARGO>'||COALESCE(OLD.CGEXL_CARGO, '')||'</CGEXL_CARGO>'||
			'<CGEXL_DESCRIPCION>'||COALESCE(OLD.CGEXL_DESCRIPCION, '')||'</CGEXL_DESCRIPCION>'||
			'<CGEXL_PERSONAS_CARGO>'||COALESCE(OLD.CGEXL_PERSONAS_CARGO, 0)||'</CGEXL_PERSONAS_CARGO>'||
			'<CGEXL_CONTACTO>'||COALESCE(OLD.CGEXL_CONTACTO, '')||'</CGEXL_CONTACTO>'||
			'<CGEXL_TELEFONO>'||COALESCE(OLD.CGEXL_TELEFONO, '')||'</CGEXL_TELEFONO>'||
			'<CGEXL_OBSERVACIONES>'||COALESCE(OLD.CGEXL_OBSERVACIONES, '')||'</CGEXL_OBSERVACIONES>'||
			'<CGEXL_ESTADO>'||COALESCE(OLD.CGEXL_ESTADO, FALSE)||'</CGEXL_ESTADO>'||
			'<CGEXL_FECHA_INSERT>'||COALESCE(OLD.CGEXL_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGEXL_FECHA_INSERT>'||
			'<CGEXL_USUARIO_INSERT>'||COALESCE(OLD.CGEXL_USUARIO_INSERT, '')||'</CGEXL_USUARIO_INSERT>'||
			'<CGEXL_FECHA_UPDATE>'||COALESCE(OLD.CGEXL_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGEXL_FECHA_UPDATE>'||
			'<CGEXL_USUARIO_UPDATE>'||COALESCE(OLD.CGEXL_USUARIO_UPDATE, '')||'</CGEXL_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGEXL_CODIGO>'||COALESCE(NEW.CGEXL_CODIGO, '')||'</CGEXL_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGEXL_FECHA_INICIO>'||COALESCE(NEW.CGEXL_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CGEXL_FECHA_INICIO>'||
			'<CGEXL_FECHA_FIN>'||COALESCE(NEW.CGEXL_FECHA_FIN, CURRENT_TIMESTAMP)||'</CGEXL_FECHA_FIN>'||
			'<CGEXL_RAZON_SOCIAL>'||COALESCE(NEW.CGEXL_RAZON_SOCIAL, '')||'</CGEXL_RAZON_SOCIAL>'||
			'<CGEXL_CIUDAD>'||COALESCE(NEW.CGEXL_CIUDAD, '')||'</CGEXL_CIUDAD>'||
			'<CGEXL_CARGO>'||COALESCE(NEW.CGEXL_CARGO, '')||'</CGEXL_CARGO>'||
			'<CGEXL_DESCRIPCION>'||COALESCE(NEW.CGEXL_DESCRIPCION, '')||'</CGEXL_DESCRIPCION>'||
			'<CGEXL_PERSONAS_CARGO>'||COALESCE(NEW.CGEXL_PERSONAS_CARGO, 0)||'</CGEXL_PERSONAS_CARGO>'||
			'<CGEXL_CONTACTO>'||COALESCE(NEW.CGEXL_CONTACTO, '')||'</CGEXL_CONTACTO>'||
			'<CGEXL_TELEFONO>'||COALESCE(NEW.CGEXL_TELEFONO, '')||'</CGEXL_TELEFONO>'||
			'<CGEXL_OBSERVACIONES>'||COALESCE(NEW.CGEXL_OBSERVACIONES, '')||'</CGEXL_OBSERVACIONES>'||
			'<CGEXL_ESTADO>'||COALESCE(NEW.CGEXL_ESTADO, FALSE)||'</CGEXL_ESTADO>'||
			'<CGEXL_FECHA_INSERT>'||COALESCE(NEW.CGEXL_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGEXL_FECHA_INSERT>'||
			'<CGEXL_USUARIO_INSERT>'||COALESCE(NEW.CGEXL_USUARIO_INSERT, '')||'</CGEXL_USUARIO_INSERT>'||
			'<CGEXL_FECHA_UPDATE>'||COALESCE(NEW.CGEXL_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGEXL_FECHA_UPDATE>'||
			'<CGEXL_USUARIO_UPDATE>'||COALESCE(NEW.CGEXL_USUARIO_UPDATE, '')||'</CGEXL_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GEM_EXPERIENCIA_LABORAL. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_EXPERIENCIA_LABORAL
*/
CREATE TRIGGER TRG_AUD_CGG_GEM_EXPERIENCIA_LABORAL AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GEM_EXPERIENCIA_LABORAL FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GEM_EXPERIENCIA_LABORAL();
COMMENT ON TRIGGER TRG_AUD_CGG_GEM_EXPERIENCIA_LABORAL ON SII.CGG_GEM_EXPERIENCIA_LABORAL IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GEM_EXPERIENCIA_LABORAL DISABLE TRIGGER TRG_AUD_CGG_GEM_EXPERIENCIA_LABORAL;

