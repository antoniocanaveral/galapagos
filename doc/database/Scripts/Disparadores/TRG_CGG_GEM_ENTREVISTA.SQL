/**
* FUNCION SII.TRG_KG_CGG_GEM_ENTREVISTA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_ENTREVISTA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GEM_ENTREVISTA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGENT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGENT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGENT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGENT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGENT_CODIGO>'||COALESCE(OLD.CGENT_CODIGO, '')||'</CGENT_CODIGO>'||
			'<CGCND_CODIGO>'||COALESCE(OLD.CGCND_CODIGO, '')||'</CGCND_CODIGO>'||
			'<CGENT_ESTADO_ENTREVISTA>'||COALESCE(OLD.CGENT_ESTADO_ENTREVISTA, '')||'</CGENT_ESTADO_ENTREVISTA>'||
			'<CGENT_FECHA_REALIZACION>'||COALESCE(OLD.CGENT_FECHA_REALIZACION, CURRENT_TIMESTAMP)||'</CGENT_FECHA_REALIZACION>'||
			'<CGENT_LUGAR>'||COALESCE(OLD.CGENT_LUGAR, '')||'</CGENT_LUGAR>'||
			'<CGENT_OBSERVACION>'||COALESCE(OLD.CGENT_OBSERVACION, '')||'</CGENT_OBSERVACION>'||
			'<CGENT_FECHA_CRITERIO_TECNICO>'||COALESCE(OLD.CGENT_FECHA_CRITERIO_TECNICO, CURRENT_TIMESTAMP)||'</CGENT_FECHA_CRITERIO_TECNICO>'||
			'<CGENT_CRITERIO_TECNICO>'||COALESCE(OLD.CGENT_CRITERIO_TECNICO, '')||'</CGENT_CRITERIO_TECNICO>'||
			'<CGENT_ESTADO>'||COALESCE(OLD.CGENT_ESTADO, FALSE)||'</CGENT_ESTADO>'||
			'<CGENT_FECHA_INSERT>'||COALESCE(OLD.CGENT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGENT_FECHA_INSERT>'||
			'<CGENT_USUARIO_INSERT>'||COALESCE(OLD.CGENT_USUARIO_INSERT, '')||'</CGENT_USUARIO_INSERT>'||
			'<CGENT_FECHA_UPDATE>'||COALESCE(OLD.CGENT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGENT_FECHA_UPDATE>'||
			'<CGENT_USUARIO_UPDATE>'||COALESCE(OLD.CGENT_USUARIO_UPDATE, '')||'</CGENT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGENT_CODIGO>'||COALESCE(NEW.CGENT_CODIGO, '')||'</CGENT_CODIGO>'||
			'<CGCND_CODIGO>'||COALESCE(NEW.CGCND_CODIGO, '')||'</CGCND_CODIGO>'||
			'<CGENT_ESTADO_ENTREVISTA>'||COALESCE(NEW.CGENT_ESTADO_ENTREVISTA, '')||'</CGENT_ESTADO_ENTREVISTA>'||
			'<CGENT_FECHA_REALIZACION>'||COALESCE(NEW.CGENT_FECHA_REALIZACION, CURRENT_TIMESTAMP)||'</CGENT_FECHA_REALIZACION>'||
			'<CGENT_LUGAR>'||COALESCE(NEW.CGENT_LUGAR, '')||'</CGENT_LUGAR>'||
			'<CGENT_OBSERVACION>'||COALESCE(NEW.CGENT_OBSERVACION, '')||'</CGENT_OBSERVACION>'||
			'<CGENT_FECHA_CRITERIO_TECNICO>'||COALESCE(NEW.CGENT_FECHA_CRITERIO_TECNICO, CURRENT_TIMESTAMP)||'</CGENT_FECHA_CRITERIO_TECNICO>'||
			'<CGENT_CRITERIO_TECNICO>'||COALESCE(NEW.CGENT_CRITERIO_TECNICO, '')||'</CGENT_CRITERIO_TECNICO>'||
			'<CGENT_ESTADO>'||COALESCE(NEW.CGENT_ESTADO, FALSE)||'</CGENT_ESTADO>'||
			'<CGENT_FECHA_INSERT>'||COALESCE(NEW.CGENT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGENT_FECHA_INSERT>'||
			'<CGENT_USUARIO_INSERT>'||COALESCE(NEW.CGENT_USUARIO_INSERT, '')||'</CGENT_USUARIO_INSERT>'||
			'<CGENT_FECHA_UPDATE>'||COALESCE(NEW.CGENT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGENT_FECHA_UPDATE>'||
			'<CGENT_USUARIO_UPDATE>'||COALESCE(NEW.CGENT_USUARIO_UPDATE, '')||'</CGENT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GEM_ENTREVISTA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_ENTREVISTA
*/
CREATE TRIGGER TRG_AUD_CGG_GEM_ENTREVISTA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GEM_ENTREVISTA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GEM_ENTREVISTA();
COMMENT ON TRIGGER TRG_AUD_CGG_GEM_ENTREVISTA ON SII.CGG_GEM_ENTREVISTA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GEM_ENTREVISTA DISABLE TRIGGER TRG_AUD_CGG_GEM_ENTREVISTA;

