/**
* FUNCION SII.TRG_KG_CGG_DHU_INSTITUCION_RESPONSABLE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_INSTITUCION_RESPONSABLE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_INSTITUCION_RESPONSABLE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDITR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDITR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDITR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDITR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDITR_CODIGO>'||COALESCE(OLD.CDITR_CODIGO, '')||'</CDITR_CODIGO>'||
			'<CDITR_NOMBRE>'||COALESCE(OLD.CDITR_NOMBRE, '')||'</CDITR_NOMBRE>'||
			'<CDITR_DESCRIPCION>'||COALESCE(OLD.CDITR_DESCRIPCION, '')||'</CDITR_DESCRIPCION>'||
			'<CDITR_ESTADO>'||COALESCE(OLD.CDITR_ESTADO, FALSE)||'</CDITR_ESTADO>'||
			'<CDITR_FECHA_INSERT>'||COALESCE(OLD.CDITR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDITR_FECHA_INSERT>'||
			'<CDITR_USUARIO_INSERT>'||COALESCE(OLD.CDITR_USUARIO_INSERT, '')||'</CDITR_USUARIO_INSERT>'||
			'<CDITR_FECHA_UPDATE>'||COALESCE(OLD.CDITR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDITR_FECHA_UPDATE>'||
			'<CDITR_USUARIO_UPDATE>'||COALESCE(OLD.CDITR_USUARIO_UPDATE, '')||'</CDITR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDITR_CODIGO>'||COALESCE(NEW.CDITR_CODIGO, '')||'</CDITR_CODIGO>'||
			'<CDITR_NOMBRE>'||COALESCE(NEW.CDITR_NOMBRE, '')||'</CDITR_NOMBRE>'||
			'<CDITR_DESCRIPCION>'||COALESCE(NEW.CDITR_DESCRIPCION, '')||'</CDITR_DESCRIPCION>'||
			'<CDITR_ESTADO>'||COALESCE(NEW.CDITR_ESTADO, FALSE)||'</CDITR_ESTADO>'||
			'<CDITR_FECHA_INSERT>'||COALESCE(NEW.CDITR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDITR_FECHA_INSERT>'||
			'<CDITR_USUARIO_INSERT>'||COALESCE(NEW.CDITR_USUARIO_INSERT, '')||'</CDITR_USUARIO_INSERT>'||
			'<CDITR_FECHA_UPDATE>'||COALESCE(NEW.CDITR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDITR_FECHA_UPDATE>'||
			'<CDITR_USUARIO_UPDATE>'||COALESCE(NEW.CDITR_USUARIO_UPDATE, '')||'</CDITR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_INSTITUCION_RESPONSABLE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_INSTITUCION_RESPONSABLE
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_INSTITUCION_RESPONSABLE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_INSTITUCION_RESPONSABLE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_INSTITUCION_RESPONSABLE();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_INSTITUCION_RESPONSABLE ON SII.CGG_DHU_INSTITUCION_RESPONSABLE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_INSTITUCION_RESPONSABLE DISABLE TRIGGER TRG_AUD_CGG_DHU_INSTITUCION_RESPONSABLE;

