/**
* FUNCION SII.TRG_KG_CGG_RES_SESION_COMITE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_SESION_COMITE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_SESION_COMITE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRSSC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRSSC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRSSC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRSSC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRSSC_CODIGO>'||COALESCE(OLD.CRSSC_CODIGO, '')||'</CRSSC_CODIGO>'||
			'<CISLA_CODIGO>'||COALESCE(OLD.CISLA_CODIGO, '')||'</CISLA_CODIGO>'||
			'<CRCOM_CODIGO>'||COALESCE(OLD.CRCOM_CODIGO, '')||'</CRCOM_CODIGO>'||
			'<CRSSC_FECHA_REUNION>'||COALESCE(OLD.CRSSC_FECHA_REUNION, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_REUNION>'||
			'<CRSSC_FECHA_FINALIZACION>'||COALESCE(OLD.CRSSC_FECHA_FINALIZACION, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_FINALIZACION>'||
			'<CRSSC_NUMERO_SESION>'||COALESCE(OLD.CRSSC_NUMERO_SESION, '')||'</CRSSC_NUMERO_SESION>'||
			'<CRSSC_EXTRACTO>'||COALESCE(OLD.CRSSC_EXTRACTO, '')||'</CRSSC_EXTRACTO>'||
			'<CRSSC_OBSERVACION>'||COALESCE(OLD.CRSSC_OBSERVACION, '')||'</CRSSC_OBSERVACION>'||
			'<CRSSC_FECHA_CONVOCATORIA>'||COALESCE(OLD.CRSSC_FECHA_CONVOCATORIA, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_CONVOCATORIA>'||
			'<CRSSC_MOTIVO_CONVOCATORIA>'||COALESCE(OLD.CRSSC_MOTIVO_CONVOCATORIA, '')||'</CRSSC_MOTIVO_CONVOCATORIA>'||
			'<CRSSC_DESCRIPCION_ADJUNTO>'||COALESCE(OLD.CRSSC_DESCRIPCION_ADJUNTO, '')||'</CRSSC_DESCRIPCION_ADJUNTO>'||
			'<CRSSC_NOMBRE_ADJUNTO>'||COALESCE(OLD.CRSSC_NOMBRE_ADJUNTO, '')||'</CRSSC_NOMBRE_ADJUNTO>'||
			'<CRSSC_ADJUNTO>'||COALESCE(encode(OLD.CRSSC_ADJUNTO, 'base64'), '')||'</CRSSC_ADJUNTO>'||
			'<CRSSC_ESTADO_CONVOCATORIA>'||COALESCE(OLD.CRSSC_ESTADO_CONVOCATORIA, 0)||'</CRSSC_ESTADO_CONVOCATORIA>'||
			'<CRSSC_ESTADO>'||COALESCE(OLD.CRSSC_ESTADO, FALSE)||'</CRSSC_ESTADO>'||
			'<CRSSC_FECHA_INSERT>'||COALESCE(OLD.CRSSC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_INSERT>'||
			'<CRSSC_USUARIO_INSERT>'||COALESCE(OLD.CRSSC_USUARIO_INSERT, '')||'</CRSSC_USUARIO_INSERT>'||
			'<CRSSC_FECHA_UPDATE>'||COALESCE(OLD.CRSSC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_UPDATE>'||
			'<CRSSC_USUARIO_UPDATE>'||COALESCE(OLD.CRSSC_USUARIO_UPDATE, '')||'</CRSSC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRSSC_CODIGO>'||COALESCE(NEW.CRSSC_CODIGO, '')||'</CRSSC_CODIGO>'||
			'<CISLA_CODIGO>'||COALESCE(NEW.CISLA_CODIGO, '')||'</CISLA_CODIGO>'||
			'<CRCOM_CODIGO>'||COALESCE(NEW.CRCOM_CODIGO, '')||'</CRCOM_CODIGO>'||
			'<CRSSC_FECHA_REUNION>'||COALESCE(NEW.CRSSC_FECHA_REUNION, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_REUNION>'||
			'<CRSSC_FECHA_FINALIZACION>'||COALESCE(NEW.CRSSC_FECHA_FINALIZACION, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_FINALIZACION>'||
			'<CRSSC_NUMERO_SESION>'||COALESCE(NEW.CRSSC_NUMERO_SESION, '')||'</CRSSC_NUMERO_SESION>'||
			'<CRSSC_EXTRACTO>'||COALESCE(NEW.CRSSC_EXTRACTO, '')||'</CRSSC_EXTRACTO>'||
			'<CRSSC_OBSERVACION>'||COALESCE(NEW.CRSSC_OBSERVACION, '')||'</CRSSC_OBSERVACION>'||
			'<CRSSC_FECHA_CONVOCATORIA>'||COALESCE(NEW.CRSSC_FECHA_CONVOCATORIA, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_CONVOCATORIA>'||
			'<CRSSC_MOTIVO_CONVOCATORIA>'||COALESCE(NEW.CRSSC_MOTIVO_CONVOCATORIA, '')||'</CRSSC_MOTIVO_CONVOCATORIA>'||
			'<CRSSC_DESCRIPCION_ADJUNTO>'||COALESCE(NEW.CRSSC_DESCRIPCION_ADJUNTO, '')||'</CRSSC_DESCRIPCION_ADJUNTO>'||
			'<CRSSC_NOMBRE_ADJUNTO>'||COALESCE(NEW.CRSSC_NOMBRE_ADJUNTO, '')||'</CRSSC_NOMBRE_ADJUNTO>'||
			'<CRSSC_ADJUNTO>'||COALESCE(encode(NEW.CRSSC_ADJUNTO, 'base64'), '')||'</CRSSC_ADJUNTO>'||
			'<CRSSC_ESTADO_CONVOCATORIA>'||COALESCE(NEW.CRSSC_ESTADO_CONVOCATORIA, 0)||'</CRSSC_ESTADO_CONVOCATORIA>'||
			'<CRSSC_ESTADO>'||COALESCE(NEW.CRSSC_ESTADO, FALSE)||'</CRSSC_ESTADO>'||
			'<CRSSC_FECHA_INSERT>'||COALESCE(NEW.CRSSC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_INSERT>'||
			'<CRSSC_USUARIO_INSERT>'||COALESCE(NEW.CRSSC_USUARIO_INSERT, '')||'</CRSSC_USUARIO_INSERT>'||
			'<CRSSC_FECHA_UPDATE>'||COALESCE(NEW.CRSSC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRSSC_FECHA_UPDATE>'||
			'<CRSSC_USUARIO_UPDATE>'||COALESCE(NEW.CRSSC_USUARIO_UPDATE, '')||'</CRSSC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_SESION_COMITE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_SESION_COMITE
*/
CREATE TRIGGER TRG_AUD_CGG_RES_SESION_COMITE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_SESION_COMITE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_SESION_COMITE();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_SESION_COMITE ON SII.CGG_RES_SESION_COMITE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_SESION_COMITE DISABLE TRIGGER TRG_AUD_CGG_RES_SESION_COMITE;

