/**
* FUNCION SII.TRG_KG_CGG_RES_TST_APLICA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TST_APLICA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_TST_APLICA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRTAO_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTAO_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRTAO_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTAO_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRTAO_CODIGO>'||COALESCE(OLD.CRTAO_CODIGO, '')||'</CRTAO_CODIGO>'||
			'<CRTSO_CODIGO>'||COALESCE(OLD.CRTSO_CODIGO, '')||'</CRTSO_CODIGO>'||
			'<CRTST_CODIGO>'||COALESCE(OLD.CRTST_CODIGO, '')||'</CRTST_CODIGO>'||
			'<CRTAO_ESTADO>'||COALESCE(OLD.CRTAO_ESTADO, FALSE)||'</CRTAO_ESTADO>'||
			'<CRTAO_FECHA_INSERT>'||COALESCE(OLD.CRTAO_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTAO_FECHA_INSERT>'||
			'<CRTAO_USUARIO_INSERT>'||COALESCE(OLD.CRTAO_USUARIO_INSERT, '')||'</CRTAO_USUARIO_INSERT>'||
			'<CRTAO_FECHA_UPDATE>'||COALESCE(OLD.CRTAO_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTAO_FECHA_UPDATE>'||
			'<CRTAO_USUARIO_UPDATE>'||COALESCE(OLD.CRTAO_USUARIO_UPDATE, '')||'</CRTAO_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRTAO_CODIGO>'||COALESCE(NEW.CRTAO_CODIGO, '')||'</CRTAO_CODIGO>'||
			'<CRTSO_CODIGO>'||COALESCE(NEW.CRTSO_CODIGO, '')||'</CRTSO_CODIGO>'||
			'<CRTST_CODIGO>'||COALESCE(NEW.CRTST_CODIGO, '')||'</CRTST_CODIGO>'||
			'<CRTAO_ESTADO>'||COALESCE(NEW.CRTAO_ESTADO, FALSE)||'</CRTAO_ESTADO>'||
			'<CRTAO_FECHA_INSERT>'||COALESCE(NEW.CRTAO_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTAO_FECHA_INSERT>'||
			'<CRTAO_USUARIO_INSERT>'||COALESCE(NEW.CRTAO_USUARIO_INSERT, '')||'</CRTAO_USUARIO_INSERT>'||
			'<CRTAO_FECHA_UPDATE>'||COALESCE(NEW.CRTAO_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTAO_FECHA_UPDATE>'||
			'<CRTAO_USUARIO_UPDATE>'||COALESCE(NEW.CRTAO_USUARIO_UPDATE, '')||'</CRTAO_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_TST_APLICA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TST_APLICA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_TST_APLICA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_TST_APLICA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_TST_APLICA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_TST_APLICA ON SII.CGG_RES_TST_APLICA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_TST_APLICA DISABLE TRIGGER TRG_AUD_CGG_RES_TST_APLICA;

