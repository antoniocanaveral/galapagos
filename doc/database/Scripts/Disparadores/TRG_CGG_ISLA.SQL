/**
* FUNCION SII.TRG_KG_CGG_ISLA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_ISLA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_ISLA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CISLA_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CISLA_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CISLA_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CISLA_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CISLA_CODIGO>'||COALESCE(OLD.CISLA_CODIGO, '')||'</CISLA_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(OLD.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CISLA_NOMBRE>'||COALESCE(OLD.CISLA_NOMBRE, '')||'</CISLA_NOMBRE>'||
			'<CISLA_ABREVIATURA>'||COALESCE(OLD.CISLA_ABREVIATURA, '')||'</CISLA_ABREVIATURA>'||
			'<CISLA_INDICE>'||COALESCE(OLD.CISLA_INDICE, 0)||'</CISLA_INDICE>'||
			'<CISLA_ESTADO>'||COALESCE(OLD.CISLA_ESTADO, FALSE)||'</CISLA_ESTADO>'||
			'<CISLA_FECHA_INSERT>'||COALESCE(OLD.CISLA_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CISLA_FECHA_INSERT>'||
			'<CISLA_USUARIO_INSERT>'||COALESCE(OLD.CISLA_USUARIO_INSERT, '')||'</CISLA_USUARIO_INSERT>'||
			'<CISLA_FECHA_UPDATE>'||COALESCE(OLD.CISLA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CISLA_FECHA_UPDATE>'||
			'<CISLA_USUARIO_UPDATE>'||COALESCE(OLD.CISLA_USUARIO_UPDATE, '')||'</CISLA_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CISLA_CODIGO>'||COALESCE(NEW.CISLA_CODIGO, '')||'</CISLA_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(NEW.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CISLA_NOMBRE>'||COALESCE(NEW.CISLA_NOMBRE, '')||'</CISLA_NOMBRE>'||
			'<CISLA_ABREVIATURA>'||COALESCE(NEW.CISLA_ABREVIATURA, '')||'</CISLA_ABREVIATURA>'||
			'<CISLA_INDICE>'||COALESCE(NEW.CISLA_INDICE, 0)||'</CISLA_INDICE>'||
			'<CISLA_ESTADO>'||COALESCE(NEW.CISLA_ESTADO, FALSE)||'</CISLA_ESTADO>'||
			'<CISLA_FECHA_INSERT>'||COALESCE(NEW.CISLA_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CISLA_FECHA_INSERT>'||
			'<CISLA_USUARIO_INSERT>'||COALESCE(NEW.CISLA_USUARIO_INSERT, '')||'</CISLA_USUARIO_INSERT>'||
			'<CISLA_FECHA_UPDATE>'||COALESCE(NEW.CISLA_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CISLA_FECHA_UPDATE>'||
			'<CISLA_USUARIO_UPDATE>'||COALESCE(NEW.CISLA_USUARIO_UPDATE, '')||'</CISLA_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_ISLA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_ISLA
*/
CREATE TRIGGER TRG_AUD_CGG_ISLA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_ISLA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_ISLA();
COMMENT ON TRIGGER TRG_AUD_CGG_ISLA ON SII.CGG_ISLA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_ISLA DISABLE TRIGGER TRG_AUD_CGG_ISLA;

