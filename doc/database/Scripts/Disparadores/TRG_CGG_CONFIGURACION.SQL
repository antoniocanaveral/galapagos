/**
* FUNCION SII.TRG_KG_CGG_CONFIGURACION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_CONFIGURACION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_CONFIGURACION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGCNF_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGCNF_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGCNF_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGCNF_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGCNF_CODIGO>'||COALESCE(OLD.CGCNF_CODIGO, '')||'</CGCNF_CODIGO>'||
			'<CGCNF_CONFIGURACION>'||COALESCE(OLD.CGCNF_CONFIGURACION, '')||'</CGCNF_CONFIGURACION>'||
			'<CGCNF_VALOR_CADENA>'||COALESCE(OLD.CGCNF_VALOR_CADENA, '')||'</CGCNF_VALOR_CADENA>'||
			'<CGCNF_VALOR_NUMERICO>'||COALESCE(OLD.CGCNF_VALOR_NUMERICO, 0)||'</CGCNF_VALOR_NUMERICO>'||
			'<CGCNF_SECCION>'||COALESCE(OLD.CGCNF_SECCION, 0)||'</CGCNF_SECCION>'||
			'<CGCNF_ESTADO>'||COALESCE(OLD.CGCNF_ESTADO, FALSE)||'</CGCNF_ESTADO>'||
			'<CGCNF_FECHA_INSERT>'||COALESCE(OLD.CGCNF_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGCNF_FECHA_INSERT>'||
			'<CGCNF_USUARIO_INSERT>'||COALESCE(OLD.CGCNF_USUARIO_INSERT, '')||'</CGCNF_USUARIO_INSERT>'||
			'<CGCNF_FECHA_UPDATE>'||COALESCE(OLD.CGCNF_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGCNF_FECHA_UPDATE>'||
			'<CGCNF_USUARIO_UPDATE>'||COALESCE(OLD.CGCNF_USUARIO_UPDATE, '')||'</CGCNF_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGCNF_CODIGO>'||COALESCE(NEW.CGCNF_CODIGO, '')||'</CGCNF_CODIGO>'||
			'<CGCNF_CONFIGURACION>'||COALESCE(NEW.CGCNF_CONFIGURACION, '')||'</CGCNF_CONFIGURACION>'||
			'<CGCNF_VALOR_CADENA>'||COALESCE(NEW.CGCNF_VALOR_CADENA, '')||'</CGCNF_VALOR_CADENA>'||
			'<CGCNF_VALOR_NUMERICO>'||COALESCE(NEW.CGCNF_VALOR_NUMERICO, 0)||'</CGCNF_VALOR_NUMERICO>'||
			'<CGCNF_SECCION>'||COALESCE(NEW.CGCNF_SECCION, 0)||'</CGCNF_SECCION>'||
			'<CGCNF_ESTADO>'||COALESCE(NEW.CGCNF_ESTADO, FALSE)||'</CGCNF_ESTADO>'||
			'<CGCNF_FECHA_INSERT>'||COALESCE(NEW.CGCNF_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGCNF_FECHA_INSERT>'||
			'<CGCNF_USUARIO_INSERT>'||COALESCE(NEW.CGCNF_USUARIO_INSERT, '')||'</CGCNF_USUARIO_INSERT>'||
			'<CGCNF_FECHA_UPDATE>'||COALESCE(NEW.CGCNF_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGCNF_FECHA_UPDATE>'||
			'<CGCNF_USUARIO_UPDATE>'||COALESCE(NEW.CGCNF_USUARIO_UPDATE, '')||'</CGCNF_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_CONFIGURACION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_CONFIGURACION
*/
CREATE TRIGGER TRG_AUD_CGG_CONFIGURACION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_CONFIGURACION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_CONFIGURACION();
COMMENT ON TRIGGER TRG_AUD_CGG_CONFIGURACION ON SII.CGG_CONFIGURACION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_CONFIGURACION DISABLE TRIGGER TRG_AUD_CGG_CONFIGURACION;

