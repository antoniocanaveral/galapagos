/**
* FUNCION SII.TRG_KG_CGG_RES_INFORME_SEGUIMIENTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_INFORME_SEGUIMIENTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_INFORME_SEGUIMIENTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRISE_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRISE_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRISE_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRISE_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRISE_CODIGO>'||COALESCE(OLD.CRISE_CODIGO, '')||'</CRISE_CODIGO>'||
			'<CRSEG_CODIGO>'||COALESCE(OLD.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CRSEC_CODIGO>'||COALESCE(OLD.CRSEC_CODIGO, '')||'</CRSEC_CODIGO>'||
			'<CRISE_NUMERO_INFORME>'||COALESCE(OLD.CRISE_NUMERO_INFORME, '')||'</CRISE_NUMERO_INFORME>'||
			'<CRISE_FECHA_INFORME>'||COALESCE(OLD.CRISE_FECHA_INFORME, CURRENT_TIMESTAMP)||'</CRISE_FECHA_INFORME>'||
			'<CRISE_ASUNTO_INFORME>'||COALESCE(OLD.CRISE_ASUNTO_INFORME, '')||'</CRISE_ASUNTO_INFORME>'||
			'<CRISE_EXTRACTO_INFORME>'||COALESCE(OLD.CRISE_EXTRACTO_INFORME, '')||'</CRISE_EXTRACTO_INFORME>'||
			'<CRISE_DESCRIPCION_ADJUNTO>'||COALESCE(OLD.CRISE_DESCRIPCION_ADJUNTO, '')||'</CRISE_DESCRIPCION_ADJUNTO>'||
			'<CRISE_ESTADO>'||COALESCE(OLD.CRISE_ESTADO, FALSE)||'</CRISE_ESTADO>'||
			'<CRISE_FECHA_INSERT>'||COALESCE(OLD.CRISE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRISE_FECHA_INSERT>'||
			'<CRISE_USUARIO_INSERT>'||COALESCE(OLD.CRISE_USUARIO_INSERT, '')||'</CRISE_USUARIO_INSERT>'||
			'<CRISE_FECHA_UPDATE>'||COALESCE(OLD.CRISE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRISE_FECHA_UPDATE>'||
			'<CRISE_USUARIO_UPDATE>'||COALESCE(OLD.CRISE_USUARIO_UPDATE, '')||'</CRISE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRISE_CODIGO>'||COALESCE(NEW.CRISE_CODIGO, '')||'</CRISE_CODIGO>'||
			'<CRSEG_CODIGO>'||COALESCE(NEW.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CRSEC_CODIGO>'||COALESCE(NEW.CRSEC_CODIGO, '')||'</CRSEC_CODIGO>'||
			'<CRISE_NUMERO_INFORME>'||COALESCE(NEW.CRISE_NUMERO_INFORME, '')||'</CRISE_NUMERO_INFORME>'||
			'<CRISE_FECHA_INFORME>'||COALESCE(NEW.CRISE_FECHA_INFORME, CURRENT_TIMESTAMP)||'</CRISE_FECHA_INFORME>'||
			'<CRISE_ASUNTO_INFORME>'||COALESCE(NEW.CRISE_ASUNTO_INFORME, '')||'</CRISE_ASUNTO_INFORME>'||
			'<CRISE_EXTRACTO_INFORME>'||COALESCE(NEW.CRISE_EXTRACTO_INFORME, '')||'</CRISE_EXTRACTO_INFORME>'||
			'<CRISE_DESCRIPCION_ADJUNTO>'||COALESCE(NEW.CRISE_DESCRIPCION_ADJUNTO, '')||'</CRISE_DESCRIPCION_ADJUNTO>'||
			'<CRISE_ESTADO>'||COALESCE(NEW.CRISE_ESTADO, FALSE)||'</CRISE_ESTADO>'||
			'<CRISE_FECHA_INSERT>'||COALESCE(NEW.CRISE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRISE_FECHA_INSERT>'||
			'<CRISE_USUARIO_INSERT>'||COALESCE(NEW.CRISE_USUARIO_INSERT, '')||'</CRISE_USUARIO_INSERT>'||
			'<CRISE_FECHA_UPDATE>'||COALESCE(NEW.CRISE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRISE_FECHA_UPDATE>'||
			'<CRISE_USUARIO_UPDATE>'||COALESCE(NEW.CRISE_USUARIO_UPDATE, '')||'</CRISE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_INFORME_SEGUIMIENTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_INFORME_SEGUIMIENTO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_INFORME_SEGUIMIENTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_INFORME_SEGUIMIENTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_INFORME_SEGUIMIENTO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_INFORME_SEGUIMIENTO ON SII.CGG_RES_INFORME_SEGUIMIENTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_INFORME_SEGUIMIENTO DISABLE TRIGGER TRG_AUD_CGG_RES_INFORME_SEGUIMIENTO;

