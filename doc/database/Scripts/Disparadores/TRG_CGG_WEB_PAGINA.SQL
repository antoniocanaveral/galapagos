/**
* FUNCION SII.TRG_KG_CGG_WEB_PAGINA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_WEB_PAGINA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_WEB_PAGINA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CWPAG_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CWPAG_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CWPAG_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CWPAG_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CWPAG_CODIGO>'||COALESCE(OLD.CWPAG_CODIGO, '')||'</CWPAG_CODIGO>'||
			'<CWPAG_NOMBRE>'||COALESCE(OLD.CWPAG_NOMBRE, '')||'</CWPAG_NOMBRE>'||
			'<CWPAG_TITULO>'||COALESCE(OLD.CWPAG_TITULO, '')||'</CWPAG_TITULO>'||
			'<CWPAG_DESCRIPCION>'||COALESCE(OLD.CWPAG_DESCRIPCION, '')||'</CWPAG_DESCRIPCION>'||
			'<CWPAG_CONFIGURACION>'||COALESCE(OLD.CWPAG_CONFIGURACION, '')||'</CWPAG_CONFIGURACION>'||
			'<CWPAG_ESTADO>'||COALESCE(OLD.CWPAG_ESTADO, FALSE)||'</CWPAG_ESTADO>'||
			'<CWPAG_FECHA_INSERT>'||COALESCE(OLD.CWPAG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CWPAG_FECHA_INSERT>'||
			'<CWPAG_USUARIO_INSERT>'||COALESCE(OLD.CWPAG_USUARIO_INSERT, '')||'</CWPAG_USUARIO_INSERT>'||
			'<CWPAG_FECHA_UPDATE>'||COALESCE(OLD.CWPAG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CWPAG_FECHA_UPDATE>'||
			'<CWPAG_USUARIO_UPDATE>'||COALESCE(OLD.CWPAG_USUARIO_UPDATE, '')||'</CWPAG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CWPAG_CODIGO>'||COALESCE(NEW.CWPAG_CODIGO, '')||'</CWPAG_CODIGO>'||
			'<CWPAG_NOMBRE>'||COALESCE(NEW.CWPAG_NOMBRE, '')||'</CWPAG_NOMBRE>'||
			'<CWPAG_TITULO>'||COALESCE(NEW.CWPAG_TITULO, '')||'</CWPAG_TITULO>'||
			'<CWPAG_DESCRIPCION>'||COALESCE(NEW.CWPAG_DESCRIPCION, '')||'</CWPAG_DESCRIPCION>'||
			'<CWPAG_CONFIGURACION>'||COALESCE(NEW.CWPAG_CONFIGURACION, '')||'</CWPAG_CONFIGURACION>'||
			'<CWPAG_ESTADO>'||COALESCE(NEW.CWPAG_ESTADO, FALSE)||'</CWPAG_ESTADO>'||
			'<CWPAG_FECHA_INSERT>'||COALESCE(NEW.CWPAG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CWPAG_FECHA_INSERT>'||
			'<CWPAG_USUARIO_INSERT>'||COALESCE(NEW.CWPAG_USUARIO_INSERT, '')||'</CWPAG_USUARIO_INSERT>'||
			'<CWPAG_FECHA_UPDATE>'||COALESCE(NEW.CWPAG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CWPAG_FECHA_UPDATE>'||
			'<CWPAG_USUARIO_UPDATE>'||COALESCE(NEW.CWPAG_USUARIO_UPDATE, '')||'</CWPAG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_WEB_PAGINA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_WEB_PAGINA
*/
CREATE TRIGGER TRG_AUD_CGG_WEB_PAGINA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_WEB_PAGINA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_WEB_PAGINA();
COMMENT ON TRIGGER TRG_AUD_CGG_WEB_PAGINA ON SII.CGG_WEB_PAGINA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_WEB_PAGINA DISABLE TRIGGER TRG_AUD_CGG_WEB_PAGINA;

