/**
* FUNCION SII.TRG_KG_CGG_RES_FASE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_FASE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_FASE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRFAS_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRFAS_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRFAS_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRFAS_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRFAS_CODIGO>'||COALESCE(OLD.CRFAS_CODIGO, '')||'</CRFAS_CODIGO>'||
			'<CRPRO_CODIGO>'||COALESCE(OLD.CRPRO_CODIGO, '')||'</CRPRO_CODIGO>'||
			'<CRETT_CODIGO>'||COALESCE(OLD.CRETT_CODIGO, '')||'</CRETT_CODIGO>'||
			'<CGG_CRETT_CODIGO>'||COALESCE(OLD.CGG_CRETT_CODIGO, '')||'</CGG_CRETT_CODIGO>'||
			'<CRSEC_CODIGO>'||COALESCE(OLD.CRSEC_CODIGO, '')||'</CRSEC_CODIGO>'||
			'<CGG_CRFAS_CODIGO>'||COALESCE(OLD.CGG_CRFAS_CODIGO, '')||'</CGG_CRFAS_CODIGO>'||
			'<CGG_CUSU_CODIGO>'||COALESCE(OLD.CGG_CUSU_CODIGO, '')||'</CGG_CUSU_CODIGO>'||
			'<CRFAS_NOMBRE>'||COALESCE(OLD.CRFAS_NOMBRE, '')||'</CRFAS_NOMBRE>'||
			'<CRFAS_ORDEN>'||COALESCE(OLD.CRFAS_ORDEN, 0)||'</CRFAS_ORDEN>'||
			'<CRFAS_TAREA_REALIZA>'||COALESCE(OLD.CRFAS_TAREA_REALIZA, '')||'</CRFAS_TAREA_REALIZA>'||
			'<CRFAS_FUNCION_EJECUTA>'||COALESCE(OLD.CRFAS_FUNCION_EJECUTA, '')||'</CRFAS_FUNCION_EJECUTA>'||
			'<CRFAS_SUBE_ADJUNTO>'||COALESCE(OLD.CRFAS_SUBE_ADJUNTO, FALSE)||'</CRFAS_SUBE_ADJUNTO>'||
			'<CRFAS_SUBE_ADJUNTO_REQUISITO>'||COALESCE(OLD.CRFAS_SUBE_ADJUNTO_REQUISITO, FALSE)||'</CRFAS_SUBE_ADJUNTO_REQUISITO>'||
			'<CRFAS_EJECUTA_DESPACHO>'||COALESCE(OLD.CRFAS_EJECUTA_DESPACHO, FALSE)||'</CRFAS_EJECUTA_DESPACHO>'||
			'<CRFAS_ATENCION_NORMAL>'||COALESCE(OLD.CRFAS_ATENCION_NORMAL, FALSE)||'</CRFAS_ATENCION_NORMAL>'||
			'<CRFAS_ESTADO>'||COALESCE(OLD.CRFAS_ESTADO, FALSE)||'</CRFAS_ESTADO>'||
			'<CRFAS_FECHA_INSERT>'||COALESCE(OLD.CRFAS_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRFAS_FECHA_INSERT>'||
			'<CRFAS_USUARIO_INSERT>'||COALESCE(OLD.CRFAS_USUARIO_INSERT, '')||'</CRFAS_USUARIO_INSERT>'||
			'<CRFAS_FECHA_UPDATE>'||COALESCE(OLD.CRFAS_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRFAS_FECHA_UPDATE>'||
			'<CRFAS_USUARIO_UPDATE>'||COALESCE(OLD.CRFAS_USUARIO_UPDATE, '')||'</CRFAS_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRFAS_CODIGO>'||COALESCE(NEW.CRFAS_CODIGO, '')||'</CRFAS_CODIGO>'||
			'<CRPRO_CODIGO>'||COALESCE(NEW.CRPRO_CODIGO, '')||'</CRPRO_CODIGO>'||
			'<CRETT_CODIGO>'||COALESCE(NEW.CRETT_CODIGO, '')||'</CRETT_CODIGO>'||
			'<CGG_CRETT_CODIGO>'||COALESCE(NEW.CGG_CRETT_CODIGO, '')||'</CGG_CRETT_CODIGO>'||
			'<CRSEC_CODIGO>'||COALESCE(NEW.CRSEC_CODIGO, '')||'</CRSEC_CODIGO>'||
			'<CGG_CRFAS_CODIGO>'||COALESCE(NEW.CGG_CRFAS_CODIGO, '')||'</CGG_CRFAS_CODIGO>'||
			'<CGG_CUSU_CODIGO>'||COALESCE(NEW.CGG_CUSU_CODIGO, '')||'</CGG_CUSU_CODIGO>'||
			'<CRFAS_NOMBRE>'||COALESCE(NEW.CRFAS_NOMBRE, '')||'</CRFAS_NOMBRE>'||
			'<CRFAS_ORDEN>'||COALESCE(NEW.CRFAS_ORDEN, 0)||'</CRFAS_ORDEN>'||
			'<CRFAS_TAREA_REALIZA>'||COALESCE(NEW.CRFAS_TAREA_REALIZA, '')||'</CRFAS_TAREA_REALIZA>'||
			'<CRFAS_FUNCION_EJECUTA>'||COALESCE(NEW.CRFAS_FUNCION_EJECUTA, '')||'</CRFAS_FUNCION_EJECUTA>'||
			'<CRFAS_SUBE_ADJUNTO>'||COALESCE(NEW.CRFAS_SUBE_ADJUNTO, FALSE)||'</CRFAS_SUBE_ADJUNTO>'||
			'<CRFAS_SUBE_ADJUNTO_REQUISITO>'||COALESCE(NEW.CRFAS_SUBE_ADJUNTO_REQUISITO, FALSE)||'</CRFAS_SUBE_ADJUNTO_REQUISITO>'||
			'<CRFAS_EJECUTA_DESPACHO>'||COALESCE(NEW.CRFAS_EJECUTA_DESPACHO, FALSE)||'</CRFAS_EJECUTA_DESPACHO>'||
			'<CRFAS_ATENCION_NORMAL>'||COALESCE(NEW.CRFAS_ATENCION_NORMAL, FALSE)||'</CRFAS_ATENCION_NORMAL>'||
			'<CRFAS_ESTADO>'||COALESCE(NEW.CRFAS_ESTADO, FALSE)||'</CRFAS_ESTADO>'||
			'<CRFAS_FECHA_INSERT>'||COALESCE(NEW.CRFAS_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRFAS_FECHA_INSERT>'||
			'<CRFAS_USUARIO_INSERT>'||COALESCE(NEW.CRFAS_USUARIO_INSERT, '')||'</CRFAS_USUARIO_INSERT>'||
			'<CRFAS_FECHA_UPDATE>'||COALESCE(NEW.CRFAS_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRFAS_FECHA_UPDATE>'||
			'<CRFAS_USUARIO_UPDATE>'||COALESCE(NEW.CRFAS_USUARIO_UPDATE, '')||'</CRFAS_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_FASE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_FASE
*/
CREATE TRIGGER TRG_AUD_CGG_RES_FASE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_FASE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_FASE();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_FASE ON SII.CGG_RES_FASE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_FASE DISABLE TRIGGER TRG_AUD_CGG_RES_FASE;

