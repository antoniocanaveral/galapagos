/**
* FUNCION SII.TRG_KG_CGG_DHU_SADJUNTO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_SADJUNTO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_SADJUNTO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDSAD_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDSAD_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDSAD_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDSAD_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDSAD_CODIGO>'||COALESCE(OLD.CDSAD_CODIGO, '')||'</CDSAD_CODIGO>'||
			'<CDACD_CODIGO>'||COALESCE(OLD.CDACD_CODIGO, '')||'</CDACD_CODIGO>'||
			'<CDBCR_CODIGO>'||COALESCE(OLD.CDBCR_CODIGO, '')||'</CDBCR_CODIGO>'||
			'<CDSAD_FECHA_REGISTRO>'||COALESCE(OLD.CDSAD_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CDSAD_FECHA_REGISTRO>'||
			'<CDSAD_NOMBRE_ADJUNTO>'||COALESCE(OLD.CDSAD_NOMBRE_ADJUNTO, '')||'</CDSAD_NOMBRE_ADJUNTO>'||
			'<CDSAD_ADJUNTO>'||COALESCE(encode(OLD.CDSAD_ADJUNTO, 'base64'), '')||'</CDSAD_ADJUNTO>'||
			'<CDSAD_OBSERVACION>'||COALESCE(OLD.CDSAD_OBSERVACION, '')||'</CDSAD_OBSERVACION>'||
			'<CDSAD_ESTADO>'||COALESCE(OLD.CDSAD_ESTADO, FALSE)||'</CDSAD_ESTADO>'||
			'<CDSAD_FECHA_INSERT>'||COALESCE(OLD.CDSAD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDSAD_FECHA_INSERT>'||
			'<CDSAD_USUARIO_INSERT>'||COALESCE(OLD.CDSAD_USUARIO_INSERT, '')||'</CDSAD_USUARIO_INSERT>'||
			'<CDSAD_FECHA_UPDATE>'||COALESCE(OLD.CDSAD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDSAD_FECHA_UPDATE>'||
			'<CDSAD_USUARIO_UPDATE>'||COALESCE(OLD.CDSAD_USUARIO_UPDATE, '')||'</CDSAD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDSAD_CODIGO>'||COALESCE(NEW.CDSAD_CODIGO, '')||'</CDSAD_CODIGO>'||
			'<CDACD_CODIGO>'||COALESCE(NEW.CDACD_CODIGO, '')||'</CDACD_CODIGO>'||
			'<CDBCR_CODIGO>'||COALESCE(NEW.CDBCR_CODIGO, '')||'</CDBCR_CODIGO>'||
			'<CDSAD_FECHA_REGISTRO>'||COALESCE(NEW.CDSAD_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CDSAD_FECHA_REGISTRO>'||
			'<CDSAD_NOMBRE_ADJUNTO>'||COALESCE(NEW.CDSAD_NOMBRE_ADJUNTO, '')||'</CDSAD_NOMBRE_ADJUNTO>'||
			'<CDSAD_ADJUNTO>'||COALESCE(encode(NEW.CDSAD_ADJUNTO, 'base64'), '')||'</CDSAD_ADJUNTO>'||
			'<CDSAD_OBSERVACION>'||COALESCE(NEW.CDSAD_OBSERVACION, '')||'</CDSAD_OBSERVACION>'||
			'<CDSAD_ESTADO>'||COALESCE(NEW.CDSAD_ESTADO, FALSE)||'</CDSAD_ESTADO>'||
			'<CDSAD_FECHA_INSERT>'||COALESCE(NEW.CDSAD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDSAD_FECHA_INSERT>'||
			'<CDSAD_USUARIO_INSERT>'||COALESCE(NEW.CDSAD_USUARIO_INSERT, '')||'</CDSAD_USUARIO_INSERT>'||
			'<CDSAD_FECHA_UPDATE>'||COALESCE(NEW.CDSAD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDSAD_FECHA_UPDATE>'||
			'<CDSAD_USUARIO_UPDATE>'||COALESCE(NEW.CDSAD_USUARIO_UPDATE, '')||'</CDSAD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_SADJUNTO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_SADJUNTO
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_SADJUNTO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_SADJUNTO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_SADJUNTO();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_SADJUNTO ON SII.CGG_DHU_SADJUNTO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_SADJUNTO DISABLE TRIGGER TRG_AUD_CGG_DHU_SADJUNTO;

