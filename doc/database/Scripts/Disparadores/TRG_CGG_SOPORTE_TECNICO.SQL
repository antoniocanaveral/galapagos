/**
* FUNCION SII.TRG_KG_CGG_SOPORTE_TECNICO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SOPORTE_TECNICO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_SOPORTE_TECNICO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CSOTE_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSOTE_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CSOTE_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSOTE_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CSOTE_CODIGO>'||COALESCE(OLD.CSOTE_CODIGO, '')||'</CSOTE_CODIGO>'||
			'<CSOTE_ASUNTO>'||COALESCE(OLD.CSOTE_ASUNTO, '')||'</CSOTE_ASUNTO>'||
			'<CSOTE_NUMERO>'||COALESCE(OLD.CSOTE_NUMERO, '')||'</CSOTE_NUMERO>'||
			'<CSOTE_DESCRIPCION>'||COALESCE(OLD.CSOTE_DESCRIPCION, '')||'</CSOTE_DESCRIPCION>'||
			'<CSOTE_CONTACTO>'||COALESCE(OLD.CSOTE_CONTACTO, '')||'</CSOTE_CONTACTO>'||
			'<CSOTE_ATENDIDO>'||COALESCE(OLD.CSOTE_ATENDIDO, 0)||'</CSOTE_ATENDIDO>'||
			'<CSOTE_ESTADO>'||COALESCE(OLD.CSOTE_ESTADO, FALSE)||'</CSOTE_ESTADO>'||
			'<CSOTE_FECHA_INSERT>'||COALESCE(OLD.CSOTE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSOTE_FECHA_INSERT>'||
			'<CSOTE_USUARIO_INSERT>'||COALESCE(OLD.CSOTE_USUARIO_INSERT, '')||'</CSOTE_USUARIO_INSERT>'||
			'<CSOTE_FECHA_UPDATE>'||COALESCE(OLD.CSOTE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSOTE_FECHA_UPDATE>'||
			'<CSOTE_USUARIO_UPDATE>'||COALESCE(OLD.CSOTE_USUARIO_UPDATE, '')||'</CSOTE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CSOTE_CODIGO>'||COALESCE(NEW.CSOTE_CODIGO, '')||'</CSOTE_CODIGO>'||
			'<CSOTE_ASUNTO>'||COALESCE(NEW.CSOTE_ASUNTO, '')||'</CSOTE_ASUNTO>'||
			'<CSOTE_NUMERO>'||COALESCE(NEW.CSOTE_NUMERO, '')||'</CSOTE_NUMERO>'||
			'<CSOTE_DESCRIPCION>'||COALESCE(NEW.CSOTE_DESCRIPCION, '')||'</CSOTE_DESCRIPCION>'||
			'<CSOTE_CONTACTO>'||COALESCE(NEW.CSOTE_CONTACTO, '')||'</CSOTE_CONTACTO>'||
			'<CSOTE_ATENDIDO>'||COALESCE(NEW.CSOTE_ATENDIDO, 0)||'</CSOTE_ATENDIDO>'||
			'<CSOTE_ESTADO>'||COALESCE(NEW.CSOTE_ESTADO, FALSE)||'</CSOTE_ESTADO>'||
			'<CSOTE_FECHA_INSERT>'||COALESCE(NEW.CSOTE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSOTE_FECHA_INSERT>'||
			'<CSOTE_USUARIO_INSERT>'||COALESCE(NEW.CSOTE_USUARIO_INSERT, '')||'</CSOTE_USUARIO_INSERT>'||
			'<CSOTE_FECHA_UPDATE>'||COALESCE(NEW.CSOTE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSOTE_FECHA_UPDATE>'||
			'<CSOTE_USUARIO_UPDATE>'||COALESCE(NEW.CSOTE_USUARIO_UPDATE, '')||'</CSOTE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_SOPORTE_TECNICO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SOPORTE_TECNICO
*/
CREATE TRIGGER TRG_AUD_CGG_SOPORTE_TECNICO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_SOPORTE_TECNICO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_SOPORTE_TECNICO();
COMMENT ON TRIGGER TRG_AUD_CGG_SOPORTE_TECNICO ON SII.CGG_SOPORTE_TECNICO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_SOPORTE_TECNICO DISABLE TRIGGER TRG_AUD_CGG_SOPORTE_TECNICO;

