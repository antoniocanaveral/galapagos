/**
* FUNCION SII.TRG_KG_CGG_RES_FASE_USUARIO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_FASE_USUARIO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_FASE_USUARIO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRFSU_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRFSU_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRFSU_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRFSU_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRFSU_CODIGO>'||COALESCE(OLD.CRFSU_CODIGO, '')||'</CRFSU_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRFAS_CODIGO>'||COALESCE(OLD.CRFAS_CODIGO, '')||'</CRFAS_CODIGO>'||
			'<CRFSU_ACTIVO>'||COALESCE(OLD.CRFSU_ACTIVO, FALSE)||'</CRFSU_ACTIVO>'||
			'<CRFSU_ESTADO>'||COALESCE(OLD.CRFSU_ESTADO, FALSE)||'</CRFSU_ESTADO>'||
			'<CRFSU_FECHA_INSERT>'||COALESCE(OLD.CRFSU_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRFSU_FECHA_INSERT>'||
			'<CRFSU_USUARIO_INSERT>'||COALESCE(OLD.CRFSU_USUARIO_INSERT, '')||'</CRFSU_USUARIO_INSERT>'||
			'<CRFSU_FECHA_UPDATE>'||COALESCE(OLD.CRFSU_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRFSU_FECHA_UPDATE>'||
			'<CRFSU_USUARIO_UPDATE>'||COALESCE(OLD.CRFSU_USUARIO_UPDATE, '')||'</CRFSU_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRFSU_CODIGO>'||COALESCE(NEW.CRFSU_CODIGO, '')||'</CRFSU_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRFAS_CODIGO>'||COALESCE(NEW.CRFAS_CODIGO, '')||'</CRFAS_CODIGO>'||
			'<CRFSU_ACTIVO>'||COALESCE(NEW.CRFSU_ACTIVO, FALSE)||'</CRFSU_ACTIVO>'||
			'<CRFSU_ESTADO>'||COALESCE(NEW.CRFSU_ESTADO, FALSE)||'</CRFSU_ESTADO>'||
			'<CRFSU_FECHA_INSERT>'||COALESCE(NEW.CRFSU_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRFSU_FECHA_INSERT>'||
			'<CRFSU_USUARIO_INSERT>'||COALESCE(NEW.CRFSU_USUARIO_INSERT, '')||'</CRFSU_USUARIO_INSERT>'||
			'<CRFSU_FECHA_UPDATE>'||COALESCE(NEW.CRFSU_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRFSU_FECHA_UPDATE>'||
			'<CRFSU_USUARIO_UPDATE>'||COALESCE(NEW.CRFSU_USUARIO_UPDATE, '')||'</CRFSU_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_FASE_USUARIO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_FASE_USUARIO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_FASE_USUARIO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_FASE_USUARIO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_FASE_USUARIO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_FASE_USUARIO ON SII.CGG_RES_FASE_USUARIO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_FASE_USUARIO DISABLE TRIGGER TRG_AUD_CGG_RES_FASE_USUARIO;

