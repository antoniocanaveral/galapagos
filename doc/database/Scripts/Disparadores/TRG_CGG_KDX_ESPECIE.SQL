/**
* FUNCION SII.TRG_KG_CGG_KDX_ESPECIE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_ESPECIE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_ESPECIE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKESP_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKESP_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKESP_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKESP_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKESP_CODIGO>'||COALESCE(OLD.CKESP_CODIGO, '')||'</CKESP_CODIGO>'||
			'<CKESP_NOMBRE>'||COALESCE(OLD.CKESP_NOMBRE, '')||'</CKESP_NOMBRE>'||
			'<CKESP_PRECIO>'||COALESCE(OLD.CKESP_PRECIO, 0)||'</CKESP_PRECIO>'||
			'<CKESP_CANTIDAD>'||COALESCE(OLD.CKESP_CANTIDAD, 0)||'</CKESP_CANTIDAD>'||
			'<CKESP_DISPONIBLE_MIN>'||COALESCE(OLD.CKESP_DISPONIBLE_MIN, 0)||'</CKESP_DISPONIBLE_MIN>'||
			'<CKESP_DISPONIBLE_MAX>'||COALESCE(OLD.CKESP_DISPONIBLE_MAX, 0)||'</CKESP_DISPONIBLE_MAX>'||
			'<CKESP_TIPO>'||COALESCE(OLD.CKESP_TIPO, 0)||'</CKESP_TIPO>'||
			'<CKESP_INDICE>'||COALESCE(OLD.CKESP_INDICE, 0)||'</CKESP_INDICE>'||
			'<CKESP_REPORTE>'||COALESCE(OLD.CKESP_REPORTE, '')||'</CKESP_REPORTE>'||
			'<CKESP_ESTADO>'||COALESCE(OLD.CKESP_ESTADO, FALSE)||'</CKESP_ESTADO>'||
			'<CKESP_FECHA_INSERT>'||COALESCE(OLD.CKESP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKESP_FECHA_INSERT>'||
			'<CKESP_USUARIO_INSERT>'||COALESCE(OLD.CKESP_USUARIO_INSERT, '')||'</CKESP_USUARIO_INSERT>'||
			'<CKESP_FECHA_UPDATE>'||COALESCE(OLD.CKESP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKESP_FECHA_UPDATE>'||
			'<CKESP_USUARIO_UPDATE>'||COALESCE(OLD.CKESP_USUARIO_UPDATE, '')||'</CKESP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKESP_CODIGO>'||COALESCE(NEW.CKESP_CODIGO, '')||'</CKESP_CODIGO>'||
			'<CKESP_NOMBRE>'||COALESCE(NEW.CKESP_NOMBRE, '')||'</CKESP_NOMBRE>'||
			'<CKESP_PRECIO>'||COALESCE(NEW.CKESP_PRECIO, 0)||'</CKESP_PRECIO>'||
			'<CKESP_CANTIDAD>'||COALESCE(NEW.CKESP_CANTIDAD, 0)||'</CKESP_CANTIDAD>'||
			'<CKESP_DISPONIBLE_MIN>'||COALESCE(NEW.CKESP_DISPONIBLE_MIN, 0)||'</CKESP_DISPONIBLE_MIN>'||
			'<CKESP_DISPONIBLE_MAX>'||COALESCE(NEW.CKESP_DISPONIBLE_MAX, 0)||'</CKESP_DISPONIBLE_MAX>'||
			'<CKESP_TIPO>'||COALESCE(NEW.CKESP_TIPO, 0)||'</CKESP_TIPO>'||
			'<CKESP_INDICE>'||COALESCE(NEW.CKESP_INDICE, 0)||'</CKESP_INDICE>'||
			'<CKESP_REPORTE>'||COALESCE(NEW.CKESP_REPORTE, '')||'</CKESP_REPORTE>'||
			'<CKESP_ESTADO>'||COALESCE(NEW.CKESP_ESTADO, FALSE)||'</CKESP_ESTADO>'||
			'<CKESP_FECHA_INSERT>'||COALESCE(NEW.CKESP_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKESP_FECHA_INSERT>'||
			'<CKESP_USUARIO_INSERT>'||COALESCE(NEW.CKESP_USUARIO_INSERT, '')||'</CKESP_USUARIO_INSERT>'||
			'<CKESP_FECHA_UPDATE>'||COALESCE(NEW.CKESP_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKESP_FECHA_UPDATE>'||
			'<CKESP_USUARIO_UPDATE>'||COALESCE(NEW.CKESP_USUARIO_UPDATE, '')||'</CKESP_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_ESPECIE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_ESPECIE
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_ESPECIE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_ESPECIE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_ESPECIE();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_ESPECIE ON SII.CGG_KDX_ESPECIE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_ESPECIE DISABLE TRIGGER TRG_AUD_CGG_KDX_ESPECIE;

