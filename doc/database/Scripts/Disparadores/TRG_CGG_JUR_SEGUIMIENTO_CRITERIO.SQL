/**
* FUNCION SII.TRG_KG_CGG_JUR_SEGUIMIENTO_CRITERIO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_SEGUIMIENTO_CRITERIO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_JUR_SEGUIMIENTO_CRITERIO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CJSGC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJSGC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CJSGC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJSGC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CJSGC_CODIGO>'||COALESCE(OLD.CJSGC_CODIGO, '')||'</CJSGC_CODIGO>'||
			'<CRSEG_CODIGO>'||COALESCE(OLD.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CJCRI_CODIGO>'||COALESCE(OLD.CJCRI_CODIGO, '')||'</CJCRI_CODIGO>'||
			'<CJSGC_REQUERIDO>'||COALESCE(OLD.CJSGC_REQUERIDO, FALSE)||'</CJSGC_REQUERIDO>'||
			'<CJSGC_RESPUESTA>'||COALESCE(OLD.CJSGC_RESPUESTA, FALSE)||'</CJSGC_RESPUESTA>'||
			'<CJSGC_JUSTIFICACION>'||COALESCE(OLD.CJSGC_JUSTIFICACION, '')||'</CJSGC_JUSTIFICACION>'||
			'<CJSGC_OBSERVACION>'||COALESCE(OLD.CJSGC_OBSERVACION, '')||'</CJSGC_OBSERVACION>'||
			'<CJSGC_NOMBRE_ADJUNTO>'||COALESCE(OLD.CJSGC_NOMBRE_ADJUNTO, '')||'</CJSGC_NOMBRE_ADJUNTO>'||
			'<CJSGC_ADJUNTO>'||COALESCE(encode(OLD.CJSGC_ADJUNTO, 'base64'), '')||'</CJSGC_ADJUNTO>'||
			'<CJSGC_ESTADO>'||COALESCE(OLD.CJSGC_ESTADO, FALSE)||'</CJSGC_ESTADO>'||
			'<CJSGC_FECHA_INSERT>'||COALESCE(OLD.CJSGC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJSGC_FECHA_INSERT>'||
			'<CJSGC_USUARIO_INSERT>'||COALESCE(OLD.CJSGC_USUARIO_INSERT, '')||'</CJSGC_USUARIO_INSERT>'||
			'<CJSGC_FECHA_UPDATE>'||COALESCE(OLD.CJSGC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJSGC_FECHA_UPDATE>'||
			'<CJSGC_USUARIO_UPDATE>'||COALESCE(OLD.CJSGC_USUARIO_UPDATE, '')||'</CJSGC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CJSGC_CODIGO>'||COALESCE(NEW.CJSGC_CODIGO, '')||'</CJSGC_CODIGO>'||
			'<CRSEG_CODIGO>'||COALESCE(NEW.CRSEG_CODIGO, '')||'</CRSEG_CODIGO>'||
			'<CJCRI_CODIGO>'||COALESCE(NEW.CJCRI_CODIGO, '')||'</CJCRI_CODIGO>'||
			'<CJSGC_REQUERIDO>'||COALESCE(NEW.CJSGC_REQUERIDO, FALSE)||'</CJSGC_REQUERIDO>'||
			'<CJSGC_RESPUESTA>'||COALESCE(NEW.CJSGC_RESPUESTA, FALSE)||'</CJSGC_RESPUESTA>'||
			'<CJSGC_JUSTIFICACION>'||COALESCE(NEW.CJSGC_JUSTIFICACION, '')||'</CJSGC_JUSTIFICACION>'||
			'<CJSGC_OBSERVACION>'||COALESCE(NEW.CJSGC_OBSERVACION, '')||'</CJSGC_OBSERVACION>'||
			'<CJSGC_NOMBRE_ADJUNTO>'||COALESCE(NEW.CJSGC_NOMBRE_ADJUNTO, '')||'</CJSGC_NOMBRE_ADJUNTO>'||
			'<CJSGC_ADJUNTO>'||COALESCE(encode(NEW.CJSGC_ADJUNTO, 'base64'), '')||'</CJSGC_ADJUNTO>'||
			'<CJSGC_ESTADO>'||COALESCE(NEW.CJSGC_ESTADO, FALSE)||'</CJSGC_ESTADO>'||
			'<CJSGC_FECHA_INSERT>'||COALESCE(NEW.CJSGC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJSGC_FECHA_INSERT>'||
			'<CJSGC_USUARIO_INSERT>'||COALESCE(NEW.CJSGC_USUARIO_INSERT, '')||'</CJSGC_USUARIO_INSERT>'||
			'<CJSGC_FECHA_UPDATE>'||COALESCE(NEW.CJSGC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJSGC_FECHA_UPDATE>'||
			'<CJSGC_USUARIO_UPDATE>'||COALESCE(NEW.CJSGC_USUARIO_UPDATE, '')||'</CJSGC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_JUR_SEGUIMIENTO_CRITERIO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_SEGUIMIENTO_CRITERIO
*/
CREATE TRIGGER TRG_AUD_CGG_JUR_SEGUIMIENTO_CRITERIO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_JUR_SEGUIMIENTO_CRITERIO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_JUR_SEGUIMIENTO_CRITERIO();
COMMENT ON TRIGGER TRG_AUD_CGG_JUR_SEGUIMIENTO_CRITERIO ON SII.CGG_JUR_SEGUIMIENTO_CRITERIO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_JUR_SEGUIMIENTO_CRITERIO DISABLE TRIGGER TRG_AUD_CGG_JUR_SEGUIMIENTO_CRITERIO;

