/**
* FUNCION SII.TRG_KG_CGG_DHU_ENCUESTA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_ENCUESTA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_ENCUESTA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDENC_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDENC_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDENC_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDENC_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDENC_CODIGO>'||COALESCE(OLD.CDENC_CODIGO, '')||'</CDENC_CODIGO>'||
			'<CDENC_NOMBRE>'||COALESCE(OLD.CDENC_NOMBRE, '')||'</CDENC_NOMBRE>'||
			'<CDENC_OBSERVACION>'||COALESCE(OLD.CDENC_OBSERVACION, '')||'</CDENC_OBSERVACION>'||
			'<CDENC_ESTADO>'||COALESCE(OLD.CDENC_ESTADO, FALSE)||'</CDENC_ESTADO>'||
			'<CDENC_FECHA_INSERT>'||COALESCE(OLD.CDENC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDENC_FECHA_INSERT>'||
			'<CDENC_USUARIO_INSERT>'||COALESCE(OLD.CDENC_USUARIO_INSERT, '')||'</CDENC_USUARIO_INSERT>'||
			'<CDENC_FECHA_UPDATE>'||COALESCE(OLD.CDENC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDENC_FECHA_UPDATE>'||
			'<CDENC_USUARIO_UPDATE>'||COALESCE(OLD.CDENC_USUARIO_UPDATE, '')||'</CDENC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDENC_CODIGO>'||COALESCE(NEW.CDENC_CODIGO, '')||'</CDENC_CODIGO>'||
			'<CDENC_NOMBRE>'||COALESCE(NEW.CDENC_NOMBRE, '')||'</CDENC_NOMBRE>'||
			'<CDENC_OBSERVACION>'||COALESCE(NEW.CDENC_OBSERVACION, '')||'</CDENC_OBSERVACION>'||
			'<CDENC_ESTADO>'||COALESCE(NEW.CDENC_ESTADO, FALSE)||'</CDENC_ESTADO>'||
			'<CDENC_FECHA_INSERT>'||COALESCE(NEW.CDENC_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDENC_FECHA_INSERT>'||
			'<CDENC_USUARIO_INSERT>'||COALESCE(NEW.CDENC_USUARIO_INSERT, '')||'</CDENC_USUARIO_INSERT>'||
			'<CDENC_FECHA_UPDATE>'||COALESCE(NEW.CDENC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDENC_FECHA_UPDATE>'||
			'<CDENC_USUARIO_UPDATE>'||COALESCE(NEW.CDENC_USUARIO_UPDATE, '')||'</CDENC_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_ENCUESTA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_ENCUESTA
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_ENCUESTA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_ENCUESTA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_ENCUESTA();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_ENCUESTA ON SII.CGG_DHU_ENCUESTA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_ENCUESTA DISABLE TRIGGER TRG_AUD_CGG_DHU_ENCUESTA;

