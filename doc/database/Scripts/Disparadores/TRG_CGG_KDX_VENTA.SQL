/**
* FUNCION SII.TRG_KG_CGG_KDX_VENTA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_VENTA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_VENTA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKVNT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKVNT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKVNT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKVNT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKVNT_CODIGO>'||COALESCE(OLD.CKVNT_CODIGO, '')||'</CKVNT_CODIGO>'||
			'<CKASG_CODIGO>'||COALESCE(OLD.CKASG_CODIGO, '')||'</CKASG_CODIGO>'||
			'<CKVNT_LOTE>'||COALESCE(OLD.CKVNT_LOTE, 0)||'</CKVNT_LOTE>'||
			'<CKVNT_FECHA_INGRESO>'||COALESCE(OLD.CKVNT_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CKVNT_FECHA_INGRESO>'||
			'<CKVNT_NUMERO_VENTA>'||COALESCE(OLD.CKVNT_NUMERO_VENTA, '')||'</CKVNT_NUMERO_VENTA>'||
			'<CKVNT_SERIE_INICIO>'||COALESCE(OLD.CKVNT_SERIE_INICIO, 0)||'</CKVNT_SERIE_INICIO>'||
			'<CKVNT_SERIE_FIN>'||COALESCE(OLD.CKVNT_SERIE_FIN, 0)||'</CKVNT_SERIE_FIN>'||
			'<CKVNT_CANTIDAD>'||COALESCE(OLD.CKVNT_CANTIDAD, 0)||'</CKVNT_CANTIDAD>'||
			'<CKVNT_TOTAL>'||COALESCE(OLD.CKVNT_TOTAL, '')||'</CKVNT_TOTAL>'||
			'<CKVNT_ESTADO_VENTA>'||COALESCE(OLD.CKVNT_ESTADO_VENTA, 0)||'</CKVNT_ESTADO_VENTA>'||
			'<CKVNT_ESTADO>'||COALESCE(OLD.CKVNT_ESTADO, FALSE)||'</CKVNT_ESTADO>'||
			'<CKVNT_FECHA_INSERT>'||COALESCE(OLD.CKVNT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKVNT_FECHA_INSERT>'||
			'<CKVNT_USUARIO_INSERT>'||COALESCE(OLD.CKVNT_USUARIO_INSERT, '')||'</CKVNT_USUARIO_INSERT>'||
			'<CKVNT_FECHA_UPDATE>'||COALESCE(OLD.CKVNT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKVNT_FECHA_UPDATE>'||
			'<CKVNT_USUARIO_UPDATE>'||COALESCE(OLD.CKVNT_USUARIO_UPDATE, '')||'</CKVNT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKVNT_CODIGO>'||COALESCE(NEW.CKVNT_CODIGO, '')||'</CKVNT_CODIGO>'||
			'<CKASG_CODIGO>'||COALESCE(NEW.CKASG_CODIGO, '')||'</CKASG_CODIGO>'||
			'<CKVNT_LOTE>'||COALESCE(NEW.CKVNT_LOTE, 0)||'</CKVNT_LOTE>'||
			'<CKVNT_FECHA_INGRESO>'||COALESCE(NEW.CKVNT_FECHA_INGRESO, CURRENT_TIMESTAMP)||'</CKVNT_FECHA_INGRESO>'||
			'<CKVNT_NUMERO_VENTA>'||COALESCE(NEW.CKVNT_NUMERO_VENTA, '')||'</CKVNT_NUMERO_VENTA>'||
			'<CKVNT_SERIE_INICIO>'||COALESCE(NEW.CKVNT_SERIE_INICIO, 0)||'</CKVNT_SERIE_INICIO>'||
			'<CKVNT_SERIE_FIN>'||COALESCE(NEW.CKVNT_SERIE_FIN, 0)||'</CKVNT_SERIE_FIN>'||
			'<CKVNT_CANTIDAD>'||COALESCE(NEW.CKVNT_CANTIDAD, 0)||'</CKVNT_CANTIDAD>'||
			'<CKVNT_TOTAL>'||COALESCE(NEW.CKVNT_TOTAL, '')||'</CKVNT_TOTAL>'||
			'<CKVNT_ESTADO_VENTA>'||COALESCE(NEW.CKVNT_ESTADO_VENTA, 0)||'</CKVNT_ESTADO_VENTA>'||
			'<CKVNT_ESTADO>'||COALESCE(NEW.CKVNT_ESTADO, FALSE)||'</CKVNT_ESTADO>'||
			'<CKVNT_FECHA_INSERT>'||COALESCE(NEW.CKVNT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKVNT_FECHA_INSERT>'||
			'<CKVNT_USUARIO_INSERT>'||COALESCE(NEW.CKVNT_USUARIO_INSERT, '')||'</CKVNT_USUARIO_INSERT>'||
			'<CKVNT_FECHA_UPDATE>'||COALESCE(NEW.CKVNT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKVNT_FECHA_UPDATE>'||
			'<CKVNT_USUARIO_UPDATE>'||COALESCE(NEW.CKVNT_USUARIO_UPDATE, '')||'</CKVNT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_VENTA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_VENTA
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_VENTA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_VENTA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_VENTA();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_VENTA ON SII.CGG_KDX_VENTA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_VENTA DISABLE TRIGGER TRG_AUD_CGG_KDX_VENTA;

