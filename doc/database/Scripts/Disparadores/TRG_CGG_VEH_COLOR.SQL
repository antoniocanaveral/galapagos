/**
* FUNCION SII.TRG_KG_CGG_VEH_COLOR. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_COLOR
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_VEH_COLOR() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CVCLR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVCLR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CVCLR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CVCLR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CVCLR_CODIGO>'||COALESCE(OLD.CVCLR_CODIGO, '')||'</CVCLR_CODIGO>'||
			'<CVCLR_NOMBRE>'||COALESCE(OLD.CVCLR_NOMBRE, '')||'</CVCLR_NOMBRE>'||
			'<CVCLR_OBSERVACION>'||COALESCE(OLD.CVCLR_OBSERVACION, '')||'</CVCLR_OBSERVACION>'||
			'<CVCLR_ESTADO>'||COALESCE(OLD.CVCLR_ESTADO, FALSE)||'</CVCLR_ESTADO>'||
			'<CVCLR_USUARIO_INSERT>'||COALESCE(OLD.CVCLR_USUARIO_INSERT, '')||'</CVCLR_USUARIO_INSERT>'||
			'<CVCLR_FECHA_INSERT>'||COALESCE(OLD.CVCLR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVCLR_FECHA_INSERT>'||
			'<CVCLR_USUARIO_UPDATE>'||COALESCE(OLD.CVCLR_USUARIO_UPDATE, '')||'</CVCLR_USUARIO_UPDATE>'||
			'<CVCLR_FECHA_UPDATE>'||COALESCE(OLD.CVCLR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVCLR_FECHA_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CVCLR_CODIGO>'||COALESCE(NEW.CVCLR_CODIGO, '')||'</CVCLR_CODIGO>'||
			'<CVCLR_NOMBRE>'||COALESCE(NEW.CVCLR_NOMBRE, '')||'</CVCLR_NOMBRE>'||
			'<CVCLR_OBSERVACION>'||COALESCE(NEW.CVCLR_OBSERVACION, '')||'</CVCLR_OBSERVACION>'||
			'<CVCLR_ESTADO>'||COALESCE(NEW.CVCLR_ESTADO, FALSE)||'</CVCLR_ESTADO>'||
			'<CVCLR_USUARIO_INSERT>'||COALESCE(NEW.CVCLR_USUARIO_INSERT, '')||'</CVCLR_USUARIO_INSERT>'||
			'<CVCLR_FECHA_INSERT>'||COALESCE(NEW.CVCLR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CVCLR_FECHA_INSERT>'||
			'<CVCLR_USUARIO_UPDATE>'||COALESCE(NEW.CVCLR_USUARIO_UPDATE, '')||'</CVCLR_USUARIO_UPDATE>'||
			'<CVCLR_FECHA_UPDATE>'||COALESCE(NEW.CVCLR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CVCLR_FECHA_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_VEH_COLOR. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_VEH_COLOR
*/
CREATE TRIGGER TRG_AUD_CGG_VEH_COLOR AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_VEH_COLOR FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_VEH_COLOR();
COMMENT ON TRIGGER TRG_AUD_CGG_VEH_COLOR ON SII.CGG_VEH_COLOR IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_VEH_COLOR DISABLE TRIGGER TRG_AUD_CGG_VEH_COLOR;

