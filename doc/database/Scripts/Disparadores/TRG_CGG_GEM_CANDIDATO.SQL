/**
* FUNCION SII.TRG_KG_CGG_GEM_CANDIDATO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_CANDIDATO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GEM_CANDIDATO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGCND_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGCND_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGCND_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGCND_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGCND_CODIGO>'||COALESCE(OLD.CGCND_CODIGO, '')||'</CGCND_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CGVCN_CODIGO>'||COALESCE(OLD.CGVCN_CODIGO, '')||'</CGVCN_CODIGO>'||
			'<CGCND_ASPIRACION_SALARIAL>'||COALESCE(OLD.CGCND_ASPIRACION_SALARIAL, 0)||'</CGCND_ASPIRACION_SALARIAL>'||
			'<CGCND_OBSERVACION>'||COALESCE(OLD.CGCND_OBSERVACION, '')||'</CGCND_OBSERVACION>'||
			'<CGCND_FECHA_ENTREVISTA>'||COALESCE(OLD.CGCND_FECHA_ENTREVISTA, CURRENT_TIMESTAMP)||'</CGCND_FECHA_ENTREVISTA>'||
			'<CGCND_DESCRIPCION>'||COALESCE(OLD.CGCND_DESCRIPCION, '')||'</CGCND_DESCRIPCION>'||
			'<CGCND_FECHA_SELECCION>'||COALESCE(OLD.CGCND_FECHA_SELECCION, CURRENT_TIMESTAMP)||'</CGCND_FECHA_SELECCION>'||
			'<CGCND_DETALLE>'||COALESCE(OLD.CGCND_DETALLE, '')||'</CGCND_DETALLE>'||
			'<CGCND_SELECCIONADO>'||COALESCE(OLD.CGCND_SELECCIONADO, 0)||'</CGCND_SELECCIONADO>'||
			'<CGCND_MOTIVO_SALIDA>'||COALESCE(OLD.CGCND_MOTIVO_SALIDA, '')||'</CGCND_MOTIVO_SALIDA>'||
			'<CGCND_FECHA_SALIDA>'||COALESCE(OLD.CGCND_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CGCND_FECHA_SALIDA>'||
			'<CGCND_ESTADO>'||COALESCE(OLD.CGCND_ESTADO, FALSE)||'</CGCND_ESTADO>'||
			'<CGCND_FECHA_INSERT>'||COALESCE(OLD.CGCND_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGCND_FECHA_INSERT>'||
			'<CGCND_USUARIO_INSERT>'||COALESCE(OLD.CGCND_USUARIO_INSERT, '')||'</CGCND_USUARIO_INSERT>'||
			'<CGCND_FECHA_UPDATE>'||COALESCE(OLD.CGCND_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGCND_FECHA_UPDATE>'||
			'<CGCND_USUARIO_UPDATE>'||COALESCE(OLD.CGCND_USUARIO_UPDATE, '')||'</CGCND_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGCND_CODIGO>'||COALESCE(NEW.CGCND_CODIGO, '')||'</CGCND_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CGVCN_CODIGO>'||COALESCE(NEW.CGVCN_CODIGO, '')||'</CGVCN_CODIGO>'||
			'<CGCND_ASPIRACION_SALARIAL>'||COALESCE(NEW.CGCND_ASPIRACION_SALARIAL, 0)||'</CGCND_ASPIRACION_SALARIAL>'||
			'<CGCND_OBSERVACION>'||COALESCE(NEW.CGCND_OBSERVACION, '')||'</CGCND_OBSERVACION>'||
			'<CGCND_FECHA_ENTREVISTA>'||COALESCE(NEW.CGCND_FECHA_ENTREVISTA, CURRENT_TIMESTAMP)||'</CGCND_FECHA_ENTREVISTA>'||
			'<CGCND_DESCRIPCION>'||COALESCE(NEW.CGCND_DESCRIPCION, '')||'</CGCND_DESCRIPCION>'||
			'<CGCND_FECHA_SELECCION>'||COALESCE(NEW.CGCND_FECHA_SELECCION, CURRENT_TIMESTAMP)||'</CGCND_FECHA_SELECCION>'||
			'<CGCND_DETALLE>'||COALESCE(NEW.CGCND_DETALLE, '')||'</CGCND_DETALLE>'||
			'<CGCND_SELECCIONADO>'||COALESCE(NEW.CGCND_SELECCIONADO, 0)||'</CGCND_SELECCIONADO>'||
			'<CGCND_MOTIVO_SALIDA>'||COALESCE(NEW.CGCND_MOTIVO_SALIDA, '')||'</CGCND_MOTIVO_SALIDA>'||
			'<CGCND_FECHA_SALIDA>'||COALESCE(NEW.CGCND_FECHA_SALIDA, CURRENT_TIMESTAMP)||'</CGCND_FECHA_SALIDA>'||
			'<CGCND_ESTADO>'||COALESCE(NEW.CGCND_ESTADO, FALSE)||'</CGCND_ESTADO>'||
			'<CGCND_FECHA_INSERT>'||COALESCE(NEW.CGCND_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGCND_FECHA_INSERT>'||
			'<CGCND_USUARIO_INSERT>'||COALESCE(NEW.CGCND_USUARIO_INSERT, '')||'</CGCND_USUARIO_INSERT>'||
			'<CGCND_FECHA_UPDATE>'||COALESCE(NEW.CGCND_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGCND_FECHA_UPDATE>'||
			'<CGCND_USUARIO_UPDATE>'||COALESCE(NEW.CGCND_USUARIO_UPDATE, '')||'</CGCND_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GEM_CANDIDATO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_CANDIDATO
*/
CREATE TRIGGER TRG_AUD_CGG_GEM_CANDIDATO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GEM_CANDIDATO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GEM_CANDIDATO();
COMMENT ON TRIGGER TRG_AUD_CGG_GEM_CANDIDATO ON SII.CGG_GEM_CANDIDATO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GEM_CANDIDATO DISABLE TRIGGER TRG_AUD_CGG_GEM_CANDIDATO;

