/**
* FUNCION SII.TRG_KG_CGG_WEB_PREGUNTAS. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_WEB_PREGUNTAS
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_WEB_PREGUNTAS() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CWPRG_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CWPRG_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CWPRG_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CWPRG_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CWPRG_CODIGO>'||COALESCE(OLD.CWPRG_CODIGO, '')||'</CWPRG_CODIGO>'||
			'<CWMOD_CODIGO>'||COALESCE(OLD.CWMOD_CODIGO, '')||'</CWMOD_CODIGO>'||
			'<CWPRG_PREGUNTA>'||COALESCE(OLD.CWPRG_PREGUNTA, '')||'</CWPRG_PREGUNTA>'||
			'<CWPRG_RESPUESTA>'||COALESCE(OLD.CWPRG_RESPUESTA, '')||'</CWPRG_RESPUESTA>'||
			'<CWPRG_OBSERVACION>'||COALESCE(OLD.CWPRG_OBSERVACION, '')||'</CWPRG_OBSERVACION>'||
			'<CWPRG_PRIORIDAD>'||COALESCE(OLD.CWPRG_PRIORIDAD, 0)||'</CWPRG_PRIORIDAD>'||
			'<CWPRG_ESTADO>'||COALESCE(OLD.CWPRG_ESTADO, FALSE)||'</CWPRG_ESTADO>'||
			'<CWPRG_FECHA_INSERT>'||COALESCE(OLD.CWPRG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CWPRG_FECHA_INSERT>'||
			'<CWPRG_USUARIO_INSERT>'||COALESCE(OLD.CWPRG_USUARIO_INSERT, '')||'</CWPRG_USUARIO_INSERT>'||
			'<CWPRG_FECHA_UPDATE>'||COALESCE(OLD.CWPRG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CWPRG_FECHA_UPDATE>'||
			'<CWPRG_USUARIO_UPDATE>'||COALESCE(OLD.CWPRG_USUARIO_UPDATE, '')||'</CWPRG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CWPRG_CODIGO>'||COALESCE(NEW.CWPRG_CODIGO, '')||'</CWPRG_CODIGO>'||
			'<CWMOD_CODIGO>'||COALESCE(NEW.CWMOD_CODIGO, '')||'</CWMOD_CODIGO>'||
			'<CWPRG_PREGUNTA>'||COALESCE(NEW.CWPRG_PREGUNTA, '')||'</CWPRG_PREGUNTA>'||
			'<CWPRG_RESPUESTA>'||COALESCE(NEW.CWPRG_RESPUESTA, '')||'</CWPRG_RESPUESTA>'||
			'<CWPRG_OBSERVACION>'||COALESCE(NEW.CWPRG_OBSERVACION, '')||'</CWPRG_OBSERVACION>'||
			'<CWPRG_PRIORIDAD>'||COALESCE(NEW.CWPRG_PRIORIDAD, 0)||'</CWPRG_PRIORIDAD>'||
			'<CWPRG_ESTADO>'||COALESCE(NEW.CWPRG_ESTADO, FALSE)||'</CWPRG_ESTADO>'||
			'<CWPRG_FECHA_INSERT>'||COALESCE(NEW.CWPRG_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CWPRG_FECHA_INSERT>'||
			'<CWPRG_USUARIO_INSERT>'||COALESCE(NEW.CWPRG_USUARIO_INSERT, '')||'</CWPRG_USUARIO_INSERT>'||
			'<CWPRG_FECHA_UPDATE>'||COALESCE(NEW.CWPRG_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CWPRG_FECHA_UPDATE>'||
			'<CWPRG_USUARIO_UPDATE>'||COALESCE(NEW.CWPRG_USUARIO_UPDATE, '')||'</CWPRG_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_WEB_PREGUNTAS. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_WEB_PREGUNTAS
*/
CREATE TRIGGER TRG_AUD_CGG_WEB_PREGUNTAS AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_WEB_PREGUNTAS FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_WEB_PREGUNTAS();
COMMENT ON TRIGGER TRG_AUD_CGG_WEB_PREGUNTAS ON SII.CGG_WEB_PREGUNTAS IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_WEB_PREGUNTAS DISABLE TRIGGER TRG_AUD_CGG_WEB_PREGUNTAS;

