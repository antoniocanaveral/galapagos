/**
* FUNCION SII.TRG_KG_CGG_GEM_PERFIL_PROF. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_PERFIL_PROF
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_GEM_PERFIL_PROF() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CGPPR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGPPR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CGPPR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CGPPR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CGPPR_CODIGO>'||COALESCE(OLD.CGPPR_CODIGO, '')||'</CGPPR_CODIGO>'||
			'<CGESP_CODIGO>'||COALESCE(OLD.CGESP_CODIGO, '')||'</CGESP_CODIGO>'||
			'<CGTPR_CODIGO>'||COALESCE(OLD.CGTPR_CODIGO, '')||'</CGTPR_CODIGO>'||
			'<CGMDC_CODIGO>'||COALESCE(OLD.CGMDC_CODIGO, '')||'</CGMDC_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGIEN_CODIGO>'||COALESCE(OLD.CGIEN_CODIGO, '')||'</CGIEN_CODIGO>'||
			'<CGPPR_NIVEL_APROBADO>'||COALESCE(OLD.CGPPR_NIVEL_APROBADO, 0)||'</CGPPR_NIVEL_APROBADO>'||
			'<CGPPR_FECHA_INICIO>'||COALESCE(OLD.CGPPR_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_INICIO>'||
			'<CGPPR_FECHA_FIN>'||COALESCE(OLD.CGPPR_FECHA_FIN, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_FIN>'||
			'<CGPPR_CONFIRMADO>'||COALESCE(OLD.CGPPR_CONFIRMADO, FALSE)||'</CGPPR_CONFIRMADO>'||
			'<CGPPR_FECHA_CONFIRMACION>'||COALESCE(OLD.CGPPR_FECHA_CONFIRMACION, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_CONFIRMACION>'||
			'<CGPPR_PREDETERMINADO>'||COALESCE(OLD.CGPPR_PREDETERMINADO, FALSE)||'</CGPPR_PREDETERMINADO>'||
			'<CGPPR_ESTADO>'||COALESCE(OLD.CGPPR_ESTADO, FALSE)||'</CGPPR_ESTADO>'||
			'<CGPPR_FECHA_INSERT>'||COALESCE(OLD.CGPPR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_INSERT>'||
			'<CGPPR_USUARIO_INSERT>'||COALESCE(OLD.CGPPR_USUARIO_INSERT, '')||'</CGPPR_USUARIO_INSERT>'||
			'<CGPPR_FECHA_UPDATE>'||COALESCE(OLD.CGPPR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_UPDATE>'||
			'<CGPPR_USUARIO_UPDATE>'||COALESCE(OLD.CGPPR_USUARIO_UPDATE, '')||'</CGPPR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CGPPR_CODIGO>'||COALESCE(NEW.CGPPR_CODIGO, '')||'</CGPPR_CODIGO>'||
			'<CGESP_CODIGO>'||COALESCE(NEW.CGESP_CODIGO, '')||'</CGESP_CODIGO>'||
			'<CGTPR_CODIGO>'||COALESCE(NEW.CGTPR_CODIGO, '')||'</CGTPR_CODIGO>'||
			'<CGMDC_CODIGO>'||COALESCE(NEW.CGMDC_CODIGO, '')||'</CGMDC_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGIEN_CODIGO>'||COALESCE(NEW.CGIEN_CODIGO, '')||'</CGIEN_CODIGO>'||
			'<CGPPR_NIVEL_APROBADO>'||COALESCE(NEW.CGPPR_NIVEL_APROBADO, 0)||'</CGPPR_NIVEL_APROBADO>'||
			'<CGPPR_FECHA_INICIO>'||COALESCE(NEW.CGPPR_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_INICIO>'||
			'<CGPPR_FECHA_FIN>'||COALESCE(NEW.CGPPR_FECHA_FIN, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_FIN>'||
			'<CGPPR_CONFIRMADO>'||COALESCE(NEW.CGPPR_CONFIRMADO, FALSE)||'</CGPPR_CONFIRMADO>'||
			'<CGPPR_FECHA_CONFIRMACION>'||COALESCE(NEW.CGPPR_FECHA_CONFIRMACION, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_CONFIRMACION>'||
			'<CGPPR_PREDETERMINADO>'||COALESCE(NEW.CGPPR_PREDETERMINADO, FALSE)||'</CGPPR_PREDETERMINADO>'||
			'<CGPPR_ESTADO>'||COALESCE(NEW.CGPPR_ESTADO, FALSE)||'</CGPPR_ESTADO>'||
			'<CGPPR_FECHA_INSERT>'||COALESCE(NEW.CGPPR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_INSERT>'||
			'<CGPPR_USUARIO_INSERT>'||COALESCE(NEW.CGPPR_USUARIO_INSERT, '')||'</CGPPR_USUARIO_INSERT>'||
			'<CGPPR_FECHA_UPDATE>'||COALESCE(NEW.CGPPR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CGPPR_FECHA_UPDATE>'||
			'<CGPPR_USUARIO_UPDATE>'||COALESCE(NEW.CGPPR_USUARIO_UPDATE, '')||'</CGPPR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_GEM_PERFIL_PROF. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_GEM_PERFIL_PROF
*/
CREATE TRIGGER TRG_AUD_CGG_GEM_PERFIL_PROF AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_GEM_PERFIL_PROF FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_GEM_PERFIL_PROF();
COMMENT ON TRIGGER TRG_AUD_CGG_GEM_PERFIL_PROF ON SII.CGG_GEM_PERFIL_PROF IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_GEM_PERFIL_PROF DISABLE TRIGGER TRG_AUD_CGG_GEM_PERFIL_PROF;

