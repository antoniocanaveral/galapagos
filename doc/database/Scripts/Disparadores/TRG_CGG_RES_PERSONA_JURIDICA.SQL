/**
* FUNCION SII.TRG_KG_CGG_RES_PERSONA_JURIDICA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_PERSONA_JURIDICA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_PERSONA_JURIDICA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRPJR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRPJR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRPJR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRPJR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRPJR_CODIGO>'||COALESCE(OLD.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CSCTP_CODIGO>'||COALESCE(OLD.CSCTP_CODIGO, '')||'</CSCTP_CODIGO>'||
			'<CVSCT_CODIGO>'||COALESCE(OLD.CVSCT_CODIGO, '')||'</CVSCT_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(OLD.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CRPJR_RAZON_SOCIAL>'||COALESCE(OLD.CRPJR_RAZON_SOCIAL, '')||'</CRPJR_RAZON_SOCIAL>'||
			'<CRPJR_NUMERO_IDENTIFICACION>'||COALESCE(OLD.CRPJR_NUMERO_IDENTIFICACION, '')||'</CRPJR_NUMERO_IDENTIFICACION>'||
			'<CRPJR_FECHA_CONSTITUCION>'||COALESCE(OLD.CRPJR_FECHA_CONSTITUCION, CURRENT_TIMESTAMP)||'</CRPJR_FECHA_CONSTITUCION>'||
			'<CRPJR_DIRECCION>'||COALESCE(OLD.CRPJR_DIRECCION, '')||'</CRPJR_DIRECCION>'||
			'<CRPJR_ACTIVIDAD>'||COALESCE(OLD.CRPJR_ACTIVIDAD, '')||'</CRPJR_ACTIVIDAD>'||
			'<CRPJR_NUMERO_EMPLEADOS>'||COALESCE(OLD.CRPJR_NUMERO_EMPLEADOS, 0)||'</CRPJR_NUMERO_EMPLEADOS>'||
			'<CRPJR_TIPO>'||COALESCE(OLD.CRPJR_TIPO, 0)||'</CRPJR_TIPO>'||
			'<CRPJR_OBSERVACION>'||COALESCE(OLD.CRPJR_OBSERVACION, '')||'</CRPJR_OBSERVACION>'||
			'<CRPJR_ESTADO>'||COALESCE(OLD.CRPJR_ESTADO, FALSE)||'</CRPJR_ESTADO>'||
			'<CRPJR_FECHA_INSERT>'||COALESCE(OLD.CRPJR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRPJR_FECHA_INSERT>'||
			'<CRPJR_USUARIO_INSERT>'||COALESCE(OLD.CRPJR_USUARIO_INSERT, '')||'</CRPJR_USUARIO_INSERT>'||
			'<CRPJR_FECHA_UPDATE>'||COALESCE(OLD.CRPJR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRPJR_FECHA_UPDATE>'||
			'<CRPJR_USUARIO_UPDATE>'||COALESCE(OLD.CRPJR_USUARIO_UPDATE, '')||'</CRPJR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CSCTP_CODIGO>'||COALESCE(NEW.CSCTP_CODIGO, '')||'</CSCTP_CODIGO>'||
			'<CVSCT_CODIGO>'||COALESCE(NEW.CVSCT_CODIGO, '')||'</CVSCT_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(NEW.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CRPJR_RAZON_SOCIAL>'||COALESCE(NEW.CRPJR_RAZON_SOCIAL, '')||'</CRPJR_RAZON_SOCIAL>'||
			'<CRPJR_NUMERO_IDENTIFICACION>'||COALESCE(NEW.CRPJR_NUMERO_IDENTIFICACION, '')||'</CRPJR_NUMERO_IDENTIFICACION>'||
			'<CRPJR_FECHA_CONSTITUCION>'||COALESCE(NEW.CRPJR_FECHA_CONSTITUCION, CURRENT_TIMESTAMP)||'</CRPJR_FECHA_CONSTITUCION>'||
			'<CRPJR_DIRECCION>'||COALESCE(NEW.CRPJR_DIRECCION, '')||'</CRPJR_DIRECCION>'||
			'<CRPJR_ACTIVIDAD>'||COALESCE(NEW.CRPJR_ACTIVIDAD, '')||'</CRPJR_ACTIVIDAD>'||
			'<CRPJR_NUMERO_EMPLEADOS>'||COALESCE(NEW.CRPJR_NUMERO_EMPLEADOS, 0)||'</CRPJR_NUMERO_EMPLEADOS>'||
			'<CRPJR_TIPO>'||COALESCE(NEW.CRPJR_TIPO, 0)||'</CRPJR_TIPO>'||
			'<CRPJR_OBSERVACION>'||COALESCE(NEW.CRPJR_OBSERVACION, '')||'</CRPJR_OBSERVACION>'||
			'<CRPJR_ESTADO>'||COALESCE(NEW.CRPJR_ESTADO, FALSE)||'</CRPJR_ESTADO>'||
			'<CRPJR_FECHA_INSERT>'||COALESCE(NEW.CRPJR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRPJR_FECHA_INSERT>'||
			'<CRPJR_USUARIO_INSERT>'||COALESCE(NEW.CRPJR_USUARIO_INSERT, '')||'</CRPJR_USUARIO_INSERT>'||
			'<CRPJR_FECHA_UPDATE>'||COALESCE(NEW.CRPJR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRPJR_FECHA_UPDATE>'||
			'<CRPJR_USUARIO_UPDATE>'||COALESCE(NEW.CRPJR_USUARIO_UPDATE, '')||'</CRPJR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_PERSONA_JURIDICA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_PERSONA_JURIDICA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_PERSONA_JURIDICA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_PERSONA_JURIDICA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_PERSONA_JURIDICA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_PERSONA_JURIDICA ON SII.CGG_RES_PERSONA_JURIDICA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_PERSONA_JURIDICA DISABLE TRIGGER TRG_AUD_CGG_RES_PERSONA_JURIDICA;

