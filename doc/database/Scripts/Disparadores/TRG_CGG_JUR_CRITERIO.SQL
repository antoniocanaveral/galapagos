/**
* FUNCION SII.TRG_KG_CGG_JUR_CRITERIO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_CRITERIO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_JUR_CRITERIO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CJCRI_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJCRI_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CJCRI_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJCRI_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CJCRI_CODIGO>'||COALESCE(OLD.CJCRI_CODIGO, '')||'</CJCRI_CODIGO>'||
			'<CRFAS_CODIGO>'||COALESCE(OLD.CRFAS_CODIGO, '')||'</CRFAS_CODIGO>'||
			'<CJCRI_REQUERIMIENTO>'||COALESCE(OLD.CJCRI_REQUERIMIENTO, '')||'</CJCRI_REQUERIMIENTO>'||
			'<CJCRI_REQUERIDO>'||COALESCE(OLD.CJCRI_REQUERIDO, FALSE)||'</CJCRI_REQUERIDO>'||
			'<CJCRI_ESTADO>'||COALESCE(OLD.CJCRI_ESTADO, FALSE)||'</CJCRI_ESTADO>'||
			'<CJCRI_FECHA_INSERT>'||COALESCE(OLD.CJCRI_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJCRI_FECHA_INSERT>'||
			'<CJCRI_USUARIO_INSERT>'||COALESCE(OLD.CJCRI_USUARIO_INSERT, '')||'</CJCRI_USUARIO_INSERT>'||
			'<CJCRI_FECHA_UPDATE>'||COALESCE(OLD.CJCRI_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJCRI_FECHA_UPDATE>'||
			'<CJCRI_USUARIO_UPDATE>'||COALESCE(OLD.CJCRI_USUARIO_UPDATE, '')||'</CJCRI_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CJCRI_CODIGO>'||COALESCE(NEW.CJCRI_CODIGO, '')||'</CJCRI_CODIGO>'||
			'<CRFAS_CODIGO>'||COALESCE(NEW.CRFAS_CODIGO, '')||'</CRFAS_CODIGO>'||
			'<CJCRI_REQUERIMIENTO>'||COALESCE(NEW.CJCRI_REQUERIMIENTO, '')||'</CJCRI_REQUERIMIENTO>'||
			'<CJCRI_REQUERIDO>'||COALESCE(NEW.CJCRI_REQUERIDO, FALSE)||'</CJCRI_REQUERIDO>'||
			'<CJCRI_ESTADO>'||COALESCE(NEW.CJCRI_ESTADO, FALSE)||'</CJCRI_ESTADO>'||
			'<CJCRI_FECHA_INSERT>'||COALESCE(NEW.CJCRI_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJCRI_FECHA_INSERT>'||
			'<CJCRI_USUARIO_INSERT>'||COALESCE(NEW.CJCRI_USUARIO_INSERT, '')||'</CJCRI_USUARIO_INSERT>'||
			'<CJCRI_FECHA_UPDATE>'||COALESCE(NEW.CJCRI_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJCRI_FECHA_UPDATE>'||
			'<CJCRI_USUARIO_UPDATE>'||COALESCE(NEW.CJCRI_USUARIO_UPDATE, '')||'</CJCRI_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_JUR_CRITERIO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_CRITERIO
*/
CREATE TRIGGER TRG_AUD_CGG_JUR_CRITERIO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_JUR_CRITERIO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_JUR_CRITERIO();
COMMENT ON TRIGGER TRG_AUD_CGG_JUR_CRITERIO ON SII.CGG_JUR_CRITERIO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_JUR_CRITERIO DISABLE TRIGGER TRG_AUD_CGG_JUR_CRITERIO;

