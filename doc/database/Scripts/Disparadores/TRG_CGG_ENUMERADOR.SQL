/**
* FUNCION SII.TRG_KG_CGG_ENUMERADOR. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_ENUMERADOR
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_ENUMERADOR() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW._USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW._USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW._USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW._USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CENUM_CODIGO>'||COALESCE(OLD.CENUM_CODIGO, '')||'</CENUM_CODIGO>'||
			'<CENUM_TABLA>'||COALESCE(OLD.CENUM_TABLA, '')||'</CENUM_TABLA>'||
			'<CENUM_COLUMNA>'||COALESCE(OLD.CENUM_COLUMNA, '')||'</CENUM_COLUMNA>'||
			'<CENUM_VALOR>'||COALESCE(OLD.CENUM_VALOR, 0)||'</CENUM_VALOR>'||
			'<CENUM_DESCRIPCION>'||COALESCE(OLD.CENUM_DESCRIPCION, '')||'</CENUM_DESCRIPCION>'||
			'<CENUM_ESTADO>'||COALESCE(OLD.CENUM_ESTADO, FALSE)||'</CENUM_ESTADO>'||
			'<CENUM_FECHA_INSERT>'||COALESCE(OLD.CENUM_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CENUM_FECHA_INSERT>'||
			'<CENUM_USUARIO_INSERT>'||COALESCE(OLD.CENUM_USUARIO_INSERT, '')||'</CENUM_USUARIO_INSERT>'||
			'<CENUM_FECHA_UPDATE>'||COALESCE(OLD.CENUM_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CENUM_FECHA_UPDATE>'||
			'<CENUM_USUARIO_UPDATE>'||COALESCE(OLD.CENUM_USUARIO_UPDATE, '')||'</CENUM_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CENUM_CODIGO>'||COALESCE(NEW.CENUM_CODIGO, '')||'</CENUM_CODIGO>'||
			'<CENUM_TABLA>'||COALESCE(NEW.CENUM_TABLA, '')||'</CENUM_TABLA>'||
			'<CENUM_COLUMNA>'||COALESCE(NEW.CENUM_COLUMNA, '')||'</CENUM_COLUMNA>'||
			'<CENUM_VALOR>'||COALESCE(NEW.CENUM_VALOR, 0)||'</CENUM_VALOR>'||
			'<CENUM_DESCRIPCION>'||COALESCE(NEW.CENUM_DESCRIPCION, '')||'</CENUM_DESCRIPCION>'||
			'<CENUM_ESTADO>'||COALESCE(NEW.CENUM_ESTADO, FALSE)||'</CENUM_ESTADO>'||
			'<CENUM_FECHA_INSERT>'||COALESCE(NEW.CENUM_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CENUM_FECHA_INSERT>'||
			'<CENUM_USUARIO_INSERT>'||COALESCE(NEW.CENUM_USUARIO_INSERT, '')||'</CENUM_USUARIO_INSERT>'||
			'<CENUM_FECHA_UPDATE>'||COALESCE(NEW.CENUM_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CENUM_FECHA_UPDATE>'||
			'<CENUM_USUARIO_UPDATE>'||COALESCE(NEW.CENUM_USUARIO_UPDATE, '')||'</CENUM_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_ENUMERADOR. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_ENUMERADOR
*/
CREATE TRIGGER TRG_AUD_CGG_ENUMERADOR AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_ENUMERADOR FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_ENUMERADOR();
COMMENT ON TRIGGER TRG_AUD_CGG_ENUMERADOR ON SII.CGG_ENUMERADOR IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_ENUMERADOR DISABLE TRIGGER TRG_AUD_CGG_ENUMERADOR;

