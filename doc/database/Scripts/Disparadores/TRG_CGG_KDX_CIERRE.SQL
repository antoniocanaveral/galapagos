/**
* FUNCION SII.TRG_KG_CGG_KDX_CIERRE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_CIERRE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_CIERRE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKCRR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKCRR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKCRR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKCRR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKCRR_CODIGO>'||COALESCE(OLD.CKCRR_CODIGO, '')||'</CKCRR_CODIGO>'||
			'<CKPVT_CODIGO>'||COALESCE(OLD.CKPVT_CODIGO, '')||'</CKPVT_CODIGO>'||
			'<CKUPV_CODIGO>'||COALESCE(OLD.CKUPV_CODIGO, '')||'</CKUPV_CODIGO>'||
			'<CKCRR_FECHA_CIERRE>'||COALESCE(OLD.CKCRR_FECHA_CIERRE, CURRENT_TIMESTAMP)||'</CKCRR_FECHA_CIERRE>'||
			'<CKCRR_NUMERO_CIERRE>'||COALESCE(OLD.CKCRR_NUMERO_CIERRE, '')||'</CKCRR_NUMERO_CIERRE>'||
			'<CKCRR_TOTAL_VENTA>'||COALESCE(OLD.CKCRR_TOTAL_VENTA, 0)||'</CKCRR_TOTAL_VENTA>'||
			'<CKCRR_TOTAL>'||COALESCE(OLD.CKCRR_TOTAL, 0)||'</CKCRR_TOTAL>'||
			'<CKCRR_OBSERVACION>'||COALESCE(OLD.CKCRR_OBSERVACION, '')||'</CKCRR_OBSERVACION>'||
			'<CKCRR_DIFERENCIA>'||COALESCE(OLD.CKCRR_DIFERENCIA, 0)||'</CKCRR_DIFERENCIA>'||
			'<CKCRR_ESTADO_CIERRE>'||COALESCE(OLD.CKCRR_ESTADO_CIERRE, 0)||'</CKCRR_ESTADO_CIERRE>'||
			'<CKCRR_ESTADO>'||COALESCE(OLD.CKCRR_ESTADO, FALSE)||'</CKCRR_ESTADO>'||
			'<CKCRR_FECHA_INSERT>'||COALESCE(OLD.CKCRR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKCRR_FECHA_INSERT>'||
			'<CKCRR_USUARIO_INSERT>'||COALESCE(OLD.CKCRR_USUARIO_INSERT, '')||'</CKCRR_USUARIO_INSERT>'||
			'<CKCRR_FECHA_UPDATE>'||COALESCE(OLD.CKCRR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKCRR_FECHA_UPDATE>'||
			'<CKCRR_USUARIO_UPDATE>'||COALESCE(OLD.CKCRR_USUARIO_UPDATE, '')||'</CKCRR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKCRR_CODIGO>'||COALESCE(NEW.CKCRR_CODIGO, '')||'</CKCRR_CODIGO>'||
			'<CKPVT_CODIGO>'||COALESCE(NEW.CKPVT_CODIGO, '')||'</CKPVT_CODIGO>'||
			'<CKUPV_CODIGO>'||COALESCE(NEW.CKUPV_CODIGO, '')||'</CKUPV_CODIGO>'||
			'<CKCRR_FECHA_CIERRE>'||COALESCE(NEW.CKCRR_FECHA_CIERRE, CURRENT_TIMESTAMP)||'</CKCRR_FECHA_CIERRE>'||
			'<CKCRR_NUMERO_CIERRE>'||COALESCE(NEW.CKCRR_NUMERO_CIERRE, '')||'</CKCRR_NUMERO_CIERRE>'||
			'<CKCRR_TOTAL_VENTA>'||COALESCE(NEW.CKCRR_TOTAL_VENTA, 0)||'</CKCRR_TOTAL_VENTA>'||
			'<CKCRR_TOTAL>'||COALESCE(NEW.CKCRR_TOTAL, 0)||'</CKCRR_TOTAL>'||
			'<CKCRR_OBSERVACION>'||COALESCE(NEW.CKCRR_OBSERVACION, '')||'</CKCRR_OBSERVACION>'||
			'<CKCRR_DIFERENCIA>'||COALESCE(NEW.CKCRR_DIFERENCIA, 0)||'</CKCRR_DIFERENCIA>'||
			'<CKCRR_ESTADO_CIERRE>'||COALESCE(NEW.CKCRR_ESTADO_CIERRE, 0)||'</CKCRR_ESTADO_CIERRE>'||
			'<CKCRR_ESTADO>'||COALESCE(NEW.CKCRR_ESTADO, FALSE)||'</CKCRR_ESTADO>'||
			'<CKCRR_FECHA_INSERT>'||COALESCE(NEW.CKCRR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKCRR_FECHA_INSERT>'||
			'<CKCRR_USUARIO_INSERT>'||COALESCE(NEW.CKCRR_USUARIO_INSERT, '')||'</CKCRR_USUARIO_INSERT>'||
			'<CKCRR_FECHA_UPDATE>'||COALESCE(NEW.CKCRR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKCRR_FECHA_UPDATE>'||
			'<CKCRR_USUARIO_UPDATE>'||COALESCE(NEW.CKCRR_USUARIO_UPDATE, '')||'</CKCRR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_CIERRE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_CIERRE
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_CIERRE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_CIERRE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_CIERRE();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_CIERRE ON SII.CGG_KDX_CIERRE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_CIERRE DISABLE TRIGGER TRG_AUD_CGG_KDX_CIERRE;

