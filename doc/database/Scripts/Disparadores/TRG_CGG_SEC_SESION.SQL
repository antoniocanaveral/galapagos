/**
* FUNCION SII.TRG_KG_CGG_SEC_SESION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SEC_SESION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_SEC_SESION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
TMP_ROW RECORD;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_ROW = NEW;
		TMP_USUARIO = NEW.CSSSN_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSSSN_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_ROW = NEW;
		TMP_USUARIO = NEW.CSSSN_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSSSN_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CSSSN_CODIGO>'||COALESCE(OLD.CSSSN_CODIGO, '')||'</CSSSN_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CSSSN_FECHA_INICIO>'||COALESCE(OLD.CSSSN_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CSSSN_FECHA_INICIO>'||
			'<CSSSN_FECHA_FIN>'||COALESCE(OLD.CSSSN_FECHA_FIN, CURRENT_TIMESTAMP)||'</CSSSN_FECHA_FIN>'||
			'<CSSSN_ID>'||COALESCE(OLD.CSSSN_ID, '')||'</CSSSN_ID>'||
			'<CSSSN_IP>'||COALESCE(OLD.CSSSN_IP, '')||'</CSSSN_IP>'||
			'<CSSSN_ZONA_TIEMPO>'||COALESCE(OLD.CSSSN_ZONA_TIEMPO, '')||'</CSSSN_ZONA_TIEMPO>'||
			'<CSSSN_ACTIVO>'||COALESCE(OLD.CSSSN_ACTIVO, FALSE)||'</CSSSN_ACTIVO>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CSSSN_CODIGO>'||COALESCE(NEW.CSSSN_CODIGO, '')||'</CSSSN_CODIGO>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CSSSN_FECHA_INICIO>'||COALESCE(NEW.CSSSN_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CSSSN_FECHA_INICIO>'||
			'<CSSSN_FECHA_FIN>'||COALESCE(NEW.CSSSN_FECHA_FIN, CURRENT_TIMESTAMP)||'</CSSSN_FECHA_FIN>'||
			'<CSSSN_ID>'||COALESCE(NEW.CSSSN_ID, '')||'</CSSSN_ID>'||
			'<CSSSN_IP>'||COALESCE(NEW.CSSSN_IP, '')||'</CSSSN_IP>'||
			'<CSSSN_ZONA_TIEMPO>'||COALESCE(NEW.CSSSN_ZONA_TIEMPO, '')||'</CSSSN_ZONA_TIEMPO>'||
			'<CSSSN_ACTIVO>'||COALESCE(NEW.CSSSN_ACTIVO, FALSE)||'</CSSSN_ACTIVO>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_SEC_SESION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SEC_SESION
*/
DROP TRIGGER TRG_AUD_CGG_SEC_SESION ON SII.CGG_SEC_SESION;
CREATE TRIGGER TRG_AUD_CGG_SEC_SESION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_SEC_SESION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_SEC_SESION();
COMMENT ON TRIGGER TRG_AUD_CGG_SEC_SESION ON SII.CGG_SEC_SESION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_SEC_SESION DISABLE TRIGGER TRG_AUD_CGG_SEC_SESION;

