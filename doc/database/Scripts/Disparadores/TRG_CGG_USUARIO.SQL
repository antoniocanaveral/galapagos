/**
* FUNCION SII.TRG_KG_CGG_USUARIO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_USUARIO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_USUARIO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CUSU_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CUSU_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CUSU_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CUSU_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CUSU_CODIGO>'||COALESCE(OLD.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRCRG_CODIGO>'||COALESCE(OLD.CRCRG_CODIGO, '')||'</CRCRG_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(OLD.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(OLD.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CISLA_CODIGO>'||COALESCE(OLD.CISLA_CODIGO, '')||'</CISLA_CODIGO>'||
			'<CUSU_NOMBRE_USUARIO>'||COALESCE(OLD.CUSU_NOMBRE_USUARIO, '')||'</CUSU_NOMBRE_USUARIO>'||
			'<CUSU_CLAVE_ACCESO>'||COALESCE(OLD.CUSU_CLAVE_ACCESO, '')||'</CUSU_CLAVE_ACCESO>'||
			'<CUSU_USUARIO_INTERNO>'||COALESCE(OLD.CUSU_USUARIO_INTERNO, FALSE)||'</CUSU_USUARIO_INTERNO>'||
			'<CUSU_CADUCA_CLAVE>'||COALESCE(OLD.CUSU_CADUCA_CLAVE, FALSE)||'</CUSU_CADUCA_CLAVE>'||
			'<CUSU_FECHA_CLAVE_ACT>'||COALESCE(OLD.CUSU_FECHA_CLAVE_ACT, CURRENT_TIMESTAMP)||'</CUSU_FECHA_CLAVE_ACT>'||
			'<CUSU_ACTIVO>'||COALESCE(OLD.CUSU_ACTIVO, FALSE)||'</CUSU_ACTIVO>'||
			'<CUSU_CAMBIAR_CLAVE>'||COALESCE(OLD.CUSU_CAMBIAR_CLAVE, FALSE)||'</CUSU_CAMBIAR_CLAVE>'||
			'<CUSU_ESTADO>'||COALESCE(OLD.CUSU_ESTADO, FALSE)||'</CUSU_ESTADO>'||
			'<CUSU_FECHA_INSERT>'||COALESCE(OLD.CUSU_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CUSU_FECHA_INSERT>'||
			'<CUSU_USUARIO_INSERT>'||COALESCE(OLD.CUSU_USUARIO_INSERT, '')||'</CUSU_USUARIO_INSERT>'||
			'<CUSU_FECHA_UPDATE>'||COALESCE(OLD.CUSU_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CUSU_FECHA_UPDATE>'||
			'<CUSU_USUARIO_UPDATE>'||COALESCE(OLD.CUSU_USUARIO_UPDATE, '')||'</CUSU_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CUSU_CODIGO>'||COALESCE(NEW.CUSU_CODIGO, '')||'</CUSU_CODIGO>'||
			'<CRCRG_CODIGO>'||COALESCE(NEW.CRCRG_CODIGO, '')||'</CRCRG_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(NEW.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CISLA_CODIGO>'||COALESCE(NEW.CISLA_CODIGO, '')||'</CISLA_CODIGO>'||
			'<CUSU_NOMBRE_USUARIO>'||COALESCE(NEW.CUSU_NOMBRE_USUARIO, '')||'</CUSU_NOMBRE_USUARIO>'||
			'<CUSU_CLAVE_ACCESO>'||COALESCE(NEW.CUSU_CLAVE_ACCESO, '')||'</CUSU_CLAVE_ACCESO>'||
			'<CUSU_USUARIO_INTERNO>'||COALESCE(NEW.CUSU_USUARIO_INTERNO, FALSE)||'</CUSU_USUARIO_INTERNO>'||
			'<CUSU_CADUCA_CLAVE>'||COALESCE(NEW.CUSU_CADUCA_CLAVE, FALSE)||'</CUSU_CADUCA_CLAVE>'||
			'<CUSU_FECHA_CLAVE_ACT>'||COALESCE(NEW.CUSU_FECHA_CLAVE_ACT, CURRENT_TIMESTAMP)||'</CUSU_FECHA_CLAVE_ACT>'||
			'<CUSU_ACTIVO>'||COALESCE(NEW.CUSU_ACTIVO, FALSE)||'</CUSU_ACTIVO>'||
			'<CUSU_CAMBIAR_CLAVE>'||COALESCE(NEW.CUSU_CAMBIAR_CLAVE, FALSE)||'</CUSU_CAMBIAR_CLAVE>'||
			'<CUSU_ESTADO>'||COALESCE(NEW.CUSU_ESTADO, FALSE)||'</CUSU_ESTADO>'||
			'<CUSU_FECHA_INSERT>'||COALESCE(NEW.CUSU_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CUSU_FECHA_INSERT>'||
			'<CUSU_USUARIO_INSERT>'||COALESCE(NEW.CUSU_USUARIO_INSERT, '')||'</CUSU_USUARIO_INSERT>'||
			'<CUSU_FECHA_UPDATE>'||COALESCE(NEW.CUSU_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CUSU_FECHA_UPDATE>'||
			'<CUSU_USUARIO_UPDATE>'||COALESCE(NEW.CUSU_USUARIO_UPDATE, '')||'</CUSU_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_USUARIO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_USUARIO
*/
CREATE TRIGGER TRG_AUD_CGG_USUARIO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_USUARIO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_USUARIO();
COMMENT ON TRIGGER TRG_AUD_CGG_USUARIO ON SII.CGG_USUARIO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_USUARIO DISABLE TRIGGER TRG_AUD_CGG_USUARIO;

