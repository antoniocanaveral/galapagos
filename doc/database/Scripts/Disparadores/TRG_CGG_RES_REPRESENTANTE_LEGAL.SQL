/**
* FUNCION SII.TRG_KG_CGG_RES_REPRESENTANTE_LEGAL. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_REPRESENTANTE_LEGAL
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_REPRESENTANTE_LEGAL() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRRPT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRRPT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRRPT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRRPT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRRPT_CODIGO>'||COALESCE(OLD.CRRPT_CODIGO, '')||'</CRRPT_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(OLD.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRRPT_FECHA_REGISTRO>'||COALESCE(OLD.CRRPT_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CRRPT_FECHA_REGISTRO>'||
			'<CRRPT_ACTIVO>'||COALESCE(OLD.CRRPT_ACTIVO, FALSE)||'</CRRPT_ACTIVO>'||
			'<CRRPT_ESTADO>'||COALESCE(OLD.CRRPT_ESTADO, FALSE)||'</CRRPT_ESTADO>'||
			'<CRRPT_FECHA_INSERT>'||COALESCE(OLD.CRRPT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRRPT_FECHA_INSERT>'||
			'<CRRPT_USUARIO_INSERT>'||COALESCE(OLD.CRRPT_USUARIO_INSERT, '')||'</CRRPT_USUARIO_INSERT>'||
			'<CRRPT_FECHA_UPDATE>'||COALESCE(OLD.CRRPT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRRPT_FECHA_UPDATE>'||
			'<CRRPT_USUARIO_UPDATE>'||COALESCE(OLD.CRRPT_USUARIO_UPDATE, '')||'</CRRPT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRRPT_CODIGO>'||COALESCE(NEW.CRRPT_CODIGO, '')||'</CRRPT_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRRPT_FECHA_REGISTRO>'||COALESCE(NEW.CRRPT_FECHA_REGISTRO, CURRENT_TIMESTAMP)||'</CRRPT_FECHA_REGISTRO>'||
			'<CRRPT_ACTIVO>'||COALESCE(NEW.CRRPT_ACTIVO, FALSE)||'</CRRPT_ACTIVO>'||
			'<CRRPT_ESTADO>'||COALESCE(NEW.CRRPT_ESTADO, FALSE)||'</CRRPT_ESTADO>'||
			'<CRRPT_FECHA_INSERT>'||COALESCE(NEW.CRRPT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRRPT_FECHA_INSERT>'||
			'<CRRPT_USUARIO_INSERT>'||COALESCE(NEW.CRRPT_USUARIO_INSERT, '')||'</CRRPT_USUARIO_INSERT>'||
			'<CRRPT_FECHA_UPDATE>'||COALESCE(NEW.CRRPT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRRPT_FECHA_UPDATE>'||
			'<CRRPT_USUARIO_UPDATE>'||COALESCE(NEW.CRRPT_USUARIO_UPDATE, '')||'</CRRPT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_REPRESENTANTE_LEGAL. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_REPRESENTANTE_LEGAL
*/
CREATE TRIGGER TRG_AUD_CGG_RES_REPRESENTANTE_LEGAL AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_REPRESENTANTE_LEGAL FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_REPRESENTANTE_LEGAL();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_REPRESENTANTE_LEGAL ON SII.CGG_RES_REPRESENTANTE_LEGAL IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_REPRESENTANTE_LEGAL DISABLE TRIGGER TRG_AUD_CGG_RES_REPRESENTANTE_LEGAL;

