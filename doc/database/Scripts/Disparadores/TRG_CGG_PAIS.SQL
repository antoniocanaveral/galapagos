/**
* FUNCION SII.TRG_KG_CGG_PAIS. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_PAIS
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_PAIS() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CPAIS_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CPAIS_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CPAIS_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CPAIS_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CPAIS_CODIGO>'||COALESCE(OLD.CPAIS_CODIGO, '')||'</CPAIS_CODIGO>'||
			'<CPAIS_NOMBRE>'||COALESCE(OLD.CPAIS_NOMBRE, '')||'</CPAIS_NOMBRE>'||
			'<CPAIS_ISO>'||COALESCE(OLD.CPAIS_ISO, '')||'</CPAIS_ISO>'||
			'<CPAIS_DESCRIPCION>'||COALESCE(OLD.CPAIS_DESCRIPCION, '')||'</CPAIS_DESCRIPCION>'||
			'<CPAIS_ESTADO>'||COALESCE(OLD.CPAIS_ESTADO, FALSE)||'</CPAIS_ESTADO>'||
			'<CPAIS_FECHA_INSERT>'||COALESCE(OLD.CPAIS_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CPAIS_FECHA_INSERT>'||
			'<CPAIS_USUARIO_INSERT>'||COALESCE(OLD.CPAIS_USUARIO_INSERT, '')||'</CPAIS_USUARIO_INSERT>'||
			'<CPAIS_FECHA_UPDATE>'||COALESCE(OLD.CPAIS_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CPAIS_FECHA_UPDATE>'||
			'<CPAIS_USUARIO_UPDATE>'||COALESCE(OLD.CPAIS_USUARIO_UPDATE, '')||'</CPAIS_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CPAIS_CODIGO>'||COALESCE(NEW.CPAIS_CODIGO, '')||'</CPAIS_CODIGO>'||
			'<CPAIS_NOMBRE>'||COALESCE(NEW.CPAIS_NOMBRE, '')||'</CPAIS_NOMBRE>'||
			'<CPAIS_ISO>'||COALESCE(NEW.CPAIS_ISO, '')||'</CPAIS_ISO>'||
			'<CPAIS_DESCRIPCION>'||COALESCE(NEW.CPAIS_DESCRIPCION, '')||'</CPAIS_DESCRIPCION>'||
			'<CPAIS_ESTADO>'||COALESCE(NEW.CPAIS_ESTADO, FALSE)||'</CPAIS_ESTADO>'||
			'<CPAIS_FECHA_INSERT>'||COALESCE(NEW.CPAIS_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CPAIS_FECHA_INSERT>'||
			'<CPAIS_USUARIO_INSERT>'||COALESCE(NEW.CPAIS_USUARIO_INSERT, '')||'</CPAIS_USUARIO_INSERT>'||
			'<CPAIS_FECHA_UPDATE>'||COALESCE(NEW.CPAIS_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CPAIS_FECHA_UPDATE>'||
			'<CPAIS_USUARIO_UPDATE>'||COALESCE(NEW.CPAIS_USUARIO_UPDATE, '')||'</CPAIS_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_PAIS. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_PAIS
*/
CREATE TRIGGER TRG_AUD_CGG_PAIS AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_PAIS FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_PAIS();
COMMENT ON TRIGGER TRG_AUD_CGG_PAIS ON SII.CGG_PAIS IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_PAIS DISABLE TRIGGER TRG_AUD_CGG_PAIS;

