/**
* FUNCION SII.TRG_KG_CGG_RES_TESTIGO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TESTIGO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_TESTIGO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRTST_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTST_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRTST_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTST_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRTST_CODIGO>'||COALESCE(OLD.CRTST_CODIGO, '')||'</CRTST_CODIGO>'||
			'<CRINT_CODIGO>'||COALESCE(OLD.CRINT_CODIGO, '')||'</CRINT_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRTST_TESTIMONIO>'||COALESCE(OLD.CRTST_TESTIMONIO, '')||'</CRTST_TESTIMONIO>'||
			'<CRTST_OBSERVACION>'||COALESCE(OLD.CRTST_OBSERVACION, '')||'</CRTST_OBSERVACION>'||
			'<CRTST_ESTADO>'||COALESCE(OLD.CRTST_ESTADO, FALSE)||'</CRTST_ESTADO>'||
			'<CRTST_FECHA_INSERT>'||COALESCE(OLD.CRTST_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTST_FECHA_INSERT>'||
			'<CRTST_USUARIO_INSERT>'||COALESCE(OLD.CRTST_USUARIO_INSERT, '')||'</CRTST_USUARIO_INSERT>'||
			'<CRTST_FECHA_UPDATE>'||COALESCE(OLD.CRTST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTST_FECHA_UPDATE>'||
			'<CRTST_USUARIO_UPDATE>'||COALESCE(OLD.CRTST_USUARIO_UPDATE, '')||'</CRTST_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRTST_CODIGO>'||COALESCE(NEW.CRTST_CODIGO, '')||'</CRTST_CODIGO>'||
			'<CRINT_CODIGO>'||COALESCE(NEW.CRINT_CODIGO, '')||'</CRINT_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRTST_TESTIMONIO>'||COALESCE(NEW.CRTST_TESTIMONIO, '')||'</CRTST_TESTIMONIO>'||
			'<CRTST_OBSERVACION>'||COALESCE(NEW.CRTST_OBSERVACION, '')||'</CRTST_OBSERVACION>'||
			'<CRTST_ESTADO>'||COALESCE(NEW.CRTST_ESTADO, FALSE)||'</CRTST_ESTADO>'||
			'<CRTST_FECHA_INSERT>'||COALESCE(NEW.CRTST_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTST_FECHA_INSERT>'||
			'<CRTST_USUARIO_INSERT>'||COALESCE(NEW.CRTST_USUARIO_INSERT, '')||'</CRTST_USUARIO_INSERT>'||
			'<CRTST_FECHA_UPDATE>'||COALESCE(NEW.CRTST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTST_FECHA_UPDATE>'||
			'<CRTST_USUARIO_UPDATE>'||COALESCE(NEW.CRTST_USUARIO_UPDATE, '')||'</CRTST_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_TESTIGO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TESTIGO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_TESTIGO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_TESTIGO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_TESTIGO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_TESTIGO ON SII.CGG_RES_TESTIGO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_TESTIGO DISABLE TRIGGER TRG_AUD_CGG_RES_TESTIGO;

