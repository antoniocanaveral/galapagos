/**
* FUNCION SII.TRG_KG_CGG_PARROQUIA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_PARROQUIA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_PARROQUIA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CPRR_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CPRR_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CPRR_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CPRR_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CPRR_CODIGO>'||COALESCE(OLD.CPRR_CODIGO, '')||'</CPRR_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(OLD.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CPRR_NOMBRE>'||COALESCE(OLD.CPRR_NOMBRE, '')||'</CPRR_NOMBRE>'||
			'<CPRR_DESCRIPCION>'||COALESCE(OLD.CPRR_DESCRIPCION, '')||'</CPRR_DESCRIPCION>'||
			'<CPRR_TIPO>'||COALESCE(OLD.CPRR_TIPO, 0)||'</CPRR_TIPO>'||
			'<CPRR_ESTADO>'||COALESCE(OLD.CPRR_ESTADO, FALSE)||'</CPRR_ESTADO>'||
			'<CPRR_FECHA_INSERT>'||COALESCE(OLD.CPRR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CPRR_FECHA_INSERT>'||
			'<CPRR_USUARIO_INSERT>'||COALESCE(OLD.CPRR_USUARIO_INSERT, '')||'</CPRR_USUARIO_INSERT>'||
			'<CPRR_FECHA_UPDATE>'||COALESCE(OLD.CPRR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CPRR_FECHA_UPDATE>'||
			'<CPRR_USUARIO_UPDATE>'||COALESCE(OLD.CPRR_USUARIO_UPDATE, '')||'</CPRR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CPRR_CODIGO>'||COALESCE(NEW.CPRR_CODIGO, '')||'</CPRR_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(NEW.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CPRR_NOMBRE>'||COALESCE(NEW.CPRR_NOMBRE, '')||'</CPRR_NOMBRE>'||
			'<CPRR_DESCRIPCION>'||COALESCE(NEW.CPRR_DESCRIPCION, '')||'</CPRR_DESCRIPCION>'||
			'<CPRR_TIPO>'||COALESCE(NEW.CPRR_TIPO, 0)||'</CPRR_TIPO>'||
			'<CPRR_ESTADO>'||COALESCE(NEW.CPRR_ESTADO, FALSE)||'</CPRR_ESTADO>'||
			'<CPRR_FECHA_INSERT>'||COALESCE(NEW.CPRR_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CPRR_FECHA_INSERT>'||
			'<CPRR_USUARIO_INSERT>'||COALESCE(NEW.CPRR_USUARIO_INSERT, '')||'</CPRR_USUARIO_INSERT>'||
			'<CPRR_FECHA_UPDATE>'||COALESCE(NEW.CPRR_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CPRR_FECHA_UPDATE>'||
			'<CPRR_USUARIO_UPDATE>'||COALESCE(NEW.CPRR_USUARIO_UPDATE, '')||'</CPRR_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_PARROQUIA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_PARROQUIA
*/
CREATE TRIGGER TRG_AUD_CGG_PARROQUIA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_PARROQUIA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_PARROQUIA();
COMMENT ON TRIGGER TRG_AUD_CGG_PARROQUIA ON SII.CGG_PARROQUIA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_PARROQUIA DISABLE TRIGGER TRG_AUD_CGG_PARROQUIA;

