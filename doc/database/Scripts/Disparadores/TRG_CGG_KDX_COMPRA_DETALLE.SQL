/**
* FUNCION SII.TRG_KG_CGG_KDX_COMPRA_DETALLE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_COMPRA_DETALLE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_COMPRA_DETALLE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKCMD_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKCMD_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKCMD_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKCMD_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKCMD_CODIGO>'||COALESCE(OLD.CKCMD_CODIGO, '')||'</CKCMD_CODIGO>'||
			'<CKCMP_CODIGO>'||COALESCE(OLD.CKCMP_CODIGO, '')||'</CKCMP_CODIGO>'||
			'<CKESP_CODIGO>'||COALESCE(OLD.CKESP_CODIGO, '')||'</CKESP_CODIGO>'||
			'<CKCMD_SERIE_INICIO>'||COALESCE(OLD.CKCMD_SERIE_INICIO, 0)||'</CKCMD_SERIE_INICIO>'||
			'<CKCMD_SERIE_FIN>'||COALESCE(OLD.CKCMD_SERIE_FIN, 0)||'</CKCMD_SERIE_FIN>'||
			'<CKCMD_CANTIDAD>'||COALESCE(OLD.CKCMD_CANTIDAD, 0)||'</CKCMD_CANTIDAD>'||
			'<CKCMD_PRECIO_UNITARIO>'||COALESCE(OLD.CKCMD_PRECIO_UNITARIO, 0)||'</CKCMD_PRECIO_UNITARIO>'||
			'<CKCMD_TOTAL>'||COALESCE(OLD.CKCMD_TOTAL, 0)||'</CKCMD_TOTAL>'||
			'<CKCMD_ESTADO>'||COALESCE(OLD.CKCMD_ESTADO, FALSE)||'</CKCMD_ESTADO>'||
			'<CKCMD_FECHA_INSERT>'||COALESCE(OLD.CKCMD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKCMD_FECHA_INSERT>'||
			'<CKCMD_USUARIO_INSERT>'||COALESCE(OLD.CKCMD_USUARIO_INSERT, '')||'</CKCMD_USUARIO_INSERT>'||
			'<CKCMD_FECHA_UPDATE>'||COALESCE(OLD.CKCMD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKCMD_FECHA_UPDATE>'||
			'<CKCMD_USUARIO_UPDATE>'||COALESCE(OLD.CKCMD_USUARIO_UPDATE, '')||'</CKCMD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKCMD_CODIGO>'||COALESCE(NEW.CKCMD_CODIGO, '')||'</CKCMD_CODIGO>'||
			'<CKCMP_CODIGO>'||COALESCE(NEW.CKCMP_CODIGO, '')||'</CKCMP_CODIGO>'||
			'<CKESP_CODIGO>'||COALESCE(NEW.CKESP_CODIGO, '')||'</CKESP_CODIGO>'||
			'<CKCMD_SERIE_INICIO>'||COALESCE(NEW.CKCMD_SERIE_INICIO, 0)||'</CKCMD_SERIE_INICIO>'||
			'<CKCMD_SERIE_FIN>'||COALESCE(NEW.CKCMD_SERIE_FIN, 0)||'</CKCMD_SERIE_FIN>'||
			'<CKCMD_CANTIDAD>'||COALESCE(NEW.CKCMD_CANTIDAD, 0)||'</CKCMD_CANTIDAD>'||
			'<CKCMD_PRECIO_UNITARIO>'||COALESCE(NEW.CKCMD_PRECIO_UNITARIO, 0)||'</CKCMD_PRECIO_UNITARIO>'||
			'<CKCMD_TOTAL>'||COALESCE(NEW.CKCMD_TOTAL, 0)||'</CKCMD_TOTAL>'||
			'<CKCMD_ESTADO>'||COALESCE(NEW.CKCMD_ESTADO, FALSE)||'</CKCMD_ESTADO>'||
			'<CKCMD_FECHA_INSERT>'||COALESCE(NEW.CKCMD_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKCMD_FECHA_INSERT>'||
			'<CKCMD_USUARIO_INSERT>'||COALESCE(NEW.CKCMD_USUARIO_INSERT, '')||'</CKCMD_USUARIO_INSERT>'||
			'<CKCMD_FECHA_UPDATE>'||COALESCE(NEW.CKCMD_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKCMD_FECHA_UPDATE>'||
			'<CKCMD_USUARIO_UPDATE>'||COALESCE(NEW.CKCMD_USUARIO_UPDATE, '')||'</CKCMD_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_COMPRA_DETALLE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_COMPRA_DETALLE
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_COMPRA_DETALLE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_COMPRA_DETALLE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_COMPRA_DETALLE();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_COMPRA_DETALLE ON SII.CGG_KDX_COMPRA_DETALLE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_COMPRA_DETALLE DISABLE TRIGGER TRG_AUD_CGG_KDX_COMPRA_DETALLE;

