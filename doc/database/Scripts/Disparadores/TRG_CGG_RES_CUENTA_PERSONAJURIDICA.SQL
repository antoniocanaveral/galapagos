/**
* FUNCION SII.TRG_KG_CGG_RES_CUENTA_PERSONAJURIDICA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_CUENTA_PERSONAJURIDICA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_CUENTA_PERSONAJURIDICA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRCPJ_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRCPJ_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRCPJ_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRCPJ_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRCPJ_CODIGO>'||COALESCE(OLD.CRCPJ_CODIGO, '')||'</CRCPJ_CODIGO>'||
			'<CGG_CRPJR_CODIGO>'||COALESCE(OLD.CGG_CRPJR_CODIGO, '')||'</CGG_CRPJR_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(OLD.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRCPJ_NUMERO_CUENTA>'||COALESCE(OLD.CRCPJ_NUMERO_CUENTA, '')||'</CRCPJ_NUMERO_CUENTA>'||
			'<CRCPJ_DESCRIPCION>'||COALESCE(OLD.CRCPJ_DESCRIPCION, '')||'</CRCPJ_DESCRIPCION>'||
			'<CRCPJ_TIPO_CUENTA>'||COALESCE(OLD.CRCPJ_TIPO_CUENTA, 0)||'</CRCPJ_TIPO_CUENTA>'||
			'<CRCPJ_ESTADO>'||COALESCE(OLD.CRCPJ_ESTADO, FALSE)||'</CRCPJ_ESTADO>'||
			'<CRCPJ_FECHA_INSERT>'||COALESCE(OLD.CRCPJ_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRCPJ_FECHA_INSERT>'||
			'<CRCPJ_USUARIO_INSERT>'||COALESCE(OLD.CRCPJ_USUARIO_INSERT, '')||'</CRCPJ_USUARIO_INSERT>'||
			'<CRCPJ_FECHA_UPDATE>'||COALESCE(OLD.CRCPJ_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRCPJ_FECHA_UPDATE>'||
			'<CRCPJ_USUARIO_UPDATE>'||COALESCE(OLD.CRCPJ_USUARIO_UPDATE, '')||'</CRCPJ_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRCPJ_CODIGO>'||COALESCE(NEW.CRCPJ_CODIGO, '')||'</CRCPJ_CODIGO>'||
			'<CGG_CRPJR_CODIGO>'||COALESCE(NEW.CGG_CRPJR_CODIGO, '')||'</CGG_CRPJR_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRCPJ_NUMERO_CUENTA>'||COALESCE(NEW.CRCPJ_NUMERO_CUENTA, '')||'</CRCPJ_NUMERO_CUENTA>'||
			'<CRCPJ_DESCRIPCION>'||COALESCE(NEW.CRCPJ_DESCRIPCION, '')||'</CRCPJ_DESCRIPCION>'||
			'<CRCPJ_TIPO_CUENTA>'||COALESCE(NEW.CRCPJ_TIPO_CUENTA, 0)||'</CRCPJ_TIPO_CUENTA>'||
			'<CRCPJ_ESTADO>'||COALESCE(NEW.CRCPJ_ESTADO, FALSE)||'</CRCPJ_ESTADO>'||
			'<CRCPJ_FECHA_INSERT>'||COALESCE(NEW.CRCPJ_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRCPJ_FECHA_INSERT>'||
			'<CRCPJ_USUARIO_INSERT>'||COALESCE(NEW.CRCPJ_USUARIO_INSERT, '')||'</CRCPJ_USUARIO_INSERT>'||
			'<CRCPJ_FECHA_UPDATE>'||COALESCE(NEW.CRCPJ_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRCPJ_FECHA_UPDATE>'||
			'<CRCPJ_USUARIO_UPDATE>'||COALESCE(NEW.CRCPJ_USUARIO_UPDATE, '')||'</CRCPJ_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_CUENTA_PERSONAJURIDICA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_CUENTA_PERSONAJURIDICA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_CUENTA_PERSONAJURIDICA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_CUENTA_PERSONAJURIDICA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_CUENTA_PERSONAJURIDICA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_CUENTA_PERSONAJURIDICA ON SII.CGG_RES_CUENTA_PERSONAJURIDICA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_CUENTA_PERSONAJURIDICA DISABLE TRIGGER TRG_AUD_CGG_RES_CUENTA_PERSONAJURIDICA;

