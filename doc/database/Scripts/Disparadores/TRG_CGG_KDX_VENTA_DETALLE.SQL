/**
* FUNCION SII.TRG_KG_CGG_KDX_VENTA_DETALLE. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_VENTA_DETALLE
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_KDX_VENTA_DETALLE() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CKVDT_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKVDT_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CKVDT_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CKVDT_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CKVDT_CODIGO>'||COALESCE(OLD.CKVDT_CODIGO, '')||'</CKVDT_CODIGO>'||
			'<CKVNT_CODIGO>'||COALESCE(OLD.CKVNT_CODIGO, '')||'</CKVNT_CODIGO>'||
			'<CKESP_CODIGO>'||COALESCE(OLD.CKESP_CODIGO, '')||'</CKESP_CODIGO>'||
			'<CTREG_CODIGO>'||COALESCE(OLD.CTREG_CODIGO, '')||'</CTREG_CODIGO>'||
			'<CRCNT_CODIGO>'||COALESCE(OLD.CRCNT_CODIGO, '')||'</CRCNT_CODIGO>'||
			'<CTIGF_CODIGO>'||COALESCE(OLD.CTIGF_CODIGO, '')||'</CTIGF_CODIGO>'||
			'<CKVDT_CANTIDAD>'||COALESCE(OLD.CKVDT_CANTIDAD, 0)||'</CKVDT_CANTIDAD>'||
			'<CKVDT_SERIE>'||COALESCE(OLD.CKVDT_SERIE, 0)||'</CKVDT_SERIE>'||
			'<CKVDT_PRECIO_UNITARIO>'||COALESCE(OLD.CKVDT_PRECIO_UNITARIO, 0)||'</CKVDT_PRECIO_UNITARIO>'||
			'<CKVDT_TOTAL>'||COALESCE(OLD.CKVDT_TOTAL, 0)||'</CKVDT_TOTAL>'||
			'<CKVDT_ESTADO>'||COALESCE(OLD.CKVDT_ESTADO, FALSE)||'</CKVDT_ESTADO>'||
			'<CKVDT_FECHA_INSERT>'||COALESCE(OLD.CKVDT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKVDT_FECHA_INSERT>'||
			'<CKVDT_USUARIO_INSERT>'||COALESCE(OLD.CKVDT_USUARIO_INSERT, '')||'</CKVDT_USUARIO_INSERT>'||
			'<CKVDT_FECHA_UPDATE>'||COALESCE(OLD.CKVDT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKVDT_FECHA_UPDATE>'||
			'<CKVDT_USUARIO_UPDATE>'||COALESCE(OLD.CKVDT_USUARIO_UPDATE, '')||'</CKVDT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CKVDT_CODIGO>'||COALESCE(NEW.CKVDT_CODIGO, '')||'</CKVDT_CODIGO>'||
			'<CKVNT_CODIGO>'||COALESCE(NEW.CKVNT_CODIGO, '')||'</CKVNT_CODIGO>'||
			'<CKESP_CODIGO>'||COALESCE(NEW.CKESP_CODIGO, '')||'</CKESP_CODIGO>'||
			'<CTREG_CODIGO>'||COALESCE(NEW.CTREG_CODIGO, '')||'</CTREG_CODIGO>'||
			'<CRCNT_CODIGO>'||COALESCE(NEW.CRCNT_CODIGO, '')||'</CRCNT_CODIGO>'||
			'<CTIGF_CODIGO>'||COALESCE(NEW.CTIGF_CODIGO, '')||'</CTIGF_CODIGO>'||
			'<CKVDT_CANTIDAD>'||COALESCE(NEW.CKVDT_CANTIDAD, 0)||'</CKVDT_CANTIDAD>'||
			'<CKVDT_SERIE>'||COALESCE(NEW.CKVDT_SERIE, 0)||'</CKVDT_SERIE>'||
			'<CKVDT_PRECIO_UNITARIO>'||COALESCE(NEW.CKVDT_PRECIO_UNITARIO, 0)||'</CKVDT_PRECIO_UNITARIO>'||
			'<CKVDT_TOTAL>'||COALESCE(NEW.CKVDT_TOTAL, 0)||'</CKVDT_TOTAL>'||
			'<CKVDT_ESTADO>'||COALESCE(NEW.CKVDT_ESTADO, FALSE)||'</CKVDT_ESTADO>'||
			'<CKVDT_FECHA_INSERT>'||COALESCE(NEW.CKVDT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CKVDT_FECHA_INSERT>'||
			'<CKVDT_USUARIO_INSERT>'||COALESCE(NEW.CKVDT_USUARIO_INSERT, '')||'</CKVDT_USUARIO_INSERT>'||
			'<CKVDT_FECHA_UPDATE>'||COALESCE(NEW.CKVDT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CKVDT_FECHA_UPDATE>'||
			'<CKVDT_USUARIO_UPDATE>'||COALESCE(NEW.CKVDT_USUARIO_UPDATE, '')||'</CKVDT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_KDX_VENTA_DETALLE. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_KDX_VENTA_DETALLE
*/
CREATE TRIGGER TRG_AUD_CGG_KDX_VENTA_DETALLE AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_KDX_VENTA_DETALLE FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_KDX_VENTA_DETALLE();
COMMENT ON TRIGGER TRG_AUD_CGG_KDX_VENTA_DETALLE ON SII.CGG_KDX_VENTA_DETALLE IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_KDX_VENTA_DETALLE DISABLE TRIGGER TRG_AUD_CGG_KDX_VENTA_DETALLE;

