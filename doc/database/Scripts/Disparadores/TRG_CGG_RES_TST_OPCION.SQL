/**
* FUNCION SII.TRG_KG_CGG_RES_TST_OPCION. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TST_OPCION
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_TST_OPCION() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRTSO_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTSO_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRTSO_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRTSO_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRTSO_CODIGO>'||COALESCE(OLD.CRTSO_CODIGO, '')||'</CRTSO_CODIGO>'||
			'<CRTSO_NOMBRE>'||COALESCE(OLD.CRTSO_NOMBRE, '')||'</CRTSO_NOMBRE>'||
			'<CRTSO_DESCRIPCION>'||COALESCE(OLD.CRTSO_DESCRIPCION, '')||'</CRTSO_DESCRIPCION>'||
			'<CRTSO_ESTADO>'||COALESCE(OLD.CRTSO_ESTADO, FALSE)||'</CRTSO_ESTADO>'||
			'<CRTSO_FECHA_INSERT>'||COALESCE(OLD.CRTSO_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTSO_FECHA_INSERT>'||
			'<CRTSO_USUARIO_INSERT>'||COALESCE(OLD.CRTSO_USUARIO_INSERT, '')||'</CRTSO_USUARIO_INSERT>'||
			'<CRTSO_FECHA_UPDATE>'||COALESCE(OLD.CRTSO_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTSO_FECHA_UPDATE>'||
			'<CRTSO_USUARIO_UPDATE>'||COALESCE(OLD.CRTSO_USUARIO_UPDATE, '')||'</CRTSO_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRTSO_CODIGO>'||COALESCE(NEW.CRTSO_CODIGO, '')||'</CRTSO_CODIGO>'||
			'<CRTSO_NOMBRE>'||COALESCE(NEW.CRTSO_NOMBRE, '')||'</CRTSO_NOMBRE>'||
			'<CRTSO_DESCRIPCION>'||COALESCE(NEW.CRTSO_DESCRIPCION, '')||'</CRTSO_DESCRIPCION>'||
			'<CRTSO_ESTADO>'||COALESCE(NEW.CRTSO_ESTADO, FALSE)||'</CRTSO_ESTADO>'||
			'<CRTSO_FECHA_INSERT>'||COALESCE(NEW.CRTSO_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRTSO_FECHA_INSERT>'||
			'<CRTSO_USUARIO_INSERT>'||COALESCE(NEW.CRTSO_USUARIO_INSERT, '')||'</CRTSO_USUARIO_INSERT>'||
			'<CRTSO_FECHA_UPDATE>'||COALESCE(NEW.CRTSO_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRTSO_FECHA_UPDATE>'||
			'<CRTSO_USUARIO_UPDATE>'||COALESCE(NEW.CRTSO_USUARIO_UPDATE, '')||'</CRTSO_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_TST_OPCION. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_TST_OPCION
*/
CREATE TRIGGER TRG_AUD_CGG_RES_TST_OPCION AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_TST_OPCION FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_TST_OPCION();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_TST_OPCION ON SII.CGG_RES_TST_OPCION IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_TST_OPCION DISABLE TRIGGER TRG_AUD_CGG_RES_TST_OPCION;

