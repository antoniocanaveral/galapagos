/**
* FUNCION SII.TRG_KG_CGG_RES_MOVILIDAD_INTERNA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_MOVILIDAD_INTERNA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_MOVILIDAD_INTERNA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRMVI_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRMVI_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRMVI_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRMVI_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRMVI_CODIGO>'||COALESCE(OLD.CRMVI_CODIGO, '')||'</CRMVI_CODIGO>'||
			'<CRMLE_CODIGO>'||COALESCE(OLD.CRMLE_CODIGO, '')||'</CRMLE_CODIGO>'||
			'<CGG_CRMLE_CODIGO>'||COALESCE(OLD.CGG_CRMLE_CODIGO, '')||'</CGG_CRMLE_CODIGO>'||
			'<CREMB_CODIGO>'||COALESCE(OLD.CREMB_CODIGO, '')||'</CREMB_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRMOV_CODIGO>'||COALESCE(OLD.CRMOV_CODIGO, '')||'</CRMOV_CODIGO>'||
			'<CARPT_CODIGO>'||COALESCE(OLD.CARPT_CODIGO, '')||'</CARPT_CODIGO>'||
			'<CGG_CARPT_CODIGO>'||COALESCE(OLD.CGG_CARPT_CODIGO, '')||'</CGG_CARPT_CODIGO>'||
			'<CRALN_CODIGO>'||COALESCE(OLD.CRALN_CODIGO, '')||'</CRALN_CODIGO>'||
			'<CRALN_DESCRIPCION_VUELO>'||COALESCE(OLD.CRALN_DESCRIPCION_VUELO, '')||'</CRALN_DESCRIPCION_VUELO>'||
			'<CRMVI_FECHA_VIAJE>'||COALESCE(OLD.CRMVI_FECHA_VIAJE, CURRENT_TIMESTAMP)||'</CRMVI_FECHA_VIAJE>'||
			'<CRMVI_TIPO_OPERACION>'||COALESCE(OLD.CRMVI_TIPO_OPERACION, 0)||'</CRMVI_TIPO_OPERACION>'||
			'<CRMVI_OBSERVACION>'||COALESCE(OLD.CRMVI_OBSERVACION, '')||'</CRMVI_OBSERVACION>'||
			'<CRMVI_ESTADO>'||COALESCE(OLD.CRMVI_ESTADO, FALSE)||'</CRMVI_ESTADO>'||
			'<CRMVI_FECHA_INSERT>'||COALESCE(OLD.CRMVI_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRMVI_FECHA_INSERT>'||
			'<CRMVI_USUARIO_INSERT>'||COALESCE(OLD.CRMVI_USUARIO_INSERT, '')||'</CRMVI_USUARIO_INSERT>'||
			'<CRMVI_FECHA_UPDATE>'||COALESCE(OLD.CRMVI_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRMVI_FECHA_UPDATE>'||
			'<CRMVI_USUARIO_UPDATE>'||COALESCE(OLD.CRMVI_USUARIO_UPDATE, '')||'</CRMVI_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRMVI_CODIGO>'||COALESCE(NEW.CRMVI_CODIGO, '')||'</CRMVI_CODIGO>'||
			'<CRMLE_CODIGO>'||COALESCE(NEW.CRMLE_CODIGO, '')||'</CRMLE_CODIGO>'||
			'<CGG_CRMLE_CODIGO>'||COALESCE(NEW.CGG_CRMLE_CODIGO, '')||'</CGG_CRMLE_CODIGO>'||
			'<CREMB_CODIGO>'||COALESCE(NEW.CREMB_CODIGO, '')||'</CREMB_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CRMOV_CODIGO>'||COALESCE(NEW.CRMOV_CODIGO, '')||'</CRMOV_CODIGO>'||
			'<CARPT_CODIGO>'||COALESCE(NEW.CARPT_CODIGO, '')||'</CARPT_CODIGO>'||
			'<CGG_CARPT_CODIGO>'||COALESCE(NEW.CGG_CARPT_CODIGO, '')||'</CGG_CARPT_CODIGO>'||
			'<CRALN_CODIGO>'||COALESCE(NEW.CRALN_CODIGO, '')||'</CRALN_CODIGO>'||
			'<CRALN_DESCRIPCION_VUELO>'||COALESCE(NEW.CRALN_DESCRIPCION_VUELO, '')||'</CRALN_DESCRIPCION_VUELO>'||
			'<CRMVI_FECHA_VIAJE>'||COALESCE(NEW.CRMVI_FECHA_VIAJE, CURRENT_TIMESTAMP)||'</CRMVI_FECHA_VIAJE>'||
			'<CRMVI_TIPO_OPERACION>'||COALESCE(NEW.CRMVI_TIPO_OPERACION, 0)||'</CRMVI_TIPO_OPERACION>'||
			'<CRMVI_OBSERVACION>'||COALESCE(NEW.CRMVI_OBSERVACION, '')||'</CRMVI_OBSERVACION>'||
			'<CRMVI_ESTADO>'||COALESCE(NEW.CRMVI_ESTADO, FALSE)||'</CRMVI_ESTADO>'||
			'<CRMVI_FECHA_INSERT>'||COALESCE(NEW.CRMVI_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRMVI_FECHA_INSERT>'||
			'<CRMVI_USUARIO_INSERT>'||COALESCE(NEW.CRMVI_USUARIO_INSERT, '')||'</CRMVI_USUARIO_INSERT>'||
			'<CRMVI_FECHA_UPDATE>'||COALESCE(NEW.CRMVI_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRMVI_FECHA_UPDATE>'||
			'<CRMVI_USUARIO_UPDATE>'||COALESCE(NEW.CRMVI_USUARIO_UPDATE, '')||'</CRMVI_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_MOVILIDAD_INTERNA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_MOVILIDAD_INTERNA
*/
CREATE TRIGGER TRG_AUD_CGG_RES_MOVILIDAD_INTERNA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_MOVILIDAD_INTERNA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_MOVILIDAD_INTERNA();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_MOVILIDAD_INTERNA ON SII.CGG_RES_MOVILIDAD_INTERNA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_MOVILIDAD_INTERNA DISABLE TRIGGER TRG_AUD_CGG_RES_MOVILIDAD_INTERNA;

