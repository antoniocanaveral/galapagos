/**
* FUNCION SII.TRG_KG_CGG_RES_COMUNICADO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_COMUNICADO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_RES_COMUNICADO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CRCOM_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRCOM_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CRCOM_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CRCOM_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CRCOM_CODIGO>'||COALESCE(OLD.CRCOM_CODIGO, '')||'</CRCOM_CODIGO>'||
			'<CRRES_CODIGO>'||COALESCE(OLD.CRRES_CODIGO, '')||'</CRRES_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGG_CRPER_CODIGO>'||COALESCE(OLD.CGG_CRPER_CODIGO, '')||'</CGG_CRPER_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(OLD.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRCOM_DESTINATARIO>'||COALESCE(OLD.CRCOM_DESTINATARIO, '')||'</CRCOM_DESTINATARIO>'||
			'<CRCOM_ASUNTO>'||COALESCE(OLD.CRCOM_ASUNTO, '')||'</CRCOM_ASUNTO>'||
			'<CRCOM_FECHA_RECEPCION>'||COALESCE(OLD.CRCOM_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CRCOM_FECHA_RECEPCION>'||
			'<CRCOM_FECHA_REVISION>'||COALESCE(OLD.CRCOM_FECHA_REVISION, CURRENT_TIMESTAMP)||'</CRCOM_FECHA_REVISION>'||
			'<CRCOM_NUMERO_DOCUMENTO>'||COALESCE(OLD.CRCOM_NUMERO_DOCUMENTO, '')||'</CRCOM_NUMERO_DOCUMENTO>'||
			'<CRCOM_DESCRIPCION>'||COALESCE(OLD.CRCOM_DESCRIPCION, '')||'</CRCOM_DESCRIPCION>'||
			'<CRCOM_OBSERVACION>'||COALESCE(OLD.CRCOM_OBSERVACION, '')||'</CRCOM_OBSERVACION>'||
			'<CRCOM_DESCRIPCION_ADJUNTO>'||COALESCE(OLD.CRCOM_DESCRIPCION_ADJUNTO, '')||'</CRCOM_DESCRIPCION_ADJUNTO>'||
			'<CRCOM_NOMBRE_ADJUNTO>'||COALESCE(OLD.CRCOM_NOMBRE_ADJUNTO, '')||'</CRCOM_NOMBRE_ADJUNTO>'||
			'<CRCOM_REMITENTE>'||COALESCE(OLD.CRCOM_REMITENTE, '')||'</CRCOM_REMITENTE>'||
			'<CRCOM_ADJUNTO>'||COALESCE(encode(OLD.CRCOM_ADJUNTO, 'base64'), '')||'</CRCOM_ADJUNTO>'||
			'<CRCOM_ESTADO_COMUNICADO>'||COALESCE(OLD.CRCOM_ESTADO_COMUNICADO, 0)||'</CRCOM_ESTADO_COMUNICADO>'||
			'<CRCOM_ESTADO>'||COALESCE(OLD.CRCOM_ESTADO, FALSE)||'</CRCOM_ESTADO>'||
			'<CRCOM_FECHA_INSERT>'||COALESCE(OLD.CRCOM_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRCOM_FECHA_INSERT>'||
			'<CRCOM_USUARIO_INSERT>'||COALESCE(OLD.CRCOM_USUARIO_INSERT, '')||'</CRCOM_USUARIO_INSERT>'||
			'<CRCOM_FECHA_UPDATE>'||COALESCE(OLD.CRCOM_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRCOM_FECHA_UPDATE>'||
			'<CRCOM_USUARIO_UPDATE>'||COALESCE(OLD.CRCOM_USUARIO_UPDATE, '')||'</CRCOM_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRCOM_CODIGO>'||COALESCE(NEW.CRCOM_CODIGO, '')||'</CRCOM_CODIGO>'||
			'<CRRES_CODIGO>'||COALESCE(NEW.CRRES_CODIGO, '')||'</CRRES_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGG_CRPER_CODIGO>'||COALESCE(NEW.CGG_CRPER_CODIGO, '')||'</CGG_CRPER_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRCOM_DESTINATARIO>'||COALESCE(NEW.CRCOM_DESTINATARIO, '')||'</CRCOM_DESTINATARIO>'||
			'<CRCOM_ASUNTO>'||COALESCE(NEW.CRCOM_ASUNTO, '')||'</CRCOM_ASUNTO>'||
			'<CRCOM_FECHA_RECEPCION>'||COALESCE(NEW.CRCOM_FECHA_RECEPCION, CURRENT_TIMESTAMP)||'</CRCOM_FECHA_RECEPCION>'||
			'<CRCOM_FECHA_REVISION>'||COALESCE(NEW.CRCOM_FECHA_REVISION, CURRENT_TIMESTAMP)||'</CRCOM_FECHA_REVISION>'||
			'<CRCOM_NUMERO_DOCUMENTO>'||COALESCE(NEW.CRCOM_NUMERO_DOCUMENTO, '')||'</CRCOM_NUMERO_DOCUMENTO>'||
			'<CRCOM_DESCRIPCION>'||COALESCE(NEW.CRCOM_DESCRIPCION, '')||'</CRCOM_DESCRIPCION>'||
			'<CRCOM_OBSERVACION>'||COALESCE(NEW.CRCOM_OBSERVACION, '')||'</CRCOM_OBSERVACION>'||
			'<CRCOM_DESCRIPCION_ADJUNTO>'||COALESCE(NEW.CRCOM_DESCRIPCION_ADJUNTO, '')||'</CRCOM_DESCRIPCION_ADJUNTO>'||
			'<CRCOM_NOMBRE_ADJUNTO>'||COALESCE(NEW.CRCOM_NOMBRE_ADJUNTO, '')||'</CRCOM_NOMBRE_ADJUNTO>'||
			'<CRCOM_REMITENTE>'||COALESCE(NEW.CRCOM_REMITENTE, '')||'</CRCOM_REMITENTE>'||
			'<CRCOM_ADJUNTO>'||COALESCE(encode(NEW.CRCOM_ADJUNTO, 'base64'), '')||'</CRCOM_ADJUNTO>'||
			'<CRCOM_ESTADO_COMUNICADO>'||COALESCE(NEW.CRCOM_ESTADO_COMUNICADO, 0)||'</CRCOM_ESTADO_COMUNICADO>'||
			'<CRCOM_ESTADO>'||COALESCE(NEW.CRCOM_ESTADO, FALSE)||'</CRCOM_ESTADO>'||
			'<CRCOM_FECHA_INSERT>'||COALESCE(NEW.CRCOM_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRCOM_FECHA_INSERT>'||
			'<CRCOM_USUARIO_INSERT>'||COALESCE(NEW.CRCOM_USUARIO_INSERT, '')||'</CRCOM_USUARIO_INSERT>'||
			'<CRCOM_FECHA_UPDATE>'||COALESCE(NEW.CRCOM_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRCOM_FECHA_UPDATE>'||
			'<CRCOM_USUARIO_UPDATE>'||COALESCE(NEW.CRCOM_USUARIO_UPDATE, '')||'</CRCOM_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_COMUNICADO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_COMUNICADO
*/
CREATE TRIGGER TRG_AUD_CGG_RES_COMUNICADO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_COMUNICADO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_COMUNICADO();
COMMENT ON TRIGGER TRG_AUD_CGG_RES_COMUNICADO ON SII.CGG_RES_COMUNICADO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_RES_COMUNICADO DISABLE TRIGGER TRG_AUD_CGG_RES_COMUNICADO;

