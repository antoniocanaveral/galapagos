/**
* FUNCION SII.TRG_KG_CGG_JUR_PROCESO_JUDICIAL. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_PROCESO_JUDICIAL
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_JUR_PROCESO_JUDICIAL() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CJPJU_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJPJU_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CJPJU_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CJPJU_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CJPJU_CODIGO>'||COALESCE(OLD.CJPJU_CODIGO, '')||'</CJPJU_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(OLD.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(OLD.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(OLD.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CJPJU_NUMERO>'||COALESCE(OLD.CJPJU_NUMERO, '')||'</CJPJU_NUMERO>'||
			'<CJPJU_TIPO_PROCESO>'||COALESCE(OLD.CJPJU_TIPO_PROCESO, 0)||'</CJPJU_TIPO_PROCESO>'||
			'<CJPJU_TIPO_ADVERSARIO>'||COALESCE(OLD.CJPJU_TIPO_ADVERSARIO, 0)||'</CJPJU_TIPO_ADVERSARIO>'||
			'<CJPJU_FECHA_INICIO>'||COALESCE(OLD.CJPJU_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CJPJU_FECHA_INICIO>'||
			'<CJPJU_FECHA_FINALIZACION>'||COALESCE(OLD.CJPJU_FECHA_FINALIZACION, CURRENT_TIMESTAMP)||'</CJPJU_FECHA_FINALIZACION>'||
			'<CJPJU_ESTADO_EJECUCION>'||COALESCE(OLD.CJPJU_ESTADO_EJECUCION, 0)||'</CJPJU_ESTADO_EJECUCION>'||
			'<CJPJU_RESPONSABLE>'||COALESCE(OLD.CJPJU_RESPONSABLE, '')||'</CJPJU_RESPONSABLE>'||
			'<CJPJU_OBSERVACION>'||COALESCE(OLD.CJPJU_OBSERVACION, '')||'</CJPJU_OBSERVACION>'||
			'<CJPJU_ESTADO>'||COALESCE(OLD.CJPJU_ESTADO, FALSE)||'</CJPJU_ESTADO>'||
			'<CJPJU_FECHA_INSERT>'||COALESCE(OLD.CJPJU_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJPJU_FECHA_INSERT>'||
			'<CJPJU_USUARIO_INSERT>'||COALESCE(OLD.CJPJU_USUARIO_INSERT, '')||'</CJPJU_USUARIO_INSERT>'||
			'<CJPJU_FECHA_UPDATE>'||COALESCE(OLD.CJPJU_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJPJU_FECHA_UPDATE>'||
			'<CJPJU_USUARIO_UPDATE>'||COALESCE(OLD.CJPJU_USUARIO_UPDATE, '')||'</CJPJU_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CJPJU_CODIGO>'||COALESCE(NEW.CJPJU_CODIGO, '')||'</CJPJU_CODIGO>'||
			'<CCTN_CODIGO>'||COALESCE(NEW.CCTN_CODIGO, '')||'</CCTN_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CJPJU_NUMERO>'||COALESCE(NEW.CJPJU_NUMERO, '')||'</CJPJU_NUMERO>'||
			'<CJPJU_TIPO_PROCESO>'||COALESCE(NEW.CJPJU_TIPO_PROCESO, 0)||'</CJPJU_TIPO_PROCESO>'||
			'<CJPJU_TIPO_ADVERSARIO>'||COALESCE(NEW.CJPJU_TIPO_ADVERSARIO, 0)||'</CJPJU_TIPO_ADVERSARIO>'||
			'<CJPJU_FECHA_INICIO>'||COALESCE(NEW.CJPJU_FECHA_INICIO, CURRENT_TIMESTAMP)||'</CJPJU_FECHA_INICIO>'||
			'<CJPJU_FECHA_FINALIZACION>'||COALESCE(NEW.CJPJU_FECHA_FINALIZACION, CURRENT_TIMESTAMP)||'</CJPJU_FECHA_FINALIZACION>'||
			'<CJPJU_ESTADO_EJECUCION>'||COALESCE(NEW.CJPJU_ESTADO_EJECUCION, 0)||'</CJPJU_ESTADO_EJECUCION>'||
			'<CJPJU_RESPONSABLE>'||COALESCE(NEW.CJPJU_RESPONSABLE, '')||'</CJPJU_RESPONSABLE>'||
			'<CJPJU_OBSERVACION>'||COALESCE(NEW.CJPJU_OBSERVACION, '')||'</CJPJU_OBSERVACION>'||
			'<CJPJU_ESTADO>'||COALESCE(NEW.CJPJU_ESTADO, FALSE)||'</CJPJU_ESTADO>'||
			'<CJPJU_FECHA_INSERT>'||COALESCE(NEW.CJPJU_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CJPJU_FECHA_INSERT>'||
			'<CJPJU_USUARIO_INSERT>'||COALESCE(NEW.CJPJU_USUARIO_INSERT, '')||'</CJPJU_USUARIO_INSERT>'||
			'<CJPJU_FECHA_UPDATE>'||COALESCE(NEW.CJPJU_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CJPJU_FECHA_UPDATE>'||
			'<CJPJU_USUARIO_UPDATE>'||COALESCE(NEW.CJPJU_USUARIO_UPDATE, '')||'</CJPJU_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_JUR_PROCESO_JUDICIAL. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_JUR_PROCESO_JUDICIAL
*/
CREATE TRIGGER TRG_AUD_CGG_JUR_PROCESO_JUDICIAL AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_JUR_PROCESO_JUDICIAL FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_JUR_PROCESO_JUDICIAL();
COMMENT ON TRIGGER TRG_AUD_CGG_JUR_PROCESO_JUDICIAL ON SII.CGG_JUR_PROCESO_JUDICIAL IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_JUR_PROCESO_JUDICIAL DISABLE TRIGGER TRG_AUD_CGG_JUR_PROCESO_JUDICIAL;

