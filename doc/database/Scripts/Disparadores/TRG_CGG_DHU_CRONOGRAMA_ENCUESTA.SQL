/**
* FUNCION SII.TRG_KG_CGG_DHU_CRONOGRAMA_ENCUESTA. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_CRONOGRAMA_ENCUESTA
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_DHU_CRONOGRAMA_ENCUESTA() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CDCRE_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDCRE_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CDCRE_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CDCRE_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CDCRE_CODIGO>'||COALESCE(OLD.CDCRE_CODIGO, '')||'</CDCRE_CODIGO>'||
			'<CDENC_CODIGO>'||COALESCE(OLD.CDENC_CODIGO, '')||'</CDENC_CODIGO>'||
			'<CDCUR_CODIGO>'||COALESCE(OLD.CDCUR_CODIGO, '')||'</CDCUR_CODIGO>'||
			'<CISLA_CODIGO>'||COALESCE(OLD.CISLA_CODIGO, '')||'</CISLA_CODIGO>'||
			'<CDCRE_DESCRIPCION>'||COALESCE(OLD.CDCRE_DESCRIPCION, '')||'</CDCRE_DESCRIPCION>'||
			'<CDCRE_FECHA_EJECUCION>'||COALESCE(OLD.CDCRE_FECHA_EJECUCION, CURRENT_TIMESTAMP)||'</CDCRE_FECHA_EJECUCION>'||
			'<CDCRE_FECHA_MAXIMA_REALIZACION>'||COALESCE(OLD.CDCRE_FECHA_MAXIMA_REALIZACION, CURRENT_TIMESTAMP)||'</CDCRE_FECHA_MAXIMA_REALIZACION>'||
			'<CDCRE_ESTADO>'||COALESCE(OLD.CDCRE_ESTADO, FALSE)||'</CDCRE_ESTADO>'||
			'<CDCRE_FECHA_INSERT>'||COALESCE(OLD.CDCRE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDCRE_FECHA_INSERT>'||
			'<CDCRE_USUARIO_INSERT>'||COALESCE(OLD.CDCRE_USUARIO_INSERT, '')||'</CDCRE_USUARIO_INSERT>'||
			'<CDCRE_FECHA_UPDATE>'||COALESCE(OLD.CDCRE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDCRE_FECHA_UPDATE>'||
			'<CDCRE_USUARIO_UPDATE>'||COALESCE(OLD.CDCRE_USUARIO_UPDATE, '')||'</CDCRE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CDCRE_CODIGO>'||COALESCE(NEW.CDCRE_CODIGO, '')||'</CDCRE_CODIGO>'||
			'<CDENC_CODIGO>'||COALESCE(NEW.CDENC_CODIGO, '')||'</CDENC_CODIGO>'||
			'<CDCUR_CODIGO>'||COALESCE(NEW.CDCUR_CODIGO, '')||'</CDCUR_CODIGO>'||
			'<CISLA_CODIGO>'||COALESCE(NEW.CISLA_CODIGO, '')||'</CISLA_CODIGO>'||
			'<CDCRE_DESCRIPCION>'||COALESCE(NEW.CDCRE_DESCRIPCION, '')||'</CDCRE_DESCRIPCION>'||
			'<CDCRE_FECHA_EJECUCION>'||COALESCE(NEW.CDCRE_FECHA_EJECUCION, CURRENT_TIMESTAMP)||'</CDCRE_FECHA_EJECUCION>'||
			'<CDCRE_FECHA_MAXIMA_REALIZACION>'||COALESCE(NEW.CDCRE_FECHA_MAXIMA_REALIZACION, CURRENT_TIMESTAMP)||'</CDCRE_FECHA_MAXIMA_REALIZACION>'||
			'<CDCRE_ESTADO>'||COALESCE(NEW.CDCRE_ESTADO, FALSE)||'</CDCRE_ESTADO>'||
			'<CDCRE_FECHA_INSERT>'||COALESCE(NEW.CDCRE_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CDCRE_FECHA_INSERT>'||
			'<CDCRE_USUARIO_INSERT>'||COALESCE(NEW.CDCRE_USUARIO_INSERT, '')||'</CDCRE_USUARIO_INSERT>'||
			'<CDCRE_FECHA_UPDATE>'||COALESCE(NEW.CDCRE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CDCRE_FECHA_UPDATE>'||
			'<CDCRE_USUARIO_UPDATE>'||COALESCE(NEW.CDCRE_USUARIO_UPDATE, '')||'</CDCRE_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_DHU_CRONOGRAMA_ENCUESTA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_DHU_CRONOGRAMA_ENCUESTA
*/
CREATE TRIGGER TRG_AUD_CGG_DHU_CRONOGRAMA_ENCUESTA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_DHU_CRONOGRAMA_ENCUESTA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_DHU_CRONOGRAMA_ENCUESTA();
COMMENT ON TRIGGER TRG_AUD_CGG_DHU_CRONOGRAMA_ENCUESTA ON SII.CGG_DHU_CRONOGRAMA_ENCUESTA IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_DHU_CRONOGRAMA_ENCUESTA DISABLE TRIGGER TRG_AUD_CGG_DHU_CRONOGRAMA_ENCUESTA;

