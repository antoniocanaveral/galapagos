CREATE TRIGGER TRG_AUD_CGG_RES_DEPOSITO_GARANTIA AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_RES_DEPOSITO_GARANTIA FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_RES_DEPOSITO_GARANTIA();
