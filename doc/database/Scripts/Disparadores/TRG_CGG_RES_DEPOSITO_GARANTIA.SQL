			'<CRDPT_CADUCA>'||COALESCE(OLD.CRDPT_CADUCA, FALSE)||'</CRDPT_CADUCA>'||
			'<CRDPT_TIPO>'||COALESCE(OLD.CRDPT_TIPO, 0)||'</CRDPT_TIPO>'||
			'<CRDPT_VALOR_DEPOSITO>'||COALESCE(OLD.CRDPT_VALOR_DEPOSITO, 0)||'</CRDPT_VALOR_DEPOSITO>'||
			'<CRDPT_FECHA_DEPOSITO>'||COALESCE(OLD.CRDPT_FECHA_DEPOSITO, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_DEPOSITO>'||
			'<CRDPT_FECHA_ENTREGA>'||COALESCE(OLD.CRDPT_FECHA_ENTREGA, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_ENTREGA>'||
			'<CRDPT_FECHA_CADUCIDAD>'||COALESCE(OLD.CRDPT_FECHA_CADUCIDAD, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_CADUCIDAD>'||
			'<CRDPT_FECHA_CONTABILIZACION>'||COALESCE(OLD.CRDPT_FECHA_CONTABILIZACION, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_CONTABILIZACION>'||
