			'<CRDPT_FECHA_DEVOLUCION_DEFINITI>'||COALESCE(OLD.CRDPT_FECHA_DEVOLUCION_DEFINITI, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_DEVOLUCION_DEFINITI>'||
			'<CRDPT_DEVUELTO>'||COALESCE(OLD.CRDPT_DEVUELTO, FALSE)||'</CRDPT_DEVUELTO>'||
			'<CRDPT_ESTADO_DEPOSITO>'||COALESCE(OLD.CRDPT_ESTADO_DEPOSITO, 0)||'</CRDPT_ESTADO_DEPOSITO>'||
			'<CRDPT_OBSERVACION>'||COALESCE(OLD.CRDPT_OBSERVACION, '')||'</CRDPT_OBSERVACION>'||
			'<CRDPT_ESTADO>'||COALESCE(OLD.CRDPT_ESTADO, FALSE)||'</CRDPT_ESTADO>'||
			'<CRDPT_FECHA_INSERT>'||COALESCE(OLD.CRDPT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_INSERT>'||
			'<CRDPT_USUARIO_INSERT>'||COALESCE(OLD.CRDPT_USUARIO_INSERT, '')||'</CRDPT_USUARIO_INSERT>'||
			'<CRDPT_FECHA_UPDATE>'||COALESCE(OLD.CRDPT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_UPDATE>'||
			'<CRDPT_USUARIO_UPDATE>'||COALESCE(OLD.CRDPT_USUARIO_UPDATE, '')||'</CRDPT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CRDPT_CODIGO>'||COALESCE(NEW.CRDPT_CODIGO, '')||'</CRDPT_CODIGO>'||
			'<CRCPJ_CODIGO>'||COALESCE(NEW.CRCPJ_CODIGO, '')||'</CRCPJ_CODIGO>'||
			'<CRPJR_CODIGO>'||COALESCE(NEW.CRPJR_CODIGO, '')||'</CRPJR_CODIGO>'||
			'<CRPER_CODIGO>'||COALESCE(NEW.CRPER_CODIGO, '')||'</CRPER_CODIGO>'||
			'<CGG_CRPJR_CODIGO>'||COALESCE(NEW.CGG_CRPJR_CODIGO, '')||'</CGG_CRPJR_CODIGO>'||
