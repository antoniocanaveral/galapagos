			'<CRDPT_DEVUELTO>'||COALESCE(NEW.CRDPT_DEVUELTO, FALSE)||'</CRDPT_DEVUELTO>'||
			'<CRDPT_ESTADO_DEPOSITO>'||COALESCE(NEW.CRDPT_ESTADO_DEPOSITO, 0)||'</CRDPT_ESTADO_DEPOSITO>'||
			'<CRDPT_OBSERVACION>'||COALESCE(NEW.CRDPT_OBSERVACION, '')||'</CRDPT_OBSERVACION>'||
			'<CRDPT_ESTADO>'||COALESCE(NEW.CRDPT_ESTADO, FALSE)||'</CRDPT_ESTADO>'||
			'<CRDPT_FECHA_INSERT>'||COALESCE(NEW.CRDPT_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_INSERT>'||
			'<CRDPT_USUARIO_INSERT>'||COALESCE(NEW.CRDPT_USUARIO_INSERT, '')||'</CRDPT_USUARIO_INSERT>'||
			'<CRDPT_FECHA_UPDATE>'||COALESCE(NEW.CRDPT_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_UPDATE>'||
			'<CRDPT_USUARIO_UPDATE>'||COALESCE(NEW.CRDPT_USUARIO_UPDATE, '')||'</CRDPT_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_RES_DEPOSITO_GARANTIA. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_RES_DEPOSITO_GARANTIA
