			'<CRDPT_TIPO>'||COALESCE(NEW.CRDPT_TIPO, 0)||'</CRDPT_TIPO>'||
			'<CRDPT_VALOR_DEPOSITO>'||COALESCE(NEW.CRDPT_VALOR_DEPOSITO, 0)||'</CRDPT_VALOR_DEPOSITO>'||
			'<CRDPT_FECHA_DEPOSITO>'||COALESCE(NEW.CRDPT_FECHA_DEPOSITO, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_DEPOSITO>'||
			'<CRDPT_FECHA_ENTREGA>'||COALESCE(NEW.CRDPT_FECHA_ENTREGA, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_ENTREGA>'||
			'<CRDPT_FECHA_CADUCIDAD>'||COALESCE(NEW.CRDPT_FECHA_CADUCIDAD, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_CADUCIDAD>'||
			'<CRDPT_FECHA_CONTABILIZACION>'||COALESCE(NEW.CRDPT_FECHA_CONTABILIZACION, CURRENT_TIMESTAMP)||'</CRDPT_FECHA_CONTABILIZACION>'||
