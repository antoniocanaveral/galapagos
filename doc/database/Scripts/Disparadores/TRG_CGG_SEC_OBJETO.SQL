/**
* FUNCION SII.TRG_KG_CGG_SEC_OBJETO. REGISTRA LA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SEC_OBJETO
*/
CREATE OR REPLACE FUNCTION SII.TRG_AUD_CGG_SEC_OBJETO() RETURNS TRIGGER AS
$$
DECLARE
TMP_CUSU_CODIGO VARCHAR;
TMP_IP VARCHAR;
TMP_SESSION_ID VARCHAR;
TMP_DATO_NUEVO VARCHAR;
TMP_DATO_ANTIGUO VARCHAR;
TMP_USUARIO VARCHAR;
TMP_CODIGO VARCHAR;
BEGIN
	TMP_DATO_ANTIGUO = '';
	TMP_DATO_NUEVO = '';
	IF (TG_OP = 'INSERT') THEN
		TMP_USUARIO = NEW.CSOBJ_USUARIO_INSERT;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSOBJ_USUARIO_INSERT;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
	ELSIF (TG_OP = 'UPDATE') THEN
		TMP_USUARIO = NEW.CSOBJ_USUARIO_UPDATE;
		SELECT CUSU_CODIGO INTO TMP_CUSU_CODIGO FROM SII.CGG_USUARIO WHERE CUSU_NOMBRE_USUARIO = NEW.CSOBJ_USUARIO_UPDATE;
		SELECT CSSSN_IP, CSSSN_ID INTO TMP_IP, TMP_SESSION_ID FROM SII.CGG_SEC_SESION WHERE CUSU_CODIGO = TMP_CUSU_CODIGO AND CSSSN_ACTIVO = TRUE;
		TMP_DATO_ANTIGUO = '<'||TG_TABLE_NAME||'>'||
			'<CSOBJ_CODIGO>'||COALESCE(OLD.CSOBJ_CODIGO, '')||'</CSOBJ_CODIGO>'||
			'<CSOBJ_NOMBRE>'||COALESCE(OLD.CSOBJ_NOMBRE, '')||'</CSOBJ_NOMBRE>'||
			'<CSOBJ_RUTA>'||COALESCE(OLD.CSOBJ_RUTA, '')||'</CSOBJ_RUTA>'||
			'<CSOBJ_DESCRIPCION>'||COALESCE(OLD.CSOBJ_DESCRIPCION, '')||'</CSOBJ_DESCRIPCION>'||
			'<CSOBJ_TIPO>'||COALESCE(OLD.CSOBJ_TIPO, 0)||'</CSOBJ_TIPO>'||
			'<CSOBJ_ESTADO>'||COALESCE(OLD.CSOBJ_ESTADO, FALSE)||'</CSOBJ_ESTADO>'||
			'<CSOBJ_FECHA_INSERT>'||COALESCE(OLD.CSOBJ_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSOBJ_FECHA_INSERT>'||
			'<CSOBJ_USUARIO_INSERT>'||COALESCE(OLD.CSOBJ_USUARIO_INSERT, '')||'</CSOBJ_USUARIO_INSERT>'||
			'<CSOBJ_FECHA_UPDATE>'||COALESCE(OLD.CSOBJ_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSOBJ_FECHA_UPDATE>'||
			'<CSOBJ_USUARIO_UPDATE>'||COALESCE(OLD.CSOBJ_USUARIO_UPDATE, '')||'</CSOBJ_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	ELSIF (TG_OP = 'DELETE') THEN
		SELECT INET_CLIENT_ADDR() INTO TMP_IP;
		SELECT SESSION_USER INTO TMP_SESSION_ID;
	END IF;

	IF (TG_OP = 'INSERT' OR TG_OP = 'UPDATE') THEN
		TMP_DATO_NUEVO = '<'||TG_TABLE_NAME||'>'||
			'<CSOBJ_CODIGO>'||COALESCE(NEW.CSOBJ_CODIGO, '')||'</CSOBJ_CODIGO>'||
			'<CSOBJ_NOMBRE>'||COALESCE(NEW.CSOBJ_NOMBRE, '')||'</CSOBJ_NOMBRE>'||
			'<CSOBJ_RUTA>'||COALESCE(NEW.CSOBJ_RUTA, '')||'</CSOBJ_RUTA>'||
			'<CSOBJ_DESCRIPCION>'||COALESCE(NEW.CSOBJ_DESCRIPCION, '')||'</CSOBJ_DESCRIPCION>'||
			'<CSOBJ_TIPO>'||COALESCE(NEW.CSOBJ_TIPO, 0)||'</CSOBJ_TIPO>'||
			'<CSOBJ_ESTADO>'||COALESCE(NEW.CSOBJ_ESTADO, FALSE)||'</CSOBJ_ESTADO>'||
			'<CSOBJ_FECHA_INSERT>'||COALESCE(NEW.CSOBJ_FECHA_INSERT, CURRENT_TIMESTAMP)||'</CSOBJ_FECHA_INSERT>'||
			'<CSOBJ_USUARIO_INSERT>'||COALESCE(NEW.CSOBJ_USUARIO_INSERT, '')||'</CSOBJ_USUARIO_INSERT>'||
			'<CSOBJ_FECHA_UPDATE>'||COALESCE(NEW.CSOBJ_FECHA_UPDATE, CURRENT_TIMESTAMP)||'</CSOBJ_FECHA_UPDATE>'||
			'<CSOBJ_USUARIO_UPDATE>'||COALESCE(NEW.CSOBJ_USUARIO_UPDATE, '')||'</CSOBJ_USUARIO_UPDATE>'||
		'</'||TG_TABLE_NAME||'>';
	END IF;

	INSERT INTO SII.CGG_SEC_AUDITORIA(
		CSADT_CODIGO,
		CSADT_FECHA,
		CSADT_USUARIO,
		CSADT_IP,
		CSADT_SESION,
		CSADT_TABLA,
		CSADT_OPERACION,
		CSADT_DATO_ANTIGUO,
		CSADT_DATO_NUEVO
	) VALUES (
		('CSADT'||NEXTVAL('scn_cgg_sec_auditoria'))::VARCHAR,
		CURRENT_TIMESTAMP,
		TMP_USUARIO,
		TMP_IP,
		TMP_SESSION_ID,
		TG_TABLE_NAME,
		TG_OP,
		TMP_DATO_ANTIGUO,
		TMP_DATO_NUEVO
	);
	RETURN NULL;
END;
$$
LANGUAGE plpgsql;

/**
* DISPARADOR TRG_KG_CGG_SEC_OBJETO. REGISTRA UNA BITACORA DE LAS ACTIVIDADES REALIZADAS SOBRE LA INFORMACION ALMACENADA EN LA TABLA SII.CGG_SEC_OBJETO
*/
CREATE TRIGGER TRG_AUD_CGG_SEC_OBJETO AFTER INSERT OR UPDATE OR DELETE ON SII.CGG_SEC_OBJETO FOR EACH ROW EXECUTE PROCEDURE SII.TRG_AUD_CGG_SEC_OBJETO();
COMMENT ON TRIGGER TRG_AUD_CGG_SEC_OBJETO ON SII.CGG_SEC_OBJETO IS 'DISPARADOR DE AUDITORIA DE LOS DATOS';
ALTER TABLE SII.CGG_SEC_OBJETO DISABLE TRIGGER TRG_AUD_CGG_SEC_OBJETO;

