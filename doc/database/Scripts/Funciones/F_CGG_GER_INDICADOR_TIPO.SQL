/**
* FUNCION SII.F_CGG_GER_INDICADOR_TIPO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_GER_INDICADOR_TIPO
* @param IN_CGGIT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CGGIT_DESCRIPCION DESCRIPCION DEL TIPO DE INDICADOR
* @param IN_CGGIT_ESTADO ESTADO DEL REGISTRO
* @param IN_CGGIT_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CGGIT_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_GER_INDICADOR_TIPO_INSERT(
IN IN_CGGIT_CODIGO SII.CGG_GER_INDICADOR_TIPO.CGGIT_CODIGO%TYPE,
IN IN_CGGIT_DESCRIPCION SII.CGG_GER_INDICADOR_TIPO.CGGIT_DESCRIPCION%TYPE,
IN IN_CGGIT_ESTADO SII.CGG_GER_INDICADOR_TIPO.CGGIT_ESTADO%TYPE,
IN IN_CGGIT_USUARIO_INSERT SII.CGG_GER_INDICADOR_TIPO.CGGIT_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CGGIT_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_GER_INDICADOR_TIPO','CGGIT_CODIGO','CGGIT') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CGGIT_CODIGO;
	END IF;
	INSERT INTO SII.CGG_GER_INDICADOR_TIPO(
		CGGIT_CODIGO, 
		CGGIT_DESCRIPCION, 
		CGGIT_ESTADO, 
		CGGIT_FECHA_INSERT, 
		CGGIT_USUARIO_INSERT, 
		CGGIT_FECHA_UPDATE, 
		CGGIT_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CGGIT_DESCRIPCION, 
		IN_CGGIT_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CGGIT_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CGGIT_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

