IN IN_CKUPV_CODIGO SII.CGG_KDX_USUARIO_PV.CKUPV_CODIGO%TYPE,
IN IN_CKPVT_CODIGO SII.CGG_KDX_USUARIO_PV.CKPVT_CODIGO%TYPE,
