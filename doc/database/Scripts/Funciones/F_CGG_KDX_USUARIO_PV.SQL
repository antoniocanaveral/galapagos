		OUT_KEY, 
		IN_CKPVT_CODIGO, 
		IN_CUSU_CODIGO, 
		IN_CKPVT_ACTIVO, 
		IN_CKUPV_SUPERVISOR, 
		IN_CKUPV_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CKUPV_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CKUPV_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
