/**
* FUNCION SII.F_CGG_RES_TRAMITE_COMITE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_TRAMITE_COMITE
* @param IN_CRTRC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TRAMITE COMITE
* @param IN_CRTST_CODIGO IDENTIFICATIVO UNICO DE REGISTRO TIPO SOLICITUD
* @param IN_CRCOM_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CRTRC_ESTADO ESTADO DEL REGISTRO
* @param IN_CRTRC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRTRC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TRAMITE_COMITE_INSERT(
IN IN_CRTRC_CODIGO SII.CGG_RES_TRAMITE_COMITE.CRTRC_CODIGO%TYPE, 
IN IN_CRTST_CODIGO SII.CGG_RES_TRAMITE_COMITE.CRTST_CODIGO%TYPE, 
IN IN_CRCOM_CODIGO SII.CGG_RES_TRAMITE_COMITE.CRCOM_CODIGO%TYPE, 
IN IN_CRTRC_ESTADO SII.CGG_RES_TRAMITE_COMITE.CRTRC_ESTADO%TYPE, 
IN IN_CRTRC_USUARIO_INSERT SII.CGG_RES_TRAMITE_COMITE.CRTRC_USUARIO_INSERT%TYPE, 
IN IN_CRTRC_USUARIO_UPDATE SII.CGG_RES_TRAMITE_COMITE.CRTRC_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRTRC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_TRAMITE_COMITE','CRTRC_CODIGO','CRTRC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRTRC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_TRAMITE_COMITE(
		CRTRC_CODIGO, 
		CRTST_CODIGO, 
		CRCOM_CODIGO, 
		CRTRC_ESTADO, 
		CRTRC_FECHA_INSERT, 
		CRTRC_USUARIO_INSERT, 
		CRTRC_FECHA_UPDATE, 
		CRTRC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRTST_CODIGO, 
		IN_CRCOM_CODIGO, 
		IN_CRTRC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRTRC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRTRC_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

