/**
* FUNCION SII.F_CGG_KDX_TIPO_PAGO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_KDX_TIPO_PAGO
* @param IN_CKTPG_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TIPO DE PAGO
* @param IN_CKTPG_DESCRIPCION NOMBRE DEL TIPO DE PAGO
* @param IN_CKTPG_OBSERVACION INFORMACION ADICIONAL
* @param IN_CKTPG_ESTADO ESTADO DEL REGISTRO
* @param IN_CKTPG_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CKTPG_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_KDX_TIPO_PAGO_INSERT(
IN IN_CKTPG_CODIGO SII.CGG_KDX_TIPO_PAGO.CKTPG_CODIGO%TYPE,
IN IN_CKTPG_DESCRIPCION SII.CGG_KDX_TIPO_PAGO.CKTPG_DESCRIPCION%TYPE,
IN IN_CKTPG_OBSERVACION SII.CGG_KDX_TIPO_PAGO.CKTPG_OBSERVACION%TYPE,
IN IN_CKTPG_ESTADO SII.CGG_KDX_TIPO_PAGO.CKTPG_ESTADO%TYPE,
IN IN_CKTPG_USUARIO_INSERT SII.CGG_KDX_TIPO_PAGO.CKTPG_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CKTPG_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_KDX_TIPO_PAGO','CKTPG_CODIGO','CKTPG') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CKTPG_CODIGO;
	END IF;
	INSERT INTO SII.CGG_KDX_TIPO_PAGO(
		CKTPG_CODIGO, 
		CKTPG_DESCRIPCION, 
		CKTPG_OBSERVACION, 
		CKTPG_ESTADO, 
		CKTPG_FECHA_INSERT, 
		CKTPG_USUARIO_INSERT, 
		CKTPG_FECHA_UPDATE, 
		CKTPG_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CKTPG_DESCRIPCION, 
		IN_CKTPG_OBSERVACION, 
		IN_CKTPG_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CKTPG_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CKTPG_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

