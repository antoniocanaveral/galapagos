/**
* FUNCION SII.F_CGG_DHU_RESPUESTA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_DHU_RESPUESTA.
* @param IN_CDRES_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE LA RESPUESTA
* @param IN_CDRES_DESCRIPCION DESCRIPCION DE LA RESPUESTA
* @param IN_CDRES_ESTADO ESTADO DEL REGISTRO
* @param IN_CDRES_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CDRES_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_DHU_RESPUESTA_INSERT(
IN IN_CDRES_CODIGO SII.CGG_DHU_RESPUESTA.CDRES_CODIGO%TYPE, 
IN IN_CDRES_DESCRIPCION SII.CGG_DHU_RESPUESTA.CDRES_DESCRIPCION%TYPE, 
IN IN_CDRES_ESTADO SII.CGG_DHU_RESPUESTA.CDRES_ESTADO%TYPE, 
IN IN_CDRES_USUARIO_INSERT SII.CGG_DHU_RESPUESTA.CDRES_USUARIO_INSERT%TYPE, 
IN IN_CDRES_USUARIO_UPDATE SII.CGG_DHU_RESPUESTA.CDRES_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CDRES_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_DHU_RESPUESTA','CDRES_CODIGO','CDRES') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CDRES_CODIGO;
	END IF;
	INSERT INTO SII.CGG_DHU_RESPUESTA(
		CDRES_CODIGO, 
		CDRES_DESCRIPCION, 
		CDRES_ESTADO, 
		CDRES_FECHA_INSERT, 
		CDRES_USUARIO_INSERT, 
		CDRES_FECHA_UPDATE, 
		CDRES_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CDRES_DESCRIPCION, 
		IN_CDRES_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CDRES_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CDRES_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

