/**
* FUNCION SII.F_CGG_DHU_RESPUESTA__SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_DHU_RESPUESTA
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_DHU_RESPUESTA_SELECT(
IN IN_USER_NAME varchar(50),
IN IN_START_INDEX int,
IN IN_LIMIT int,
IN IN_SORT_FIELD_NAME varchar,
IN IN_DIRECTION varchar,
IN IN_FIND_TEXT text
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CDRES_CODIGO, 
		CDRES_DESCRIPCION, 
		CDRES_ESTADO, 
		CDRES_FECHA_INSERT, 
		CDRES_USUARIO_INSERT, 
		CDRES_FECHA_UPDATE, 
		CDRES_USUARIO_UPDATE
	FROM SII.CGG_DHU_RESPUESTA
	WHERE CDRES_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CDRES_CODIGO IS NULL THEN '''' ELSE CDRES_CODIGO END ||'' ''||CASE WHEN CDRES_DESCRIPCION IS NULL THEN '''' ELSE CDRES_DESCRIPCION END ||'' ''||CASE WHEN CDRES_ESTADO IS NULL THEN FALSE ELSE CDRES_ESTADO END ||'' ''||CASE WHEN CDRES_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CDRES_FECHA_INSERT END ||'' ''||CASE WHEN CDRES_USUARIO_INSERT IS NULL THEN '''' ELSE CDRES_USUARIO_INSERT END ||'' ''||CASE WHEN CDRES_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CDRES_FECHA_UPDATE END ||'' ''||CASE WHEN CDRES_USUARIO_UPDATE IS NULL THEN '''' ELSE CDRES_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

