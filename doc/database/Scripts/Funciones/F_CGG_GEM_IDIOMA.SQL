/**
* FUNCION SII.F_CGG_GEM_IDIOMA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_GEM_IDIOMA.
* @param IN_CGIDM_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CGIDM_NOMBRE IDENTIFICATIVO DEL IDIOMA
* @param IN_CGIDM_ESTADO ESTADO DEL REGISTRO
* @param IN_CGIDM_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CGIDM_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_GEM_IDIOMA_INSERT(
IN IN_CGIDM_CODIGO SII.CGG_GEM_IDIOMA.CGIDM_CODIGO%TYPE, 
IN IN_CGIDM_NOMBRE SII.CGG_GEM_IDIOMA.CGIDM_NOMBRE%TYPE, 
IN IN_CGIDM_ESTADO SII.CGG_GEM_IDIOMA.CGIDM_ESTADO%TYPE, 
IN IN_CGIDM_USUARIO_INSERT SII.CGG_GEM_IDIOMA.CGIDM_USUARIO_INSERT%TYPE, 
IN IN_CGIDM_USUARIO_UPDATE SII.CGG_GEM_IDIOMA.CGIDM_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CGIDM_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_GEM_IDIOMA','CGIDM_CODIGO','CGIDM') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CGIDM_CODIGO;
	END IF;
	INSERT INTO SII.CGG_GEM_IDIOMA(
		CGIDM_CODIGO, 
		CGIDM_NOMBRE, 
		CGIDM_ESTADO, 
		CGIDM_FECHA_INSERT, 
		CGIDM_USUARIO_INSERT, 
		CGIDM_FECHA_UPDATE, 
		CGIDM_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CGIDM_NOMBRE, 
		IN_CGIDM_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CGIDM_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CGIDM_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

