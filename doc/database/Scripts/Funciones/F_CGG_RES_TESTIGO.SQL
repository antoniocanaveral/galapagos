/**
* FUNCION SII.F_CGG_RES_TESTIGO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_TESTIGO
* @param IN_CRTST_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TESTIGO
* @param IN_CRINT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE INFORME TESTIGO
* @param IN_CRPER_CODIGO CODIGO IDENTIFICATIVO DE REGISTRO DE PERSONA
* @param IN_CRTST_TESTIMONIO TESTIMONIO DE UN PERSONA
* @param IN_CRTST_OBSERVACION INFORMACION ADICIONAL
* @param IN_CRTST_ESTADO ESTADO DEL REGISTRO
* @param IN_CRTST_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRTST_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TESTIGO_INSERT(
IN IN_CRTST_CODIGO SII.CGG_RES_TESTIGO.CRTST_CODIGO%TYPE, 
IN IN_CRINT_CODIGO SII.CGG_RES_TESTIGO.CRINT_CODIGO%TYPE, 
IN IN_CRPER_CODIGO SII.CGG_RES_TESTIGO.CRPER_CODIGO%TYPE, 
IN IN_CRTST_TESTIMONIO SII.CGG_RES_TESTIGO.CRTST_TESTIMONIO%TYPE, 
IN IN_CRTST_OBSERVACION SII.CGG_RES_TESTIGO.CRTST_OBSERVACION%TYPE, 
IN IN_CRTST_ESTADO SII.CGG_RES_TESTIGO.CRTST_ESTADO%TYPE, 
IN IN_CRTST_USUARIO_INSERT SII.CGG_RES_TESTIGO.CRTST_USUARIO_INSERT%TYPE, 
IN IN_CRTST_USUARIO_UPDATE SII.CGG_RES_TESTIGO.CRTST_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRTST_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_TESTIGO','CRTST_CODIGO','CRTST') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRTST_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_TESTIGO(
		CRTST_CODIGO, 
		CRINT_CODIGO, 
		CRPER_CODIGO, 
		CRTST_TESTIMONIO, 
		CRTST_OBSERVACION, 
		CRTST_ESTADO, 
		CRTST_FECHA_INSERT, 
		CRTST_USUARIO_INSERT, 
		CRTST_FECHA_UPDATE, 
		CRTST_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRINT_CODIGO, 
		IN_CRPER_CODIGO, 
		IN_CRTST_TESTIMONIO, 
		IN_CRTST_OBSERVACION, 
		IN_CRTST_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRTST_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRTST_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

