/*
* FUNCION SII.F_CGG_RES_ESTADO_CIVIL__SELECT
* SELECCIONA N REGISTROS DE LA TABLA SII.CGG_RES_ESTADO_CIVIL
* @param IN_USER_NAME NOMBRE DE USUARIO DEL SERVIDOR DE APP. O DE LA BASE DE DATOS.
* @param IN_START_INDEX INDICE DE INICIO DE REGISTROS.
* @param IN_LIMIT NUMERO DE REGISTRO DISCRIMINADOS
* @param IN_SORT_FIELD_NAME COLUMNA DE ORDENAMIENTO. 
* @param IN_DIRECTION DIRECCION DEL ORDENAMIENTO ASC/DESC. 
* @param IN_FIND_TEXT CRITERIO DE BUSQUEDA.
* @return REFCURSOR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_ESTADO_CIVIL_SELECT(
IN IN_USER_NAME varchar(50),
IN IN_START_INDEX int,
IN IN_LIMIT int,
IN IN_SORT_FIELD_NAME varchar,
IN IN_DIRECTION varchar,
IN IN_FIND_TEXT text
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CRECV_CODIGO, 
		CRECV_DESCRPCION, 
		CRECV_ESTADO, 
		CRECV_FECHA_INSERT, 
		CRECV_USUARIO_INSERT, 
		CRECV_FECHA_UPDATE, 
		CRECV_USUARIO_UPDATE
	FROM SII.CGG_RES_ESTADO_CIVIL
	WHERE CRECV_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CRECV_CODIGO IS NULL THEN '''' ELSE CRECV_CODIGO END ||'' ''||CASE WHEN CRECV_DESCRPCION IS NULL THEN '''' ELSE CRECV_DESCRPCION END ||'' ''||CASE WHEN CRECV_ESTADO IS NULL THEN FALSE ELSE CRECV_ESTADO END ||'' ''||CASE WHEN CRECV_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CRECV_FECHA_INSERT END ||'' ''||CASE WHEN CRECV_USUARIO_INSERT IS NULL THEN '''' ELSE CRECV_USUARIO_INSERT END ||'' ''||CASE WHEN CRECV_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CRECV_FECHA_UPDATE END ||'' ''||CASE WHEN CRECV_USUARIO_UPDATE IS NULL THEN '''' ELSE CRECV_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

