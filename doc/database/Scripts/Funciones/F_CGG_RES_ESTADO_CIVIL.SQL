/**
* FUNCION SII.F_CGG_RES_ESTADO_CIVIL_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_ESTADO_CIVIL
* @param IN_CRECV_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DEL ESTADO CIVIL
* @param IN_CRECV_DESCRPCION DESCRIPCION DEL TIPO DE RESIDENCIA
* @param IN_CRECV_ESTADO ESTADO DEL REGISTRO
* @param IN_CRECV_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRECV_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_ESTADO_CIVIL_INSERT(
IN IN_CRECV_CODIGO SII.CGG_RES_ESTADO_CIVIL.CRECV_CODIGO%TYPE, 
IN IN_CRECV_DESCRPCION SII.CGG_RES_ESTADO_CIVIL.CRECV_DESCRPCION%TYPE, 
IN IN_CRECV_ESTADO SII.CGG_RES_ESTADO_CIVIL.CRECV_ESTADO%TYPE, 
IN IN_CRECV_USUARIO_INSERT SII.CGG_RES_ESTADO_CIVIL.CRECV_USUARIO_INSERT%TYPE, 
IN IN_CRECV_USUARIO_UPDATE SII.CGG_RES_ESTADO_CIVIL.CRECV_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRECV_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_ESTADO_CIVIL','CRECV_CODIGO','CRECV') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRECV_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_ESTADO_CIVIL(
		CRECV_CODIGO, 
		CRECV_DESCRPCION, 
		CRECV_ESTADO, 
		CRECV_FECHA_INSERT, 
		CRECV_USUARIO_INSERT, 
		CRECV_FECHA_UPDATE, 
		CRECV_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRECV_DESCRPCION, 
		IN_CRECV_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRECV_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRECV_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

