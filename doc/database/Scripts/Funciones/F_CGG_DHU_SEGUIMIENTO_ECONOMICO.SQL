		CDSEC_USUARIO_INSERT, 
		CDSEC_FECHA_UPDATE, 
		CDSEC_USUARIO_UPDATE
	FROM SII.CGG_DHU_SEGUIMIENTO_ECONOMICO
	WHERE CDSEC_ESTADO = TRUE AND 
