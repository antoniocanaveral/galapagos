/**
* FUNCION SII.F_CGG_RES_CARGO_COMITE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_CARGO_COMITE
* @param IN_CRCRG_CODIGO IDENTIFICATIVO UNICO DE REGSITRO
* @param IN_CRCOM_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CRCRG_NOMBRE DESCRIPCION DEL CARGO 
* @param IN_CRCRG_PRINCIPAL INDICADOR DE QUE EL CARGO ES PRINCIPAL
* @param IN_CRCRG_VOTO_DIRIMENTE ESTATUS QUE DETERMINA SI EL VOTO APLICADO POR EL REPRESENTANTE TIENE MAYOR INCIDENCIA EN CASOS ESPECIALES
* @param IN_CRCRG_ESTADO ESTADO DEL REGISTRO
* @param IN_CRCRG_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRCRG_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_CARGO_COMITE_INSERT(
IN IN_CRCRG_CODIGO SII.CGG_RES_CARGO_COMITE.CRCRG_CODIGO%TYPE,
IN IN_CRCOM_CODIGO SII.CGG_RES_CARGO_COMITE.CRCOM_CODIGO%TYPE,
IN IN_CRCRG_NOMBRE SII.CGG_RES_CARGO_COMITE.CRCRG_NOMBRE%TYPE,
IN IN_CRCRG_PRINCIPAL SII.CGG_RES_CARGO_COMITE.CRCRG_PRINCIPAL%TYPE,
IN IN_CRCRG_VOTO_DIRIMENTE SII.CGG_RES_CARGO_COMITE.CRCRG_VOTO_DIRIMENTE%TYPE,
IN IN_CRCRG_ESTADO SII.CGG_RES_CARGO_COMITE.CRCRG_ESTADO%TYPE,
IN IN_CRCRG_USUARIO_INSERT SII.CGG_RES_CARGO_COMITE.CRCRG_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
TMP_COUNT INT;
BEGIN
	SELECT COUNT(CRCRG_CODIGO) INTO TMP_COUNT FROM SII.CGG_RES_CARGO_COMITE WHERE CRCRG_ESTADO AND CRCOM_CODIGO = IN_CRCOM_CODIGO AND UPPER(CRCRG_NOMBRE) = UPPER(IN_CRCRG_NOMBRE);
	IF (TMP_COUNT>0) THEN
		RAISE EXCEPTION 'El cargo % ya se encuentra registrado.', IN_CRCRG_NOMBRE;
	END IF;
	SELECT COUNT(CRCRG_CODIGO) INTO TMP_COUNT FROM SII.CGG_RES_CARGO_COMITE WHERE CRCRG_ESTADO AND CRCOM_CODIGO = IN_CRCOM_CODIGO AND CRCRG_VOTO_DIRIMENTE AND IN_CRCRG_VOTO_DIRIMENTE;
	IF (TMP_COUNT>0) THEN
		RAISE EXCEPTION 'El voto dirimente ya esta asignado.';
	END IF;
	IF (UPPER(IN_CRCRG_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_CARGO_COMITE','CRCRG_CODIGO','CRCRG') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRCRG_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_CARGO_COMITE(
		CRCRG_CODIGO, 
		CRCOM_CODIGO, 
		CRCRG_NOMBRE, 
		CRCRG_PRINCIPAL, 
		CRCRG_VOTO_DIRIMENTE, 
		CRCRG_ESTADO, 
		CRCRG_FECHA_INSERT, 
		CRCRG_USUARIO_INSERT, 
		CRCRG_FECHA_UPDATE, 
		CRCRG_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRCOM_CODIGO, 
		IN_CRCRG_NOMBRE, 
		IN_CRCRG_PRINCIPAL, 
		IN_CRCRG_VOTO_DIRIMENTE, 
		IN_CRCRG_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRCRG_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRCRG_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

