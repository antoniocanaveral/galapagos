/**
* FUNCION SII.F_CGG_WEB_AGENCIA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_WEB_AGENCIA
* @param IN_CWAGE_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CWMOD_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CWAGE_NOMBRE NOMBRE DE LA AGENCIA
* @param IN_CWAGE_DESCRIPCION DESCRIPCION ACERCA DE LA AGENCIA
* @param IN_CWAGE_ENCARGADO ALMACENA EL NOMBRE DEL ENCARGADO DE LA AGENCIA
* @param IN_CWAGE_ESTADO ESTADO DEL REGISTRO
* @param IN_CWAGE_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CWAGE_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_WEB_AGENCIA_INSERT(
IN IN_CWAGE_CODIGO SII.CGG_WEB_AGENCIA.CWAGE_CODIGO%TYPE,
IN IN_CWMOD_CODIGO SII.CGG_WEB_AGENCIA.CWMOD_CODIGO%TYPE,
IN IN_CWAGE_NOMBRE SII.CGG_WEB_AGENCIA.CWAGE_NOMBRE%TYPE,
IN IN_CWAGE_DESCRIPCION SII.CGG_WEB_AGENCIA.CWAGE_DESCRIPCION%TYPE,
IN IN_CWAGE_ENCARGADO SII.CGG_WEB_AGENCIA.CWAGE_ENCARGADO%TYPE,
IN IN_CWAGE_ESTADO SII.CGG_WEB_AGENCIA.CWAGE_ESTADO%TYPE,
IN IN_CWAGE_USUARIO_INSERT SII.CGG_WEB_AGENCIA.CWAGE_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CWAGE_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_WEB_AGENCIA','CWAGE_CODIGO','CWAGE') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CWAGE_CODIGO;
	END IF;
	INSERT INTO SII.CGG_WEB_AGENCIA(
		CWAGE_CODIGO, 
		CWMOD_CODIGO, 
		CWAGE_NOMBRE, 
		CWAGE_DESCRIPCION, 
		CWAGE_ENCARGADO, 
		CWAGE_ESTADO, 
		CWAGE_FECHA_INSERT, 
		CWAGE_USUARIO_INSERT, 
		CWAGE_FECHA_UPDATE, 
		CWAGE_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CWMOD_CODIGO, 
		IN_CWAGE_NOMBRE, 
		IN_CWAGE_DESCRIPCION, 
		IN_CWAGE_ENCARGADO, 
		IN_CWAGE_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CWAGE_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CWAGE_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

