		CWAGE_USUARIO_INSERT, 
		CWAGE_FECHA_UPDATE, 
		CWAGE_USUARIO_UPDATE
	FROM SII.CGG_WEB_AGENCIA
	WHERE CWAGE_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CWAGE_NOMBRE, '''')||'' ''||COALESCE(CWAGE_DESCRIPCION, '''')||'' ''||COALESCE(CWAGE_ENCARGADO, '''')||'' ''||COALESCE(CWAGE_ESTADO, FALSE)||'' ''||COALESCE(CWAGE_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CWAGE_USUARIO_INSERT, '''')||'' ''||COALESCE(CWAGE_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CWAGE_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

