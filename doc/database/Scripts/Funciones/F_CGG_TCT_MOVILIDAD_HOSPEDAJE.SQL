/**
* FUNCION SII.F_CGG_TCT_MOVILIDAD_HOSPEDAJE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_MOVILIDAD_HOSPEDAJE
* @param IN_CTMVH_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CTTHJ_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE HOSPEDAJE
* @param IN_CRMOV_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE MOVILIDAD
* @param IN_CTMVH_ESTADO ESTADO DEL REGISTRO
* @param IN_CTMVH_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTMVH_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_MOVILIDAD_HOSPEDAJE_INSERT(
IN IN_CTMVH_CODIGO SII.CGG_TCT_MOVILIDAD_HOSPEDAJE.CTMVH_CODIGO%TYPE,
IN IN_CTTHJ_CODIGO SII.CGG_TCT_MOVILIDAD_HOSPEDAJE.CTTHJ_CODIGO%TYPE,
IN IN_CRMOV_CODIGO SII.CGG_TCT_MOVILIDAD_HOSPEDAJE.CRMOV_CODIGO%TYPE,
IN IN_CTMVH_ESTADO SII.CGG_TCT_MOVILIDAD_HOSPEDAJE.CTMVH_ESTADO%TYPE,
IN IN_CTMVH_USUARIO_INSERT SII.CGG_TCT_MOVILIDAD_HOSPEDAJE.CTMVH_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CTMVH_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_MOVILIDAD_HOSPEDAJE','CTMVH_CODIGO','CTMVH') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTMVH_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_MOVILIDAD_HOSPEDAJE(
		CTMVH_CODIGO, 
		CTTHJ_CODIGO, 
		CRMOV_CODIGO, 
		CTMVH_ESTADO, 
		CTMVH_FECHA_INSERT, 
		CTMVH_USUARIO_INSERT, 
		CTMVH_FECHA_UPDATE, 
		CTMVH_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTTHJ_CODIGO, 
		IN_CRMOV_CODIGO, 
		IN_CTMVH_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTMVH_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTMVH_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

