/**
* FUNCION SII.F_CGG_INSTITUCION_CONTACTO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_INSTITUCION_CONTACTO.
* @param IN_CINTC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE INSTITUCION CONTACTO
* @param IN_CGIED_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE INSTITUTUCION EDUCATIVA
* @param IN_CRTCO_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TIPO DE CONTACTO
* @param IN_CINTC_DESCRIPCION VALOR ASIGNADO AL CONTACTO
* @param IN_CINTC_CONTACTO NUMERO DE CONTACTO, DIRECCION DE CORREO ELECTRONICO
* @param IN_CINTC_ESTADO ESTADO DEL REGISTRO
* @param IN_CINTC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CINTC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_INSTITUCION_CONTACTO_INSERT(
IN IN_CINTC_CODIGO SII.CGG_INSTITUCION_CONTACTO.CINTC_CODIGO%TYPE, 
IN IN_CGIED_CODIGO SII.CGG_INSTITUCION_CONTACTO.CGIED_CODIGO%TYPE, 
IN IN_CRTCO_CODIGO SII.CGG_INSTITUCION_CONTACTO.CRTCO_CODIGO%TYPE, 
IN IN_CINTC_DESCRIPCION SII.CGG_INSTITUCION_CONTACTO.CINTC_DESCRIPCION%TYPE, 
IN IN_CINTC_CONTACTO SII.CGG_INSTITUCION_CONTACTO.CINTC_CONTACTO%TYPE, 
IN IN_CINTC_ESTADO SII.CGG_INSTITUCION_CONTACTO.CINTC_ESTADO%TYPE, 
IN IN_CINTC_USUARIO_INSERT SII.CGG_INSTITUCION_CONTACTO.CINTC_USUARIO_INSERT%TYPE, 
IN IN_CINTC_USUARIO_UPDATE SII.CGG_INSTITUCION_CONTACTO.CINTC_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CINTC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_INSTITUCION_CONTACTO','CINTC_CODIGO','CINTC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CINTC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_INSTITUCION_CONTACTO(
		CINTC_CODIGO, 
		CGIED_CODIGO, 
		CRTCO_CODIGO, 
		CINTC_DESCRIPCION, 
		CINTC_CONTACTO, 
		CINTC_ESTADO, 
		CINTC_FECHA_INSERT, 
		CINTC_USUARIO_INSERT, 
		CINTC_FECHA_UPDATE, 
		CINTC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CGIED_CODIGO, 
		IN_CRTCO_CODIGO, 
		IN_CINTC_DESCRIPCION, 
		IN_CINTC_CONTACTO, 
		IN_CINTC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CINTC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CINTC_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

