CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_REGISTRO_ACTIVIDAD_SELECT(
IN IN_USER_NAME varchar(50),
IN IN_START_INDEX int,
IN IN_LIMIT int,
IN IN_SORT_FIELD_NAME varchar,
IN IN_DIRECTION varchar,
IN IN_FIND_TEXT text
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR EXECUTE
	'SELECT
		CTRAC_CODIGO, 
		CTREG_CODIGO, 
		CTACT_CODIGO, 
		CTRAC_ESTADO, 
		CTRAC_FECHA_INSERT, 
