		CTRAC_USUARIO_UPDATE
	FROM SII.CGG_TCT_REGISTRO_ACTIVIDAD
	WHERE CTRAC_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CTRAC_CODIGO IS NULL THEN '''' ELSE CTRAC_CODIGO END ||'' ''||CASE WHEN CTREG_CODIGO IS NULL THEN '''' ELSE CTREG_CODIGO END ||'' ''||CASE WHEN CTACT_CODIGO IS NULL THEN '''' ELSE CTACT_CODIGO END ||'' ''||CASE WHEN CTRAC_ESTADO IS NULL THEN FALSE ELSE CTRAC_ESTADO END ||'' ''||CASE WHEN CTRAC_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CTRAC_FECHA_INSERT END ||'' ''||CASE WHEN CTRAC_USUARIO_INSERT IS NULL THEN '''' ELSE CTRAC_USUARIO_INSERT END ||'' ''||CASE WHEN CTRAC_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CTRAC_FECHA_UPDATE END ||'' ''||CASE WHEN CTRAC_USUARIO_UPDATE IS NULL THEN '''' ELSE CTRAC_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

