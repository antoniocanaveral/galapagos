/**
* FUNCION SII.F_CGG_TCT_REGISTRO_ACTIVIDAD_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_REGISTRO_ACTIVIDAD
* @param IN_CTRAC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CTREG_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TCT
* @param IN_CTACT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ACTIVIDADES
* @param IN_CTRAC_ESTADO ESTADO DEL REGISTRO
* @param IN_CTRAC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTRAC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_REGISTRO_ACTIVIDAD_INSERT(
IN IN_CTRAC_CODIGO SII.CGG_TCT_REGISTRO_ACTIVIDAD.CTRAC_CODIGO%TYPE, 
IN IN_CTREG_CODIGO SII.CGG_TCT_REGISTRO_ACTIVIDAD.CTREG_CODIGO%TYPE, 
IN IN_CTACT_CODIGO SII.CGG_TCT_REGISTRO_ACTIVIDAD.CTACT_CODIGO%TYPE, 
IN IN_CTRAC_ESTADO SII.CGG_TCT_REGISTRO_ACTIVIDAD.CTRAC_ESTADO%TYPE, 
IN IN_CTRAC_USUARIO_INSERT SII.CGG_TCT_REGISTRO_ACTIVIDAD.CTRAC_USUARIO_INSERT%TYPE, 
IN IN_CTRAC_USUARIO_UPDATE SII.CGG_TCT_REGISTRO_ACTIVIDAD.CTRAC_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CTRAC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_REGISTRO_ACTIVIDAD','CTRAC_CODIGO','CTRAC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTRAC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_REGISTRO_ACTIVIDAD(
		CTRAC_CODIGO, 
		CTREG_CODIGO, 
		CTACT_CODIGO, 
		CTRAC_ESTADO, 
		CTRAC_FECHA_INSERT, 
		CTRAC_USUARIO_INSERT, 
		CTRAC_FECHA_UPDATE, 
		CTRAC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTREG_CODIGO, 
		IN_CTACT_CODIGO, 
		IN_CTRAC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTRAC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTRAC_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

