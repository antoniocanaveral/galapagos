		CTFSL_USUARIO_UPDATE
	FROM SII.CGG_TCT_FORMA_SALIDA
	WHERE CTFSL_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',CASE WHEN CTFSL_CODIGO IS NULL THEN '''' ELSE CTFSL_CODIGO END ||'' ''||CASE WHEN CTFSL_NOMBRE IS NULL THEN '''' ELSE CTFSL_NOMBRE END ||'' ''||CASE WHEN CTFSL_ESTADO IS NULL THEN FALSE ELSE CTFSL_ESTADO END ||'' ''||CASE WHEN CTFSL_FECHA_INSERT IS NULL THEN CURRENT_TIMESTAMP ELSE CTFSL_FECHA_INSERT END ||'' ''||CASE WHEN CTFSL_USUARIO_INSERT IS NULL THEN '''' ELSE CTFSL_USUARIO_INSERT END ||'' ''||CASE WHEN CTFSL_FECHA_UPDATE IS NULL THEN CURRENT_TIMESTAMP ELSE CTFSL_FECHA_UPDATE END ||'' ''||CASE WHEN CTFSL_USUARIO_UPDATE IS NULL THEN '''' ELSE CTFSL_USUARIO_UPDATE END ) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

