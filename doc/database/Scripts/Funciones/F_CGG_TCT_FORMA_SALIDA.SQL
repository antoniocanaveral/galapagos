/**
* FUNCION SII.F_CGG_TCT_FORMA_SALIDA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_FORMA_SALIDA
* @param IN_CTFSL_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CTFSL_NOMBRE NOMBRE DE LA FORMA DE SALIDA
* @param IN_CTFSL_ESTADO ESTADO DEL REGISTRO
* @param IN_CTFSL_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTFSL_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_FORMA_SALIDA_INSERT(
IN IN_CTFSL_CODIGO SII.CGG_TCT_FORMA_SALIDA.CTFSL_CODIGO%TYPE, 
IN IN_CTFSL_NOMBRE SII.CGG_TCT_FORMA_SALIDA.CTFSL_NOMBRE%TYPE, 
IN IN_CTFSL_ESTADO SII.CGG_TCT_FORMA_SALIDA.CTFSL_ESTADO%TYPE, 
IN IN_CTFSL_USUARIO_INSERT SII.CGG_TCT_FORMA_SALIDA.CTFSL_USUARIO_INSERT%TYPE, 
IN IN_CTFSL_USUARIO_UPDATE SII.CGG_TCT_FORMA_SALIDA.CTFSL_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
TMP_COUNT INT;
BEGIN
	SELECT COUNT(IN_CTFSL_NOMBRE) INTO TMP_COUNT FROM SII.CGG_TCT_FORMA_SALIDA WHERE UPPER(CTFSL_NOMBRE) = UPPER(IN_CTFSL_NOMBRE) AND CTFSL_ESTADO= TRUE;
	IF (TMP_COUNT > 0) THEN
		RAISE EXCEPTION 'La Forma de Salida ya se encuentra registrada.';
	END IF;

	IF (UPPER(IN_CTFSL_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_FORMA_SALIDA','CTFSL_CODIGO','CTFSL') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTFSL_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_FORMA_SALIDA(
		CTFSL_CODIGO, 
		CTFSL_NOMBRE, 
		CTFSL_ESTADO, 
		CTFSL_FECHA_INSERT, 
		CTFSL_USUARIO_INSERT, 
		CTFSL_FECHA_UPDATE, 
		CTFSL_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTFSL_NOMBRE, 
		IN_CTFSL_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTFSL_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTFSL_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

