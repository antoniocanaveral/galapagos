/**
* FUNCION SII.F_CGG_TCT_TIPO_HOSPEDAJE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_TIPO_HOSPEDAJE
* @param IN_CTTHJ_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE HOSPEDAJE
* @param IN_CTTHJ_NOMBRE NOMBRE DEL TIPO DE HOSPEDAJE
* @param IN_CTTHJ_ESTADO ESTADO DEL REGISTRO
* @param IN_CTTHJ_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTTHJ_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_TIPO_HOSPEDAJE_INSERT(
IN IN_CTTHJ_CODIGO SII.CGG_TCT_TIPO_HOSPEDAJE.CTTHJ_CODIGO%TYPE, 
IN IN_CTTHJ_NOMBRE SII.CGG_TCT_TIPO_HOSPEDAJE.CTTHJ_NOMBRE%TYPE, 
IN IN_CTTHJ_ESTADO SII.CGG_TCT_TIPO_HOSPEDAJE.CTTHJ_ESTADO%TYPE, 
IN IN_CTTHJ_USUARIO_INSERT SII.CGG_TCT_TIPO_HOSPEDAJE.CTTHJ_USUARIO_INSERT%TYPE, 
IN IN_CTTHJ_USUARIO_UPDATE SII.CGG_TCT_TIPO_HOSPEDAJE.CTTHJ_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
TMP_COUNT INT;
BEGIN
	SELECT COUNT(IN_CTTHJ_NOMBRE) INTO TMP_COUNT FROM SII.CGG_TCT_TIPO_HOSPEDAJE WHERE UPPER(CTTHJ_NOMBRE) = UPPER(IN_CTTHJ_NOMBRE) AND CTTHJ_ESTADO = TRUE;
	IF (TMP_COUNT > 0) THEN
		RAISE EXCEPTION 'El Tipo Hospedaje ya se encuentra registrado.';
	END IF;

	IF (UPPER(IN_CTTHJ_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_TIPO_HOSPEDAJE','CTTHJ_CODIGO','CTTHJ') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTTHJ_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_TIPO_HOSPEDAJE(
		CTTHJ_CODIGO, 
		CTTHJ_NOMBRE, 
		CTTHJ_ESTADO, 
		CTTHJ_FECHA_INSERT, 
		CTTHJ_USUARIO_INSERT, 
		CTTHJ_FECHA_UPDATE, 
		CTTHJ_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTTHJ_NOMBRE, 
		IN_CTTHJ_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTTHJ_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTTHJ_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

