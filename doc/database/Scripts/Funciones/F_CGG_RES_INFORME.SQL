		OUT_KEY, 
		IN_CRSEG_CODIGO, 
		IN_CRSEC_CODIGO, 
		IN_CRINF_NUMERO_INFORME, 
		IN_CRINF_FECHA_INFORME, 
		IN_CRINF_ASUNTO_INFORME, 
		IN_CRINF_EXTRACTO_INFORME, 
		IN_CRINF_DESCRIPCION_ADJUNTO, 
		IN_CRINF_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRINF_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRINF_USUARIO_UPDATE
