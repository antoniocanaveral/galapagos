IN IN_CRINF_CODIGO SII.CGG_RES_INFORME.CRINF_CODIGO%TYPE, 
IN IN_CRSEG_CODIGO SII.CGG_RES_INFORME.CRSEG_CODIGO%TYPE, 
IN IN_CRSEC_CODIGO SII.CGG_RES_INFORME.CRSEC_CODIGO%TYPE, 
IN IN_CRINF_NUMERO_INFORME SII.CGG_RES_INFORME.CRINF_NUMERO_INFORME%TYPE, 
IN IN_CRINF_FECHA_INFORME SII.CGG_RES_INFORME.CRINF_FECHA_INFORME%TYPE, 
IN IN_CRINF_ASUNTO_INFORME SII.CGG_RES_INFORME.CRINF_ASUNTO_INFORME%TYPE, 
