IN IN_CRINF_USUARIO_UPDATE SII.CGG_RES_INFORME.CRINF_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRINF_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_INFORME','CRINF_CODIGO','CRINF') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRINF_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_INFORME(
		CRINF_CODIGO, 
		CRSEG_CODIGO, 
		CRSEC_CODIGO, 
		CRINF_NUMERO_INFORME, 
		CRINF_FECHA_INFORME, 
		CRINF_ASUNTO_INFORME, 
		CRINF_EXTRACTO_INFORME, 
		CRINF_DESCRIPCION_ADJUNTO, 
		CRINF_ESTADO, 
		CRINF_FECHA_INSERT, 
		CRINF_USUARIO_INSERT, 
		CRINF_FECHA_UPDATE, 
		CRINF_USUARIO_UPDATE
