/**
* FUNCION SII.F_CGG_RES_TIPO_CONTACTO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_TIPO_CONTACTO
* @param IN_CRTCO_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TIPO DE CONTACTO
* @param IN_CRTCO_NOMBRE NOMBRE DEL TIPO DE CONTACTO
* @param IN_CRTCO_NOMBRE_IMAGEN NOMBRE DEL ARCHIVO QUE REPRESENTA EL TIPO DE CONTACTO
* @param IN_CRTCO_IMAGEN IMAGEN DESCRIPTIVA AFIN AL TIPO DE CONTACTO
* @param IN_CRTCO_ESTADO ESTADO DEL REGISTRO
* @param IN_CRTCO_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRTCO_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TIPO_CONTACTO_INSERT(
IN IN_CRTCO_CODIGO SII.CGG_RES_TIPO_CONTACTO.CRTCO_CODIGO%TYPE, 
IN IN_CRTCO_NOMBRE SII.CGG_RES_TIPO_CONTACTO.CRTCO_NOMBRE%TYPE, 
IN IN_CRTCO_NOMBRE_IMAGEN SII.CGG_RES_TIPO_CONTACTO.CRTCO_NOMBRE_IMAGEN%TYPE, 
IN IN_CRTCO_IMAGEN SII.CGG_RES_TIPO_CONTACTO.CRTCO_IMAGEN%TYPE, 
IN IN_CRTCO_ESTADO SII.CGG_RES_TIPO_CONTACTO.CRTCO_ESTADO%TYPE, 
IN IN_CRTCO_USUARIO_INSERT SII.CGG_RES_TIPO_CONTACTO.CRTCO_USUARIO_INSERT%TYPE, 
IN IN_CRTCO_USUARIO_UPDATE SII.CGG_RES_TIPO_CONTACTO.CRTCO_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRTCO_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_TIPO_CONTACTO','CRTCO_CODIGO','CRTCO') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRTCO_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_TIPO_CONTACTO(
		CRTCO_CODIGO, 
		CRTCO_NOMBRE, 
		CRTCO_NOMBRE_IMAGEN, 
		CRTCO_IMAGEN, 
		CRTCO_ESTADO, 
		CRTCO_FECHA_INSERT, 
		CRTCO_USUARIO_INSERT, 
		CRTCO_FECHA_UPDATE, 
		CRTCO_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRTCO_NOMBRE, 
		IN_CRTCO_NOMBRE_IMAGEN, 
		IN_CRTCO_IMAGEN, 
		IN_CRTCO_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRTCO_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRTCO_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

