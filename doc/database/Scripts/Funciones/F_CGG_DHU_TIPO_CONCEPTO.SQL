/**
* FUNCION SII.F_CGG_DHU_TIPO_CONCEPTO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_DHU_TIPO_CONCEPTO.
* @param IN_CDTCP_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE CONCEPTO DE DESEMBOLSO
* @param IN_CDTCP_NOMBRE_DESEMBOLSO NOMBRE DEL CONCEPTO DE DESEMBOLSO
* @param IN_CDTCP_ESTADO ESTADO DEL REGISTRO
* @param IN_CDTCP_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CDTCP_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_DHU_TIPO_CONCEPTO_INSERT(
IN IN_CDTCP_CODIGO SII.CGG_DHU_TIPO_CONCEPTO.CDTCP_CODIGO%TYPE, 
IN IN_CDTCP_NOMBRE_DESEMBOLSO SII.CGG_DHU_TIPO_CONCEPTO.CDTCP_NOMBRE_DESEMBOLSO%TYPE, 
IN IN_CDTCP_ESTADO SII.CGG_DHU_TIPO_CONCEPTO.CDTCP_ESTADO%TYPE, 
IN IN_CDTCP_USUARIO_INSERT SII.CGG_DHU_TIPO_CONCEPTO.CDTCP_USUARIO_INSERT%TYPE, 
IN IN_CDTCP_USUARIO_UPDATE SII.CGG_DHU_TIPO_CONCEPTO.CDTCP_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CDTCP_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_DHU_TIPO_CONCEPTO','CDTCP_CODIGO','CDTCP') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CDTCP_CODIGO;
	END IF;
	INSERT INTO SII.CGG_DHU_TIPO_CONCEPTO(
		CDTCP_CODIGO, 
		CDTCP_NOMBRE_DESEMBOLSO, 
		CDTCP_ESTADO, 
		CDTCP_FECHA_INSERT, 
		CDTCP_USUARIO_INSERT, 
		CDTCP_FECHA_UPDATE, 
		CDTCP_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CDTCP_NOMBRE_DESEMBOLSO, 
		IN_CDTCP_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CDTCP_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CDTCP_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

