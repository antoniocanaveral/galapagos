/**
* FUNCION SII.F_CGG_RES_TIPO_SANGUINEO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_TIPO_SANGUINEO
* @param IN_CRTSG_CODIGO IDENTIFICATIVO UNICO DE REGSITRO
* @param IN_CRTSG_DESCRPCION DESCRIPCION DEL TIPO SANGUINEO
* @param IN_CRTSG_ESTADO ESTADO DEL REGISTRO
* @param IN_CRTSG_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRTSG_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TIPO_SANGUINEO_INSERT(
IN IN_CRTSG_CODIGO SII.CGG_RES_TIPO_SANGUINEO.CRTSG_CODIGO%TYPE, 
IN IN_CRTSG_DESCRPCION SII.CGG_RES_TIPO_SANGUINEO.CRTSG_DESCRPCION%TYPE, 
IN IN_CRTSG_ESTADO SII.CGG_RES_TIPO_SANGUINEO.CRTSG_ESTADO%TYPE, 
IN IN_CRTSG_USUARIO_INSERT SII.CGG_RES_TIPO_SANGUINEO.CRTSG_USUARIO_INSERT%TYPE, 
IN IN_CRTSG_USUARIO_UPDATE SII.CGG_RES_TIPO_SANGUINEO.CRTSG_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRTSG_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_TIPO_SANGUINEO','CRTSG_CODIGO','CRTSG') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRTSG_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_TIPO_SANGUINEO(
		CRTSG_CODIGO, 
		CRTSG_DESCRPCION, 
		CRTSG_ESTADO, 
		CRTSG_FECHA_INSERT, 
		CRTSG_USUARIO_INSERT, 
		CRTSG_FECHA_UPDATE, 
		CRTSG_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRTSG_DESCRPCION, 
		IN_CRTSG_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRTSG_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRTSG_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

