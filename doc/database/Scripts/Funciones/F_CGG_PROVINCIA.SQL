/**
* FUNCION SII.F_CGG_PROVINCIA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_PROVINCIA
* @param IN_CGPRV_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CPAIS_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CGPRV_NOMBRE NOMBRE DE LA PROVINCIA
* @param IN_CGPRV_DESCRIPCION DESCRIPCION DE LA PROVINCIA
* @param IN_CGPRV_ESTADO ESTADO DEL REGISTRO
* @param IN_CGPRV_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CGPRV_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_PROVINCIA_INSERT(
IN IN_CGPRV_CODIGO SII.CGG_PROVINCIA.CGPRV_CODIGO%TYPE, 
IN IN_CPAIS_CODIGO SII.CGG_PROVINCIA.CPAIS_CODIGO%TYPE, 
IN IN_CGPRV_NOMBRE SII.CGG_PROVINCIA.CGPRV_NOMBRE%TYPE, 
IN IN_CGPRV_DESCRIPCION SII.CGG_PROVINCIA.CGPRV_DESCRIPCION%TYPE, 
IN IN_CGPRV_ESTADO SII.CGG_PROVINCIA.CGPRV_ESTADO%TYPE, 
IN IN_CGPRV_USUARIO_INSERT SII.CGG_PROVINCIA.CGPRV_USUARIO_INSERT%TYPE, 
IN IN_CGPRV_USUARIO_UPDATE SII.CGG_PROVINCIA.CGPRV_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
TMP_COUNT INT;
BEGIN
	
	SELECT COUNT(CGPRV_NOMBRE) INTO TMP_COUNT FROM SII.CGG_PROVINCIA WHERE UPPER(CGPRV_NOMBRE) = UPPER(IN_CGPRV_NOMBRE) AND CPAIS_CODIGO = IN_CPAIS_CODIGO AND CGPRV_ESTADO;
	IF(TMP_COUNT > 0)THEN
		RAISE EXCEPTION 'El nombre de provincia ya esta registrado.';
	END IF;
	IF (UPPER(IN_CGPRV_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_PROVINCIA','CGPRV_CODIGO','CGPRV') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CGPRV_CODIGO;
	END IF;
	INSERT INTO SII.CGG_PROVINCIA(
		CGPRV_CODIGO, 
		CPAIS_CODIGO, 
		CGPRV_NOMBRE, 
		CGPRV_DESCRIPCION, 
		CGPRV_ESTADO, 
		CGPRV_FECHA_INSERT, 
		CGPRV_USUARIO_INSERT, 
		CGPRV_FECHA_UPDATE, 
		CGPRV_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CPAIS_CODIGO, 
		IN_CGPRV_NOMBRE, 
		IN_CGPRV_DESCRIPCION, 
		IN_CGPRV_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CGPRV_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CGPRV_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

