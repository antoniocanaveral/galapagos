/**
* FUNCION SII.F_CGG_TCT_MOVILIDAD_INT_ACTIVIDAD_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_MOVILIDAD_INT_ACTIVIDAD
* @param IN_CTMAC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CTACT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ACTIVIDADES
* @param IN_CRMVI_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE MOVILIDAD INTERNA
* @param IN_CTMAC_ESTADO ESTADO DEL REGISTRO
* @param IN_CTMAC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTMAC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_MOVILIDAD_INT_ACTIVIDAD_INSERT(
IN IN_CTMAC_CODIGO SII.CGG_TCT_MOVILIDAD_INT_ACTIVIDAD.CTMAC_CODIGO%TYPE, 
IN IN_CTACT_CODIGO SII.CGG_TCT_MOVILIDAD_INT_ACTIVIDAD.CTACT_CODIGO%TYPE, 
IN IN_CRMVI_CODIGO SII.CGG_TCT_MOVILIDAD_INT_ACTIVIDAD.CRMVI_CODIGO%TYPE, 
IN IN_CTMAC_ESTADO SII.CGG_TCT_MOVILIDAD_INT_ACTIVIDAD.CTMAC_ESTADO%TYPE, 
IN IN_CTMAC_USUARIO_INSERT SII.CGG_TCT_MOVILIDAD_INT_ACTIVIDAD.CTMAC_USUARIO_INSERT%TYPE, 
IN IN_CTMAC_USUARIO_UPDATE SII.CGG_TCT_MOVILIDAD_INT_ACTIVIDAD.CTMAC_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CTMAC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_MOVILIDAD_INT_ACTIVIDAD','CTMAC_CODIGO','CTMAC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTMAC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_MOVILIDAD_INT_ACTIVIDAD(
		CTMAC_CODIGO, 
		CTACT_CODIGO, 
		CRMVI_CODIGO, 
		CTMAC_ESTADO, 
		CTMAC_FECHA_INSERT, 
		CTMAC_USUARIO_INSERT, 
		CTMAC_FECHA_UPDATE, 
		CTMAC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTACT_CODIGO, 
		IN_CRMVI_CODIGO, 
		IN_CTMAC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTMAC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTMAC_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

