/**
* FUNCION SII.F_CGG_RES_INFORME_TESTIGO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_INFORME_TESTIGO
* @param IN_CRINT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE INFORME TESTIGO
* @param IN_CRMTR_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CRINT_NUMERO NUMERO DE INFORME TESTIGOS
* @param IN_CRINT_OBSERVACION INFORMACION ADICIONAL - HISTORIAL JSON
* @param IN_CRINT_ESTADO ESTADO DEL REGISTRO
* @param IN_CRINT_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRINT_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_INFORME_TESTIGO_INSERT(
IN IN_CRINT_CODIGO SII.CGG_RES_INFORME_TESTIGO.CRINT_CODIGO%TYPE, 
IN IN_CRMTR_CODIGO SII.CGG_RES_INFORME_TESTIGO.CRMTR_CODIGO%TYPE, 
IN IN_CRINT_NUMERO SII.CGG_RES_INFORME_TESTIGO.CRINT_NUMERO%TYPE, 
IN IN_CRINT_OBSERVACION SII.CGG_RES_INFORME_TESTIGO.CRINT_OBSERVACION%TYPE, 
IN IN_CRINT_ESTADO SII.CGG_RES_INFORME_TESTIGO.CRINT_ESTADO%TYPE, 
IN IN_CRINT_USUARIO_INSERT SII.CGG_RES_INFORME_TESTIGO.CRINT_USUARIO_INSERT%TYPE, 
IN IN_CRINT_USUARIO_UPDATE SII.CGG_RES_INFORME_TESTIGO.CRINT_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRINT_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_INFORME_TESTIGO','CRINT_CODIGO','CRINT') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRINT_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_INFORME_TESTIGO(
		CRINT_CODIGO, 
		CRMTR_CODIGO, 
		CRINT_NUMERO, 
		CRINT_OBSERVACION, 
		CRINT_ESTADO, 
		CRINT_FECHA_INSERT, 
		CRINT_USUARIO_INSERT, 
		CRINT_FECHA_UPDATE, 
		CRINT_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRMTR_CODIGO, 
		IN_CRINT_NUMERO, 
		IN_CRINT_OBSERVACION, 
		IN_CRINT_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRINT_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRINT_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

