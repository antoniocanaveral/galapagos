/**
* FUNCION SII.F_CGG_NACIONALIDAD_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_NACIONALIDAD
* @param IN_CGNCN_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE NACIONALIDAD
* @param IN_CGNCN_NACIONALIDAD NOMBRE DE LA NACIONALIDAD
* @param IN_CGNCN_ESTADO ESTADO DEL REGISTRO
* @param IN_CGNCN_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CGNCN_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_NACIONALIDAD_INSERT(
IN IN_CGNCN_CODIGO SII.CGG_NACIONALIDAD.CGNCN_CODIGO%TYPE, 
IN IN_CGNCN_NACIONALIDAD SII.CGG_NACIONALIDAD.CGNCN_NACIONALIDAD%TYPE, 
IN IN_CGNCN_ESTADO SII.CGG_NACIONALIDAD.CGNCN_ESTADO%TYPE, 
IN IN_CGNCN_USUARIO_INSERT SII.CGG_NACIONALIDAD.CGNCN_USUARIO_INSERT%TYPE, 
IN IN_CGNCN_USUARIO_UPDATE SII.CGG_NACIONALIDAD.CGNCN_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CGNCN_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_NACIONALIDAD','CGNCN_CODIGO','CGNCN') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CGNCN_CODIGO;
	END IF;
	INSERT INTO SII.CGG_NACIONALIDAD(
		CGNCN_CODIGO, 
		CGNCN_NACIONALIDAD, 
		CGNCN_ESTADO, 
		CGNCN_FECHA_INSERT, 
		CGNCN_USUARIO_INSERT, 
		CGNCN_FECHA_UPDATE, 
		CGNCN_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CGNCN_NACIONALIDAD, 
		IN_CGNCN_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CGNCN_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CGNCN_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

