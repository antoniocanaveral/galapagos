/**
* FUNCION SII.F_CGG_TCT_REGISTRO_HOSPEDAJE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_REGISTRO_HOSPEDAJE
* @param IN_CTRHJ_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CTTHJ_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE HOSPEDAJE
* @param IN_CTREG_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TCT
