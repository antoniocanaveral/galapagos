* @param IN_CTRHJ_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_REGISTRO_HOSPEDAJE_INSERT(
IN IN_CTRHJ_CODIGO SII.CGG_TCT_REGISTRO_HOSPEDAJE.CTRHJ_CODIGO%TYPE, 
IN IN_CTTHJ_CODIGO SII.CGG_TCT_REGISTRO_HOSPEDAJE.CTTHJ_CODIGO%TYPE, 
IN IN_CTREG_CODIGO SII.CGG_TCT_REGISTRO_HOSPEDAJE.CTREG_CODIGO%TYPE, 
IN IN_CTRHJ_LUGAR_HOSPEDAJE SII.CGG_TCT_REGISTRO_HOSPEDAJE.CTRHJ_LUGAR_HOSPEDAJE%TYPE, 
IN IN_CTRHJ_ESTADO SII.CGG_TCT_REGISTRO_HOSPEDAJE.CTRHJ_ESTADO%TYPE, 
IN IN_CTRHJ_USUARIO_INSERT SII.CGG_TCT_REGISTRO_HOSPEDAJE.CTRHJ_USUARIO_INSERT%TYPE, 
IN IN_CTRHJ_USUARIO_UPDATE SII.CGG_TCT_REGISTRO_HOSPEDAJE.CTRHJ_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CTRHJ_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_REGISTRO_HOSPEDAJE','CTRHJ_CODIGO','CTRHJ') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTRHJ_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_REGISTRO_HOSPEDAJE(
		CTRHJ_CODIGO, 
		CTTHJ_CODIGO, 
		CTREG_CODIGO, 
		CTRHJ_LUGAR_HOSPEDAJE, 
		CTRHJ_ESTADO, 
		CTRHJ_FECHA_INSERT, 
		CTRHJ_USUARIO_INSERT, 
		CTRHJ_FECHA_UPDATE, 
		CTRHJ_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTTHJ_CODIGO, 
		IN_CTREG_CODIGO, 
		IN_CTRHJ_LUGAR_HOSPEDAJE, 
		IN_CTRHJ_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTRHJ_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTRHJ_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

