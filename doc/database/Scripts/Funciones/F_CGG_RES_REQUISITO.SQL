/**
* FUNCION SII.F_CGG_RES_REQUISITO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_REQUISITO
* @param IN_CRREQ_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE REQUISITO
* @param IN_CRREQ_DESCRIPCION DESCRIPCION DEL REQUISITO
* @param IN_CRREQ_OBSERVACION INFORMACION ADICIONAL
* @param IN_CRREQ_ESTADO ESTADO DEL REGISTRO
* @param IN_CRREQ_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION
* @param IN_CRREQ_USUARIO_UPDATE USUARIO QUE ACTUALIZO LA INFORMACION EN EL SISTEMA
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_REQUISITO_INSERT(
IN IN_CRREQ_CODIGO SII.CGG_RES_REQUISITO.CRREQ_CODIGO%TYPE, 
IN IN_CRREQ_DESCRIPCION SII.CGG_RES_REQUISITO.CRREQ_DESCRIPCION%TYPE, 
IN IN_CRREQ_OBSERVACION SII.CGG_RES_REQUISITO.CRREQ_OBSERVACION%TYPE, 
IN IN_CRREQ_ESTADO SII.CGG_RES_REQUISITO.CRREQ_ESTADO%TYPE, 
IN IN_CRREQ_USUARIO_INSERT SII.CGG_RES_REQUISITO.CRREQ_USUARIO_INSERT%TYPE, 
IN IN_CRREQ_USUARIO_UPDATE SII.CGG_RES_REQUISITO.CRREQ_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRREQ_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_REQUISITO','CRREQ_CODIGO','CRREQ') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRREQ_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_REQUISITO(
		CRREQ_CODIGO, 
		CRREQ_DESCRIPCION, 
		CRREQ_OBSERVACION, 
		CRREQ_ESTADO, 
		CRREQ_FECHA_INSERT, 
		CRREQ_USUARIO_INSERT, 
		CRREQ_FECHA_UPDATE, 
		CRREQ_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRREQ_DESCRIPCION, 
		IN_CRREQ_OBSERVACION, 
		IN_CRREQ_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRREQ_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRREQ_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

