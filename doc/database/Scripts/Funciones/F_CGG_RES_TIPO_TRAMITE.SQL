/**
* FUNCION SII.F_CGG_RES_TIPO_TRAMITE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_TIPO_TRAMITE
* @param IN_CRTPT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TIPO DE TRAMITE
* @param IN_CRTPT_NOMBRE NOMBRE DEL TIPO DE TRAMITE
* @param IN_CRTPT_ABREVIATURA ABREVIATURA DEL NOMBRE DEL TIPO DE TRAMITE
* @param IN_CRTPT_INDICE INDICE DE RELEVANCIA
* @param IN_CRTPT_OBSERVACIONES OBSERVACIONES AL TIPO DE TRAMITE
* @param IN_CRTPT_ESTADO ESTADO DEL REGISTRO
* @param IN_CRTPT_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRTPT_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TIPO_TRAMITE_INSERT(
IN IN_CRTPT_CODIGO SII.CGG_RES_TIPO_TRAMITE.CRTPT_CODIGO%TYPE,
IN IN_CRTPT_NOMBRE SII.CGG_RES_TIPO_TRAMITE.CRTPT_NOMBRE%TYPE,
IN IN_CRTPT_ABREVIATURA SII.CGG_RES_TIPO_TRAMITE.CRTPT_ABREVIATURA%TYPE,
IN IN_CRTPT_INDICE SII.CGG_RES_TIPO_TRAMITE.CRTPT_INDICE%TYPE,
IN IN_CRTPT_OBSERVACIONES SII.CGG_RES_TIPO_TRAMITE.CRTPT_OBSERVACIONES%TYPE,
IN IN_CRTPT_ESTADO SII.CGG_RES_TIPO_TRAMITE.CRTPT_ESTADO%TYPE,
IN IN_CRTPT_USUARIO_INSERT SII.CGG_RES_TIPO_TRAMITE.CRTPT_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRTPT_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_TIPO_TRAMITE','CRTPT_CODIGO','CRTPT') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRTPT_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_TIPO_TRAMITE(
		CRTPT_CODIGO, 
		CRTPT_NOMBRE, 
		CRTPT_ABREVIATURA, 
		CRTPT_INDICE, 
		CRTPT_OBSERVACIONES, 
		CRTPT_ESTADO, 
		CRTPT_FECHA_INSERT, 
		CRTPT_USUARIO_INSERT, 
		CRTPT_FECHA_UPDATE, 
		CRTPT_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRTPT_NOMBRE, 
		IN_CRTPT_ABREVIATURA, 
		IN_CRTPT_INDICE, 
		IN_CRTPT_OBSERVACIONES, 
		IN_CRTPT_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRTPT_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRTPT_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

