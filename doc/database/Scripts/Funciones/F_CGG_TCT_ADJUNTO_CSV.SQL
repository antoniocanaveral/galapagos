/**
* FUNCION SII.F_CGG_TCT_ADJUNTO_CSV_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_ADJUNTO_CSV
* @param IN_CTADC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ADJUNTO CSV
* @param IN_CTADC_NOMBRE_ADJUNTO NOMBRE DEL DOCUMENTO ADJUNTO
* @param IN_CTADC_DESCRIPCION_ADJUNTO DESCRIPCION DEL DOCUMENTO ADJUNTO
* @param IN_CTADC_FECHA_REGISTRO FECHA DE REGISTRO DEL ARCHIVO DIGITAL
* @param IN_CTADC_ADJUNTO DOCUMENTO DIGITAL ANEXO EN FORMATO CSV
* @param IN_CTADC_ESTADO ESTADO DEL REGISTRO
* @param IN_CTADC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_ADJUNTO_CSV_INSERT(
IN IN_CTADC_CODIGO SII.CGG_TCT_ADJUNTO_CSV.CTADC_CODIGO%TYPE, 
IN IN_CTADC_NOMBRE_ADJUNTO SII.CGG_TCT_ADJUNTO_CSV.CTADC_NOMBRE_ADJUNTO%TYPE, 
IN IN_CTADC_DESCRIPCION_ADJUNTO SII.CGG_TCT_ADJUNTO_CSV.CTADC_DESCRIPCION_ADJUNTO%TYPE, 
IN IN_CTADC_FECHA_REGISTRO SII.CGG_TCT_ADJUNTO_CSV.CTADC_FECHA_REGISTRO%TYPE, 
IN IN_CTADC_ADJUNTO SII.CGG_TCT_ADJUNTO_CSV.CTADC_ADJUNTO%TYPE, 
IN IN_CTADC_ESTADO SII.CGG_TCT_ADJUNTO_CSV.CTADC_ESTADO%TYPE, 
IN IN_CTADC_USUARIO_INSERT SII.CGG_TCT_ADJUNTO_CSV.CTADC_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CTADC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_ADJUNTO_CSV','CTADC_CODIGO','CTADC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTADC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_ADJUNTO_CSV(
		CTADC_CODIGO, 
		CTADC_NOMBRE_ADJUNTO, 
		CTADC_DESCRIPCION_ADJUNTO, 
		CTADC_FECHA_REGISTRO, 
		CTADC_ADJUNTO, 
		CTADC_ESTADO, 
		CTADC_FECHA_INSERT, 
		CTADC_USUARIO_INSERT, 
		CTADC_FECHA_UPDATE, 
		CTADC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTADC_NOMBRE_ADJUNTO, 
		IN_CTADC_DESCRIPCION_ADJUNTO, 
		IN_CTADC_FECHA_REGISTRO, 
		IN_CTADC_ADJUNTO, 
		IN_CTADC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTADC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTADC_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

