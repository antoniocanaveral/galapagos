/**
* FUNCION SII.F_CGG_RES_TIPO_NOTIFICACION_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_TIPO_NOTIFICACION
* @param IN_CRTNT_CODIGO IDENTIFICATIVO UNICO DE REGSITRO
* @param IN_CRTNT_DESCRIPCION DESCRIPCION DEL TIPO DE RESIDENCIA
* @param IN_CRTNT_CAUSAL_SALIDA ESTADO QUE DETERMINA SI EL TIPO DE NOTIFICACION ES CAUSAL DE SALIDA
* @param IN_CRTNT_SUSTENTO_LEGAL DESCRIPCION DEL SUSTENTO LEGAL QUE RESPALDA LA NOTIFICACION
* @param IN_CRTNT_CAUSAL_CADUCIDAD ESTADO QUE DETERMINA SI EL TIPO DE NOTIFICACION TENDRA UNA CONSECUENCIA
* @param IN_CRTNT_ESTADO ESTADO DEL REGISTRO
* @param IN_CRTNT_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRTNT_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TIPO_NOTIFICACION_INSERT(
IN IN_CRTNT_CODIGO SII.CGG_RES_TIPO_NOTIFICACION.CRTNT_CODIGO%TYPE, 
IN IN_CRTNT_DESCRIPCION SII.CGG_RES_TIPO_NOTIFICACION.CRTNT_DESCRIPCION%TYPE, 
IN IN_CRTNT_CAUSAL_SALIDA SII.CGG_RES_TIPO_NOTIFICACION.CRTNT_CAUSAL_SALIDA%TYPE, 
IN IN_CRTNT_SUSTENTO_LEGAL SII.CGG_RES_TIPO_NOTIFICACION.CRTNT_SUSTENTO_LEGAL%TYPE, 
IN IN_CRTNT_CAUSAL_CADUCIDAD SII.CGG_RES_TIPO_NOTIFICACION.CRTNT_CAUSAL_CADUCIDAD%TYPE, 
IN IN_CRTNT_ESTADO SII.CGG_RES_TIPO_NOTIFICACION.CRTNT_ESTADO%TYPE, 
IN IN_CRTNT_USUARIO_INSERT SII.CGG_RES_TIPO_NOTIFICACION.CRTNT_USUARIO_INSERT%TYPE, 
IN IN_CRTNT_USUARIO_UPDATE SII.CGG_RES_TIPO_NOTIFICACION.CRTNT_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRTNT_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_TIPO_NOTIFICACION','CRTNT_CODIGO','CRTNT') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRTNT_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_TIPO_NOTIFICACION(
		CRTNT_CODIGO, 
		CRTNT_DESCRIPCION, 
		CRTNT_CAUSAL_SALIDA, 
		CRTNT_SUSTENTO_LEGAL, 
		CRTNT_CAUSAL_CADUCIDAD, 
		CRTNT_ESTADO, 
		CRTNT_FECHA_INSERT, 
		CRTNT_USUARIO_INSERT, 
		CRTNT_FECHA_UPDATE, 
		CRTNT_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRTNT_DESCRIPCION, 
		IN_CRTNT_CAUSAL_SALIDA, 
		IN_CRTNT_SUSTENTO_LEGAL, 
		IN_CRTNT_CAUSAL_CADUCIDAD, 
		IN_CRTNT_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRTNT_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRTNT_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

