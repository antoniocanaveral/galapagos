/**
* FUNCION SII.F_CGG_TCT_MOVILIDAD_ACTIVIDAD_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_TCT_MOVILIDAD_ACTIVIDAD
* @param IN_CTMVC_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CTACT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE ACTIVIDADES
* @param IN_CRMOV_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE MOVILIDAD
* @param IN_CTMVC_ESTADO ESTADO DEL REGISTRO
* @param IN_CTMVC_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CTMVC_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_TCT_MOVILIDAD_ACTIVIDAD_INSERT(
IN IN_CTMVC_CODIGO SII.CGG_TCT_MOVILIDAD_ACTIVIDAD.CTMVC_CODIGO%TYPE,
IN IN_CTACT_CODIGO SII.CGG_TCT_MOVILIDAD_ACTIVIDAD.CTACT_CODIGO%TYPE,
IN IN_CRMOV_CODIGO SII.CGG_TCT_MOVILIDAD_ACTIVIDAD.CRMOV_CODIGO%TYPE,
IN IN_CTMVC_ESTADO SII.CGG_TCT_MOVILIDAD_ACTIVIDAD.CTMVC_ESTADO%TYPE,
IN IN_CTMVC_USUARIO_INSERT SII.CGG_TCT_MOVILIDAD_ACTIVIDAD.CTMVC_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CTMVC_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_TCT_MOVILIDAD_ACTIVIDAD','CTMVC_CODIGO','CTMVC') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CTMVC_CODIGO;
	END IF;
	INSERT INTO SII.CGG_TCT_MOVILIDAD_ACTIVIDAD(
		CTMVC_CODIGO, 
		CTACT_CODIGO, 
		CRMOV_CODIGO, 
		CTMVC_ESTADO, 
		CTMVC_FECHA_INSERT, 
		CTMVC_USUARIO_INSERT, 
		CTMVC_FECHA_UPDATE, 
		CTMVC_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CTACT_CODIGO, 
		IN_CRMOV_CODIGO, 
		IN_CTMVC_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CTMVC_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CTMVC_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

