		CTMVC_USUARIO_UPDATE
	FROM SII.CGG_TCT_MOVILIDAD_ACTIVIDAD
	WHERE CTMVC_ESTADO = TRUE AND SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CTMVC_ESTADO, FALSE)||'' ''||COALESCE(CTMVC_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CTMVC_USUARIO_INSERT, '''')||'' ''||COALESCE(CTMVC_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CTMVC_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

