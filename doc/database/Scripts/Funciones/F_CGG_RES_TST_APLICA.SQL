/**
* FUNCION SII.F_CGG_RES_TST_APLICA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_TST_APLICA
* @param IN_CRTAO_CODIGO IDENTIFICADOR UNICO DE REGISTRO DE TSAO
* @param IN_CRTSO_CODIGO IDENTIFICADOR UNICO DE REGISTRO DE TST OPCIONES
* @param IN_CRTST_CODIGO IDENTIFICATIVO UNICO DE REGISTRO TIPO SOLICITUD
* @param IN_CRTAO_ESTADO ESTADO DEL REGISTRO, SI ESTA VIGENTE O ELIMINADO
* @param IN_CRTAO_USUARIO_INSERT NOMBRE DEL USUARIO QUE CREO EL REGISTRO
* @param IN_CRTAO_USUARIO_UPDATE NOMBRE DEL USUARIO QUE REALIZO LA ACTUALIZACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_TST_APLICA_INSERT(
IN IN_CRTAO_CODIGO SII.CGG_RES_TST_APLICA.CRTAO_CODIGO%TYPE,
IN IN_CRTSO_CODIGO SII.CGG_RES_TST_APLICA.CRTSO_CODIGO%TYPE,
IN IN_CRTST_CODIGO SII.CGG_RES_TST_APLICA.CRTST_CODIGO%TYPE,
IN IN_CRTAO_ESTADO SII.CGG_RES_TST_APLICA.CRTAO_ESTADO%TYPE,
IN IN_CRTAO_USUARIO_INSERT SII.CGG_RES_TST_APLICA.CRTAO_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRTAO_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_TST_APLICA','CRTAO_CODIGO','CRTAO') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRTAO_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_TST_APLICA(
		CRTAO_CODIGO, 
		CRTSO_CODIGO, 
		CRTST_CODIGO, 
		CRTAO_ESTADO, 
		CRTAO_FECHA_INSERT, 
		CRTAO_USUARIO_INSERT, 
		CRTAO_FECHA_UPDATE, 
		CRTAO_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CRTSO_CODIGO, 
		IN_CRTST_CODIGO, 
		IN_CRTAO_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRTAO_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRTAO_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

