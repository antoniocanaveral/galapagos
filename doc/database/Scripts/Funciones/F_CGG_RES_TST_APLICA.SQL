IN IN_CRTST_CODIGO SII.CGG_RES_TST_APLICA.CRTST_CODIGO%TYPE
)RETURNS SETOF REFCURSOR AS
$$
DECLARE
	TMP_REF REFCURSOR;
BEGIN
	OPEN TMP_REF FOR
	SELECT
		TAP.CRTAO_CODIGO, 
		TOP.CRTSO_CODIGO, 
		TAP.CRTST_CODIGO,
		TOP.CRTSO_NOMBRE, 
		TOP.CRTSO_DESCRIPCION,
		CASE WHEN TAP.CRTAO_CODIGO IS NULL THEN FALSE ELSE TRUE END AS APLICA,
		TAP.CRTAO_ESTADO, 
		TAP.CRTAO_FECHA_INSERT, 
		TAP.CRTAO_USUARIO_INSERT, 
		TAP.CRTAO_FECHA_UPDATE, 
		TAP.CRTAO_USUARIO_UPDATE
	FROM SII.CGG_RES_TST_OPCION TOP 
	LEFT JOIN SII.CGG_RES_TST_APLICA TAP ON TAP.CRTSO_CODIGO = TOP.CRTSO_CODIGO AND TAP.CRTST_CODIGO = IN_CRTST_CODIGO
	WHERE CRTSO_ESTADO
	ORDER BY TOP.CRTSO_NOMBRE;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

