/**
* FUNCION SII.F_CGG_AREA_INFLUENCIA_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_AREA_INFLUENCIA.
* @param IN_CAINF_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE AREA DE INFLUENCIA
* @param IN_CAINF_NOMBRE NOMBRE DEL AREA DE INFLUENCIA
* @param IN_CAINF_DESCRIPCION ALMACENA LA DESCRIPCION DEL AREA DE INFLUENCIA
* @param IN_CAINF_ESTADO ESTADO DEL REGISTRO DEL AREA DE INFLUENCIA
* @param IN_CAINF_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA DEL AREA DE INFLUENCIA
* @param IN_CAINF_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION DEL AREA DE INFLUENCIA
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_AREA_INFLUENCIA_INSERT(
IN IN_CAINF_CODIGO SII.CGG_AREA_INFLUENCIA.CAINF_CODIGO%TYPE,
IN IN_CAINF_NOMBRE SII.CGG_AREA_INFLUENCIA.CAINF_NOMBRE%TYPE,
IN IN_CAINF_DESCRIPCION SII.CGG_AREA_INFLUENCIA.CAINF_DESCRIPCION%TYPE,
IN IN_CAINF_ESTADO SII.CGG_AREA_INFLUENCIA.CAINF_ESTADO%TYPE,
IN IN_CAINF_USUARIO_INSERT SII.CGG_AREA_INFLUENCIA.CAINF_USUARIO_INSERT%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CAINF_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_AREA_INFLUENCIA','CAINF_CODIGO','CAINF') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CAINF_CODIGO;
	END IF;
	INSERT INTO SII.CGG_AREA_INFLUENCIA(
		CAINF_CODIGO, 
		CAINF_NOMBRE, 
		CAINF_DESCRIPCION, 
		CAINF_ESTADO, 
		CAINF_FECHA_INSERT, 
		CAINF_USUARIO_INSERT, 
		CAINF_FECHA_UPDATE, 
		CAINF_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CAINF_NOMBRE, 
		IN_CAINF_DESCRIPCION, 
		IN_CAINF_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CAINF_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CAINF_USUARIO_INSERT
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

