/**
* FUNCION SII.F_CGG_GEM_TIPO_SALARIO_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_GEM_TIPO_SALARIO
* @param IN_CGTSA_CODIGO NO ESPECIFICADO
* @param IN_CGTSA_NOMBRE NO ESPECIFICADO
* @param IN_CGTSA_DESCRIPCION NO ESPECIFICADO
* @param IN_CGTSA_ESTADO ESTADO DEL REGISTRO
* @param IN_CGTSA_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CGTSA_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_GEM_TIPO_SALARIO_INSERT(
IN IN_CGTSA_CODIGO SII.CGG_GEM_TIPO_SALARIO.CGTSA_CODIGO%TYPE, 
IN IN_CGTSA_NOMBRE SII.CGG_GEM_TIPO_SALARIO.CGTSA_NOMBRE%TYPE, 
IN IN_CGTSA_DESCRIPCION SII.CGG_GEM_TIPO_SALARIO.CGTSA_DESCRIPCION%TYPE, 
IN IN_CGTSA_ESTADO SII.CGG_GEM_TIPO_SALARIO.CGTSA_ESTADO%TYPE, 
IN IN_CGTSA_USUARIO_INSERT SII.CGG_GEM_TIPO_SALARIO.CGTSA_USUARIO_INSERT%TYPE, 
IN IN_CGTSA_USUARIO_UPDATE SII.CGG_GEM_TIPO_SALARIO.CGTSA_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CGTSA_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_GEM_TIPO_SALARIO','CGTSA_CODIGO','CGTSA') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CGTSA_CODIGO;
	END IF;
	INSERT INTO SII.CGG_GEM_TIPO_SALARIO(
		CGTSA_CODIGO, 
		CGTSA_NOMBRE, 
		CGTSA_DESCRIPCION, 
		CGTSA_ESTADO, 
		CGTSA_FECHA_INSERT, 
		CGTSA_USUARIO_INSERT, 
		CGTSA_FECHA_UPDATE, 
		CGTSA_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CGTSA_NOMBRE, 
		IN_CGTSA_DESCRIPCION, 
		IN_CGTSA_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CGTSA_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CGTSA_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

