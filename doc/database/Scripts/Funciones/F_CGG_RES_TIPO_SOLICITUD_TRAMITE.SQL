BEGIN
	
	TMP_SQL := 'SELECT
		TSTRA.CRTST_CODIGO, 
		TSTRA.CGG_CRTST_CODIGO, 
		TSTRA.CRTPT_CODIGO, 		
		(WITH RECURSIVE TIPO(CRTST_CODIGO, CGG_CRTST_CODIGO)AS(
		SELECT TS.CRTST_CODIGO, TS.CGG_CRTST_CODIGO, TS.CRTST_DESCRIPCION FROM CGG_RES_TIPO_SOLICITUD_TRAMITE TS WHERE (TS.CRTST_CODIGO = TSTRA.CGG_CRTST_CODIGO)
