			TST.CRTST_VEHICULO, 
			TST.CRTST_UNANIMIDAD, 
			TST.CRTST_APLICA_OTRO, 
			TST.CRTST_APLICA_BENEFICIARIO, 
			TST.CRTST_INDICE, 
			TST.CRTST_ESTADO, 
			TST.CRTST_FECHA_INSERT, 
			TST.CRTST_USUARIO_INSERT, 
			TST.CRTST_FECHA_UPDATE, 
			TST.CRTST_USUARIO_UPDATE
		FROM SII.CGG_RES_TIPO_SOLICITUD_TRAMITE TST
		INNER JOIN SII.CGG_RES_TIPO_TRAMITE TPT ON TPT.CRTPT_CODIGO = TST.CRTPT_CODIGO
		WHERE CRTST_ESTADO AND 
			(NOT '||IN_SW||' OR '||IN_SW||' = CRTST_APLICA_TRAMITE) AND
			(LENGTH('||QUOTE_LITERAL(TMP_TST_TIPOS)||') = 0 OR STRPOS('||QUOTE_LITERAL(TMP_TST_TIPOS)||', CRTST_CODIGO) > 0 OR STRPOS('||QUOTE_LITERAL(TMP_TST_TIPOS)||', TST.CRTPT_CODIGO) > 0)
	) AS TIP
	WHERE (LENGTH('||QUOTE_LITERAL(IN_FIND_TEXT)||') = 0 OR SII.F_STRING_IN('''||IN_FIND_TEXT||''',
			COALESCE(TIP.CRTST_DESCRIPCION, '''')||'' ''||
			COALESCE(TIP.CRTST_OBSERVACION, '''')||'' ''||
			COALESCE(TIP.CRTST_APLICA_GARANTIA, FALSE)||'' ''||
			COALESCE(TIP.CRTST_APLICA_TRAMITE, FALSE)||'' ''||
			COALESCE(TIP.CRTST_APLICA_GRUPO, FALSE)||'' ''||
			COALESCE(TIP.CRTST_ATENCION_CLIENTE, FALSE)||'' ''||
			COALESCE(TIP.CRTST_RESTRINGIDO, FALSE)||'' ''||
