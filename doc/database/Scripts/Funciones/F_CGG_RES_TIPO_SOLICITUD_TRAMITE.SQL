		WHERE TP.CGG_CRTST_CODIGO = TST.CRTST_CODIGO) SELECT CRTST_CODIGO FROM TIPO WHERE CGG_CRTST_CODIGO IS NULL) AS CODIGO_PADRE,		
		TSTRA.CRPRO_CODIGO, 
		TSTRA.CRRES_CODIGO, 
		TSTRA.CKESP_CODIGO, 
		TSTRA.CRTST_DESCRIPCION, 
		TSTRA.CRTST_OBSERVACION, 
		TSTRA.CRTST_APLICA_GARANTIA, 
		TSTRA.CRTST_APLICA_TRAMITE, 
		TSTRA.CRTST_APLICA_GRUPO, 
		TSTRA.CRTST_ATENCION_CLIENTE, 
		TSTRA.CRTST_RESTRINGIDO, 
		TSTRA.CRTST_COMUNICADO_RADIAL, 
		TSTRA.CRTST_NUMERO_DIAS, 
		TSTRA.CRTST_VEHICULO, 
		TSTRA.CRTST_UNANIMIDAD, 
		TSTRA.CRTST_APLICA_OTRO, 
		TSTRA.CRTST_APLICA_BENEFICIARIO, 
		TSTRA.CRTST_INDICE, 
		TSTRA.CRTST_ESTADO, 
		TSTRA.CRTST_FECHA_INSERT, 
		TSTRA.CRTST_USUARIO_INSERT, 
		TSTRA.CRTST_FECHA_UPDATE, 
		TSTRA.CRTST_USUARIO_UPDATE
	FROM SII.CGG_RES_TIPO_SOLICITUD_TRAMITE TSTRA
	WHERE CRTST_ESTADO AND CRTST_APLICA_TRAMITE = TRUE AND		
		SII.F_STRING_IN('''||IN_FIND_TEXT||''',COALESCE(CRTST_DESCRIPCION, '''')||'' ''||COALESCE(CRTST_OBSERVACION, '''')||'' ''||COALESCE(CRTST_APLICA_GARANTIA, FALSE)||'' ''||COALESCE(CRTST_APLICA_TRAMITE, FALSE)||'' ''||COALESCE(CRTST_APLICA_GRUPO, FALSE)||'' ''||COALESCE(CRTST_ATENCION_CLIENTE, FALSE)||'' ''||COALESCE(CRTST_RESTRINGIDO, FALSE)||'' ''||COALESCE(CRTST_COMUNICADO_RADIAL, FALSE)||'' ''||COALESCE(CRTST_NUMERO_DIAS, 0)||'' ''||COALESCE(CRTST_VEHICULO, FALSE)||'' ''||COALESCE(CRTST_UNANIMIDAD, FALSE)||'' ''||COALESCE(CRTST_APLICA_OTRO, FALSE)||'' ''||COALESCE(CRTST_ESTADO, FALSE)||'' ''||COALESCE(CRTST_FECHA_INSERT, CURRENT_TIMESTAMP)||'' ''||COALESCE(CRTST_USUARIO_INSERT, '''')||'' ''||COALESCE(CRTST_FECHA_UPDATE, CURRENT_TIMESTAMP)||'' ''||COALESCE(CRTST_USUARIO_UPDATE, '''')) = 1
	ORDER BY '||IN_SORT_FIELD_NAME||' '||IN_DIRECTION||
	' LIMIT '||IN_LIMIT||' OFFSET '||IN_START_INDEX;
	OPEN TMP_REF FOR EXECUTE TMP_SQL;
	RETURN NEXT TMP_REF;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

