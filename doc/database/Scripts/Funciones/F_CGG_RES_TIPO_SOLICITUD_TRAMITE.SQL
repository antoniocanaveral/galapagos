/**
*FUNCION. VERIFICA SI UN USUARIO PUEDE INICIAR UN TRAMITE PARA EL TIPO DE SOLICITUD ESPECIFICADO.
*@param IN_CRTST_CODIGO CODIGO DE SOLICITUD DE TIPO DE TRAMITE.
*@param IN_CUSU_CODIGO CODIGO DE USUARIO.
*@return NUMERIC 1/SI INICIA - 0 / NO INICIA.
*/
CREATE OR REPLACE FUNCTION SII.F_INICIA_TIPO_SOLICITUD_TRAMITE(
IN IN_CRTST_CODIGO VARCHAR,
IN IN_CUSU_CODIGO VARCHAR
)RETURNS NUMERIC AS
$$
DECLARE
	TMP_NUM NUMERIC;	
	TMP_FASE_CURSOR REFCURSOR;
	TMP_ROW RECORD;	
	CRFAS_CODIGO_INICIO VARCHAR(20);	
	CRPROCODIGO VARCHAR(20);
	CISLACODIGO VARCHAR(20);
BEGIN	

	TMP_NUM :=0;
	
	SELECT COALESCE(CRPRO_CODIGO,'') INTO CRPROCODIGO FROM SII.cgg_res_tipo_solicitud_tramite WHERE crtst_codigo = IN_CRTST_CODIGO;
	SELECT COALESCE(CISLA_CODIGO,'') INTO CISLACODIGO FROM SII.CGG_USUARIO WHERE CUSU_CODIGO= IN_CUSU_CODIGO;
	
	TMP_FASE_CURSOR := SII.F_CGG_RES_FASE_INICIO(CRPROCODIGO,CISLACODIGO);
	
	LOOP
		FETCH TMP_FASE_CURSOR INTO TMP_ROW;		
		EXIT WHEN NOT FOUND;
		CRFAS_CODIGO_INICIO := TMP_ROW.CRFAS_CODIGO;
	END LOOP;
	CLOSE TMP_FASE_CURSOR;
	
	SELECT COALESCE(COUNT(FU.CRFSU_CODIGO),0) INTO TMP_NUM FROM SII. CGG_RES_PROCESO P
	INNER JOIN  SII.CGG_RES_FASE F ON (P.CRPRO_CODIGO = F.CRPRO_CODIGO)
	INNER JOIN SII.CGG_RES_FASE_USUARIO FU ON (F.CRFAS_CODIGO = FU.CRFAS_CODIGO)
	WHERE P.CRPRO_CODIGO = CRPROCODIGO AND
		  FU.CUSU_CODIGO = IN_CUSU_CODIGO AND 
		  F.CRFAS_CODIGO = CRFAS_CODIGO_INICIO AND
		  FU.CRFSU_ESTADO = TRUE;
		  
	RETURN TMP_NUM;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

/**FUNCION SII.F.TIPO_SOLICITUD_PADRE
* SELECCIONA LA INFORMACION CORRESPONDIENTE A LA TABLA CGG_RES_TIPO_SOLICITUD_TRAMITE.
* @param IN_CRTST_CODIGO IDENTIFICATIVO UNICO DE TIPO DE SOLICITUD TRAMITE.
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_TIPO_SOLICITUD_PADRE(
IN IN_CRTST_CODIGO VARCHAR
)RETURNS VARCHAR AS
$$
DECLARE
	CGGCRTSTCODIGO VARCHAR;
	OUT_CODIGO VARCHAR;
BEGIN	
	OUT_CODIGO:='';
	SELECT COALESCE(CGG_CRTST_CODIGO,'') INTO CGGCRTSTCODIGO FROM SII.CGG_RES_TIPO_SOLICITUD_TRAMITE WHERE CRTST_CODIGO = IN_CRTST_CODIGO;	
	IF(LENGTH(TRIM(CGGCRTSTCODIGO))>=1)THEN
		OUT_CODIGO := SII.F_TIPO_SOLICITUD_PADRE(CGGCRTSTCODIGO);
	ELSE
		OUT_CODIGO := IN_CRTST_CODIGO;
	END IF;

	RETURN OUT_CODIGO;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;


/**
