/**
* FUNCION SII.F_CGG_RES_PARTICIPANTE_TRAMITE_INSERT
* INGRESA UN REGISTRO EN LA TABLA SII.CGG_RES_PARTICIPANTE_TRAMITE
* @param IN_CRPTR_CODIGO IDENTIFICATIVO UNICO DE REGISTRO PARTICIPANTE TRAMITE
* @param IN_CUSU_CODIGO IDENTIFICATIVO UNICO DE REGISTRO
* @param IN_CRTPT_CODIGO IDENTIFICATIVO UNICO DE REGISTRO DE TIPO DE TRAMITE
* @param IN_CRPTR_ESTADO ESTADO DEL REGISTRO
* @param IN_CRPTR_USUARIO_INSERT USUARIO QUE INGRESO LA INFORMACION EN EL SISTEMA
* @param IN_CRPTR_USUARIO_UPDATE USUARIO QUE REALIZO LA ACTUALIZACION DE LA INFORMACION
* @return VARCHAR
*/
CREATE OR REPLACE FUNCTION SII.F_CGG_RES_PARTICIPANTE_TRAMITE_INSERT(
IN IN_CRPTR_CODIGO SII.CGG_RES_PARTICIPANTE_TRAMITE.CRPTR_CODIGO%TYPE, 
IN IN_CUSU_CODIGO SII.CGG_RES_PARTICIPANTE_TRAMITE.CUSU_CODIGO%TYPE, 
IN IN_CRTPT_CODIGO SII.CGG_RES_PARTICIPANTE_TRAMITE.CRTPT_CODIGO%TYPE, 
IN IN_CRPTR_ESTADO SII.CGG_RES_PARTICIPANTE_TRAMITE.CRPTR_ESTADO%TYPE, 
IN IN_CRPTR_USUARIO_INSERT SII.CGG_RES_PARTICIPANTE_TRAMITE.CRPTR_USUARIO_INSERT%TYPE, 
IN IN_CRPTR_USUARIO_UPDATE SII.CGG_RES_PARTICIPANTE_TRAMITE.CRPTR_USUARIO_UPDATE%TYPE
)RETURNS VARCHAR AS
$$
DECLARE
OUT_KEY VARCHAR;
BEGIN
	IF (UPPER(IN_CRPTR_CODIGO) = 'KEYGEN') THEN
		SELECT SII.F_KEYGEN('CGG_RES_PARTICIPANTE_TRAMITE','CRPTR_CODIGO','CRPTR') INTO OUT_KEY;
	ELSE
		OUT_KEY := IN_CRPTR_CODIGO;
	END IF;
	INSERT INTO SII.CGG_RES_PARTICIPANTE_TRAMITE(
		CRPTR_CODIGO, 
		CUSU_CODIGO, 
		CRTPT_CODIGO, 
		CRPTR_ESTADO, 
		CRPTR_FECHA_INSERT, 
		CRPTR_USUARIO_INSERT, 
		CRPTR_FECHA_UPDATE, 
		CRPTR_USUARIO_UPDATE
	) VALUES (
		OUT_KEY, 
		IN_CUSU_CODIGO, 
		IN_CRTPT_CODIGO, 
		IN_CRPTR_ESTADO, 
		CURRENT_TIMESTAMP, 
		IN_CRPTR_USUARIO_INSERT, 
		CURRENT_TIMESTAMP, 
		IN_CRPTR_USUARIO_UPDATE
	);
	RETURN OUT_KEY;
END
$$
LANGUAGE 'plpgsql' VOLATILE CALLED ON NULL INPUT;

